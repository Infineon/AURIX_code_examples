 /*\title Interrupt generation via STM trigger
 * \abstract The STM is configured to trigger an interrupt every 500 ms. The interrupt toggles an LED.
 * \description The STM is configured to trigger an interrupt every 500 ms. The interrupt toggles an LED.
 *              The STM counts up and on reaching a preconfigured compare value, it triggers an interrupt.
 *              On each interrupt, the compare value is incremented and an LED is toggled.
 *
 * \name iLLD_TC4D7_LK_ADS_STM_Interrupt
 * \version V1.0.0
 * \board AURIX TC4D7 lite Kit, KIT_A3G_TC4D7_LITE, TC4D7XP_A-Step_CC_COM
 * \keywords AURIX, Interrupt, LED, STM, STM_Interrupt_1, blink
 * \documents See README.md
 * \lastUpdated 2025-04-27
 *********************************************************************************************************************/

#include "Ifx_Types.h"
#include "Ifx_Cfg.h"
#include "IfxCpu.h"
#include "IfxWtu.h"
#include "STM_Interrupt.h"


void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword());
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());
    
    initPeripherals(); /* Initialize the STM module and the LED */

    while (1)
    {
    }
}
