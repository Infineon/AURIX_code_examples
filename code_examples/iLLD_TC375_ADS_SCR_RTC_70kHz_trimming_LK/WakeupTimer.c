/*
 * Function: configureWUT
 * --------------------
 * Configure WUT period. Disables WUT before setting new counter value, otherwise
 * not updated. Clock divider WUTDIV is not used.
 *
 * period: value in WUT ticks. Not converted
 *
 * returns: period value as 24-bit truncated
 */
uint32 configureWUT(uint32 period)
{
    period = period & 0x00FFFFFF;

    IfxScuWdt_clearCpuEndinit (IfxScuWdt_getCpuWatchdogPassword ());
    IfxScuWdt_clearSafetyEndinit (IfxScuWdt_getSafetyWatchdogPassword ());

    PMS_PMSWCR3.U = 0; /* Disable and clear request */

    while(PMS_PMSWCR3.B.BUSY);
    PMS_PMSWCR3.U = (1 << 27) | period; /* WUTREL+WUTEN. WUTDIV = 0 */

    IfxScuWdt_setCpuEndinit (IfxScuWdt_getCpuWatchdogPassword ());
    IfxScuWdt_setSafetyEndinit (IfxScuWdt_getSafetyWatchdogPassword ());

    return period;
}

/*
 * Function: calibrateWUT
 * --------------------
 * Calculates new calibration parameters based on the CCU6 deltaPeriod in CCU6 timer T1x ticks.
 * CCU6 T1x frequency is used.
 *
 * currWUTstate: current calibration parameters.
 * deltaCCU6ticksPeriod: CCU6 T1x captured delta in CCU6 clock ticks.
 * CCU6freq: CCU6 timer T1x clock frequency.
 *
 * returns: new calibration parameters.
 */
WUTcalibValues calibrateWUT(WUTcalibValues currWUTstate, uint32 deltaCCU6ticksPeriod, uint32 CCU6freq)
{
    float deltaWUTticksPeriod = (float)currWUTstate.period;
    /* Convert CCU6 delta ticks into WUT delta ticks */
    deltaWUTticksPeriod = ((float)currWUTstate.freq / CCU6freq) * deltaCCU6ticksPeriod;
    /* Calculate new calibration coefficient */
    currWUTstate.calibCoeff = ((float)currWUTstate.period) / deltaWUTticksPeriod;
    /* Calculate new WUT period */
    currWUTstate.period = (uint32)(currWUTstate.period / currWUTstate.calibCoeff);
    /* Calculate real WUT frequency */
    currWUTstate.freq = currWUTstate.freq * currWUTstate.calibCoeff;
    /* Configure WUT with new period value */
    configureWUT(currWUTstate.period);

    return currWUTstate;
}
