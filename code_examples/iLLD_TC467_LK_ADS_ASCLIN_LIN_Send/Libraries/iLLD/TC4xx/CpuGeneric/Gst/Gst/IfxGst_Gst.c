boolean IfxGst_Gst_init(IfxGst_Gst *gst, const IfxGst_Gst_Config *config)
{
    IfxGst_Gst_Pins *pinCfg = config->pins;
    /* Store the sfr info into the handle */
    gst->gstSFR = config->gstSFR;
    Ifx_GST         *gstSFR = gst->gstSFR;
#if defined(IFX_PROT_ENABLED)
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&gstSFR->PROTE, IfxApProt_State_config);
#endif
#endif
    /* Enable the GST clock */
    gstSFR->CLC.B.DISR = 0U;
#if defined(IFX_PROT_ENABLED)
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&gstSFR->PROTE, IfxApProt_State_run);
#endif
#endif

    /* Wait for clock to get enable */
    while (gstSFR->CLC.B.DISS != 0U)
    {}

    Ifx_GST_CFG cfg;
    cfg.U         = gstSFR->CFG.U;
    /* Update the clock prescalar to divide the Fspb */
    cfg.B.PRE     = (uint8)config->clockPrescalar;
    /* Enable dma interrupt on trigger event */
    cfg.B.DMA     = config->dmaInterruptEnable;
    /* Enable the PTP signal as reference time*/
    cfg.B.PTPEN   = config->ptpEnable;

    gstSFR->CFG.U = cfg.U;

    if (config->ptpEnable == FALSE)
    {
        /* Adjust the reference time resolution */
        gstSFR->CNTINC.U = config->counterStepSize;
    }

    if (config->triggerConfig != NULL_PTR)
    {
        IfxGst_Gst_TriggerConfig *trigConfig = config->triggerConfig;

        cfg.U         = gstSFR->CFG.U;
        /* Select the Trigger type */
        cfg.B.TRGTYP  = (uint8)trigConfig->triggerType;
        /* Configure the trigger pulse length */
        cfg.B.PLE     = (uint8)trigConfig->triggerPulselength;

        gstSFR->CFG.U = cfg.U;

        /* Check the trigger type */
        if (trigConfig->triggerType == IfxGst_TriggerType_delayed)
        {
            /* configure the delay after trigger generation */
            gstSFR->REL.U = trigConfig->delayTriggerReloadValue;
        }
        else if (trigConfig->triggerType == IfxGst_TriggerType_matching)
        {
            /* configure the compare mask value for trigger generation */
            gstSFR->CMPMSK.U = trigConfig->timebasedTriggerConfig.compareMask;
            /* configure the compare value for trigger generation */
            gstSFR->CMP.U    = trigConfig->timebasedTriggerConfig.compareValue;
        }
        else if (trigConfig->triggerType == IfxGst_TriggerType_both)
        {
            /* configure the delay after trigger generation in delayed trigger mode*/
            gstSFR->REL.U    = trigConfig->delayTriggerReloadValue;
            /* configure compare mask and value in matching trigger mode*/
            gstSFR->CMPMSK.U = trigConfig->timebasedTriggerConfig.compareMask;
            gstSFR->CMP.U    = trigConfig->timebasedTriggerConfig.compareValue;
        }
        else
        {
            /* no trigger selected */
        }

        /* Check the priority for dma interrupt*/
        if (config->dmaInterrupt.isrPriority > 0)
        {
            volatile Ifx_SRC_SRCR *src = IfxGst_getSrcPointer(gstSFR, IfxGst_Interrupt_dma);
            IfxSrc_init(src, config->dmaInterrupt.isrProvider, config->dmaInterrupt.isrPriority, config->dmaInterrupt.vmId);
            IfxSrc_enable(src);
        }
    }

    /* Pin configurations  */
    if (pinCfg != NULL_PTR)
    {
        /* Hardware trigger output pin configuration */
        if (pinCfg->trigOutPin != NULL_PTR)
        {
            IfxGst_initTriggerOutPin(pinCfg->trigOutPin, pinCfg->outPinMode, pinCfg->padDriver);
        }

        /*  Port trigger capture pin configuration */
        if (pinCfg->inCapturePin != NULL_PTR)
        {
            IfxGst_initCaptureInPin(pinCfg->inCapturePin, pinCfg->inPinMode, pinCfg->padDriver);
        }
    }

    /* Check the priority for counter overflow interrupt*/
    if (config->counterOverflow.isrPriority > 0)
    {
        volatile Ifx_SRC_SRCR *src = IfxGst_getSrcPointer(gstSFR, IfxGst_Interrupt_counterOverflow);
        IfxSrc_init(src, config->counterOverflow.isrProvider, config->counterOverflow.isrPriority, config->counterOverflow.vmId);
        IfxSrc_enable(src);
    }

    /* Check the priority for buffer overflow interrupt*/
    if (config->bufferOverflow.isrPriority > 0)
    {
        volatile Ifx_SRC_SRCR *src = IfxGst_getSrcPointer(gstSFR, IfxGst_Interrupt_bufferOverflow);
        IfxSrc_init(src, config->bufferOverflow.isrProvider, config->bufferOverflow.isrPriority, config->bufferOverflow.vmId);
        IfxSrc_enable(src);
    }

    Ifx_GST_SRCENA srcena;
    srcena.U  = gstSFR->SRCENA.U & ~config->sourceEventSel.sourceSelA.mask;
    /*update with configured value */
    srcena.U |= (config->sourceEventSel.sourceSelA.mask & config->sourceEventSel.sourceSelA.value);

    Ifx_GST_SRCENB srcenb;
    srcenb.U         = gstSFR->SRCENB.U & ~config->sourceEventSel.sourceSelB.mask;
    /*update with configured value */
    srcenb.U        |= (config->sourceEventSel.sourceSelB.mask & config->sourceEventSel.sourceSelB.value);

    gstSFR->SRCENA.U = srcena.U;
    gstSFR->SRCENB.U = srcenb.U;

    return TRUE;
}


void IfxGst_Gst_initConfig(Ifx_GST *gstSFR, IfxGst_Gst_Config *config)
{
    const IfxGst_Gst_Config IfxGst_Gst_defaultConfig = {
        .clockPrescalar = IfxGst_ClockPrescalar_div1,
        .sourceEventSel = {
            .sourceSelA     = {
                .value = IFXGST_CFG_SRCENA,
                .mask  = IFXGST_CFG_SRCENA_MASK
            },
            .sourceSelB     = {
                .value = IFXGST_CFG_SRCENB,
                .mask  = IFXGST_CFG_SRCENB_MASK
            }
        },
        .ptpEnable          = FALSE,
        .counterStepSize    = 0x0U,
        .triggerConfig      = NULL_PTR,
        .pins               = NULL_PTR,
        .dmaInterruptEnable = FALSE,
        .counterOverflow    = {
            .isrPriority = 0U,
            .isrProvider = IfxSrc_Tos_cpu0,
            .vmId        = IfxSrc_VmId_0
        },
        .bufferOverflow     = {
            .isrPriority = 0U,
            .isrProvider = IfxSrc_Tos_cpu0,
            .vmId        = IfxSrc_VmId_0
        },
        .dmaInterrupt       = {
            .isrPriority = 0U,
            .isrProvider = IfxSrc_Tos_dma0,
            .vmId        = IfxSrc_VmId_0
        }
    };

    *config        = IfxGst_Gst_defaultConfig;
    config->gstSFR = gstSFR;
}


boolean IfxGst_Gst_getTimestampEventInfo(IfxGst_Gst *gst)
{
    IfxGst_Gst_TimestampEventInfo timeStampInfo;
    uint8                         idx;
    boolean                       status = FALSE;
    Ifx_GST                      *gstSFR = gst->gstSFR;

    for (idx = 0U; idx < IFXGST_GST_CYCLIC_BUFFER_SIZE; idx++)
    {
        /* Check whether any event occurs and update status*/
        status                            |= (gstSFR->HIST[idx].TRGSRC.U != 0x0U) ? TRUE : FALSE;
        /* Store the time stamp and event info */
        timeStampInfo.timeStampBuffer[idx] = gstSFR->HIST[idx].TRGEVNT.U;
        timeStampInfo.eventBuffer[idx].U   = gstSFR->HIST[idx].TRGSRC.U;
    }

    /* Upate the handle */
    gst->timestampEventInfo = timeStampInfo;

    return status;
}


boolean IfxGst_Gst_getStatus(IfxGst_Gst *gst)
{
    boolean  status = FALSE;
    Ifx_GST *gstSFR = gst->gstSFR;

    if (gstSFR->STAT.U != 0x0U)
    {
        gst->status.U = gstSFR->STAT.U;
        status        = TRUE;
    }

    return status;
}

