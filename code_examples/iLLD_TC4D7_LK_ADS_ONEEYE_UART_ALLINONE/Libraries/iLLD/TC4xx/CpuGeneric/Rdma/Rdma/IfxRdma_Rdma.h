/** \addtogroup IfxLld_Rdma_Rdma_Struct
 * \{ */
/** \brief Channel loop Parameters
 */
typedef struct
{
    uint32 offset;       /**< \brief Specifies loop offset value: byte align */
    uint32 repeat;       /**< \brief Specifies loop count value */
    uint32 extent;       /**< \brief Specifies loop extent value: NA for Aux loop and Output rdma */
} IfxRdma_Rdma_Loop;

/** \} */

/** \addtogroup IfxLld_Rdma_Rdma_Struct
 * \{ */
/** \brief Configuration structure for channel interrupt enable
 */
typedef struct
{
    uint8 sourceErrorEnabled : 1;            /**< \brief Enable the source error interrupt */
    uint8 destinationErrorEnabled : 1;       /**< \brief Enable the destination error enable */
} IfxRdma_Rdma_EnableErrorInterrupt;

typedef struct
{
    Ifx_UReg_32Bit lastErrorChannel : 3;                /**< \brief The last error channel number */
    Ifx_UReg_32Bit reserved_3 : 13;                     /**< \brief Reserved 3-15 bits */
    Ifx_UReg_32Bit sourceErrorEvent : 1;                /**< \brief Source error event info */
    Ifx_UReg_32Bit destErrorEvent : 1;                  /**< \brief destination error event info */
    Ifx_UReg_32Bit reserved_18 : 10;                    /**< \brief Reserved 18-27 bits */
    Ifx_UReg_32Bit chanBusyTransReqLostEvent : 1;       /**< \brief Channel busy transaction request lost error info */
    Ifx_UReg_32Bit chanDisTransReqLostEvent : 1;        /**< \brief Channel disabled transaction request lost error info */
    Ifx_UReg_32Bit reserved_30 : 2;                     /**< \brief Reserved 30-31 bits */
} IfxRdma_Rdma_ErrorInfoBits;

/** \brief Configuration structure for Input RDMA channel
 */
typedef struct
{
    IfxRdma_DataFormat          format;                /**< \brief Select the data format for input RDMA */
    IfxRdma_InputAddressingMode addressingMode;        /**< \brief Select the address mode:linear, transpose */
    IfxRdma_InputProcessingMode processingMode;        /**< \brief Select the processing mode:default,integration */
    IfxRdma_CircularBufferSize  circularBufSize;       /**< \brief Specifies the circular buffer size */
    IfxRdma_NumBlocks           numBlocks;             /**< \brief Specifies the block size in integration mode */
    uint32                      baseAddress;           /**< \brief Specifies the base address for Input RDMA: byte align */
    IfxRdma_Rdma_Loop           bin;                   /**< \brief Contains the configuration for bin loop */
    IfxRdma_Rdma_Loop           inner;                 /**< \brief Contains the configuration for inner loop */
    IfxRdma_Rdma_Loop           outer;                 /**< \brief Contains the configuration for outer loop */
    IfxRdma_AuxilliaryLoop      auxLoop;               /**< \brief Select the auxiliary loop:inner,outer or bin */
    IfxRdma_Rdma_Loop           aux;                   /**< \brief Contains the configuration for inner loop: extents not used for aux */
} IfxRdma_Rdma_InputDma;

/** \brief Configuration structure for channel interrupt enable
 */
typedef struct
{
    boolean      triggerCompletionEnabled;       /**< \brief Enable the trigger completion interrupt */
    boolean      transactionLostEnabled;         /**< \brief Enable the transaction lost interrupt */
    Ifx_Priority priority;                       /**< \brief Priority of the channel interrupt trigger */
    IfxSrc_Tos   typeOfService;                  /**< \brief Interrupt service provider */
    IfxSrc_VmId  vmId;                           /**< \brief VM interrupt service provider */
} IfxRdma_Rdma_InterruptConfig;

/** \brief Configuration structure for Output RDMA channel  (Only relevant for cube transfer mode).
 */
typedef struct
{
    uint32                       baseAddress;           /**< \brief Specifies the base address for Output RDMA: byte align */
    IfxRdma_DataFormat           format;                /**< \brief Select the data format for output RDMA */
    uint8                        exponent;              /**< \brief Defines number of bits to adjust the data representation(EXPNT) when format=IfxRdma_DataFormat_cmprb */
    IfxRdma_OutputAddressingMode addressMode;           /**< \brief Select the processing mode:linear,array */
    IfxRdma_CircularBufferSize   circularBufSize;       /**< \brief Specifies the circular buffer size */
    IfxRdma_Rdma_Loop            bin;                   /**< \brief Contains the configuration for bin loop: extent not used */
    IfxRdma_Rdma_Loop            inner;                 /**< \brief Contains the configuration for inner loop: extent not used */
    IfxRdma_Rdma_Loop            outer;                 /**< \brief Contains the configuration for bin loop: extent not used */
} IfxRdma_Rdma_OutputDma;

/** \brief Configuration structure for Target detection and Extraction
 */
typedef struct
{
    uint32                    baseAddressA;                                               /**< \brief Specifies the base address for bitmap A: byte align */
    uint16                    bitmapXaxisSize;                                            /**< \brief Specifies the target detection bitmap size in number of bins along the X-axis */
    IfxRdma_BitmapAlongYaxis  bitmapYaxisSize;                                            /**< \brief Specifies the target detection bitmap size in number of bins along the Y-axis, where Y can be the inner dimension of the 2D bitmap array depending on the axis map configuration */
    boolean                   bitmapBEnabled;                                             /**< \brief Enable the target detection bitmap B: byte align */
    uint32                    baseAddressB;                                               /**< \brief Specifies the base address for bitmap B */
    IfxRdma_CombineOperator   bitmapOperation;                                            /**< \brief Combine operator for bitmap A and B: AND,OR and XOR */
    boolean                   bitmapOutputEnabled;                                        /**< \brief Enable the output bitmap */
    uint32                    bitmapOutputAddress;                                        /**< \brief Specifies the output bitmap address, in bytes, aligned to 256-bit */
    IfxRdma_AxisMapping       axisMap;                                                    /**< \brief Select the axis map for target extraction */
    IfxRdma_ExtractionPattern extractionPattern;                                          /**< \brief Select the target extraction pattern */
    IfxRdma_ExtentionMode     extractionMode;                                             /**< \brief Specifies the target extraction mode: modulo,zeropad */
    uint8                     extractionLoopRepeat;                                       /**< \brief Specifies the value of target extraction loop repeat */
    uint32                    extractionLoopOffset[IFXRDMA_RDMA_MAX_TMAP_REPEAT];         /**< \brief Specifies the target extraction loop offset */
    boolean                   subarrayEnabled;                                            /**< \brief Enable the sub-array processing */
    uint16                    subarrayLoopOffset[IFXRDMA_RDMA_MAX_SUBARRAY_REPEAT];       /**< \brief Specifies the sub-array processing loop offset */
    uint8                     subarrayRepeatCount;                                        /**< \brief Specifies the value of sub-array processing loop repeat */
    IfxRdma_CombineOperator   subarrayOperation;                                          /**< \brief Combine operator for sub-array processing: AND,OR and XOR */
    boolean                   labellistEnabled;                                           /**< \brief Enable the target label list */
    uint16                    labellistXMultiplier;                                       /**< \brief Specifies the X index multiplier for label list */
    uint16                    labellistYMultiplier;                                       /**< \brief Specifies the Y index multiplier for label list */
    uint32                    labellistAddress;                                           /**< \brief Specifies the output base address for target label list, in bytes, aligned to 256-bit */
} IfxRdma_Rdma_TargetDetectExtract;

/** \brief Configuration structure for Trigger
 */
typedef struct
{
    IfxRdma_TriggerMode         incrementalTriggerMode;       /**< \brief Select how the RDMA will process the data */
    IfxRdma_PreTriggerThreshold preTriggerThreshold;          /**< \brief Define the input pre-trigger threshold count */
    uint8                       outputTriggerBatchSize;       /**< \brief Define the threshold to raise the interrupt in batch mode */
    uint8                       outputTriggerBuffSize;        /**< \brief Defines the buffer size for output trigger */
    IfxRdma_TriggerAckReq       spuRequest;                   /**< \brief Select the SPU trigger acknowledgment request */
} IfxRdma_Rdma_TriggerConfig;

/** \} */

/** \addtogroup IfxLld_Rdma_Rdma_Struct
 * \{ */
/** \brief RDMA base address data structure (Module handle)
 */
typedef struct
{
    Ifx_RDMA *rdmaSFR;       /**< \brief Specifies the pointer to the RDMA registers */
} IfxRdma_Rdma;

/** \brief Channel handle
 */
typedef struct
{
    Ifx_RDMA         *rdmaSFR;           /**< \brief Specifies the pointer to the RDMA registers */
    IfxRdma_ChannelId channelId;         /**< \brief Specifies the RDMA channel */
    Ifx_RDMA_CH      *channelSFR;        /**< \brief Specifies the pointer to RDMA channel registers */
} IfxRdma_Rdma_Channel;

/** \brief Configuration data structure of the channel
 */
typedef struct
{
    Ifx_RDMA                         *rdmaSFR;                     /**< \brief Specifies pointer to the IfxRdma_Rdma module handle */
    IfxRdma_ChannelId                 channelId;                   /**< \brief Specifies the channel being used */
    IfxRdma_ProcessingMode            processingMode;              /**< \brief Select the processing mode of RDMA:cube,target extraction */
    IfxRdma_Rdma_TriggerConfig        triggerConfig;               /**< \brief Contain the configuration of trigger */
    IfxRdma_Rdma_InterruptConfig      interruptConfig;             /**< \brief Contain the configuration of channel interrupt */
    IfxRdma_Rdma_TargetDetectExtract *targetConfig;                /**< \brief Contains the configuration for target detection and extraction */
    IfxRdma_Rdma_InputDma             inputDma;                    /**< \brief Contains the configuration of input RDMA */
    IfxRdma_Rdma_OutputDma            outputDma;                   /**< \brief Contains the configuration of output RDMA */
    boolean                           hardwareRequestEnabled;      /**< \brief Enabling channel transaction via hardware request */
    boolean                           crcCheckEnabled;             /**< \brief Enable the crc check */
    uint32                            crcValue;                    /**< \brief Specifies the expected CRC value */
} IfxRdma_Rdma_ChannelConfig;

/** \brief RDMA base address data structure (Module handle)
 */
typedef struct
{
    Ifx_RDMA                          *rdmaSFR;                          /**< \brief Specifies the pointer to the RDMA registers */
    IfxRdma_Rdma_EnableErrorInterrupt *errInterruptConfig;               /**< \brief Contain the configuration of error interrupt */
    Ifx_Priority                       errorInterruptPriority;           /**< \brief Priority of the error interrupt trigger */
    IfxSrc_Tos                         errorInterruptTypeOfService;      /**< \brief Error Interrupt service provider */
    IfxSrc_VmId                        errorVmId;                        /**< \brief Error VM interrupt service provider */
} IfxRdma_Rdma_Config;

/** \brief RDMA error information in bits and words
 */
typedef union
{
    uint32                     U;       /**< \brief Word holding the status bits */
    IfxRdma_Rdma_ErrorInfoBits B;       /**< \brief Structure of event capture bits */
} IfxRdma_Rdma_ErrorInfo;

