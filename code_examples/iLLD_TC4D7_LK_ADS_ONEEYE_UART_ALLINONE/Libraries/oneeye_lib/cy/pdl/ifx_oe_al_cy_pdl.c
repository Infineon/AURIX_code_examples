sint32 Ifx_Oe_getTimerFrequency(void)
{
    return SystemCoreClock;
}


volatile Ifx_Oe_TickTime timeTicks = 0;
Ifx_Oe_TickTime timeTicksIncrement = 0;

//#define CHECK_LOGIC
#ifdef CHECK_LOGIC
volatile Ifx_Oe_TickTime previousTick = 0;
#endif


Ifx_Oe_TickTime Ifx_Oe_Time_now(void)
{

	sint32 timer_1 = 0;
	sint32 timer_2 = 0;
	uint32 reload = SysTick->LOAD;

	boolean state = Ifx_Oe_disableInterrupts();

	/* Timer value before checking the flag */
	timer_1 = SysTick->VAL;
	/* ---> If the count flag is set between the timer value read and the countflag read, the timer value and count flag are incoherent */
	/* The blow coherency check assumes that the timer reload can occur at most once during the execution of this function */
	if(SysTick->CTRL & SysTick_CTRL_COUNTFLAG_Msk)
	{
		/* The counter as counted to 0 but wasn't yet taken in account by the SysTick_Handler() function
		 * Do the increment here
	     * The read of SysTick->CTRL clears the count flag to indicate that the timeTicks has been incremented
	     */
	    timeTicks+=timeTicksIncrement;

		/* Timer value after checking the flag */
		timer_2 = SysTick->VAL;
	    if (timer_2<timer_1)
	    {
	    	/* The timer has not been reloaded between the timer_1 and timer_2 reads, the timer_1 value and count flag are coherent */
	    }
	    else
	    {
	    	/* The timer has been reloaded between the timer_1 and timer_2 reads, the timer_1 value and count flag are incoherent, use timer_2 instead */
        	timer_1 = timer_2;
	    }
	}
	else
	{
		/* The timer_1 value and count flag are coherent */
	}

	sint32 offset = reload - timer_1;


    Ifx_Oe_TickTime newTick= timeTicks+offset;

#ifdef CHECK_LOGIC
    IFX_OE_ASSERT(newTick>=previousTick);
#endif

    Ifx_Oe_restoreInterrupts(state);


#ifdef CHECK_LOGIC
    previousTick=newTick;
#endif

    return newTick;
}


/* Default empty handler */
void Dummy_task(void);

void SysTick_task(void) __attribute__ ((weak, alias("Dummy_task")));

void Dummy_task(void)
{
}

void SysTick_Handler(void)
{
	boolean state = Ifx_Oe_disableInterrupts();

	if(SysTick->CTRL & SysTick_CTRL_COUNTFLAG_Msk)
	{
		/* The counter as counted to 0, increment the timeTicks counter
	     * The read of SysTick->CTRL clears the count flag to indicate that the timeTicks has been incremented
	     */
	    timeTicks+=timeTicksIncrement;
	}
    Ifx_Oe_restoreInterrupts(state);

    SysTick_task();
}


void SysTick_init(void)
{
    // Set Systick to 1ms interval
    if (SysTick_Config(SystemCoreClock / 1000))
    {
        // Capture error
        while (TRUE)
        {
        }
    }
    timeTicksIncrement = Ifx_Oe_getTimerFrequency()/1000;
}


void Ifx_Oe_softwareReset(void)
{
	NVIC_SystemReset();
}


unsigned int Ifx_Oe_cmpAndSwap(unsigned int volatile* address, unsigned int value, unsigned int condition)
{
	unsigned int old_value;
	unsigned int result = value; // CAS failed

	bool state = Ifx_Oe_disableInterrupts();

	// Load the value from memory with exclusive access
	old_value = __LDREXW((volatile uint32_t*)address);

	// Compare and check if the current value matches the expected value
	if (old_value == condition)
	{
		// If it matches, attempt to store the new value atomically
		if (__STREXW(value, (volatile uint32_t*)address) == 0)
		{
			// Successful store, the value was swapped atomically
			result = condition;
		}
	}
	Ifx_Oe_restoreInterrupts(state);

    return result;
}

