#define BOARD_APPLICATION_KIT_TC27X    (1)
#define BOARD_APPLICATION_KIT_TC38X    (2)
#define BOARD_AURIX_SHIELD_BUDDY_TC37X (3)
#define BOARD_AURIX_TRIBOARD_TC49X     (4)
#define BOARD_AURIX_TRIBOARD_TC4DX     (5)

/* Specify the hardware board used. BOAD is defined in CMakeProjects.txt */
#if ( (BOARD == BOARD_APPLICATION_KIT_TC27X) || (BOARD == BOARD_APPLICATION_KIT_TC38X) || (BOARD == BOARD_AURIX_TRIBOARD_TC49X) )
#define CFG_ASC0_MODULE (MODULE_ASCLIN0)
#define CFG_ASC0_RX_PIN (IfxAsclin0_RXA_P14_1_IN)
#define CFG_ASC0_TX_PIN (IfxAsclin0_TX_P14_0_OUT)
#elif (BOARD == BOARD_AURIX_TRIBOARD_TC4DX)
#define CFG_ASC0_MODULE (MODULE_ASCLIN0)
#define CFG_ASC0_RX_PIN (IfxAsclin0_RXA_F_P14_1_IN)
#define CFG_ASC0_TX_PIN (IfxAsclin0_TX_F_P14_0_OUT)
#elif (BOARD == BOARD_AURIX_SHIELD_BUDDY_TC37X)
#define CFG_ASC0_MODULE (MODULE_ASCLIN3)
#define CFG_ASC0_RX_PIN (IfxAsclin3_RXD_P32_2_IN)
#define CFG_ASC0_TX_PIN (IfxAsclin3_TX_P15_7_OUT)
#else
#pragma error Board not supported
#endif
/*------------------------------------------------------------------------------*/

#define CFG_ASC0_BAUDRATE (115200.0)

uint8 g_ascTxBuffer[IFX_OE_SYNCPROTOCOL_FIFO_SIZE + sizeof(Ifx_Fifo) + 8];
uint8 g_ascRxBuffer[IFX_OE_SYNCPROTOCOL_FIFO_SIZE + sizeof(Ifx_Fifo) + 8];

/*------------------------------------------------------------------------------*/

#define ISR_PRIORITY_ASC0_RX (4)
#define ISR_PRIORITY_ASC0_TX (8)
#define ISR_PRIORITY_ASC0_EX (12)

#define ISR_PROVIDER_ASC0 IfxSrc_Tos_cpu0

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/

Ifx_Oe_UartDPipe g_OneEyeUart_uart;                                          /* UART driver                          */

/*********************************************************************************************************************/
/*---------------------------------------------Function Prototypes ----------------------------------------------*/
/*********************************************************************************************************************/
IFX_INTERRUPT(ISR_ASCLIN0_Tx, 0, ISR_PRIORITY_ASC0_TX);
IFX_INTERRUPT(ISR_ASCLIN0_Rx, 0, ISR_PRIORITY_ASC0_RX);
IFX_INTERRUPT(ISR_ASCLIN0_Ex, 0, ISR_PRIORITY_ASC0_EX);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Adding of the interrupt service routines */
/*******************************************************************************
* Function Name: USIC0_0_IRQHandler
********************************************************************************
* Summary:
* Transmit IRQ Handler. The function called everytime the number of elements
* in the TX FIFO reduces below TX FIFO Limit (set to one). The function is used
* to fill the TX FIFO with the next element in the tx_data buffer.
*
* Parameters:
*  void
*
* Return:
*  void
*
*******************************************************************************/
void ISR_ASCLIN0_Tx(void)
{
    Ifx_Oe_StdIf_DPipe_onTransmit(&g_OneEyeUart_uart.pipe);
}


/*******************************************************************************
* Function Name: USIC0_0_IRQHandler
********************************************************************************
* Summary:
* Receive handling IRQ . The function called everytime the number of elements
* in the RX FIFO exceeds above Rx FIFO Limit (set to seven).
* The function is used to read the RX FIFO until it is empty and set a flag
* when all the data has been received.
*
* Parameters:
*  void
*
* Return:
*  void
*
*******************************************************************************/
void ISR_ASCLIN0_Rx(void)
{
    Ifx_Oe_StdIf_DPipe_onReceive(&g_OneEyeUart_uart.pipe);
}


void ISR_ASCLIN0_Ex(void)
{
    IfxAsclin_Asc_isrError(&g_OneEyeUart_uart.driver);
}

/* This function initializes the UART module */
void OneEyeUart_init(void)
{
    IfxAsclin_Asc_Config ascConfig;
    IfxAsclin_Asc_initModuleConfig(&ascConfig, &CFG_ASC0_MODULE);
    ascConfig.baudrate.baudrate             = CFG_ASC0_BAUDRATE;
    ascConfig.baudrate.oversampling         = IfxAsclin_OversamplingFactor_16;
    ascConfig.bitTiming.medianFilter        = IfxAsclin_SamplesPerBit_three;
    ascConfig.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;
    ascConfig.frame.parityBit               = FALSE;
    ascConfig.frame.parityType              = IfxAsclin_ParityType_even;
    ascConfig.interrupt.rxPriority          = ISR_PRIORITY_ASC0_RX;
    ascConfig.interrupt.txPriority          = ISR_PRIORITY_ASC0_TX;
    ascConfig.interrupt.erPriority          = ISR_PRIORITY_ASC0_EX;
    ascConfig.interrupt.typeOfService       = ISR_PROVIDER_ASC0;

    IfxAsclin_Asc_Pins ascPins = {
        .cts       = NULL_PTR,
        .ctsMode   = IfxPort_InputMode_noPullDevice,
        .rx        = &CFG_ASC0_RX_PIN,
        .rxMode    = IfxPort_InputMode_noPullDevice,
        .rts       = NULL_PTR,
        .rtsMode   = IfxPort_OutputMode_pushPull,
        .tx        = &CFG_ASC0_TX_PIN,
        .txMode    = IfxPort_OutputMode_pushPull,
        .pinDriver = IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConfig.pins           = &ascPins;
    ascConfig.txBuffer = g_ascTxBuffer;
    ascConfig.txBufferSize   = Ifx_Oe_SyncProtocol_getFifoSize();
    ascConfig.rxBuffer = g_ascRxBuffer;
    ascConfig.rxBufferSize   = Ifx_Oe_SyncProtocol_getFifoSize();
    ascConfig.dataBufferMode = Ifx_DataBufferMode_normal;
    IfxAsclin_Asc_initModule(&g_OneEyeUart_uart.driver, &ascConfig);

    /* Initialize the standard interface for the UART */
    Ifx_Oe_UartDPipe_initDPipe(&g_OneEyeUart_uart.pipe, &g_OneEyeUart_uart.driver);
}
