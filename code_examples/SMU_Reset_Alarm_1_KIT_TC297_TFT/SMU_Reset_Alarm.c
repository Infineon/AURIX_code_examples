/* This function configures the SMU module to trigger a reset if a software alarm occurs */
void config_SMU_reset(void)
{
    /* Clear Safety ENDINIT protection to allow register modification */
    IfxScuWdt_clearSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());
    SCU_RSTCON.B.SMU = 0x1; /* Enable the SMU system reset request */
    /* Set Safety ENDINIT protection to allow register modification */
    IfxScuWdt_setSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());

    /* Enable the SMU register configuration by setting the CFGLCK field of the SMU_KEYS register to 0xBC */
    IfxSmu_unlock(&MODULE_SMU);
    /* SMU alarm configuration: select Software Alarm 0 (alarm group 5) and send a reset request to the SCU */
    IfxSmu_setAlarmConfig(&MODULE_SMU, IfxSmu_Alarm_SoftwareAlarm0, IfxSmu_AlarmConfig_scuReset);
    IfxSmu_lock(&MODULE_SMU); /* After configuration set temporary lock of SMU configuration */

    IfxSmu_cmd(&MODULE_SMU, IfxSmu_Cmd_start, 0); /* Start the SMU state machine (SSM) */
}

/* This function triggers the Software Alarm 0 */
void trigger_SMU_reset(void)
{
    IfxSmu_triggerAlarm(&MODULE_SMU, IfxSmu_Alarm_SoftwareAlarm0); /* Trigger Software Alarm 0 */
}
