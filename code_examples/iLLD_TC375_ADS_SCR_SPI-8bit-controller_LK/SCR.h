/* SCR boot mode selection, Definition in Ifx_PMS_PMSWCR4.SCRCFG */
typedef enum
{
    ScrBootMode_UserMode_0       = 0,   /* User code in XRAM will not be executed */
    ScrBootMode_UserMode_1       = 1,   /* User Code in XRAM at address 0000H will be executed */
    ScrBootMode_DebugMode_DAPx_0 = 2,   /* Debug mode using SCR DAP0_0 pin and DAP1_0 pin */
    ScrBootMode_DebugMode_DAPx_1 = 3,   /* Debug mode using SCR DAP0_1 pin and DAP1_1 pin */
    ScrBootMode_DebugMode_SPD_0  = 4,   /* Debug mode using SCR SPD_0 pin */
    ScrBootMode_DebugMode_SPD_0a = 5,   /* Debug mode using SCR SPD_0 pin */
    ScrBootMode_DebugMode_SPD_1  = 6,   /* Debug mode using SCR SPD_1 pin */
    ScrBootMode_DebugMode_SPD_1a = 7,   /* Debug mode using SCR SPD_1 pin */
    ScrBootMode_DebugMode_DAP    = 0xA, /* Debug mode using SOC DAP interface */
    ScrBootMode_DebugMode_DAPa   = 0xB, /* Debug mode using SOC DAP interface */
    ScrBootMode_DebugMode_SPD    = 0xC, /* Debug mode using SOC SPD interface */
    ScrBootMode_DebugMode_SPDa1  = 0xD, /* Debug mode using SOC SPD interface */
    ScrBootMode_DebugMode_SPDa2  = 0xE, /* Debug mode using SOC SPD interface */
    ScrBootMode_DebugMode_SPDa3  = 0xF  /* Debug mode using SOC SPD interface */
} ScrBootMode;

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
