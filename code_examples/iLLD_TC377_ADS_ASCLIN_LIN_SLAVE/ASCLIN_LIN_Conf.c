#define SERIAL_BAUDRATE     19200                               /* Set the baud rate                                */

/* Definition of the interrupt priorities */
#define INTPRIO_ASCLIN3_RX   4         /* Define the LIN1 receive interrupt priority */
#define INTPRIO_ASCLIN3_TX   8         /* Define the LIN1 transmit interrupt priority */
#define INTPRIO_ASCLIN3_ER   12

#define INTPRIO_ASCLIN2_RX      14
#define INTPRIO_ASCLIN2_TX      16

#define RXm_PIN              IfxAsclin2_RXD_P10_6_IN             /* Set RXmaster port pin                                  */
#define TXm_PIN              IfxAsclin2_TX_P10_5_OUT             /* Set TXmaster port pin                                  */

#define RXs_PIN              IfxAsclin3_RXE_P00_1_IN             /* Set RXslave port pin                                  */
#define TXs_PIN              IfxAsclin3_TX_P00_0_OUT             /* Set RXslave port pin                                  */

#define MesMas_1            {0x68, 0x65, 0x6C, 0x6C, 0x6F, 0x5F, 0x53, 0x31}
#define MesMas_2            {0x68, 0x65, 0x6C, 0x6C, 0x6F, 0x5F, 0x53, 0x32}
#define SIZE                8                                   /* Size of the message                              */
/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
static IfxAsclin_Lin g_linMaster;
static IfxAsclin_Lin g_linSlave;

uint8 rxData_slave[SIZE];

uint8 IDslave;
uint8 IDmaster;

/* select for polling or interrupt mode. Polling mode can only be used with 2 boards */
boolean Polling_mode = FALSE;

/* counter increment when the parity bits are not correctly calculated */
int err_PID = 0;

uint8 uartMess[] = "\n\rHeader ignored\n\r";
Ifx_SizeT uartSize = sizeof(uartMess);
/*********************************************************************************************************************/
/*----------------------------------------------Function Implementations---------------------------------------------*/
/*********************************************************************************************************************/
/* Adding of the interrupt service routines */
IFX_INTERRUPT(asclin2RxISR, 0, INTPRIO_ASCLIN3_RX);
void asclin2RxISR(void)
 {
     IfxAsclin_Lin_isrReceive(&g_linSlave);
     slaveRX_ISR_process();
 }

IFX_INTERRUPT(asclin2TxISR, 0, INTPRIO_ASCLIN3_TX);
void asclin2TxISR(void)
 {
     IfxAsclin_Lin_isrTransmit(&g_linSlave);
 }

/* This function initializes the ASCLIN LIN module in master mode */
void init_ASCLIN_LIN_master(void)
{
    /* Initialize one instance of IfxAsclin_Lin_Config with default values */
    IfxAsclin_Lin_Config linMasterConfig;
    IfxAsclin_Lin_initModuleConfig(&linMasterConfig, &MODULE_ASCLIN2);

    linMasterConfig.linMode = IfxAsclin_LinMode_master;         /* Set the LIN mode of operation                    */
    linMasterConfig.brg.baudrate = SERIAL_BAUDRATE;             /* Set the desired baud rate                        */
    linMasterConfig.receiveIdEnable = TRUE;

    const IfxAsclin_Lin_Pins pins =
    {
        &RXm_PIN,   IfxPort_InputMode_pullUp,                    /* RX port pin                                      */
        &TXm_PIN,   IfxPort_OutputMode_pushPull,                 /* TX port pin                                      */
        IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    linMasterConfig.pins = &pins;                                /* Port pins configuration                          */

    /* Initialize module */
    IfxAsclin_Lin_initModule(&g_linMaster, &linMasterConfig);
}

/* This function initializes the ASCLIN LIN module in slave mode */
void init_ASCLIN_LIN_slave(void)
{
    /* Initialize one instance of IfxAsclin_Lin_Config with default values */
    IfxAsclin_Lin_Config linSlaveConfig;
    IfxAsclin_Lin_initModuleConfig(&linSlaveConfig, &MODULE_ASCLIN3);

    linSlaveConfig.linMode = IfxAsclin_LinMode_slave;           /* Set the LIN mode of operation                    */
    linSlaveConfig.brg.baudrate = SERIAL_BAUDRATE;              /* Set the desired baud rate                        */
    linSlaveConfig.frame.idleDelay = IfxAsclin_IdleDelay_0;

    /* choose the auto baud rate detection configuration */
    linSlaveConfig.brd.abd = FALSE;
    linSlaveConfig.brd.lowerLimit = 0x00;
    linSlaveConfig.brd.upperLimit = 0xFF;

    const IfxAsclin_Lin_Pins pins =
    {
        &RXs_PIN,   IfxPort_InputMode_pullUp,                    /* RX port pin                                      */
        &TXs_PIN,   IfxPort_OutputMode_openDrain,                /* TX port pin                                      */
        IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    linSlaveConfig.pins = &pins;                                /* Port pins configuration                          */

    /* ISR priorities and interrupt target */
    linSlaveConfig.interrupt.txPriority = INTPRIO_ASCLIN3_TX;
    linSlaveConfig.interrupt.rxPriority = INTPRIO_ASCLIN3_RX;
    linSlaveConfig.interrupt.exPriority = INTPRIO_ASCLIN3_ER;
    linSlaveConfig.interrupt.typeOfService = IfxCpu_Irq_getTos(IfxCpu_getCoreIndex());

    /* linSlaveConfig.isInterruptMode = False: Polling mode or TRUE: enable interrupt*/
    if(Polling_mode)
    {
        linSlaveConfig.isInterruptMode = FALSE;
    }
    else
    {
        linSlaveConfig.isInterruptMode = TRUE;
    }

    /* Initialize module */
    IfxAsclin_Lin_initModule(&g_linSlave, &linSlaveConfig);
    /* Prepare to receive a first header */
    if(Polling_mode == FALSE)
    {
        g_linSlave.linFrameData.rxResponseLength = SIZE;
        IfxAsclin_Lin_prepareHeaderReception(&g_linSlave);
    }
}

/* This function is used by the master to send Header and response frame */
void send_ASCLIN_LIN_message(void)
{
    /* create the response to send */
    uint8 txData[SIZE] = MesMas_1;

    /* send the Header and check the ID send */
    IfxAsclin_Lin_sendHeader(&g_linMaster, &IDmaster);
    /* After the transmission of the LIN header by the master, the master itself starts the transmission
     * of the response if IDmaster = Send_S1*/

    if(g_linMaster.acknowledgmentFlags.txHeaderEnd == 1 && IDmaster == Send_S1)
    {
        IfxAsclin_Lin_sendResponse(&g_linMaster, txData, SIZE);
    }
}

/* This function is used by the slave in Polling mode to receive Header frame and react.
 * In Polling mode comment send_ASCLIN_LIN_message() in main.c*/
void slaveRX_POLLING_process(void)
{
    if(Polling_mode)
    {
        /* Wait for a Header to be received and check the received ID */
        IfxAsclin_Lin_receiveHeader(&g_linSlave, &IDslave);

        /* Remove parity bits */
        if((IDslave & 0x3F) == Send_S1)
        {
            IfxAsclin_Lin_receiveResponse(&g_linSlave, rxData_slave, SIZE);
            send_ASCLIN_UART_message(rxData_slave, SIZE);
            IDslave = 0;
        }
        else
        {
            IfxAsclin_Lin_ignoreHeader(&g_linSlave);
            send_ASCLIN_UART_message(uartMess, uartSize);
            IDslave = 0;
        }
    }
}

/* This function is used by the slave in interrupt mode to receive frame from the master */
void slaveRX_ISR_process(void)
{
    /* Upon triggering the Rx interrupt of the slave the acknowledgmentFlags is check to determine if the received frame is a header or a response */
    if (g_linSlave.acknowledgmentFlags.rxHeaderEnd == 1)
    {
        /* Read and check Header ID */
        IDslave = g_linSlave.linFrameData.headerID;

        /* Remove parity bits */
        if((IDslave & 0x3F) == Send_S1)
        {
            /* prepare the slave to receive response frame */
            IfxAsclin_setHeaderResponseSelect(g_linSlave.asclin, IfxAsclin_HeaderResponseSelect_headerAndResponse);
            IfxAsclin_Lin_prepareResponseReception(&g_linSlave, SIZE);
            g_linSlave.linFrameData.flags.rxResponseInProgress = TRUE;
        }
        else
        {
            /* ignore futur response until next header */
            IfxAsclin_Lin_ignoreHeader(&g_linSlave);
            send_ASCLIN_UART_message(uartMess, uartSize);
        }
        g_linSlave.acknowledgmentFlags.rxHeaderEnd = 0;

    }

    if (g_linSlave.acknowledgmentFlags.rxResponseEnd == 1)
    {
        /* Read master response */
        memcpy(rxData_slave, g_linSlave.linFrameData.rxResponseData, SIZE);
        send_ASCLIN_UART_message(rxData_slave, SIZE);

        /* Prepare the slave to receive new header */
        g_linSlave.acknowledgmentFlags.rxResponseEnd = 0;
        g_linSlave.linFrameData.flags.rxHeaderInProgress = TRUE;

        IfxAsclin_Lin_prepareHeaderReception(&g_linSlave);
    }
}

/*Setter function for master ID*/
void setIDm(uint8 idea)
{
    IDmaster = idea;
}
