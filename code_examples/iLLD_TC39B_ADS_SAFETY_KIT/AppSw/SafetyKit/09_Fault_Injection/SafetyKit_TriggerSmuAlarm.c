/*
 * Inject errors to trigger several SMU alarms
 * */
void safetyKitFaultInjection(IfxSmu_Alarm alarm)
{
    switch (alarm)
    {
        case IfxSmu_Alarm_CPU0_Lockstep_ComparatorError : /* ALM0[0] */
        case IfxSmu_Alarm_CPU1_Lockstep_ComparatorError : /* ALM1[0] */
        case IfxSmu_Alarm_CPU2_Lockstep_ComparatorError : /* ALM2[0] */
        case IfxSmu_Alarm_CPU3_Lockstep_ComparatorError : /* ALM3[0] */
            injectLockstepError(alarm);
            break;

        case IfxSmu_Alarm_EVR_Undervoltage_Alarm : /* ALM9[5] */
            /* The duty cycle in percent was determined by testing with Encoder and observing the duty cycle either with
             * the debugger or scope when the alarm got triggered */
            undervoltageSimulationOverwriteGptTimerVal(69);
            break;

        case IfxSmu_Alarm_HSM_Undervoltage_Alarm : /* ALM9[17] */
            /* The duty cycle in percent was determined by testing with Encoder and observing the duty cycle either with
             * the debugger or scope when the alarm got triggered */
            undervoltageSimulationOverwriteGptTimerVal(68);
            break;

        case IfxSmu_Alarm_DMA_DMASRI_EccError :/* ALM8[23] */
            injectECCErrorDMA();
            break;

        case SOFT_SMU_ALM_SMU :
            /* IfxSmu_Alarm_Software_Alarm1 / ALM10[1] */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_SMU);
            break;

        default:
            break;
    }
}
