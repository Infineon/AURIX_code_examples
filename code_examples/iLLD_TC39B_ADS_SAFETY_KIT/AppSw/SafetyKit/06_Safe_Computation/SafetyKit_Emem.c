/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
#define CRC_EXPECTED_RESULT_EMEM     0xA7EE4C1C              /* EMEM Integrity Check                                 */
#define DATA_LENGTH_WORDS_EMEM       8U                      /* Message data size in words (32-bit)                  */
/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/
const uint32 dummySafetyDataEmem [DATA_LENGTH_WORDS_EMEM] =
{
        0x0e9957bb, 0x1c706c1e, 0x14c3db3f, 0x7fb17a93, 0xb0d9d5a7, 0x768093e0, 0x88b206a0, 0xc51299e4,
};

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void setAllTileConfigModeEMEM(const IfxEmem_TileConfigMode mode);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/*
 * set all tile to particular config mode
 * */
void setAllTileConfigModeEMEM(const IfxEmem_TileConfigMode mode)
{
    MODULE_EMEM.TILECONFIG.U = ((uint32)mode << 30) | ((uint32)mode << 28) | ((uint32)mode << 26) |
                               ((uint32)mode << 24) | ((uint32)mode << 22) | ((uint32)mode << 20) |
                               ((uint32)mode << 18) | ((uint32)mode << 16) | ((uint32)mode << 14) |
                               ((uint32)mode << 12) |((uint32)mode << 10) | ((uint32)mode << 8) |
                               ((uint32)mode << 6) | ((uint32)mode << 4) | ((uint32)mode << 2) | ((uint32)mode << 0);
}

/*
 * Initialize EMEM module
 * */
void initEMEM()
{
    /* Enable Emem Adas Memory */
    while (IfxEmem_isModuleEnabled()==FALSE)
    {
        IfxEmem_setClockEnableState(IfxEmem_State_enabled);
    }

    /* Unlock Emem */
    IfxEmem_setUnlockMode(&MODULE_EMEM);

    /* wait for lock to be unlock */
    while (IfxEmem_getLockedState()==IfxEmem_LockedState_locked);

    /* change tile mode to unused mode */
    setAllTileConfigModeEMEM(IfxEmem_TileConfigMode_unusedMode);

    /* change tile mode to common/calibration memory mode  */
    setAllTileConfigModeEMEM(IfxEmem_TileConfigMode_commonMemoryMode);

    /* Disable ECC_ERR before EMEM initialization */
    uint16 passwd = IfxScuWdt_getCpuWatchdogPassword();
    IfxScuWdt_clearCpuEndinit(passwd);

    /* Disable ECC by configuring MEMCON */
    IfxEmem_disableEccErrorReporting(IfxEmem_MpuIndex_0);
    IfxEmem_disableEccErrorReporting(IfxEmem_MpuIndex_1);
    IfxEmem_disableEccErrorReporting(IfxEmem_MpuIndex_2);
    IfxEmem_disableEccErrorReporting(IfxEmem_MpuIndex_3);

    IfxScuWdt_setCpuEndinit(passwd);

    /* Initialize 4MB EMEM to 0  */
    uint64* pmem;
    uint32  i;
    pmem = (uint64 *)IFXEMEM_START_ADDR_CPU;
    for (i=0; i<(IFXEMEM_SIZE/8); i++)
    {
        *(pmem++) = (uint64)0x0;
    }

    /* Enable ECC_ERR after EMEM initialization  */
    passwd = IfxScuWdt_getCpuWatchdogPassword();
    IfxScuWdt_clearCpuEndinit(passwd);

    /* Enable ECC by configuring MEMCON */
    IfxEmem_enableEccErrorReporting(IfxEmem_MpuIndex_0);
    IfxEmem_enableEccErrorReporting(IfxEmem_MpuIndex_1);
    IfxEmem_enableEccErrorReporting(IfxEmem_MpuIndex_2);
    IfxEmem_enableEccErrorReporting(IfxEmem_MpuIndex_3);

    IfxScuWdt_setCpuEndinit(passwd);
}

/*
 *  SM:EMEM:DATA_INTEGRITY */
/* put data to EMEM
 */
void initDataEMEM(void)
{
    uint32 *ptrEmemStartAdd  = (uint32 *)IFXEMEM_START_ADDR_CPU;

    /* put the dummy data to  EMEM */
    uint32 i;
    for (i=0; i < DATA_LENGTH_WORDS_EMEM; i++)
    {
        *ptrEmemStartAdd++ = dummySafetyDataEmem[i];
    }
}

/* Start the calculation
 * SM:EMEM:DATA_INTEGRITY *
 */
void runDataIntegrityEMEM(void)
{
    uint32 *ememSafetyData = (uint32 * )IFXEMEM_START_ADDR_CPU;

    /* CRC calculation with FCE Kernel 0 (CRC32) */
    runCrcCheckFCE(CRC_EXPECTED_RESULT_EMEM, ememSafetyData, DATA_LENGTH_WORDS_EMEM);
}
