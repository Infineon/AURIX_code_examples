fceCrc g_fceCrc;                  /* Structure to store information                                                  */

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
IFX_INTERRUPT(isrErrorFCE, 0, ISR_PRIORITY_FCE_ER);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* FCE ISR check length & CRC mismatch error, Bus & Configuration Error */
void isrErrorFCE(void)
{
    Ifx_FCE_IN_STS interruptStatus;

    /* Get FCE CRC interrupt status */
    interruptStatus = IfxFce_Crc_getInterruptStatus(&g_fceCrc.fceCrc);

    /* Check if error flags are set */
    if(interruptStatus.U & FCE_CRC_ERROR_MASK != 0x0U)
    {
        /* trigger software alarm  */

        softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_PFLASH);

        /* Clear Error flags */
        IfxFce_Crc_clearErrorFlags(&g_fceCrc.fceCrc);
    }
}

/*
 * Initialization of FCE for data integrity check
 * This function is called from main during initialization phase
 * SM:FCE:CRC_CFG
 */
void initFCECRC()
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Create FCE module configuration */
    IfxFce_Crc_Config fceConfig;
    IfxFce_Crc_initModuleConfig(&fceConfig, &MODULE_FCE);

    /* ISR priorities and interrupt target */
    fceConfig.isrPriority = ISR_PRIORITY_FCE_ER;
    fceConfig.isrTypeOfService = (IfxSrc_Tos)IfxCpu_getCoreIndex();

    /* Initialize module */
    IfxFce_Crc_initModule(&g_fceCrc.fce, &fceConfig);

    /* Initialize CRC kernel with default configuration */
    IfxFce_Crc_CrcConfig crcConfig;
    IfxFce_Crc_initCrcConfig(&crcConfig, &g_fceCrc.fce);

    /* For All CRC calculations */
    /* Enable Interrupt in case of CRC Mismatch */
    crcConfig.enabledInterrupts.crcMismatch = TRUE;
    crcConfig.useDma = TRUE;

    /* Initialize FCE CRC */
    crcConfig.crcKernel = IfxFce_CrcKernel_0;
    crcConfig.fceChannelId = IfxDma_ChannelId_20;
    IfxFce_Crc_initCrc(&g_fceCrc.fceCrc, &crcConfig);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}

/*
 * start FCE CRC calculation , if mismatch ISR will be call
 */
uint32 runCrcCheckFCE(uint32 expectedCrcValue, uint32 *safetyData, uint16 safetyDataLength)
{
    /* CRC calculation with FCE Kernel 0 (CRC32) */
    /* Set CRC expected value */
    g_fceCrc.fceCrc.expectedCrc = expectedCrcValue;

    /* calculate CRC and get resultant value */
    g_fceCrc.crc_result = IfxFce_Crc_calculateCrc(&g_fceCrc.fceCrc, safetyData, safetyDataLength, 0xFFFFFFFF);
    return g_fceCrc.crc_result;
}
