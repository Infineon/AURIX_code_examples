/*
 * SM:STM:MONITOR
 * */
void runSTMMonitoring(IfxCpu_ResourceCpu cpuIndex)
{
#if (IFXCPU_NUM_MODULES < 2)
    #error "The current implementation of the SM:STM:MONITOR requires an AURIX with minimum 2 CPUs"
#endif

#ifdef SAFETYKIT_TRIGGERSTMALARM_H_
    /* Only run the STM monitoring if all STM modules are currently enabled. They might be disabled during
     * STM error injection (triggerStmAlarm).*/
    if(checkIfAnySTMisDisabled())
    {
        return;
    }
#endif

    Ifx_STM *stmX;
    Ifx_STM *stmY;

    switch (cpuIndex)
    {
            case IfxCpu_ResourceCpu_0:
                stmX = &MODULE_STM0;
                stmY = &MODULE_STM1;
                break;
#if (IFXCPU_NUM_MODULES > 1)
            case IfxCpu_ResourceCpu_1:
                stmX = &MODULE_STM1;
#if (IFXCPU_NUM_MODULES > 2)
                stmY = &MODULE_STM2;
#else
                stmY = &MODULE_STM0;
#endif
                break;
#endif
#if (IFXCPU_NUM_MODULES > 2)
            case IfxCpu_ResourceCpu_2:
                stmX = &MODULE_STM2;
#if (IFXCPU_NUM_MODULES > 3)
                stmY = &MODULE_STM3;
#else
                stmY = &MODULE_STM0;
#endif
                break;
#endif
#if (IFXCPU_NUM_MODULES > 3)
            case IfxCpu_ResourceCpu_3:
                stmX = &MODULE_STM3;
#if (IFXCPU_NUM_MODULES > 4)
                stmY = &MODULE_STM4;
#else
                stmY = &MODULE_STM0;
#endif
                break;
#endif
#if (IFXCPU_NUM_MODULES > 4)
            case IfxCpu_ResourceCpu_4:
                stmX = &MODULE_STM4;
#if (IFXCPU_NUM_MODULES > 5)
                stmY = &MODULE_STM5;
#else
                stmY = &MODULE_STM0;
#endif
                break;
#endif
#if (IFXCPU_NUM_MODULES > 5)
            case IfxCpu_ResourceCpu_5:
                stmX = &MODULE_STM5;
                stmY = &MODULE_STM0;
                break;
#endif
            default:
                while (1)
                {};
                break;
    }

    /* In case the STM counter value read out is not plausible, then the application may try reading the STM
     * again (up to N times, application dependent) */
    uint8 repeatNtimes = REPEAT_COUNT;
    while(repeatNtimes)
    {
        /* Compare tick counter of STM module X and STM module Y, raise alarm if deviation is greater than 2 milliseconds */
        /* Get STM counter values */
        uint64 stmXvalue = IfxStm_get(stmX);
        uint64 stmYvalue = IfxStm_get(stmY);

        uint64 stmDifference = stmXvalue > stmYvalue ? stmXvalue - stmYvalue : stmYvalue - stmXvalue;

        if(stmDifference > (2*IFX_CFG_STM_TICKS_PER_MS) )
        {
            /* Try N times, if difference is still too high trigger appropriate reaction. */
            repeatNtimes--;
            if(repeatNtimes == 0)
            {

/* Safety manual recommends an application reset but it is disabled for Safety Kit demonstration */
#if (STM_APP_RESET == 0)
                /* Trigger appropriate reaction */
                softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_STM);
#else
                /* Trigger appropriate reaction */
                safetyKitTriggerSwReset(safetyKitResetTypeApplication);
#endif
            }
        }
        else
        {
            break;
        }
    }
}
