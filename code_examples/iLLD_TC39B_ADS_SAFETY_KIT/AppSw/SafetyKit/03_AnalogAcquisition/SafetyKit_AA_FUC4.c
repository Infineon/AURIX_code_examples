void evadcBrokenWireMissionChIsrAAcqFuc4(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_BRK_WR_CH_ID_AA_FUC4] = getEvadcChannelResult(
            &g_safetyKitEvadcCfgAAcqFuc4.evadcChannel[EVADC_BRK_WR_CH_ID_AA_FUC4]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_BRK_WR_CH_ID_AA_FUC4] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_BRK_WR_CH_ID_AA_FUC4]);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcBrokenWireMeasDoneFlag = TRUE;
}

void evadcSignalOutsideAreaIsr(void)
{
    IfxCpu_enableInterrupts();

    softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC_BND);
}

void initAAcqFuc4BrokenWR(void)
{
    /* Reset initCnt to ignore first measurements during change of the active FUC mode */
    g_analogAcquisitionStatus.initCnt                    = 0;
    g_analogAcquisitionStatus.evadcBrokenWireMeasDoneFlag = FALSE; /* Reset flag */

    g_safetyKitEvadcCfgAAcqFuc4.amountOfGroups          = EVADC_GROUPS_NUM_AA_FUC4;
    g_safetyKitEvadcCfgAAcqFuc4.amountOfChannels        = EVADC_CHANNELS_NUM_AA_FUC4;
    g_safetyKitEvadcCfgAAcqFuc4.evadcGroup              = g_evadcGroupAAcqFuc4;
    g_safetyKitEvadcCfgAAcqFuc4.evadcChannel            = g_evadcChannelAAcqFuc4;
    g_safetyKitEvadcCfgAAcqFuc4.evadcChannelConfig      = g_evadcChannelConfigAAcqFuc4;

    g_safetyKitEdsadcCfgAAcqFuc4.amountOfChannels       = EDSADC_CHANNELS_NUM_AA_FUC4;
    g_safetyKitEdsadcCfgAAcqFuc4.edsadcChannel          = g_edsadcChannelAAcqFuc4;
    g_safetyKitEdsadcCfgAAcqFuc4.edsadcChannelConfig    = g_edsadcChannelConfig_AA_FUC4;

    initCONVCTRL();                                      /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_safetyKitEvadcCfgAAcqFuc4);      /* Initialize the EVADC module */
    initEVADCGroups(&g_safetyKitEvadcCfgAAcqFuc4);      /* Initialize the EVADC group */
    initEVADCChannels(&g_safetyKitEvadcCfgAAcqFuc4);    /* Initialize the EVADC channels */

    initEVADCBrokenWireDetection(&BROKEN_WIRE_POTI_EVADC_PIN, VOLT_TO_10BIT_ADC(BROKEN_WIRE_LOWER_BOUNDAY),
            VOLT_TO_10BIT_ADC(BROKEN_WIRE_UPPER_BOUNDAY), BROKEN_WIRE_CHANNEL_SRC_NR,
            ISR_PROVIDER_EVADC_BRK_WR_BOUNDARY_AA_FUC4, ISR_PRIO_EVADC_BRK_WR_BOUNDARY_AA_FUC4);

    initEDSADCModule(&g_safetyKitEdsadcCfgAAcqFuc4);    /* Initialize the EDSADC module */
    initEDSADCChannels(&g_safetyKitEdsadcCfgAAcqFuc4);  /* Initialize the EDSADC channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or
     * number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_CH_ID_AA_FUC4] = evadcVarefPlausibilityCheck(
            &g_safetyKitEvadcCfgAAcqFuc4, EVADC_VAREF_CH_ID_AA_FUC4, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE,
            g_safetyKitEvadcCfgAAcqFuc4.evadcChannelConfig[EVADC_VAREF_CH_ID_AA_FUC4].queueRequestSourceId);

    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_VAREF_CH_ID_AA_FUC4] = edsadcVarefPlausibilityCheck(
            &g_safetyKitEdsadcCfgAAcqFuc4, EDSADC_VAREF_CH_ID_AA_FUC4, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE);
}

/*
 * Execute EVADC measurement and get the result
 * */
void runEvadcMeasurementAAcqFuc4(SafetyKitEvadcCfgType *evadcCfg)
{
    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_BRK_WR_CH_ID_AA_FUC4],
            evadcCfg->evadcChannelConfig[EVADC_BRK_WR_CH_ID_AA_FUC4].queueRequestSourceId,
            IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_BRK_WR_CH_ID_AA_FUC4].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_BRK_WR_CH_ID_AA_FUC4].queueRequestSourceId);
}

/*
 * Execute EDSADC measurement and get the result
 * */
void runEdsadcMeasurementAAcqFuc4(SafetyKitEdsadcCfgType *edsadcCfg)
{
    /* Get the ESDADC raw value */
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_BRK_WR_CH_ID_AA_FUC4] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_BRK_WR_CH_ID_AA_FUC4]);

    /* Check if only the negative channel is used */
    if ((edsadcCfg->edsadcChannelConfig[EDSADC_BRK_WR_CH_ID_AA_FUC4].posInput == NULL_PTR)  &&
        (edsadcCfg->edsadcChannelConfig[EDSADC_BRK_WR_CH_ID_AA_FUC4].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_BRK_WR_CH_ID_AA_FUC4] *= -1;
    }

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_BRK_WR_CH_ID_AA_FUC4] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_BRK_WR_CH_ID_AA_FUC4]);
}

/*
 * Execute SM:EDSADC:PLAUSIBILITY and SM:EVADC:PLAUSIBILITY
 * */
void plausibilityCheckAAcqFuc4(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Continue only if EVADC measurements is done */
    static uint8 notDoneCnt;
    if(!g_analogAcquisitionStatus.evadcBrokenWireMeasDoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is not
         * successfully measured anymore */
        if(notDoneCnt > 100)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        return;
    }

    /* Reset static notDoneCnt counter */
    notDoneCnt = 0;

    /* Comparison of EVADC mission and EDSADC monitor channels values */
    plausibilityCheck(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_BRK_WR_CH_ID_AA_FUC4],
            g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_BRK_WR_CH_ID_AA_FUC4], MAX_DIFF, UPPER_LIMIT,
            LOWER_LIMIT, SOFT_SMU_ALM_ADC, SOFT_SMU_ALM_ADC); /* [V]*/

    /* Reset measurement done flags */
    g_analogAcquisitionStatus.evadcBrokenWireMeasDoneFlag = FALSE;
}

/*
 * Execute Analog Acquisition Function Use Case 4
 * */
void runAAcqFuc4BrokenWR(void)
{
    runEvadcMeasurementAAcqFuc4(&g_safetyKitEvadcCfgAAcqFuc4);    /* Run EVADC measurement and get results */
    runEdsadcMeasurementAAcqFuc4(&g_safetyKitEdsadcCfgAAcqFuc4);  /* Run EDSADC measurement and get results */

    plausibilityCheckAAcqFuc4();
}
