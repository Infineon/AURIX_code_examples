/* Analog acquisition with redundant EDSADC channels:
 * Redundant safety-related analog signals are delivered by the system and redundantly processed by internal resources of the
 * EDSADC. The results of the redundant processing are transported from the
 * EDSADC to a system volatile memory via the CPU or the DMA and compared by the CPU.
 * Following SMs are required:
 * SM:EDSADC:PLAUSIBILITY
 * SM:EDSADC:VAREF_PLAUSIBILITY
 * SM:EDSADC:DIVERSE_REDUNDANCY
 * SM:CONVCTRL:CONFIG_CHECK
 */

/* Note: The pinning of the port pins used in this example is not compliant with the Common Cause Failure rules. */
/* Note: This example is based on the AURIX Expert Training EDSADC_1_KIT_TC397_TFT */
