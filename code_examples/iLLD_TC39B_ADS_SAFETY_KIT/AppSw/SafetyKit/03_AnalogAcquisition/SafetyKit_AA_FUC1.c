/*
Signal              ||  Port            ||  Detail
RV1 NTC EDSADC          AN3                 channel0 negative
RV2 NTC EDSADC          P00.2               channel5 positive
Varef EDSADC            AN0                 channel3 positive
 */

/* Macros for EDSADC group and channel configuration */
#define EDSADC_RV1_CH_ID    0 /* Array Id */
#define EDSADC_RV2_CH_ID    1 /* Array Id */
#define EDSADC_VAREF_CH_ID  2 /* Array Id */

