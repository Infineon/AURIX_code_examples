/*
 * initial Analog Acquisition Functional Use Case 1
 * */
void initAAcqFuc1(void)
{
    g_analogAcquisitionStatus.initCnt              = 0;          /* Reset initCnt to ignore first measurements during
                                                                    change of active FUC mode */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = FALSE;      /* Reset flag */
    g_analogAcquisitionStatus.evadcRv2MeasDoneFlag = FALSE;      /* Reset flag */
    g_analogAcquisitionStatus.evadcRv1Calibration  = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.evadcRv2Calibration  = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration = 0;          /* Reset calibration values */

    g_safetyKitEdsadcCfg.amountOfChannels     = EDSADC_CHANNELS_NUM_AA_FUC1;
    g_safetyKitEdsadcCfg.edsadcChannel        = g_edsadcChannel;
    g_safetyKitEdsadcCfg.edsadcChannelConfig  = g_edsadcChannelConfig;

    initCONVCTRL();                                     /* Initialize and verify the CONVTRL module */
    initEDSADCModule(&g_safetyKitEdsadcCfg);              /* Initialize the EDSADC module */
    initEDSADCChannels(&g_safetyKitEdsadcCfg);            /* Initialize the channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or
     * number of conversions. */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_VAREF_CH_ID] = edsadcVarefPlausibilityCheck(
            &g_safetyKitEdsadcCfg, EDSADC_VAREF_CH_ID, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE);

    /* Run offset calibration as both NTC might have not exactly the same resistance */
    edsadcStartupCalibration(&g_safetyKitEdsadcCfg);
}

/*
 *  This function gets the result of the most recent conversion
 *  */
void runEdsadcMeasurement(SafetyKitEdsadcCfgType *edsadcCfg)
{
    /* Get the ESDADC raw value */
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV1_CH_ID] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_RV1_CH_ID]);
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_RV2_CH_ID]);

    /* Check if only the negative channel is used */
    if((edsadcCfg->edsadcChannelConfig[EDSADC_RV1_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_RV1_CH_ID].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV1_CH_ID] *= -1;
    }
    if((edsadcCfg->edsadcChannelConfig[EDSADC_RV2_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_RV2_CH_ID].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID] *= -1;
    }

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV1_CH_ID] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV1_CH_ID]);
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV1_CH_ID] -= g_analogAcquisitionStatus.edsadcRv1Calibration;
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID] -= g_analogAcquisitionStatus.edsadcRv2Calibration;

    /* Calculate NTC resistor value */
    float32 resistorValue[2];
    resistorValue[EDSADC_RV1_CH_ID] = VOLT_TO_NTC_R(g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV1_CH_ID]);
    resistorValue[EDSADC_RV2_CH_ID] = VOLT_TO_NTC_R(g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID]);

    /* Calculate the temperature */
    g_analogAcquisitionStatus.edsadcRv1NtcTemp = convertNtcResistanceToDegrees(resistorValue[EDSADC_RV1_CH_ID]);
    g_analogAcquisitionStatus.edsadcRv2NtcTemp = convertNtcResistanceToDegrees(resistorValue[EDSADC_RV2_CH_ID]);
}

/*
 * Function to run the EDSADC plausibility check: SM:EDSADC:PLAUSIBILITY
 * */
void edsadcPlausibilityCheck(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Comparison of mission and monitor channels values */
    plausibilityCheck(g_analogAcquisitionStatus.edsadcRv1NtcTemp, g_analogAcquisitionStatus.edsadcRv2NtcTemp,
            MAX_TEMP_DELTA, MAX_TEMP_NTC, MIN_TEMP_NTC, SOFT_SMU_ALM_ADC, SOFT_SMU_ALM_ADC);
}

/*
 * As there might be a slight difference between both NTC temperature sensors this function executes
 * CALIBRATION_SAMPLE_AMOUNT measurements at the beginning to determine the offset.
 * */
void edsadcStartupCalibration(SafetyKitEdsadcCfgType *edsadcCfg)
{
    /* Accumulate CALIBRATION_SAMPLE_AMOUNT ADC samples */
    float32 accumulationResult[2] = {0, 0};
    for(uint8 sampleCnt = 0; sampleCnt < CALIBRATION_SAMPLE_AMOUNT; sampleCnt++)
    {
        /* Get the ESDADC raw value i.e Negative channel value multiple by -1 */
        accumulationResult[0] += EDSADC_RESULT_TO_V(-1*
                IfxEdsadc_Edsadc_getMainResult(&edsadcCfg->edsadcChannel[EDSADC_RV1_CH_ID]));
        accumulationResult[1] += EDSADC_RESULT_TO_V(
                IfxEdsadc_Edsadc_getMainResult(&edsadcCfg->edsadcChannel[EDSADC_RV2_CH_ID]));
    }

    /* Get the calibration offset */
    float32 calibrationOffset[2] = {0};
    getCalibrationOffset(accumulationResult, CALIBRATION_SAMPLE_AMOUNT, calibrationOffset);

    /* Save calibration values to global structure */
    g_analogAcquisitionStatus.edsadcRv1Calibration = calibrationOffset[0];
    g_analogAcquisitionStatus.edsadcRv2Calibration = calibrationOffset[1];
}

/*
 * Execute Analog Acquisition Functional Use Case 1
 * */
void runAAcqFuc1(void)
{
    runEdsadcMeasurement(&g_safetyKitEdsadcCfg);         /* Run EDSADC measurement and get results */

    /* Run plausibility check by comparing EDSADC RV1 NTC as the mission signal and EDSADC RV2 NTC as the monitoring signal */
    edsadcPlausibilityCheck();
}
