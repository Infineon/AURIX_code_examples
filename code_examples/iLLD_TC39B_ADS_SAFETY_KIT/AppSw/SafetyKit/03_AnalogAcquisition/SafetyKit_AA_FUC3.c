/*
 * ISR for Under voltage simulation
 * */
void evadcUvSim1ChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG1_AA_FUC3] = getEvadcChannelResult(
            &g_safetyKitEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG1_AA_FUC3]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG1_AA_FUC3] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG1_AA_FUC3]);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcUvSimSig1DoneFlag = TRUE;
}

void evadcUvSim2ChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG2_AA_FUC3] = getEvadcChannelResult(
            &g_safetyKitEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG2_AA_FUC3]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG2_AA_FUC3] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG2_AA_FUC3]);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcUvSimSig2DoneFlag = TRUE;
}

/*
 * Initial function for Analog acquisition Function use case 0
 * */
void initAAcqFuc3(void)
{
    g_analogAcquisitionStatus.initCnt                    = 0; /* Reset initCnt to ignore first measurements during change of the active FUC mode */
    g_analogAcquisitionStatus.evadcUvSimSig1DoneFlag     = FALSE; /* Reset flag */
    g_analogAcquisitionStatus.evadcUvSimSig2DoneFlag     = FALSE; /* Reset flag */

    g_safetyKitEvadcCfgAAcqFuc3.amountOfGroups          = EVADC_GROUPS_NUM_AA_FUC3;
    g_safetyKitEvadcCfgAAcqFuc3.amountOfChannels        = EVADC_CHANNELS_NUM_AA_FUC3;
    g_safetyKitEvadcCfgAAcqFuc3.evadcGroup              = g_evadcGroupAAcqFuc3;
    g_safetyKitEvadcCfgAAcqFuc3.evadcChannel            = g_evadcChannelAAcqFuc3;
    g_safetyKitEvadcCfgAAcqFuc3.evadcChannelConfig      = g_evadcChannelConfigAAcqFuc3;

    initCONVCTRL();                                      /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_safetyKitEvadcCfgAAcqFuc3);      /* Initialize the EVADC module */
    initEVADCGroups(&g_safetyKitEvadcCfgAAcqFuc3);      /* Initialize the EVADC group */

    /* Enable GTM ADC Triggering for the two channels. They need to by synchronous */
    initEVADCGtmTrigger(&g_safetyKitEvadcCfgAAcqFuc3, EVADC_UV_SIM_SIG1_G_ID_AA_FUC3,
            IfxEvadc_TriggerMode_uponFallingEdge, IfxEvadc_TriggerSource_9);
    initEVADCGtmTrigger(&g_safetyKitEvadcCfgAAcqFuc3, EVADC_UV_SIM_SIG2_G_ID_AA_FUC3,
            IfxEvadc_TriggerMode_uponFallingEdge, IfxEvadc_TriggerSource_9);

    initEVADCChannels(&g_safetyKitEvadcCfgAAcqFuc3);    /* Initialize the EVADC channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_CH_ID_AA_FUC3] = evadcVarefPlausibilityCheck(
            &g_safetyKitEvadcCfgAAcqFuc3, EVADC_VAREF_CH_ID_AA_FUC3, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE,
            g_safetyKitEvadcCfgAAcqFuc3.evadcChannelConfig[EVADC_VAREF_CH_ID_AA_FUC3].queueRequestSourceId);

    /* Start the two EVADC channels, enable external triggering (via GTM ATOM) and automatic queue refill */
    IfxEvadc_Adc_addToQueue(&g_safetyKitEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG1_G_ID_AA_FUC3],
            g_safetyKitEvadcCfgAAcqFuc3.evadcChannelConfig[EVADC_UV_SIM_SIG1_G_ID_AA_FUC3].queueRequestSourceId,
            EVADC_REFILL_TRIG);

    IfxEvadc_Adc_addToQueue(&g_safetyKitEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG2_G_ID_AA_FUC3],
            g_safetyKitEvadcCfgAAcqFuc3.evadcChannelConfig[EVADC_UV_SIM_SIG2_G_ID_AA_FUC3].queueRequestSourceId,
            EVADC_REFILL_TRIG);
}

/*
 * Function is for plausibility check
 * */
void plausibilityCheckAAcqFuc3(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Continue only if both measurements are done */
    static uint8 notDoneCnt;
    if(!g_analogAcquisitionStatus.evadcUvSimSig1DoneFlag && !g_analogAcquisitionStatus.evadcUvSimSig2DoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is not successfully measured anymore */
        if(notDoneCnt > 100)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);
            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        return;
    }
    /* Reset static notDoneCnt counter */
    notDoneCnt = 0;

    /* Comparison of mission and monitor channels values */
    plausibilityCheck(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG1_AA_FUC3],
            g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG2_AA_FUC3], MAX_DIFF, UPPER_LIMIT,
            LOWER_LIMIT, SOFT_SMU_ALM_ADC, SOFT_SMU_ALM_ADC); /* [V] */

    /* Reset measurement done flags */
    g_analogAcquisitionStatus.evadcUvSimSig1DoneFlag = FALSE;
    g_analogAcquisitionStatus.evadcUvSimSig2DoneFlag = FALSE;
}

/*Execute the analog acquisition FUC 0 */
void runAAcqFuc3(void)
{
    /* Run plausibility check. Could also be called from an EVADC channel ISR */
    plausibilityCheckAAcqFuc3();
}
