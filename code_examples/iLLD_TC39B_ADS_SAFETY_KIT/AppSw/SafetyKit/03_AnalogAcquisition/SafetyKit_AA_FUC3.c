/* Single analog acquisition with EVADC channels
 * Single safety-related analog signal is received on single pin of the MCU and redundantly processed
 * by internal resources of the EVADC. The results of the redundant processing are transported from
 * the EVADC to a system volatile memory via the CPU or the DMA and compared by the CPU.
 * Following SMs are required:
 * SM:EVADC:PLAUSIBILITY
 * SM:EVADC:VAREF_PLAUSIBILITY
 * SM:EVADC:DIVERSE_REDUNDANCY
 * SM:EVADC:CONFIG_CHECK
 *
 * SM:CONVCTRL:CONFIG_CHECK
 */
