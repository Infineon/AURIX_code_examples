/* RV1 NTC sensor EVADC ISR */
void evadcRv1NtcChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV1_CH_ID] = getEvadcChannelResult(
            &g_safetyKitEvadcCfg.evadcChannel[EVADC_RV1_CH_ID]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV1_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID] -= g_analogAcquisitionStatus.evadcRv1Calibration;

    /* Calculate NTC resistor value */
    float32 resistorValue = 0;
    resistorValue = VOLT_TO_NTC_R(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID]);

    /* Calculate resistor value and temperature */
    g_analogAcquisitionStatus.evadcRv1NtcTemp = convertNtcResistanceToDegrees(resistorValue);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = TRUE;
}

/* RV2 NTC sensor EVADC ISR */
void evadcRv2NtcChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV2_CH_ID] = getEvadcChannelResult(
            &g_safetyKitEvadcCfg.evadcChannel[EVADC_RV2_CH_ID]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV2_CH_ID] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV2_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV2_CH_ID] -= g_analogAcquisitionStatus.evadcRv2Calibration;

    /* Calculate NTC resistor value */
    float32 resistorValue;
    resistorValue = VOLT_TO_NTC_R(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV2_CH_ID]);

    /* Calculate resistor value and temperature */
    g_analogAcquisitionStatus.evadcRv2NtcTemp = convertNtcResistanceToDegrees(resistorValue);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcRv2MeasDoneFlag = TRUE;
}

/*
 * initial Analog acquisition FUC 0
 * */
void initAAcqFuc0(void)
{
    /* Reset initCnt to ignore first measurements during change of active FUC mode */
    g_analogAcquisitionStatus.initCnt                = 0;
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag   = FALSE;        /* Reset flag */
    g_analogAcquisitionStatus.evadcRv2MeasDoneFlag   = FALSE;        /* Reset flag */
    g_analogAcquisitionStatus.evadcRv1Calibration    = 0;            /* Reset calibration values */
    g_analogAcquisitionStatus.evadcRv2Calibration    = 0;            /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration   = 0;            /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration   = 0;            /* Reset calibration values */

    g_safetyKitEvadcCfg.amountOfGroups              = EVADC_GROUPS_NUM_AA_FUC0;
    g_safetyKitEvadcCfg.amountOfChannels            = EVADC_CHANNELS_NUM_AA_FUC0;
    g_safetyKitEvadcCfg.evadcGroup                  = g_evadcGroup;
    g_safetyKitEvadcCfg.evadcChannel                = g_evadcChannel;
    g_safetyKitEvadcCfg.evadcChannelConfig          = g_evadcChannelConfig;

    initCONVCTRL();                                                 /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_safetyKitEvadcCfg);                          /* Initialize the EVADC module */
    initEVADCGroups(&g_safetyKitEvadcCfg);                          /* Initialize the EVADC group */
    initEVADCChannels(&g_safetyKitEvadcCfg);                        /* Initialize the channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or
     * number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_CH_ID] = evadcVarefPlausibilityCheck(&g_safetyKitEvadcCfg,
            EVADC_VAREF_CH_ID, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE,
            g_safetyKitEvadcCfg.evadcChannelConfig[EVADC_VAREF_CH_ID].queueRequestSourceId);

    /* Run offset calibration as both NTC might have not exactly the same resistance */
    evadcStartupCalibration(&g_safetyKitEvadcCfg);
}

/*
 * Function to add each channel to the queue and start it
 * Start SM:EVADC:DIVERSE_REDUNDANCY with RV1 NTC temperature sensor and RV2 NTC temperature sensor
 * */
void runEvadcMeasurement(SafetyKitEvadcCfgType *evadcCfg)
{
    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].queueRequestSourceId);

    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV2_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].queueRequestSourceId);
}

/*
 * Function to run the EVADC plausibility check: SM:EVADC:PLAUSIBILITY
 * */
void evadcPlausibilityCheck(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Continue only if both measurements are done */
    static uint8 notDoneCnt;
    if(!g_analogAcquisitionStatus.evadcRv1MeasDoneFlag && !g_analogAcquisitionStatus.evadcRv2MeasDoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is
         * not successfully measured anymore */
        if(notDoneCnt > 100)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        return;
    }
    /* Reset static notDoneCnt counter */
    notDoneCnt = 0;

    /* Comparison of mission and monitor channels values */
    plausibilityCheck(g_analogAcquisitionStatus.evadcRv1NtcTemp, g_analogAcquisitionStatus.evadcRv2NtcTemp,
            MAX_TEMP_DELTA, MAX_TEMP_NTC, MIN_TEMP_NTC, SOFT_SMU_ALM_ADC, SOFT_SMU_ALM_ADC);

    /* Reset measurement done flags */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = FALSE;
    g_analogAcquisitionStatus.evadcRv2MeasDoneFlag = FALSE;
}

/*
 * As there might be a slight difference between both NTC temperature sensors this function executes
 * CALIBRATION_SAMPLE_AMOUNT measurements at the beginning to determine the offset.
 * */
void evadcStartupCalibration(SafetyKitEvadcCfgType *evadcCfg)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Add channel to queue with refill option enabled */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].queueRequestSourceId);

    /* Add channel to queue with refill option enabled */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV2_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].queueRequestSourceId);

    /* Accumulate CALIBRATION_SAMPLE_AMOUNT adc samples */
    float32 accumulationResult[2] = {0, 0};
    for(uint8 sampleCnt = 0; sampleCnt < CALIBRATION_SAMPLE_AMOUNT; sampleCnt++)
    {
        /* Get the EVADC raw value */
        accumulationResult[0] += EVADC_RESULT_TO_V(getEvadcChannelResult(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID]));
        accumulationResult[1] += EVADC_RESULT_TO_V(getEvadcChannelResult(&evadcCfg->evadcChannel[EVADC_RV2_CH_ID]));
    }

    /* Stop and flush the queue */
    IfxEvadc_Adc_clearQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID].queueRequestSourceId);
    IfxEvadc_Adc_clearQueue(evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV2_CH_ID].queueRequestSourceId);

    /* Get the calibration offset */
    float32 calibrationOffset[2] = {0};
    getCalibrationOffset(accumulationResult, CALIBRATION_SAMPLE_AMOUNT, calibrationOffset);

    /* Save calibration values to global structure */
    g_analogAcquisitionStatus.evadcRv1Calibration = calibrationOffset[0];
    g_analogAcquisitionStatus.evadcRv2Calibration = calibrationOffset[1];

    /* Enable interrupts again*/
    IfxCpu_restoreInterrupts(interruptState);
}

/*
 * Execute Analog Acquisition Function use case 0
 * */
void runAAcqFuc0(void)
{
    /* Fill the queue start the measurement */
    runEvadcMeasurement(&g_safetyKitEvadcCfg);

    /* Run plausibility check by comparing EVADC RV1 NTC as the mission signal and EVADC RV2 NTC as
     * the monitoring signal */
    evadcPlausibilityCheck();
}
