#define EVADC_RV1_CH_ID     0 /* Array Id */
#define EVADC_RV2_CH_ID     1 /* Array Id */
#define EVADC_VAREF_CH_ID   2 /* Array Id */

/* Note: If all three EVADC channels are of different groups also three different array IDs are required. */
#define EVADC_RV1_G_ID      0 /* Array Id */
#define EVADC_RV2_G_ID      1 /* Array Id */
#define EVADC_VAREF_G_ID    2 /* Array Id */

