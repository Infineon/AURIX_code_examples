/* Analog acquisition with one EVADC channel and one EDSADC channel
 * Redundant safety-related analog signals are delivered by the system and redundantly
 * processed by internal resources of the EVADC and the EDSADC. The results of the redundant
 * processing are transported from the EVADC and the EDSADC to a system volatile memory via the
 * CPU or the DMA and compared by the CPU.
 * Following SMs are required:
 * SM:EDSADC:PLAUSIBILITY
 * SM:EDSADC:VAREF_PLAUSIBILITY
 * SM:EDSADC:DIVERSE_REDUNDANCY
 *
 * SM:EVADC:PLAUSIBILITY
 * SM:EVADC:VAREF_PLAUSIBILITY
 * SM:EVADC:DIVERSE_REDUNDANCY
 * SM:EVADC:CONFIG_CHECK
 *
 * SM:CONVCTRL:CONFIG_CHECK
 */

