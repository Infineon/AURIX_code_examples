/* ISR for RV1 NTC sensor EVADC */
void evadcRv1NtcChIsrAAcqFuc2(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV1_CH_ID_AA_FUC2] = getEvadcChannelResult(
            &g_safetyKitEvadcCfgAAcqFuc2.evadcChannel[EVADC_RV1_CH_ID_AA_FUC2]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID_AA_FUC2] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_RV1_CH_ID_AA_FUC2]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID_AA_FUC2] -=
            g_analogAcquisitionStatus.evadcRv1Calibration;

    /* Calculate NTC resistor value */
    float32 resistorValue;
    resistorValue = VOLT_TO_NTC_R(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_RV1_CH_ID_AA_FUC2]);

    /* Calculate resistor value and temperature */
    g_analogAcquisitionStatus.evadcRv1NtcTemp = convertNtcResistanceToDegrees(resistorValue);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = TRUE;
}

/*
 * Initial Analog Acquisition Function Use Case 2
 * */
void initAAcqFuc2(void)
{
    g_analogAcquisitionStatus.initCnt              = 0;          /* Reset initCnt to ignore first measurements
                                                                    during change of active FUC mode */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = FALSE;      /* Reset flag */
    g_analogAcquisitionStatus.evadcRv2MeasDoneFlag = FALSE;      /* Reset flag */
    g_analogAcquisitionStatus.evadcRv1Calibration  = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.evadcRv2Calibration  = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration = 0;          /* Reset calibration values */
    g_analogAcquisitionStatus.edsadcRv1Calibration = 0;          /* Reset calibration values */

    g_safetyKitEvadcCfgAAcqFuc2.amountOfGroups        = EVADC_GROUPS_NUM_AA_FUC2;
    g_safetyKitEvadcCfgAAcqFuc2.amountOfChannels      = EVADC_CHANNELS_NUM_AA_FUC2;
    g_safetyKitEvadcCfgAAcqFuc2.evadcGroup            = g_evadcGroupAAcqFuc2;
    g_safetyKitEvadcCfgAAcqFuc2.evadcChannel          = g_evadcChannelAAcqFuc2;
    g_safetyKitEvadcCfgAAcqFuc2.evadcChannelConfig    = g_evadcChannelConfigAAcqFuc2;

    g_safetyKitEdsadcCfgAAcqFuc2.amountOfChannels     = EDSADC_CHANNELS_NUM_AA_FUC2;
    g_safetyKitEdsadcCfgAAcqFuc2.edsadcChannel        = g_edsadcChannel_AA_FUC2;
    g_safetyKitEdsadcCfgAAcqFuc2.edsadcChannelConfig  = g_edsadcChannelConfig_AA_FUC2;

    initCONVCTRL();                                     /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_safetyKitEvadcCfgAAcqFuc2);      /* Initialize the EVADC module */
    initEVADCGroups(&g_safetyKitEvadcCfgAAcqFuc2);      /* Initialize the EVADC group */
    initEVADCChannels(&g_safetyKitEvadcCfgAAcqFuc2);    /* Initialize the EVADC channels */

    initEDSADCModule(&g_safetyKitEdsadcCfgAAcqFuc2);    /* Initialize the EDSADC module */
    initEDSADCChannels(&g_safetyKitEdsadcCfgAAcqFuc2);  /* Initialize the EDSADC channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time
     * or number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_CH_ID_AA_FUC2] = evadcVarefPlausibilityCheck(
            &g_safetyKitEvadcCfgAAcqFuc2, EVADC_VAREF_CH_ID_AA_FUC2, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE,
            g_safetyKitEvadcCfgAAcqFuc2.evadcChannelConfig[EVADC_VAREF_CH_ID_AA_FUC2].queueRequestSourceId);

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time
     * or number of conversions. */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_VAREF_CH_ID_AA_FUC2] = edsadcVarefPlausibilityCheck(
            &g_safetyKitEdsadcCfgAAcqFuc2, EDSADC_VAREF_CH_ID_AA_FUC2, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE);

    startupCalibrationAAcqFuc2(&g_safetyKitEvadcCfgAAcqFuc2, &g_safetyKitEdsadcCfgAAcqFuc2);
}

/*
 * Function to add each channel to the queue and start it
 * */
void runEvadcMeasurementAAcqFuc2(SafetyKitEvadcCfgType *evadcCfg)
{
    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID_AA_FUC2],
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].queueRequestSourceId,
            IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].queueRequestSourceId);
}

/*
 * Function to add each channel to the queue and start it
 * */
void runEdsadcMeasurementAAcqFuc2(SafetyKitEdsadcCfgType *edsadcCfg)
{
    float32 resistorValue[1];

    /* Get the ESDADC raw value */
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID_AA_FUC2] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_RV2_CH_ID_AA_FUC2]);

    /* Check if only the negative channel is used */
    if((edsadcCfg->edsadcChannelConfig[EDSADC_RV2_CH_ID_AA_FUC2].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_RV2_CH_ID_AA_FUC2].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID_AA_FUC2] *= -1;
    }

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID_AA_FUC2] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_RV2_CH_ID_AA_FUC2]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID_AA_FUC2] -=
            g_analogAcquisitionStatus.edsadcRv2Calibration;

    /* Calculate NTC resistor value */
    resistorValue[EDSADC_RV2_CH_ID_AA_FUC2] = VOLT_TO_NTC_R(
            g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_RV2_CH_ID_AA_FUC2]);

    /* Calculate the temperature */
    g_analogAcquisitionStatus.edsadcRv2NtcTemp = convertNtcResistanceToDegrees(
            resistorValue[EDSADC_RV2_CH_ID_AA_FUC2]);
}

/*
 * As there might be a slight difference between both NTC temperature sensors this function executes
 * CALIBRATION_SAMPLE_AMOUNT measurements at the beginning to determine the offset.
 * */
void startupCalibrationAAcqFuc2(SafetyKitEvadcCfgType *evadcCfg, SafetyKitEdsadcCfgType *edsadcCfg)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Add channel to queue with refill option enabled */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID_AA_FUC2],
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].queueRequestSourceId, IFXEVADC_QUEUE_REFILL);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].queueRequestSourceId);

    /* Accumulate CALIBRATION_SAMPLE_AMOUNT adc samples */
    float32 accumulationResult[2] = {0, 0};
    for(uint8 sampleCnt = 0; sampleCnt < CALIBRATION_SAMPLE_AMOUNT; sampleCnt++)
    {
        /* Get the EVADC raw value */
        accumulationResult[0] += EVADC_RESULT_TO_V(
                getEvadcChannelResult(&evadcCfg->evadcChannel[EVADC_RV1_CH_ID_AA_FUC2]));
        accumulationResult[1] += EDSADC_RESULT_TO_V(
                IfxEdsadc_Edsadc_getMainResult(&edsadcCfg->edsadcChannel[EDSADC_RV2_CH_ID_AA_FUC2]));
    }

    /* Stop and flush the queue */
    IfxEvadc_Adc_clearQueue(evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_RV1_CH_ID_AA_FUC2].queueRequestSourceId);

    /* Get the calibration offset */
    float32 calibrationOffset[2] = {0};
    getCalibrationOffset(accumulationResult, CALIBRATION_SAMPLE_AMOUNT, calibrationOffset);

    /* Save calibration values to global structure */
    g_analogAcquisitionStatus.evadcRv1Calibration = calibrationOffset[0];
    g_analogAcquisitionStatus.edsadcRv2Calibration = calibrationOffset[1];

    /* Enable interrupts again*/
    IfxCpu_restoreInterrupts(interruptState);
}

/*
 * Plausibility check of Analog Acquisition Function use case 2
 * */
void plausibilityCheckAAcqFuc2(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Continue only if EVADC measurements is done */
    static uint8 notDoneCnt;
    if(!g_analogAcquisitionStatus.evadcRv1MeasDoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is not successfully
         * measured anymore */
        if(notDoneCnt > 100)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        return;
    }

    /* Reset static notDoneCnt counter */
    notDoneCnt = 0;

    /* Comparison of EVADC mission and EDSADC monitor channels values */
    plausibilityCheck(g_analogAcquisitionStatus.evadcRv1NtcTemp, g_analogAcquisitionStatus.edsadcRv2NtcTemp,
            MAX_TEMP_DELTA, MAX_TEMP_NTC, MIN_TEMP_NTC, SOFT_SMU_ALM_ADC, SOFT_SMU_ALM_ADC);/* [degree celcsis] */

    /* Reset measurement done flags */
    g_analogAcquisitionStatus.evadcRv1MeasDoneFlag = FALSE;
}

/*
 * Execute Analog Acquisition Function Use Case 2
 * */
void runAAcqFuc2(void)
{
    runEvadcMeasurementAAcqFuc2(&g_safetyKitEvadcCfgAAcqFuc2);    /* Run EVADC measurement and get results */
    runEdsadcMeasurementAAcqFuc2(&g_safetyKitEdsadcCfgAAcqFuc2);  /* Run EDSADC measurement and get results */

    plausibilityCheckAAcqFuc2();
}
