/*
 * Handle the 'showtlf' command
 *  showtlf x: show the value of TLF register x
 */
boolean showTLFRegister(pchar args, void *data, IfxStdIf_DPipe *io)
{
    uint32 regNr, uiValue;

    if (Ifx_Shell_matchToken(&args, "?") != FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntax     : showtlf x"ENDL);
        IfxStdIf_DPipe_print(io, "           > Show the value of TLF register x"ENDL);
        return TRUE;
    }
    /* first we get the first argument */
    regNr = 0;
    if (Ifx_Shell_parseUInt32(&args, &regNr, FALSE) == FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid register number %s"ENDL, args);
        return TRUE;
    }
    if (regNr > 0x3A)
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid register number %d"ENDL, regNr);
        return TRUE;
    }

    Tlf35584SpiFrameType spiFrame;
    spiFrame.U = transferDataTLF35584(SpiCommand_read, regNr, (uint8)DUMMY_DATA);

    uiValue = spiFrame.B.data;

    IfxStdIf_DPipe_print(io, "Reg%d : 0x%02X %03d"ENDL, regNr, uiValue, uiValue);

    return TRUE;
}

/*
 * Handle the 'standby' command
 * Syntax
 * standby : switch the TLF35584 to standby state or TLF3068X to disable state if possible (wake not activ)
 */
boolean tflStandbyDisable(pchar args, void *data, IfxStdIf_DPipe *io)
{
    if (Ifx_Shell_matchToken(&args, "?") != FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntax     : standby"ENDL);
        IfxStdIf_DPipe_print(io, "           > Switch TLF35584 to standby or TLF3068X to disable state"ENDL);
    }
    else
    {
        safetyKitTlf35584GotoStandbyPowerdownState();
    }

    return TRUE;
}

