/*
 * enable the driving of FSP0 by the SMU_stdby and checks if configuration has been applied
 */
SmuStatusType configSMUFSPstdbyFSP0(void)
{
    Ifx_PMS_CMD_STDBY CMD_STDBY;

    /* Get current register value
     * Lift write protection for one write access
     * Enable SMU_stdby to set FSP0 into fault state */
    CMD_STDBY.U = PMS_CMD_STDBY.U;
    CMD_STDBY.B.BITPROT = 1;
    CMD_STDBY.B.FSP0EN = 1;

    /* Apply configuration to CMD_stdby register */
    IfxScuWdt_clearSafetyEndinitInline (IfxScuWdt_getSafetyWatchdogPasswordInline ());
    PMS_CMD_STDBY.U = CMD_STDBY.U;
    IfxScuWdt_setSafetyEndinitInline (IfxScuWdt_getSafetyWatchdogPasswordInline ());

    /* BITPROT is always read as a zero, thus the zero here */
    CMD_STDBY.B.BITPROT = 0;

    /* Check if configuration has been applied */
    if(PMS_CMD_STDBY.U == CMD_STDBY.U)
        return pass;
    else
        return fail;
}

