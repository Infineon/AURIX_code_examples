/*
 * Handle the 'trigAlarm' command
 *
 * Syntax
 *  - trigAlarm AG AN: Trigger the SMU alarm AN of group AG
 */
boolean trigSmuAlarm(pchar args, void *data, IfxStdIf_DPipe *io)
{
    uint32 alarmGroup, alarmNumber;

    if (Ifx_Shell_matchToken(&args, "?") != FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntax     : trigAlarm AG AN"ENDL);
        IfxStdIf_DPipe_print(io, "           > Trigger the SMU alarm AN of group AG"ENDL);
        return TRUE;
    }

    /* now we get the first argument (alarm group) */
    if (Ifx_Shell_parseUInt32(&args, &alarmGroup, FALSE) == FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid node %s"ENDL, args);
        return TRUE;
    }
    if (alarmGroup >= (IFXSMU_NUM_ALARM_GROUPS))
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid alarm group %d"ENDL, alarmGroup);
        return TRUE;
    }
    /* Get the second argument (alarm number) */
    if (Ifx_Shell_parseUInt32(&args, &alarmNumber, FALSE) == FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid alarm number %s"ENDL, args);
        return TRUE;
    }
    if (alarmNumber >= (32))
    {
        IfxStdIf_DPipe_print(io, "Syntaxerror : invalid alarm number %d"ENDL, alarmNumber);
        return TRUE;
    }

    IfxSmu_Alarm alarm = (IfxSmu_Alarm)((alarmGroup * 32) + alarmNumber);
    safetyKitFaultInjection(alarm);

    return TRUE;
}

/*
 * print SMU alarm on console
 * */
void consolePrintSmuAlarm(void)
{
    for(uint16 i = g_SafetyKitStatus.smuAlarmPending.alarmCounter; i > 0; i--)
    {
        if(g_SafetyKitStatus.smuAlarmPending.lastAlarmRaised[i-1]->alarmState == pending)
        {
            Ifx_Console_print(ENDL);
            Ifx_Console_print("%s"ENDL, popMessage);
            Ifx_Console_print("%s"ENDL,
                    messagSMUAlarmsName[g_SafetyKitStatus.smuAlarmPending.lastAlarmRaised[i - 1]->alarmConfig->alarm]);
            Ifx_Console_print(ENDL);

            /* Millisecond delay between every print because otherwise the printing got stuck in do while loop in
             * Ifx_Fifo_write. Therefore this function has to be called in infinite while loop (background task) */
            IfxStm_waitTicks(&MODULE_STM0, IfxStm_getTicksFromMilliseconds(&MODULE_STM0, 10));
        }
    }
}

