                RTAC00.B.ALID0 = alarm1 % 32;
                RTAC00.B.GID1 = alarm1 / 32;
                RTAC00.B.ALID1 = alarm1 % 32;
                RTAC01.B.GID2 = alarm1 / 32;
                RTAC01.B.ALID2 = alarm1 % 32;
                RTAC01.B.GID3 = alarm1 / 32;
                RTAC01.B.ALID3 = alarm1 % 32;
