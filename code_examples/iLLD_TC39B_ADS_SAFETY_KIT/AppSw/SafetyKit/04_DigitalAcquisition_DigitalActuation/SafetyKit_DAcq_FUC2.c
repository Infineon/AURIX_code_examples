/* Digital Acquisition with redundant CCU6/GPT12 channels
 * the CCU6 and the GPT12 receives redundant signals, the equality of both processing result shall be checked by the
 * application software if the PWM signal is within the expected range
 *
 * Following SMs are required:
 * SM:CCU6_CAPTURE_MON_BY_GPT12
 *
 * TOM0_4 TOUT9 on pin P00.0 generate dummy PWM which is given to p00.1 and P02.8
 * CCU60 CC60INB on pin P00.9 (CCU6_INPUT_MISSION_PIN) is used to capture PWM signal which is feedback to
 * monitor signal
 * GPT12 T4INA on pin P02.8 (GTP12_MONITOR_PIN) is used as monitoring channel capture
 * compare both capture(ccu6) and captured(gpt12)  periods and the duty cycles of the PWM signal and compare
 *
 * This example is also referred as "CCU6_CAPTURE_MON_BY_GPT12" example
 * */

