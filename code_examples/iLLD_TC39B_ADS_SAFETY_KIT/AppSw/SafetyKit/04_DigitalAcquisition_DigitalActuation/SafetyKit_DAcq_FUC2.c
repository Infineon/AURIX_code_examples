IFX_INTERRUPT(CCU6PeriodMatchIsrDAcqFuc2, ISR_PROVIDER_CCU6_MONITOR_DACQ_FUC1,
        ISR_PRIORITY_CCU6_MONITOR_PM_DACQ_FUC3);

