#define MAX_VAL_T12                     (0xFFFF - 1)    /* Maximum number of ticks between two overflows             */
#define TIM_CCU6_REDUNDANCY_MAX_DIFF    (float32)1.0    /* Tolerance for TIM CCU6 redundancy                         */

