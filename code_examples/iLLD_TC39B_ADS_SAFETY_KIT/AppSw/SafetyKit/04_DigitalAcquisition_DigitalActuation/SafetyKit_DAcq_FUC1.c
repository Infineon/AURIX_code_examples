/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void gtmTimPwmMissionIsrDAcqFuc1(void)
{
    IfxCpu_enableInterrupts();

    IfxGtm_Tim_In_onIsr(&g_driverTIM);
    /* Corner case not covered by IfxGtm_Tim_In_update(driver); --> overflow without NEWVAL notification */
    if(!g_driverTIM.newData)
    {
        g_driverTIM.overflowCnt = IfxGtm_Tim_Ch_isCntOverflowEvent(g_driverTIM.channel);
        if (g_driverTIM.overflowCnt )
        {

            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);
            g_driverTIM.overflowCnt = FALSE;
        }
    }
    else
    {
        if(g_driverTIM.dataCoherent == FALSE)
        {
            /* Duty and period values were not measured from the same period */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);
        }
        g_digitalAcqActStatus.timMissionMeasDoneFlagDacqFuc2 = TRUE;
        measurePWMTIM();
        checkRedundancyTIMCCU6();
    }
}

/* Interrupt Service Routine of the CCU6, which gets triggered by a rising edge of the input port pin of timer T12 */
void ccu6PwmMonitorIsrEdgeDetection(void)
{
    uint32  risingEdgeCntVal = 0;           /* Number of ticks between the last overflow and the rising edge       */
    uint32  fallingEdgeCntVal = 0;          /* Number of ticks between the last overflow and the falling edge      */

    /* Check on the occurrence of the rising edge */
    if (g_icuChannel.ccu6->IS.B.ICC62R)
    {
        /* Reset interrupt status flag */
        IfxCcu6_clearInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc62RisingEdge);

        /* Get the captured rising edge counter value from the counter register */
        risingEdgeCntVal = IfxCcu6_getCaptureRegisterValue(g_icuChannel.ccu6, g_icuChannel.channelId);
    }

    /* Check on the occurrence of the falling edge */
    if (g_icuChannel.ccu6->IS.B.ICC62F)
    {
        /* Reset interrupt status flag */
        IfxCcu6_clearInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc62FallingEdge);

        /* Get the captured falling edge counter value from the shadow register */
        fallingEdgeCntVal = IfxCcu6_getCaptureShadowRegisterValue(g_icuChannel.ccu6, g_icuChannel.channelId);
    }

    /* Define a variable to calculate the final timer counter value */
    uint32 finalFreqCntVal, finalDutyCycCntVal = 0;

    /* If no overflow was detected during the period measurement */
    if(g_cntOverflowPeriod == 0)
    {
        finalFreqCntVal = risingEdgeCntVal - g_previousCntValCcu6;      /* Total counter value calculation        */
    }
    /* If one or more overflows were detected during the period measurement */
    else
    {
        /* Add to the current counter value, the amount of counter ticks which passed before the first overflow,
         * plus 65534 (0xFFFF - 1) for each additional overflow occurred between two rising edges.
         */
        finalFreqCntVal = (uint32) (risingEdgeCntVal + (MAX_VAL_T12 - g_previousCntValCcu6)
                + ((g_cntOverflowPeriod - 1) * MAX_VAL_T12));
    }

    /* If no overflows were detected during signal high level time measurement */
    if(g_cntOverflowHighLevel == 0)
    {
        finalDutyCycCntVal = fallingEdgeCntVal - g_previousCntValCcu6;
    }

    /* If one or more overflows were detected during signal high level time measurement */
    else
    {
        /* Add to the current counter value, the amount of counter ticks which passed before the first overflow,
         * plus 65534 (0xFFFF - 1) for each additional overflow occurred between the rising and the falling edge.
         */
        finalDutyCycCntVal = (uint32) (fallingEdgeCntVal + (MAX_VAL_T12 - g_previousCntValCcu6)
                + ((g_cntOverflowHighLevel - 1) * MAX_VAL_T12));
    }

    /* The PWM frequency is calculated by dividing the captured frequency with the final total counter value
     *      total number of ticks * (1 / CCU6 frequency) = period of the measured signal =>
     *      => frequency of the measured signal = CCU6 frequency / total number of ticks
     */
    g_digitalAcqActStatus.ccu6MonitorMeasDoneFlag = TRUE;
    g_digitalAcqActStatus.ccu6MonitorMeasPwmFreqHz = g_CCU6Frequency_Hz / (float32)finalFreqCntVal;

    /* The PWM duty cycle is calculated by dividing the captured signal high level time by the period */
    g_digitalAcqActStatus.ccu6MonitorMeasPwmDutyCycle = 100 * (finalDutyCycCntVal / (float32)finalFreqCntVal);

    g_previousCntValCcu6 = risingEdgeCntVal;    /* Update g_previousCntValCcu6 for the next calculation         */
    g_cntOverflowPeriod = 0;                /* Reset overflow counter of the period measurement                 */
    g_cntOverflowHighLevel = 0;             /* Reset overflow counter of the signal high level time measurement */
}

/* Interrupt Service Routine of the CCU6, which gets triggered at each period match event for counting overflows*/
void CCU6PeriodMatchIsr(void)
{
    g_cntOverflowPeriod++;

    /* Increments high level time measurement overflows number only if the signal is still high
     * (no falling edge occurrence yet)
     */
    if(IfxCcu6_getInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc62FallingEdge) == FALSE)
    {
        g_cntOverflowHighLevel++;
    }
}

/*
 * Configure the CCU6 for capturing the rising edge of a PWM signal
 * */
void initCCU6ICU(void)
{
    IfxCcu6_Icu ccu6Handle;                                          /* CCU6 handle                          */
    IfxCcu6_Icu_Config ccu6Config;                                   /* Create a configuration structure     */

    IfxCcu6_Icu_initModuleConfig(&ccu6Config, &MODULE_CCU61);        /* Fill the structure with default values */
    IfxCcu6_Icu_initModule(&ccu6Handle, &ccu6Config);                /* Initialize the module                  */

    /* Store the configured captured frequency in a global variable */
    g_CCU6Frequency_Hz = ccu6Handle.captureFrequency;

    IfxCcu6_Icu_ChannelConfig ccu6ChannelConfig;                       /* Create a channel configuration structure */
    IfxCcu6_Icu_initChannelConfig(&ccu6ChannelConfig, &MODULE_CCU61);  /* Fill the structure with default values   */

    ccu6ChannelConfig.channelId = IfxCcu6_T12Channel_2;                /* Select Timer T12 channel 2               */

    /* Select the capture mode: IfxCcu6_T12ChannelMode_doubleRegisterCaptureRisingAndFalling
     * With this particular value, the CCU6 stores the T12 value in the CC6xR register when a rising edge is detected,
     * while it stores the T12 value in the CC6xSR shadow register when a falling edge is detected.
     */
    ccu6ChannelConfig.channelMode = IfxCcu6_T12ChannelMode_doubleRegisterCaptureRisingAndFalling;

    /* Service Request 0 for interrupt at any rising or falling edge */
    ccu6ChannelConfig.interrupt1.source = IfxCcu6_InterruptSource_cc62RisingEdge; /* Select the interrupt source    */
    ccu6ChannelConfig.interrupt1.serviceRequest = IfxCcu6_ServiceRequest_0; /* Select the service request output    */
    ccu6ChannelConfig.interrupt1.priority = ISR_PRIORITY_CCU6_MONITIR_RE_DACQ_FUC1;  /* Configure int priority */
    ccu6ChannelConfig.interrupt1.typeOfService = IRQ_GET_TOS(ISR_PROVIDER_CCU6_MONITOR_DACQ_FUC1);

    /* Service Request 1 for interrupt at T12 period match */
    ccu6ChannelConfig.interrupt2.source = IfxCcu6_InterruptSource_t12PeriodMatch; /* Select the interrupt source    */
    ccu6ChannelConfig.interrupt2.serviceRequest = IfxCcu6_ServiceRequest_1; /* Select the service request output    */
    ccu6ChannelConfig.interrupt2.priority = ISR_PRIORITY_CCU6_MONITIR_PM_DACQ_FUC1; /* Configure the int priority */
    ccu6ChannelConfig.interrupt2.typeOfService = IRQ_GET_TOS(ISR_PROVIDER_CCU6_MONITOR_DACQ_FUC1);

    /* Configure internal start of timer T12 controlled by the run bit T12R */
    ccu6ChannelConfig.trigger.extInputTrigger = NULL_PTR;

    /* CCU6 ICU pin configuration */
    IfxCcu6_Icu_Pins pins =
    {
        NULL_PTR,                   /* CC60In pin                   */
        NULL_PTR,                   /* CC61In pin not used          */
        &CCU6_INPUT_MONITOR_PIN,    /* CC62In pin not used          */
        NULL_PTR,                   /* CCPOS0In pin not used        */
        NULL_PTR,                   /* CCPOS1In pin not used        */
        NULL_PTR,                   /* CCPOS2In pin not used        */
        IfxPort_InputMode_pullUp,   /* CC6x pin input mode          */
        IfxPort_InputMode_pullUp,   /* CCPOSx pin input mode        */
        NULL_PTR,                   /* T12HR input signal not used  */
        NULL_PTR,                   /* T13HR input signal not used  */
        IfxPort_InputMode_pullUp    /* Pad driver mode              */
    };
    ccu6ChannelConfig.pins = &pins;                                     /* Set the configuration of the port pins   */

    ccu6ChannelConfig.multiInputCaptureEnabled = FALSE;                 /* Disable multiple input capture mode      */

    IfxCcu6_Icu_initChannel(&g_icuChannel, &ccu6ChannelConfig);         /* Initialize the channel                   */

    /* Start the timer T12 by setting the run bit T12R with the associated bit T12RS */
    IfxCcu6_Icu_startCapture(&g_icuChannel);
}

/*
 * Initial GTM TIM as mission channel for measuring the PWM
 * */
void initGTMTIM(void)
{
    IfxGtm_enable(&MODULE_GTM);                                         /* Enable the GTM                           */
    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK0);        /* Enable the CMU clock 0                   */

    IfxGtm_Tim_In_Config configTIM;

    IfxGtm_Tim_In_initConfig(&configTIM, &MODULE_GTM);                  /* Initialize default parameters            */
    configTIM.filter.inputPin = &GTM_TIM_MISSION_FUC2;                  /* Select input port pin                    */
    configTIM.filter.inputPinMode = IfxPort_InputMode_pullDown;         /* Select input port pin mode               */
    /* ISR configuration */
    configTIM.capture.irqOnNewVal         = TRUE; /* Trigger interrupt on new value */
    configTIM.capture.irqOnCntOverflow    = TRUE; /* Trigger interrupt on counter overflow */
    configTIM.isrProvider                 = IRQ_GET_TOS(ISR_PROVIDER_GTM_TIM_MISSION_DACQ_FUC1);
    configTIM.isrPriority                 = ISR_PRIORITY_GTM_TIM_MISSION_DACQ_FUC1;
    /* Initialize the TIM */
    boolean success = IfxGtm_Tim_In_init(&g_driverTIM, &configTIM);
    if(!success)
    {
        softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);
        return;
    }

    /* Validate if configuration was written successfully to the registers */
    safetyKitTimInConfigReadback(&configTIM, SOFT_SMU_ALM_GTM_TIM_CCU6);
}

/*
 * Initial GTM TOM to generate a dummy output PWM signal
 * */
void initGTMTOM(void)
{
    IfxGtm_Tom_Pwm_Config tomConfig;                                            /* Timer configuration structure     */
    IfxGtm_Tom_Pwm_Driver tomDriver;                                            /* Timer driver structure            */

    IfxGtm_Tom_Pwm_initConfig(&tomConfig, &MODULE_GTM);                         /* Initialize default parameters     */

    tomConfig.tom = GTM_TOM_SAFE_PIN.tom;                                       /* Select the TOM                    */
    tomConfig.tomChannel = GTM_TOM_SAFE_PIN.channel;                            /* Select the channel                */
    tomConfig.period = TOM_PWM_PERIOD;                                          /* Set timer period                  */
    tomConfig.dutyCycle = (uint32)(TOM_DUTY_CYCLE * 0.01 * tomConfig.period);   /* Set the duty cycle                */
    tomConfig.pin.outputPin = &GTM_TOM_SAFE_PIN;                                /* Set the pin as output             */
    tomConfig.synchronousUpdateEnabled = TRUE;                                  /* Enable synchronous update         */
    tomConfig.clock = IfxGtm_Tom_Ch_ClkSrc_cmuFxclk2;                           /* Select the FXU clock 1            */

    boolean success = IfxGtm_Tom_Pwm_init(&tomDriver, &tomConfig);              /* Initialize the module             */
    if(!success)
    {
        softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);
        return;
    }
    /* Validate if configuration was written successfully to the registers */
    safetyKitTomPwmConfigReadBack(&tomConfig, SOFT_SMU_ALM_GTM_TIM_CCU6);

    /* Start the generation of the PWM signal   */
    IfxGtm_Tom_Pwm_start(&tomDriver, TRUE);
}

/*
 * This function measures the period, the frequency and the duty cycle of the PWM signal
 * */
void measurePWMTIM(void)
{
    IfxGtm_Tim_In_update(&g_driverTIM);                                         /* Update the measured data         */
    g_measuredPwmPeriodTim = IfxGtm_Tim_In_getPeriodSecond(&g_driverTIM);       /* Get the period of the PWM signal */
    /* Calculate the frequency          */
    g_digitalAcqActStatus.timMissionMeasPwmFreqHzDacqFuc2 = 1 / g_measuredPwmPeriodTim;
    /* Get the duty cycle     */
    g_digitalAcqActStatus.timMissionMeasPwmDutyCycleDacqFuc2 = IfxGtm_Tim_In_getDutyPercent(&g_driverTIM,
            &g_dataCoherent);
}

/*
 * SM:GTM_CCU6_REDUNDANCY
 * */
void checkRedundancyTIMCCU6(void)
{
    /* Continue only if both measurements are done */
    static uint8 notDoneCnt;
    if(!g_digitalAcqActStatus.timMissionMeasDoneFlagDacqFuc2 || !g_digitalAcqActStatus.ccu6MonitorMeasDoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two i.e. TIM or CCU6,
         * is not successfully measured anymore */
        if(notDoneCnt > 100)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        return;
    }
    /* Reset static notDoneCnt counter */
    notDoneCnt = 0;

    /* Difference validation */
    float32 freqDifference = 0, dutyCycleDifference = 0;

    freqDifference =
            (g_digitalAcqActStatus.timMissionMeasPwmFreqHzDacqFuc2 < g_digitalAcqActStatus.ccu6MonitorMeasPwmFreqHz) ?
                    (g_digitalAcqActStatus.ccu6MonitorMeasPwmFreqHz
                            - g_digitalAcqActStatus.timMissionMeasPwmFreqHzDacqFuc2) :
                    (g_digitalAcqActStatus.timMissionMeasPwmFreqHzDacqFuc2
                            - g_digitalAcqActStatus.ccu6MonitorMeasPwmFreqHz);
    dutyCycleDifference =
            (g_digitalAcqActStatus.timMissionMeasPwmDutyCycleDacqFuc2
                    < g_digitalAcqActStatus.ccu6MonitorMeasPwmDutyCycle) ?
                    (g_digitalAcqActStatus.ccu6MonitorMeasPwmDutyCycle
                            - g_digitalAcqActStatus.timMissionMeasPwmDutyCycleDacqFuc2) :
                    (g_digitalAcqActStatus.timMissionMeasPwmDutyCycleDacqFuc2
                            - g_digitalAcqActStatus.ccu6MonitorMeasPwmDutyCycle);

    /* Appropriate reaction if difference is higher than the limit */
    if((freqDifference > TIM_CCU6_REDUNDANCY_MAX_DIFF) || (dutyCycleDifference > TIM_CCU6_REDUNDANCY_MAX_DIFF))
    {
        /* Trigger SMU software alarm */
        softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_GTM_TIM_CCU6);
    }

    /* Reset measurement done flags */
    g_digitalAcqActStatus.timMissionMeasDoneFlagDacqFuc2 = FALSE;
    g_digitalAcqActStatus.ccu6MonitorMeasDoneFlag = FALSE;
}

/*
 * SM:GTM_CCU6_REDUNDANCY
 * */
void initDAcqFuc1(void)
{
    resetDAconfiguration();

    /* Part 1: Initialize SM:TIM_CLOCK_MONITORING */
    initEclkMonitoring();

    /* Part 2: Configure GTM TOM for the dummy output PWM signal by the TOMs */
    initGTMTOM();

    /* Part 3: Configure GTM TIM as mission channel for measuring the generated dummy PWM signal */
    initGTMTIM();

    /* Part 4: Configure CCU6 as monitor channel for measuring the generated dummy PWM signal */
    initCCU6ICU();
}
