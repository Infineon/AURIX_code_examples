/*
 * This function initializes the STM for generating an interrupt every milliseconds used for basic periodically
 * tasks scheduling.
 */
void initStm(AppStm *lclAppStm, IfxCpu_ResourceCpu cpuIndex)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Initialize configuration-structure with defaults. */
    IfxStm_initCompareConfig(&lclAppStm->stmConfig);

    /* Change the comparator configuration */
    switch (cpuIndex)
    {
        case IfxCpu_ResourceCpu_0:
            lclAppStm->stmSfr = &MODULE_STM0;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu0;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#if (IFXCPU_NUM_MODULES > 1)
        case IfxCpu_ResourceCpu_1:
            lclAppStm->stmSfr = &MODULE_STM1;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu1;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 2)
        case IfxCpu_ResourceCpu_2:
            lclAppStm->stmSfr = &MODULE_STM2;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu2;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 3)
        case IfxCpu_ResourceCpu_3:
            lclAppStm->stmSfr = &MODULE_STM3;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu3;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 4)
        case IfxCpu_ResourceCpu_4:
            lclAppStm->stmSfr = &MODULE_STM4;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu4;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 5)
        case IfxCpu_ResourceCpu_5:
            lclAppStm->stmSfr = &MODULE_STM5;
            lclAppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu5;
            lclAppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
        default:
            while (1)
            {};
            break;
    }

    lclAppStm->stmConfig.comparatorInterrupt   = IfxStm_ComparatorInterrupt_ir0; /* Select the interrupt output */
    lclAppStm->stmConfig.ticks                 = IFX_CFG_STM_TICKS_PER_MS*10;    /* Start after 10ms */

    /* Suspend by debugger enabled */
    IfxStm_enableOcdsSuspend(lclAppStm->stmSfr);

    /* Now Initialized the comparator */
    IfxStm_initCompare(lclAppStm->stmSfr, &lclAppStm->stmConfig);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}
