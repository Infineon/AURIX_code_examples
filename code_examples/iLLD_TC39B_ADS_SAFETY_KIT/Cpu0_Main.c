void core0_main(void)
{
    /*
     * !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdog in the demo if it is required and also service the watchdog periodically
     * */
    IfxScuWdt_disableCpuWatchdog (IfxScuWdt_getCpuWatchdogPassword ());
    IfxScuWdt_disableSafetyWatchdog (IfxScuWdt_getSafetyWatchdogPassword ());

    /* Execute the sequence of safety mechanisms when coming from an unpowered state. */
    runSafeAppSwStartup();

    /* Cpu sync event wait, we wait until all CPUs are in corex_main to avoid initialization problems of variables */
    /* this is CPU0 which initialize all variables, expected is that we will be here as latest CPU */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 100);

    /* Initialize the application state */
    initAppState(&g_AppCpu0, (IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* Initialize STM for generating an interrupt every milliseconds for basic scheduling. */
    initStm(&g_AppStm0, (IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());

    /* Initialization of hardware and peripherals on Application Kit
     * Note: Interrupts might get disabled for first 50 loops, they will be enabled in run_AppKit_endless_loop() again.*/
    initAppKit((IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());

    /* Initialization of several Safety Kit implementations and features */
    initSafetyKit();

    /* Wait until SafetyKit initialization is done */
    while(!g_SafetyKitStatus.safetyKitInitDone)
    {
        __nop();
    }

#if SAFETYKIT_CFG_INIT_CPU_WDT
    /* Initialize the CPU Watchdog WDTCPU */
    initCpuWatchdog((IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());
#endif /* SAFETYKIT_CFG_INIT_CPU_WDT */

#if CPU_WHICH_RUN_SAFETY_WDT == 0 && SAFETYKIT_CFG_INIT_SAFETY_WDT
    /* Initialize the Safety Watchdog WDTS. */
    initSafetyWatchdog();
#endif /* CPU_WHICH_RUN_SAFETY_WDT == 0 && SAFETYKIT_CFG_INIT_SAFETY_WDT */

    /* background endless loop */
    while (1)
    {
        /* Background endless loop for several tasks and processes on Application Kit */
        runAppKitEndlessLoop((IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());

        /* Background endless loop for several tasks and processes off Safety Kit */
        runSafetyKitEndlessLoopCpu0();
    }
}

/*
 * This interrupt is raised by the STM0. The initialization is done by initStm().
 * isrProvider CPU0 and isrPriority ISR_PRIORITY_OS_TICK
 */
void userIsrSTM0()
{
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM0, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    g_AppCpu0.TickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* Return if Safety Kit initialization is not yet finished */
    if(!g_SafetyKitStatus.safetyKitInitDone)
    {
        return;
    }

    /* action to do each 1ms */
    /* Execute several periodic tasks implemented and used for Safety Kit */
    runSafetyKitStmIsr(&g_AppCpu0, (IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());
}
