#if defined(__TASKING__)
/*!
 * \brief   Initializes C variables
 *
 * This function is called in the startup. This function initialize the all variables in .data section
 * and clears the .bss section
 *
 * Parameters: Nil
 * Return: Nil
 */
void Ifx_C_Init(void)
{
    extern void _c_init(void);
    extern void _c_init_tc1(void);
    extern void _c_init_tc2(void);
    extern void _c_init_tc3(void);
    extern void _c_init_tc4(void);
    extern void _c_init_tc5(void);

    switch(__mfcr (CPU_CORE_ID))
    {
    case 0:
        _c_init();                  /* initialize global data and local for CPU0 */
        break;
#if (IFXCPU_NUM_MODULES>1)
    case 1:
        _c_init_tc1();              /* initialize global data and local for CPU1 */
        break;
#endif
#if (IFXCPU_NUM_MODULES>2)
    case 2:
        _c_init_tc2();              /* initialize global data and local for CPU2 */
        break;
#endif
#if (IFXCPU_NUM_MODULES>3)
    case 3:
        _c_init_tc3();              /* initialize global data and local for CPU3 */
        break;
#endif
#if (IFXCPU_NUM_MODULES>4)
    case 4:
        _c_init_tc4();              /* initialize global data and local for CPU4 */
        break;
#endif
#if (IFXCPU_NUM_MODULES>5)
    case 6:
        _c_init_tc5();              /* initialize global data and local for CPU5 */
        break;
#endif
    default:
        /* this is an invalid cpu, we do nothing */
        return;
    }
}


#endif
