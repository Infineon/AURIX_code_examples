/**
 ******************************************************************************
 * The routine void ISR_Stm0(void)
 *
 * This routine is the ISR for the system timer 0 event
 ******************************************************************************
 */
void ISR_Stm0()
{
    Ifx_STM *stm = &MODULE_STM0;
    IfxStm_updateCompare(stm, IfxStm_Comparator_0, IfxStm_getLower(stm) + IFX_CFG_STM_TICKS_PER_MS);
    Core0_Stm_1ms++;
    TaskCpu0_1ms();
}

