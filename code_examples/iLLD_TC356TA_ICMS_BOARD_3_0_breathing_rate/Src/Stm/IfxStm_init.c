void IfxStm_Init(void)
{
    IfxStm0_Init();
    IfxStm1_Init();
    IfxStm2_Init();
}

/*
 ******************************************************************************
 * The routine void IfxStm0_init(void)
 *
 * This routine initializes the System Timer 0
 ******************************************************************************
 */
void IfxStm0_Init(void)
{
    Ifx_STM             *stm = &MODULE_STM0;
    IfxStm_CompareConfig stmCompareConfig;

    /* suspend by debugger enabled*/
    IfxStm_enableOcdsSuspend(stm);

    /*Call the constructor of configuration*/
    IfxStm_initCompareConfig(&stmCompareConfig);
    /*Modify only the number of ticks and enable the trigger output*/
    stmCompareConfig.ticks           = IFX_CFG_STM_TICKS_PER_MS;
    stmCompareConfig.triggerPriority = ISR_PRIORITY_STM0;
    stmCompareConfig.typeOfService   = ISR_PROVIDER_CPU0;

    /*Now Compare functionality is initialized*/
    IfxStm_initCompare(stm, &stmCompareConfig);
}

