/*
 ******************************************************************************
 * The  routine void IfxRmem_Init(void)
 * This routine initializes the full RMEM for ADAS Application
 ******************************************************************************
 */
void IfxRmem_Init(){
    /* Local Variables */
    Radar *rdr = &g_rdr;
    uint8 iTile;

    rdr->emem = &MODULE_EMEM;

    /* Enable Radar Memory
    Disable ECC_ERR mechanism before initialization */
    IfxScuWdt_clearCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());
    while (IfxEmem_isModuleEnabled()==FALSE){
        IfxEmem_setClockEnableState(IfxEmem_State_enabled);
    }
    IfxScuWdt_setCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());

    /* Unlock Emem */
    IfxEmem_setUnlockMode(rdr->emem);

    for (iTile=0;iTile<8;iTile++){
        IfxEmem_setTileConfigMode(IfxEmem_TileConfigMode_unusedMode, iTile);
        IfxEmem_setTileConfigMode(IfxEmem_TileConfigMode_commonMemoryMode, iTile);
    }

    IfxMtu_RmemInit();

    rdr->rmem = (SPU_Rmem_Map_t*)IFXEMEM_START_ADDR_CPU;

}
