#define FLT_EPSILON_DEF     (1.19209290E-07F)

/* SCM */
csint16 scme_Xnn[RADAR_NUM_RAMPS/2][RADAR_NUM_VRX_AZIMUTH]; /* ramps/2 due to TDM */
csint16 scme_Xn[RADAR_NUM_VRX_AZIMUTH];
float32 scme_G[(RADAR_NUM_RX-1)*(RADAR_NUM_RX-1)*2*2];     /* G in a linear vector representation where G = (Re Im, -Im Re) */
float32 scme_Ginv[(RADAR_NUM_RX-1)*(RADAR_NUM_RX-1)*2*2];     /* G in a linear vector representation where G = (Re Im, -Im Re) */

/*generates a filtered covariance matrix for one range bin*/
void SCM(uint32 bin) /* uses only a single ramp*/
{
    Radar * rdr = &g_rdr;

    /* Xn Estimation for the bin selected */

        uint32 ramp = RADAR_NUM_MICRO_DOPPLER-1;  /* use only a single ramp*/
        uint8 index_vRx_azimuth[RADAR_NUM_VRX_AZIMUTH] = {2, 4, 5, 7}; /* index of azimuth virtual  antennas combined from Tx1 and Tx2 to virtual array*/
        for (uint32 j = 0; j < RADAR_NUM_VRX_AZIMUTH; j++)
        {   /* antenna index in 8 element vector {2,4,5,7};*/
            volatile uint8 indexvirtantenna = index_vRx_azimuth[j];
            scme_Xn[j].real = rdr->rmem->microDopplerclutterremoved[ramp][indexvirtantenna][bin].real;
            scme_Xn[j].imag = rdr->rmem->microDopplerclutterremoved[ramp][indexvirtantenna][bin].imag;

        }


    /* Spatial Covariance Matrix Estimation Rn = Xn * Xn' */
    for (uint32 i = 0; i < RADAR_NUM_VRX_AZIMUTH; i++)
    {
        for (uint32 j = 0; j < RADAR_NUM_VRX_AZIMUTH; j++)
        {
           rdr->rmem->Rn[bin][i][j].real  = (scme_Xn[i].real *scme_Xn[j].real) + (scme_Xn[i].imag *scme_Xn[j].imag); /* changed sign in complex product to account for conjugate transpose*/
           rdr->rmem->Rn[bin][i][j].imag = -(scme_Xn[i].real *scme_Xn[j].imag) + (scme_Xn[i].imag *scme_Xn[j].real); /* changed sign in complex product to account for conjugate transpose*/
        }
    }


    /* adding spatial smoothing */
    csint32 R_ss[RADAR_NUM_VRX_AZIMUTH-1][RADAR_NUM_VRX_AZIMUTH-1];
    /* set R_ss to zero*/
    for (int i = 0; i < RADAR_NUM_VRX_AZIMUTH-1; i++) {
        for (int j = 0; j < RADAR_NUM_VRX_AZIMUTH-1; j++) {
            R_ss[i][j].real = 0;
            R_ss[i][j].imag = 0;
        }
    }
    /* fill with smoothed values */
    for (int k = 0; k < 2; k++) {
        for (int i = 0; i < (RADAR_NUM_VRX_AZIMUTH-1); i++) {
            for (int j = 0; j < (RADAR_NUM_VRX_AZIMUTH-1); j++) {
                R_ss[i][j].real += rdr->rmem->Rn[bin][k+i][k+j].real;
                R_ss[i][j].imag += rdr->rmem->Rn[bin][k+i][k+j].imag;
            }
        }
    }

    /* adding forward backward filtering */
    csint32 R_fb[RADAR_NUM_VRX_AZIMUTH-1][RADAR_NUM_VRX_AZIMUTH-1];

    /* set R_fb to zero */
    for(int i = 0; i < (RADAR_NUM_VRX_AZIMUTH-1); i++) {
        for(int j = 0; j < (RADAR_NUM_VRX_AZIMUTH-1); j++) {
            R_fb[i][j].real = 0;
            R_fb[i][j].imag = 0;

        }
    }

    /* Combined transpose and 180-degree rotation in single pass */
    int k;
    for (k = 1; k <= 2; k++) {
        int i, j;
        for (i = 1; i < (RADAR_NUM_VRX_AZIMUTH); i++) {
            for (j = 1; j < (RADAR_NUM_VRX_AZIMUTH); j++) {
                R_fb[i-1][j-1].real += rdr->rmem->Rn[bin][RADAR_NUM_VRX_AZIMUTH-j-1+(k-1)][RADAR_NUM_VRX_AZIMUTH-i-1+(k-1)].real;
                R_fb[i-1][j-1].imag += rdr->rmem->Rn[bin][RADAR_NUM_VRX_AZIMUTH-j-1+(k-1)][RADAR_NUM_VRX_AZIMUTH-i-1+(k-1)].imag;
            }
        }
    }


    /* combine spatial smoothing and forward backward filtering */
    for (int i = 0; i < (RADAR_NUM_VRX_AZIMUTH-1); i++) {
        for (int j = 0; j < (RADAR_NUM_VRX_AZIMUTH-1); j++) {
            rdr->rmem->Rn_filtered[bin][i][j].real= R_ss[i][j].real +R_fb[i][j].real;
            rdr->rmem->Rn_filtered[bin][i][j].imag= R_ss[i][j].imag +R_fb[i][j].imag;
        }
    }

}


/*generates covariance matrices for all range bins*/
void SCM_Generator(uint32 nbbin)
{
    for (uint32 n = 0; n < nbbin; n++)
    {
       SCM(n); /* Each call generates a Spatial Covariance Matrix*/
    }
}

/*********************************************************************************************************\
*    calculates the inverse of a complex valued matrix
*    reference :  https://ieeexplore.ieee.org/document/1100466                                            *
*                                                                                                         *
*    creating (2*mtxsize) by (2*mtxsize) matrix                                                                                  *
*                                                                                                         *
\*********************************************************************************************************/
void ComplexMatrixInversion(uint32 mtxnumber, uint32 mtxsize)
{
    Radar * rdr = &g_rdr;


    for (uint32 bin = 0; bin < mtxnumber ; bin++) /*RADAR_NUM_SAMPLES/2*/
    {
        uint32 n = 0;
        /* setup G so that G= (Re , Im ; -Im , Re) */
        for (int i = 0; i < mtxsize; i++) {
                for (int j = 0; j < mtxsize; j++) {
                    if(i==j){
                        scme_G[n++] = rdr->rmem->Rn_filtered[bin][i][j].real+20*FLT_EPSILON_DEF; /*add reg term to diagonal prior to inversion*/
                    }
                    else{
                        scme_G[n++] = rdr->rmem->Rn_filtered[bin][i][j].real;
                    }
                }
                for (int j = 0; j < mtxsize; j++) {
                    scme_G[n++] = rdr->rmem->Rn_filtered[bin][i][j].imag;
                }
            }

            for (int i = 0; i < mtxsize; i++) {
                for (int j = 0; j < mtxsize; j++) {
                    scme_G[n++] = -rdr->rmem->Rn_filtered[bin][i][j].imag;
                }
                for (int j = 0; j < mtxsize; j++) {
                    if(i==j){
                        scme_G[n++] = rdr->rmem->Rn_filtered[bin][i][j].real+20*FLT_EPSILON_DEF; /*add reg term to diagonal prior to inversion*/
                    }
                    else{
                        scme_G[n++] = rdr->rmem->Rn_filtered[bin][i][j].real;
                    }
                }
            }


        /* Calculating G^(-1) */
        struct Ifx_mtxInvRealF32State state0 = {IFX_MODE_REFERENCE_IMPLEMENTATION, scme_Ginv, scme_G, mtxsize*2};
        Ifx_mtxInvRealF32(&state0);


        /* retrieve inverse Matrix real and imaginary parts according to Ginv = (Re Im, -Im Re) */
        n = 0;
        for (int i = 0; i < mtxsize; i++)
        {
            for (int j = 0; j < mtxsize; j++)
                {
                    rdr->rmem->Rnn[bin][i][j].real = scme_Ginv[n++];
                }
            for (int j = 0; j < mtxsize; j++)
                {
                    rdr->rmem->Rnn[bin][i][j].imag = scme_Ginv[n++];
                }
        }
    }
}
