/** \brief TOM Timer interface
 */
typedef struct
{
    Ifx_EGTM                   *egtm;                   /**< \brief EGTM module used for the timer functionality */
    Ifx_EGTM_CLS_ATOM          *atom;                   /**< \brief ATOM used for the timer functionality */
    Ifx_EGTM_CLS_ATOM_AGC      *agc;                    /**< \brief Pointer to the AGC object */
    IfxEgtm_Cluster             clsIndex;               /**< \brief Enum for CLS objects */
    IfxEgtm_Atom_Ch             timerChannel;           /**< \brief ATOM channel used for the timer */
    IfxEgtm_Atom_Ch             triggerChannel;         /**< \brief ATOM channel used for the trigger, if identical to the timerChannel, the trigger interrupt is having the same interrupt level as  the timer interrupt */
    uint16                      channelsMask;           /**< \brief Mask for channels to be modified together */
    uint32                      offset;                 /**< \brief Timer initial offset in ticks */
    Ifx_EGTM_CLS_CDTM_DTM      *dtm;                    /**< \brief Pointer to DTM object used by ATOM */
    IfxEgtm_Dtm_Ch              dtmChannel;             /**< \brief DTM channel */
    uint32                      agcDisableUpdate;       /**< \brief AGC value for disable update */
    uint32                      agcApplyUpdate;         /**< \brief AGC value for apply update */
    uint32                      period;                 /**< \brief Timer period in ticks (cached value) */
    boolean                     triggerEnabled;         /**< \brief If TRUE, the trigger functionality is initialized */
    float32                     clockFreq;              /**< \brief Timer input clock frequency (cached value) */
    IfxEgtm_Atom_Timer_CountDir countDir;               /**< \brief Timer counting mode */
} IfxEgtm_Atom_Timer;

/** \brief Configuration structure for TOM Timer
 */
typedef struct
{
    Ifx_EGTM                    *egtm;                 /**< \brief EGTM used for the timer functionality */
    IfxEgtm_Cluster              cluster;              /**< \brief CLS used for the timer functionality */
    IfxEgtm_Atom_Ch              timerChannel;         /**< \brief ATOM channel used for the timer */
    IfxEgtm_Atom_ToutMap        *triggerOut;           /**< \brief ATOM channel used for the trigger output, can be identical to the timer channe */
    IfxEgtm_Atom_Ch_ClkSrc       clock;                /**< \brief Timer input clock */
    IfxEgtm_Dtm_ClockSource      dtmClockSource;       /**< \brief DTM clock source */
    float32                      frequency;            /**< \brief PWM frequency in Hz. This parameter is only used to initialize the timer structure. An additional cell is required to build the timer. */
    float32                      minResolution;        /**< \brief Minimum resolution of the timer in seconds. if 0, this parameter is ignored. If the configuration does not enable this setting a warning is given */
    float32                      startOffset;          /**< \brief FIXME make startOffset as Ifx_TimerValue. Timer initial offset in % of the period */
    IfxEgtm_Atom_Timer_CountDir  countDir;             /**< \brief Timer counting mode */
    IfxEgtm_Atom_Timer_Interrupt interrupt;            /**< \brief Interrupt configuration */
    IfxEgtm_Atom_Timer_Trigger   trigger;              /**< \brief trigger configuration */
    boolean                      initPins;             /**< \brief TRUE: Initialize pins in driver
                                                        * FALSE: Don't initialize pins in driver : user handles separately */
} IfxEgtm_Atom_Timer_Config;

