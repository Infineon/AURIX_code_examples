 /*\title Watchdog configuration
 * \abstract The watchdog is serviced with correct password in order to prevent a reset.
 * \description This example shows how to initialize and service the CPU0 Watchdog.
 *              The Watchdog is configured to trigger a reset after approximately 1.3s if not serviced.
 *              Pressing the on-board button stops the Watchdog from being serviced by the CPU, which then resets the device when the Watchdog timer expires.
 *              This example also shows how to implement logical monitoring using or without using automatic password sequencing provided by WDT. In this case if a button is pressed a wrong password is provided to WDT which then resets the device.
 *
 * \name iLLD_TC4D7_LK_ADS_Watchdog
 * \version V1.0.0
 * \board AURIX TC4D7 lite Kit, KIT_A3G_TC4D7_LITE, TC4D7XP_A-Step_CC_COM
 * \keywords AURIX, SCU, WDT, Watchdog_1, system control unit, timer, watchdog
 * \documents See README.md
 * \lastUpdated 2025-05-22
 *********************************************************************************************************************/
#include "Ifx_Types.h"
#include "Ifx_Cfg.h"
#include "IfxCpu.h"
#include "IfxWtu.h"
#include "Watchdog.h"
#include "IfxCbs_reg.h"
#include "Bsp.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define REL_VAL     0xE000                                              /* Reload value for extended timeout window */
#define WAIT_TIME   80                                                 /* Wait time constant in milliseconds   */
volatile uint8 serviceWdt = TRUE;
void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword()); */               /* Set CPU0 WD timer active     */
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());

    /* Initialization of the button and LEDs used in this example */
    initGPIOs();

    /* Initialization of the Watchdog */
    initWatchdog(REL_VAL);

    serviceWdt = TRUE;                   /* Variable used to decide whether to service the Watchdog or not   */


#if ( ( AUTOMATIC_PASSWORD_SEQUENCING == 0 ) && ( MANUAL_PASSWORD_SEQUENCING == 0 ) )
/* Temporal Monitoring only */
    while(1)
    {
        /* Check the button state */
        if(isButtonPressed() == TRUE)
        {
            serviceWdt = FALSE;                     /* Stop servicing the Watchdog if the button has been pressed   */
            IfxPort_setPinLow(LED2);                /* Turn on the LED2 to signal that the button has been pressed  */
        }

        /* Service the Watchdog only if the button has not been pressed yet */
        if(serviceWdt == TRUE)
        {
            IfxWtu_serviceCpuWatchdog(IfxWtu_getCpuWatchdogPassword());             /* Continuously service CPU0WDT */
        }

        IfxPort_togglePin(LED1);                                                    /* Toggle the state of the LED  */

    }
#endif

#if ( ( AUTOMATIC_PASSWORD_SEQUENCING == 1 ) && ( MANUAL_PASSWORD_SEQUENCING == 0 ) )

     volatile uint8 jump_code = FALSE;
     volatile uint16 wdt_pwd_table[8];

    wdt_pwd_table[0]=IfxWtu_calculateLfsr(IfxWtu_getCpuWatchdogPassword());
    wdt_pwd_table[1]=IfxWtu_calculateLfsr(wdt_pwd_table[0]);
    wdt_pwd_table[2]=IfxWtu_calculateLfsr(wdt_pwd_table[1]);
    wdt_pwd_table[3]=IfxWtu_calculateLfsr(wdt_pwd_table[2]);
    wdt_pwd_table[4]=IfxWtu_calculateLfsr(wdt_pwd_table[3]);
    wdt_pwd_table[5]=IfxWtu_calculateLfsr(wdt_pwd_table[4]);
    wdt_pwd_table[6]=IfxWtu_calculateLfsr(wdt_pwd_table[5]);
    wdt_pwd_table[7]=IfxWtu_calculateLfsr(wdt_pwd_table[6]);

    IfxWtu_serviceCpuWatchdog(wdt_pwd_table[0]);

    while(1)
    {
        /* Check the button state */
        if(isButtonPressed() == TRUE)
        {
            jump_code=TRUE;
        }

        IfxWtu_changeCpuWatchdogReload(wdt_pwd_table[1], IfxWtu_getCpuWatchdogTim());
        IfxWtu_serviceCpuWatchdog(wdt_pwd_table[2]);
        IfxPort_togglePin(LED1);
        waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));

        if (jump_code==FALSE)
        {
            IfxWtu_changeCpuWatchdogReload(wdt_pwd_table[3], IfxWtu_getCpuWatchdogTim());
            IfxWtu_serviceCpuWatchdog(wdt_pwd_table[4]);
            IfxPort_togglePin(LED1);
            waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));
        }
        /* Reload init timer value */
        IfxWtu_changeCpuWatchdogReload(wdt_pwd_table[5], REL_VAL);
        /* Real service for temporal monitoring */
        IfxWtu_serviceCpuWatchdog(wdt_pwd_table[6]);
        IfxWtu_changeCpuWatchdogPassword(wdt_pwd_table[7], wdt_pwd_table[0]);
        IfxPort_togglePin(LED1);
        waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));
    }
#endif

#if ( ( AUTOMATIC_PASSWORD_SEQUENCING == 0 ) && ( MANUAL_PASSWORD_SEQUENCING == 1 ) )

    volatile uint8 jump_code = FALSE;

    while(1)
    {
        if(isButtonPressed() == TRUE)
        {
            jump_code=TRUE;
        }

        IfxWtu_changeCpuWatchdogPassword(IfxWtu_getCpuWatchdogPassword(), 0xA0);

        IfxPort_togglePin(LED2);
        waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));

        IfxWtu_changeCpuWatchdogReload(0xA0, IfxWtu_getCpuWatchdogTim());
        IfxWtu_serviceCpuWatchdog(0xA0);

        if (jump_code==FALSE)
        {
            IfxWtu_changeCpuWatchdogPassword(IfxWtu_getCpuWatchdogPassword(), 0xB1);
            IfxPort_togglePin(LED2);
            waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));
        }
        IfxWtu_changeCpuWatchdogReload(0xB1, IfxWtu_getCpuWatchdogTim());
        IfxWtu_serviceCpuWatchdog(0xB1);

        IfxWtu_changeCpuWatchdogPassword(IfxWtu_getCpuWatchdogPassword(), 0xC2);
        IfxPort_togglePin(LED2);
        waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));

        IfxWtu_changeCpuWatchdogReload(0xC2, IfxWtu_getCpuWatchdogTim());
        IfxWtu_serviceCpuWatchdog(0xC2);

        IfxWtu_changeCpuWatchdogPassword(IfxWtu_getCpuWatchdogPassword(), 0xD3);
        /* Reload init value */
        IfxWtu_changeCpuWatchdogReload(0xD3, REL_VAL);
        /* Service for Time Monitoring */
        IfxWtu_serviceCpuWatchdog(0xD3);
}
#endif

}
