size_t Ifx_Oe_Malloc_size(Ifx_Oe_Malloc_Pool* memoryPool)
{
    return memoryPool->_lc_eh - memoryPool->_lc_bh;
}


Ifx_Oe_Malloc_Pool* Ifx_Oe_Malloc_create(void* address, size_t size)
{
    Ifx_Oe_Malloc_Pool* memoryPool = (Ifx_Oe_Malloc_Pool*)address;

    if (IFX_OE_ALIGN_ON_64((uint32)address) != (uint32)address)
    {
        return (Ifx_Oe_Malloc_Pool*)NULL;  // not aligned on 64 bit boundary
    }

    size              &= ~(IFX_OE_ALIGN_64 - 1); // align Size on 64bit boundary

    memoryPool->_lc_bh = (char*)memoryPool + IFX_OE_ALIGN_ON_64(sizeof(Ifx_Oe_Malloc_Pool)) + IFX_OE_ALIGN_ON_64(sizeof(Ifx_Oe_Malloc_Blk));    // begin is align on 64 bit
    memoryPool->_lc_eh = (char*)((uint32)memoryPool + size);    // end is align on 64 bit

    if (memoryPool->_lc_bh >= memoryPool->_lc_eh)
    {
        return (Ifx_Oe_Malloc_Pool*)NULL;   // memory block not big enough
    }

    memoryPool->_end  = memoryPool->_lc_bh;
    memoryPool->last  = NULL;
    memoryPool->start = NULL;
#ifdef MEMORY_POOL_STATS

    memoryPool->memMaxUsed = memoryPool->memUsed = IFX_OE_ALIGN_ON_64(sizeof(Ifx_Oe_Malloc_Pool)) - IFX_OE_ALIGN_ON_64(sizeof(Ifx_Oe_Malloc_Blk));
    memoryPool->memFree    = size - memoryPool->memUsed;
#endif

    memoryPool->mutex = FALSE;
    return memoryPool;
}


void Ifx_Oe_Malloc_destroy(Ifx_Oe_Malloc_Pool* memoryPool)
{
    if (!memoryPool)
    {
        return;
    }

    memoryPool->mutex  = FALSE;

    memoryPool->_lc_bh = NULL;
    memoryPool->_lc_eh = NULL;
    memoryPool->_end   = NULL;
#ifdef MEMORY_POOL_STATS

    memoryPool->memUsed = 0;
    memoryPool->memFree = 0;
#endif
}


void* Ifx_Oe_Malloc_malloc(Ifx_Oe_Malloc_Pool* memoryPool, size_t size)
{
    void* mem;
    size = IFX_OE_ALIGN_ON_64(size);

    Ifx_Oe_Malloc_getMutex(memoryPool);

    mem = Ifx_Oe_Malloc_mallocPrivate(memoryPool, size);

    Ifx_Oe_Malloc_releaseMutex(memoryPool);
    return mem;
}


void* Ifx_Oe_Malloc_calloc(Ifx_Oe_Malloc_Pool* memoryPool, size_t nel, size_t size)
{
    void* result;

    size *= nel;

    if ((result = Ifx_Oe_Malloc_malloc(memoryPool, size)))
    {
        memset(result, 0, size);
    }

    return result;
}


void Ifx_Oe_Malloc_free(void* MemoryBlock)
{
    Ifx_Oe_Malloc_Pool* memoryPool;

    if (!MemoryBlock)
    {
        return;         /* null pointer is allowed by ANSI */
    }

    memoryPool = Ifx_Oe_Malloc_getPool(MemoryBlock);

    Ifx_Oe_Malloc_getMutex(memoryPool);

    Ifx_Oe_Malloc_freePrivate(MemoryBlock);

    Ifx_Oe_Malloc_releaseMutex(memoryPool);
}


void* Ifx_Oe_Malloc_realloc(void* MemoryBlock, size_t newsize)
{
    return Ifx_Oe_Malloc_reallocMove(Ifx_Oe_Malloc_getPool(MemoryBlock), MemoryBlock, newsize);
}


void* Ifx_Oe_Malloc_reallocMove(Ifx_Oe_Malloc_Pool* memoryPool, void* MemoryBlock, size_t newsize)
{
    void*              newmem;
    size_t             size;
    Ifx_Oe_Malloc_Blk* old;

    newmem = Ifx_Oe_Malloc_malloc(memoryPool, newsize);

    if (!MemoryBlock)
    {   /* ANSI requirement: behave like malloc() */
        return newmem;
    }

    if (newmem)        /* implies (newsize != 0) */
    {
        old  = (Ifx_Oe_Malloc_Blk*)((Ifx_Oe_Malloc_Addr)MemoryBlock - sizeof(Ifx_Oe_Malloc_Blk));
        size = IFX_OE_MALLOC_SIZE(old);

        if (newsize < size)
        {
            size = newsize;
        }

        (void)memcpy(newmem, MemoryBlock, size);
    }

    if (newmem || (newsize == 0))
    {
        Ifx_Oe_Malloc_free(MemoryBlock);
    }

    return newmem;
}
