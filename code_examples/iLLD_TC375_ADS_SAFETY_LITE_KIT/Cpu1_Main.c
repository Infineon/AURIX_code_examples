/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void core1_main(void)
{
    IfxCpu_enableInterrupts();
    /* !!WATCHDOG1 IS DISABLED HERE!!
     * Enable the watchdog and service it periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    g_AppCpu1.TickCount_1ms = 0;
    /* Initialize STM for generating an interrupt every milliseconds for basic scheduling. */
    initStm(&g_AppStm1, (IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());
    while(!g_SlkStatus.slkInitDone)
    {
        __nop();
    }
    while(1)
    {
        /* init digital acquistion/actuation i.e. the selected FUC from OneEye GUI */
        initDigitalAcquistionActuation();

        /* init analog acquistion i.e. the selected FUC from OneEye GUI */
        initAnalogAcquisition();
    }
}

/*
 * This interrupt is raised by the STM1. The initialization is done by initStm().
 * isrProvider CPU1 and isrPriority ISR_PRIORITY_OS_TICK
 */
void userIsrSTM1()
{
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM1, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    g_AppCpu1.TickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* Return if Safety lite kit initialization is not yet finished */
    if(!g_SlkStatus.slkInitDone)
    {
        return;
    }

    /* Execute the sequence of safety mechanism in the interrupt function */
    runSlkStmIsr((IfxCpu_ResourceCpu)IfxCpu_getCoreIndex(), &g_AppCpu1);
}
