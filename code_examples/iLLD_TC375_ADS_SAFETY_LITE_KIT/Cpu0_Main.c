void core0_main (void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());

    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    /* Execute application software safe startup sequence */
    runAppSwStartup();

    /* Initialize tick count value */
    g_AppCpu0.TickCount_1ms = 0;

    /* Initialize STM for generating an interrupt every milliseconds for basic scheduling. */
    initStm(&g_AppStm0, (IfxCpu_ResourceCpu) IfxCpu_getCoreIndex());

    /* Initialization of several safety lite kit implementations and features */
    initSafetyLiteKit();

    while (!g_SlkStatus.slkInitDone)
    {
        __nop();
    }

    while (1)
    {
        /* interaction with OneEye interface */
        runtimeSmuOneEye();
    }
}

/*
 * This interrupt is raised by the STM0. The initialization is done by initStm().
 * isrProvider CPU0 and isrPriority ISR_PRIORITY_OS_TICK
 */
void userIsrStm0()
{
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM0, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    g_AppCpu0.TickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* Return if Safety lite kit initialization is not yet finished */
    if (!g_SlkStatus.slkInitDone)
    {
        return;
    }

    /* Execute the sequence of safety mechanism in the interrupt function */
    runSlkStmIsr((IfxCpu_ResourceCpu) IfxCpu_getCoreIndex(), &g_AppCpu0);
}
