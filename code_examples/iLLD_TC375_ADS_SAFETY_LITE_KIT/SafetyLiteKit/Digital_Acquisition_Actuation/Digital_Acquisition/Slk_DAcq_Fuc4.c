/* Interrupt Service Routine of the CCU6, which gets triggered by a rising edge of the input port pin of timer T12 */
void isrCcu6PwmMissionEdgeDetectionDAcqFuc4(void)
{
    uint32  risingEdgeCntVal = 0;           /* Number of ticks between the last overflow and the rising edge       */
    uint32  fallingEdgeCntVal = 0;          /* Number of ticks between the last overflow and the falling edge      */

    /* Check on the occurrence of the rising edge */
    if (g_icuChannelMissionFuc4.ccu6->IS.B.ICC60R)
    {
        /* Reset interrupt status flag */
        IfxCcu6_clearInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc60RisingEdge);

        /* Get the captured rising edge counter value from the counter register */
        risingEdgeCntVal = IfxCcu6_getCaptureRegisterValue(g_icuChannelMissionFuc4.ccu6, g_icuChannelMissionFuc4.channelId);
    }

    /* Check on the occurrence of the falling edge */
    if (g_icuChannelMissionFuc4.ccu6->IS.B.ICC60F)
    {
        /* Reset interrupt status flag */
        IfxCcu6_clearInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc60FallingEdge);

        /* Get the captured falling edge counter value from the shadow register */
        fallingEdgeCntVal = IfxCcu6_getCaptureShadowRegisterValue(g_icuChannelMissionFuc4.ccu6, g_icuChannelMissionFuc4.channelId);
    }

    /* Define a variable to calculate the final timer counter value */
    uint32 finalFreqCntVal, finalDutyCycCntVal = 0;

    /* If no overflow was detected during the period measurement */
    if(g_cntOverflowPeriodFuc4 == 0)
    {
        finalFreqCntVal = risingEdgeCntVal - g_previousCntValCcu6Fuc4;      /* Total counter value calculation          */
    }
    /* If one or more overflows were detected during the period measurement */
    else
    {
        /* Add to the current counter value, the amount of counter ticks which passed before the first overflow,
         * plus 65534 (0xFFFF - 1) for each additional overflow occurred between two rising edges.
         */
        finalFreqCntVal = (uint32) (risingEdgeCntVal + (MAX_VAL_16_BIT - g_previousCntValCcu6Fuc4)
                + ((g_cntOverflowPeriodFuc4 - 1) * MAX_VAL_16_BIT));
    }

    /* If no overflows were detected during signal high level time measurement */
    if(g_cntOverflowHighLevelFuc4 == 0)
    {
        finalDutyCycCntVal = fallingEdgeCntVal - g_previousCntValCcu6Fuc4;
    }

    /* If one or more overflows were detected during signal high level time measurement */
    else
    {
        /* Add to the current counter value, the amount of counter ticks which passed before the first overflow,
         * plus 65534 (0xFFFF - 1) for each additional overflow occurred between the rising and the falling edge.
         */
        finalDutyCycCntVal = (uint32) (fallingEdgeCntVal + (MAX_VAL_16_BIT - g_previousCntValCcu6Fuc4)
                + ((g_cntOverflowHighLevelFuc4 - 1) * MAX_VAL_16_BIT));
    }

    /* The PWM frequency is calculated by dividing the captured frequency with the final total counter value
     *      total number of ticks * (1 / CCU6 frequency) = period of the measured signal =>
     *      => frequency of the measured signal = CCU6 frequency / total number of ticks
     */
    g_digitalAcqActStatus.doneFlag[0] = TRUE;
    g_digitalAcqActStatus.pwmFrequency[0] = g_Ccu6CaputreFrequencyFuc4 / (float32)finalFreqCntVal;
    /* The PWM duty cycle is calculated by dividing the captured signal high level time by the period */
    g_digitalAcqActStatus.pwmDutyCylce[0] = 100 * (finalDutyCycCntVal / (float32)finalFreqCntVal);

    g_previousCntValCcu6Fuc4 = risingEdgeCntVal;    /* Update g_previousCntValCcu6Fuc4 for the next calculation        */
    g_cntOverflowPeriodFuc4 = 0;                /* Reset overflow counter of the period measurement                 */
    g_cntOverflowHighLevelFuc4 = 0;             /* Reset overflow counter of the signal high level time measurement */

}

/* Interrupt Service Routine of the CCU6, which gets triggered at each period match event for counting overflows */
void isrCcu6PeriodMatchDAcqFuc4(void)
{
    g_cntOverflowPeriodFuc4++;

    /* Increments high level time measurement overflows number only if the signal is still high
     * (no falling edge occurrence yet)
     */
    if(IfxCcu6_getInterruptStatusFlag(&MODULE_CCU61, IfxCcu6_InterruptSource_cc60FallingEdge) == FALSE)
    {
        g_cntOverflowHighLevelFuc4++;
    }
}

/* Interrupt Service Routine of timer T2, gets triggered by rising edge on input pin of timer T2 */
void isrGpt12T2MonitorDAcqFuc4(void)
{

        uint32 currentCntVal = IfxGpt12_T2_getTimerValue(&MODULE_GPT120); /* Get timer value of timer T2 */
        uint32 finalCntVal = 0; /* Variable to calculate final timer counter value */

        if(g_cntOverflowFuc4 == 0)
        {
            /* If no overflow detected */
            finalCntVal = currentCntVal - g_previousCntValFuc4; /* Total counter value calculation */
        }
        else
        {
            /* One or more overflows detected */
            /* Add to the current counter value, the amount of counter ticks which passed before the first overflow,
             * plus 65525 (0xFFFF) for each additional overflow since the previous rising edge.
             */
            finalCntVal = (uint32) (currentCntVal + (MAX_VAL_16_BIT - g_previousCntValFuc4)
                    + ((g_cntOverflowFuc4 - 1) * MAX_VAL_16_BIT));
        }
        /* Calculation of the PWM frequency by dividing the frequency of timer T3 through the final total counter value */
        g_digitalAcqActStatus.pwmFrequency[1] = IfxGpt12_T3_getFrequency(&MODULE_GPT120) / finalCntVal;
        g_digitalAcqActStatus.doneFlag[1] = TRUE;
        g_previousCntValFuc4 = currentCntVal;    /* Set g_previousCntVal to currentCntVal for the next calculation */
        g_cntOverflowFuc4 = 0;                   /* Reset overflow flag */

}

/* Interrupt Service Routine of timer T3, gets triggered after T3 timer overflow */
void istGpt12T3MonitorDAcqFuc4(void)
{
    g_cntOverflowFuc4++; /* Increase overflow counter */
}

/*
 * SM:CCU6_CAPTURE_MON_BY_GPT12
 *  */
void checkPlausibilityCcu6Gpt12Fuc4(void)
{
    /* Continue only if both measurements are done */
    static uint8 notDoneCnt;

    if (FALSE == g_digitalAcqActStatus.doneFlag[0] ||
        FALSE == g_digitalAcqActStatus.doneFlag[1])
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two i.e. CCU6 or GPT12,
         * is not successfully measured anymore */
        if(notDoneCnt > 3)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_DIGITAL_ACQ_ACT);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
        /* Reset measurement mission frequency */
        g_digitalAcqActStatus.pwmFrequency[0] = 0;
        g_digitalAcqActStatus.pwmFrequency[1] = 0;
    }
    else
    {
        notDoneCnt = 0;
        /* Difference validation */
        /* Pwm frequency comparison */
        comparePwmFrequency(g_digitalAcqActStatus.pwmFrequency[0], g_digitalAcqActStatus.pwmFrequency[1]);

        /* Reset measurement done flags */
        g_digitalAcqActStatus.doneFlag[0] = FALSE;
        g_digitalAcqActStatus.doneFlag[1] = FALSE;
    }
}

/*
 * Configure the CCU6 for capturing the rising edge of a PWM signal
 * */
void initCcu6IcuMissionDAcqFuc4(void)
{
    IfxCcu6_Icu ccu6Handle;                                           /* CCU6 handle                            */
    IfxCcu6_Icu_Config ccu6Config;                                    /* Create a configuration structure       */

    IfxCcu6_Icu_initModuleConfig(&ccu6Config, &MODULE_CCU61);         /* Fill the structure with default values */
    IfxCcu6_Icu_initModule(&ccu6Handle, &ccu6Config);                 /* Initialize the module                  */

    /* Store the configured captured frequency in a global variable */
    g_Ccu6CaputreFrequencyFuc4 = ccu6Handle.captureFrequency;

    IfxCcu6_Icu_ChannelConfig ccu6ChannelConfig;                      /* Create a channel configuration structure */
    IfxCcu6_Icu_initChannelConfig(&ccu6ChannelConfig, &MODULE_CCU61); /* Fill the structure with default values   */

    ccu6ChannelConfig.channelId = IfxCcu6_T12Channel_0;               /* Select Timer T12 channel 0               */

    /* Select the capture mode: IfxCcu6_T12ChannelMode_doubleRegisterCaptureRisingAndFalling
     * With this particular value, the CCU6 stores the T12 value in the CC6xR register when a rising edge is detected,
     * while it stores the T12 value in the CC6xSR shadow register when a falling edge is detected.
     */
    ccu6ChannelConfig.channelMode = IfxCcu6_T12ChannelMode_doubleRegisterCaptureRisingAndFalling;

    /* Service Request 0 for interrupt at any rising or falling edge */
    ccu6ChannelConfig.interrupt1.source = IfxCcu6_InterruptSource_cc60RisingEdge; /* Select the interrupt source */
    ccu6ChannelConfig.interrupt1.serviceRequest = IfxCcu6_ServiceRequest_0; /* Select the service request output */
    ccu6ChannelConfig.interrupt1.priority = ISR_PRIORITY_CCU6_RE_DACQ_FUC4;/*Configure the int priority*/
    /* Select the type of interrupt service */
    ccu6ChannelConfig.interrupt1.typeOfService = IfxCpu_Irq_getTos(CPU1_RUNNING_TASK);

    /* Service Request 1 for interrupt at T12 period match */
    ccu6ChannelConfig.interrupt2.source = IfxCcu6_InterruptSource_t12PeriodMatch; /* Select the interrupt source    */
    ccu6ChannelConfig.interrupt2.serviceRequest = IfxCcu6_ServiceRequest_1; /* Select the service request output    */
    ccu6ChannelConfig.interrupt2.priority = ISR_PRIORITY_CCU6_PM_DACQ_FUC4;/*Configure the int priority*/
    /* Select the type of interrupt service */
    ccu6ChannelConfig.interrupt2.typeOfService = IfxCpu_Irq_getTos(CPU1_RUNNING_TASK);;

    /* Configure internal start of timer T12 controlled by the run bit T12R */
    ccu6ChannelConfig.trigger.extInputTrigger = NULL_PTR;

    /* CCU6 ICU pin configuration */
    IfxCcu6_Icu_Pins pins =
    {
        &CCU6_INPUT_MISSION_PIN_FUC4, /* CC60In pin                   */
        NULL_PTR,                     /* CC61In pin not used          */
        NULL_PTR,                     /* CC62In pin not used          */
        NULL_PTR,                     /* CCPOS0In pin not used        */
        NULL_PTR,                     /* CCPOS1In pin not used        */
        NULL_PTR,                     /* CCPOS2In pin not used        */
        IfxPort_InputMode_pullUp,     /* CC6x pin input mode          */
        IfxPort_InputMode_pullUp,     /* CCPOSx pin input mode        */
        NULL_PTR,                     /* T12HR input signal not used  */
        NULL_PTR,                     /* T13HR input signal not used  */
        IfxPort_InputMode_pullUp      /* Pad driver mode              */
    };
    ccu6ChannelConfig.pins = &pins;                                     /* Set the configuration of the port pins   */

    ccu6ChannelConfig.multiInputCaptureEnabled = FALSE;                 /* Disable multiple input capture mode      */

    IfxCcu6_Icu_initChannel(&g_icuChannelMissionFuc4, &ccu6ChannelConfig);         /* Initialize the channel                  */

    /* Start the timer T12 by setting the run bit T12R with the associated bit T12RS */
    IfxCcu6_Icu_startCapture(&g_icuChannelMissionFuc4);
}

/*
 * This function initializes timer T2 and T3 of the block GPT1 of module GPT12 to capture PWM signals.
 *  */
void initGpt12MonitorDAcqFuc4(void)
{
    /* Enable GPT12 module */
    IfxGpt12_enableModule(&MODULE_GPT120);

    /* Select 4 as prescaler for the GPT1 module -> fastest clock frequency for best accuracy */
    IfxGpt12_setGpt1BlockPrescaler(&MODULE_GPT120, IfxGpt12_Gpt1BlockPrescaler_4);

    /* Set core timer T3 in timer mode */
    IfxGpt12_T3_setMode(&MODULE_GPT120, IfxGpt12_Mode_timer);

    /* Set auxiliary timer T2 in capture mode */
    IfxGpt12_T2_setMode(&MODULE_GPT120, IfxGpt12_Mode_capture);

    /* Select input pin B of timer T2 which is P00.7  */
    IfxGpt12_initTxInPin(&GPT12_INPUT_MONITOR_PIN_FUC4, IfxPort_InputMode_pullUp);

    /* Select rising edge as capture event */
    IfxGpt12_T2_setCaptureInputMode(&MODULE_GPT120, IfxGpt12_CaptureInputMode_risingEdgeTxIN);

    /* Service request configuration */
    /* Get source pointer of timer T2, initialize and enable */
    volatile Ifx_SRC_SRCR *src = IfxGpt12_T2_getSrc(&MODULE_GPT120);
    IfxSrc_init(src, IfxSrc_Tos_cpu1, ISR_PRIORITY_GPT12_T2_INT_QACQ_FUC4);
    IfxSrc_enable(src);

    /* Get source pointer of timer T2, initialize and enable */
    src = IfxGpt12_T3_getSrc(&MODULE_GPT120);
    IfxSrc_init(src, IfxSrc_Tos_cpu1, ISR_PRIORITY_GPT12_T3_INT_QACQ_FUC4);
    IfxSrc_enable(src);

    /* Start timer T3*/
    IfxGpt12_T3_run(&MODULE_GPT120, IfxGpt12_TimerRun_start);
}

/*
 * InitialGTM TOM to generate a dummy output PWM signal
 * */
void generatePwmFuc4(void)
{
    IfxGtm_Tom_Pwm_Config tomConfig;                                            /* Timer configuration structure     */
    IfxGtm_Tom_Pwm_Driver tomDriver;                                            /* Timer driver structure            */

    IfxGtm_Tom_Pwm_initConfig(&tomConfig, &MODULE_GTM);                         /* Initialize default parameters     */

    tomConfig.tom = GTM_TOM_SAFE_PIN.tom;                                       /* Select the TOM                    */
    tomConfig.tomChannel = GTM_TOM_SAFE_PIN.channel;                            /* Select the channel                */
    tomConfig.period = TOM_PWM_PERIOD;                                          /* Set timer period                  */
    tomConfig.dutyCycle = (uint32)(TOM_DUTY_CYCLE * 0.01 * tomConfig.period);   /* Set the duty cycle                */
    tomConfig.pin.outputPin = &GTM_TOM_SAFE_PIN;                                /* Set the pin as output             */
    tomConfig.synchronousUpdateEnabled = TRUE;                                  /* Enable synchronous update         */
    tomConfig.clock = IfxGtm_Tom_Ch_ClkSrc_cmuFxclk2;                           /* Select the FXU clock 1            */

    boolean success = IfxGtm_Tom_Pwm_init(&tomDriver, &tomConfig);              /* Initialize the module             */
    if(!success)
    {
        softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_DIGITAL_ACQ_ACT);
        return;
    }
    /* Validate if configuration was written successfully to the registers */
    slkTomPwmConfigReadBack(&tomConfig, SOFT_SMU_ALM_DIGITAL_ACQ_ACT);

    /* Start the generation of the PWM signal   */
    IfxGtm_Tom_Pwm_start(&tomDriver, TRUE);
}

/*
 * SM:CCU6_CAPTURE_MON_BY_GPT12
 * */
void initDAcqFuc4(void)
{
    resetDAconfiguration();

    /* Initialize the GPT module as mission channel or measuring the generated dummy PWM signal */
    initGpt12MonitorDAcqFuc4();

    /* Configure CCU6 as mission channel for measuring the generated dummy PWM signal */
    initCcu6IcuMissionDAcqFuc4();

    /* Configure GTM TOM for the dummy output PWM signal which is measured by the TOMs */
    generatePwmFuc4();

}
