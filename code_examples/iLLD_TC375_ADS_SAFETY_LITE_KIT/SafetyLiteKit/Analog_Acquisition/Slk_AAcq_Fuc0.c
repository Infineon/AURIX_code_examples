/* Input1 EVADC ISR */
void evadcInput1ChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_INPUT1_CH_ID] = getEvadcChannelResult(
            &g_slkEvadcCfg.evadcChannel[EVADC_INPUT1_CH_ID]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT1_CH_ID] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_INPUT1_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT1_CH_ID] -= g_analogAcquisitionStatus.evadcInput1Calibration;

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcInput1MeasDoneFlag = TRUE;
}

/* Input2 EVADC ISR */
void evadcInput2ChISR(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_INPUT2_CH_ID] = getEvadcChannelResult(
            &g_slkEvadcCfg.evadcChannel[EVADC_INPUT2_CH_ID]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT2_CH_ID] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_INPUT2_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT2_CH_ID] -= g_analogAcquisitionStatus.evadcInput2Calibration;

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcInput2MeasDoneFlag = TRUE;
}

/*
 * As there might be a slight difference between both Inputs this function executes
 * CALIBRATION_SAMPLE_AMOUNT measurements at the beginning to determine the offset.
 * */
void evadcStartupCalibration(SlkEvadcCfgType *evadcCfg)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Add channel to queue with refill option enabled */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_INPUT1_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].queueRequestSourceId);

    /* Add channel to queue with refill option enabled */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_INPUT2_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].queueRequestSourceId);

    /* Accumulate CALIBRATION_SAMPLE_AMOUNT adc samples */
    float32 accumulationResult[2] = {0, 0};
    for(uint8 sampleCnt = 0; sampleCnt < CALIBRATION_SAMPLE_AMOUNT; sampleCnt++)
    {
        /* Get the EVADC raw value */
        accumulationResult[0] += EVADC_RESULT_TO_V(getEvadcChannelResult(&evadcCfg->evadcChannel[EVADC_INPUT1_CH_ID]));
        accumulationResult[1] += EVADC_RESULT_TO_V(getEvadcChannelResult(&evadcCfg->evadcChannel[EVADC_INPUT2_CH_ID]));
    }

    /* Stop and flush the queue */
    IfxEvadc_Adc_clearQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].queueRequestSourceId);
    IfxEvadc_Adc_clearQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].queueRequestSourceId);

    /* Get the calibration offset */
    float32 calibrationOffset[2] = {0};
    getCalibrationOffset(accumulationResult, CALIBRATION_SAMPLE_AMOUNT, calibrationOffset);

    /* Save calibration values to global structure */
    g_analogAcquisitionStatus.evadcInput1Calibration = calibrationOffset[0];
    g_analogAcquisitionStatus.evadcInput2Calibration = calibrationOffset[1];

    /* Enable interrupts again*/
    IfxCpu_restoreInterrupts(interruptState);
}

/*
 * Function to add each channel to the queue and start it
 * Start SM:EVADC:DIVERSE_REDUNDANCY with Input1 and Input2
 * */
void runEvadcMeasurement(SlkEvadcCfgType *evadcCfg)
{
    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_INPUT1_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT1_CH_ID].queueRequestSourceId);

    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_INPUT2_CH_ID],
            evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_INPUT2_CH_ID].queueRequestSourceId);
}

/*
 * Function to run the EVADC plausibility check: SM:EVADC:PLAUSIBILITY
 * */
void evadcPlausibilityCheck(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
        return;
    }

    /* Continue only if both measurements are done */
    static uint8 notDoneCnt;
    if(!g_analogAcquisitionStatus.evadcInput1MeasDoneFlag && !g_analogAcquisitionStatus.evadcInput2MeasDoneFlag)
    {
        /* Increase static not done counter */
        notDoneCnt++;
        /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is
         * not successfully measured anymore */
        if(notDoneCnt > 10)
        {
            /* Trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);

            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;
        }
    }
    else
    {
        /* Reset static notDoneCnt counter */
        notDoneCnt = 0;

        /* Comparison of mission and monitor channels values */
        plausibilityCheck(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT1_CH_ID],
                g_analogAcquisitionStatus.evadcVoltageResults[EVADC_INPUT2_CH_ID],
                MAX_DIFF, UPPER_LIMIT, LOWER_LIMIT, SOFT_SMU_ALM_ADC);

        /* Reset measurement done flags */
        g_analogAcquisitionStatus.evadcInput1MeasDoneFlag = FALSE;
        g_analogAcquisitionStatus.evadcInput2MeasDoneFlag = FALSE;
    }
}

/*
 * Execute Analog Acquisition Function use case 0
 * */
void runAAcqFuc0(void)
{
    /* Fill the queue start the measurement */
    runEvadcMeasurement(&g_slkEvadcCfg);

    /* Run plausibility check by comparing EVADC Input1 as the mission signal and EVADC Input2 as
     * the monitoring signal */
    evadcPlausibilityCheck();
}

/*
 * initial Analog acquisition FUC 0
 * */
void initAAcqFuc0(void)
{
    /* Reset status */
    resetAnalogAcqStatus();

    g_slkEvadcCfg.amountOfGroups              = EVADC_GROUPS_NUM_AA_FUC0;
    g_slkEvadcCfg.amountOfChannels            = EVADC_CHANNELS_NUM_AA_FUC0;
    g_slkEvadcCfg.evadcGroup                  = g_evadcGroup;
    g_slkEvadcCfg.evadcChannel                = g_evadcChannel;
    g_slkEvadcCfg.evadcChannelConfig          = g_evadcChannelConfig;

    initCONVCTRL();                                                 /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_slkEvadcCfg);                          /* Initialize the EVADC module */
    initEVADCGroups(&g_slkEvadcCfg, EVADC_VAREF_G_ID);        /* Initialize the EVADC group */
    initEVADCChannels(&g_slkEvadcCfg);                        /* Initialize the channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or
     * number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_G_ID] = evadcVarefPlausibilityCheck(&g_slkEvadcCfg,
            EVADC_VAREF_G_ID, g_slkEvadcCfg.evadcChannelConfig[EVADC_VAREF_G_ID].queueRequestSourceId);

    /* Run offset calibration as both Input might have not exactly the same resistance */
    evadcStartupCalibration(&g_slkEvadcCfg);
}
