/*
 * ISR for Under voltage simulation
 * */
void isrEvadcSig1ChFuc3(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG1_AA_FUC3] = getEvadcChannelResult(
            &g_slkEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG1_AA_FUC3]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG1_AA_FUC3] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG1_AA_FUC3]);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcSig1DoneFlag = TRUE;
}

void isrEvadcSig2ChFuc3(void)
{
    IfxCpu_enableInterrupts();

    /* Get the EVADC conversion result */
    g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG2_AA_FUC3] = getEvadcChannelResult(
            &g_slkEvadcCfgAAcqFuc3.evadcChannel[EVADC_UV_SIM_SIG2_AA_FUC3]);

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG2_AA_FUC3] = EVADC_RESULT_TO_V(
            g_analogAcquisitionStatus.evadcConversionResult[EVADC_UV_SIM_SIG2_AA_FUC3]);

    /* Set measurement done flag */
    g_analogAcquisitionStatus.evadcSig2DoneFlag = TRUE;
}

/*
 * Function is for plausibility check
 * */
void plausibilityCheckAAcqFuc3(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
    }
    else
    {
        /* Continue only if both measurements are done */
        static uint8 notDoneCnt;
        if(!g_analogAcquisitionStatus.evadcSig1DoneFlag && !g_analogAcquisitionStatus.evadcSig2DoneFlag)
        {
            /* Increase static not done counter */
            notDoneCnt++;
            /* If notDoneCnt is not reset frequently it means that one of the two EVADCs is not successfully measured anymore */
            if(notDoneCnt > 10)
            {
                /* Trigger SMU software alarm */
                softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_ADC);
                /* Reset static notDoneCnt counter */
                notDoneCnt = 0;
            }
        }
        else
        {
            /* Reset static notDoneCnt counter */
            notDoneCnt = 0;

            /* Comparison of mission and monitor channels values */
            plausibilityCheck(g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG1_AA_FUC3],
                    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_UV_SIM_SIG2_AA_FUC3], MAX_DIFF, UPPER_LIMIT, LOWER_LIMIT,
                    SOFT_SMU_ALM_ADC);

            /* Reset measurement done flags */
            g_analogAcquisitionStatus.evadcSig1DoneFlag = FALSE;
            g_analogAcquisitionStatus.evadcSig2DoneFlag = FALSE;
        }
    }
}

/*
 * Function to add each channel to the queue and start it
 * Start SM:EVADC:DIVERSE_REDUNDANCY with Input1 and Input2
 * */
void runEvadcMeasurementFuc3(SlkEvadcCfgType *evadcCfg)
{
    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_UV_SIM_SIG1_AA_FUC3],
            evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG1_AA_FUC3].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG1_AA_FUC3].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG1_AA_FUC3].queueRequestSourceId);

    /* Add channel to queue with refill option DISABLED */
    IfxEvadc_Adc_addToQueue(&evadcCfg->evadcChannel[EVADC_UV_SIM_SIG2_AA_FUC3],
            evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG2_AA_FUC3].queueRequestSourceId, IFXEVADC_QUEUE_REFILL_DISABLED);

    /* Start the queue */
    IfxEvadc_Adc_startQueue(evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG2_AA_FUC3].groupHandle,
            evadcCfg->evadcChannelConfig[EVADC_UV_SIM_SIG2_AA_FUC3].queueRequestSourceId);
}

/*Execute the analog acquisition FUC 0 */
void runAAcqFuc3(void)
{
    /* Fill the queue start the measurement */
    runEvadcMeasurementFuc3(&g_slkEvadcCfgAAcqFuc3);

    /* Run plausibility check. Could also be called from an EVADC channel ISR */
    plausibilityCheckAAcqFuc3();
}

/*
 * Initial function for Analog acquisition Function use case 0
 * */
void initAAcqFuc3(void)
{
    /* Reset status */
    resetAnalogAcqStatus();

    g_slkEvadcCfgAAcqFuc3.amountOfGroups          = EVADC_GROUPS_NUM_AA_FUC3;
    g_slkEvadcCfgAAcqFuc3.amountOfChannels        = EVADC_CHANNELS_NUM_AA_FUC3;
    g_slkEvadcCfgAAcqFuc3.evadcGroup              = g_evadcGroupAAcqFuc3;
    g_slkEvadcCfgAAcqFuc3.evadcChannel            = g_evadcChannelAAcqFuc3;
    g_slkEvadcCfgAAcqFuc3.evadcChannelConfig      = g_evadcChannelConfigAAcqFuc3;

    initCONVCTRL();                                      /* Initialize and verify the CONVTRL module */
    initEVADCModule(&g_slkEvadcCfgAAcqFuc3);      /* Initialize the EVADC module */
    initEVADCGroups(&g_slkEvadcCfgAAcqFuc3, EVADC_VAREF_G_ID_AA_FUC3);      /* Initialize the EVADC group */
    initEVADCChannels(&g_slkEvadcCfgAAcqFuc3);    /* Initialize the EVADC channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or number of conversions. */
    g_analogAcquisitionStatus.evadcVoltageResults[EVADC_VAREF_CH_ID_AA_FUC3] = evadcVarefPlausibilityCheck(
            &g_slkEvadcCfgAAcqFuc3, EVADC_VAREF_CH_ID_AA_FUC3,
            g_slkEvadcCfgAAcqFuc3.evadcChannelConfig[EVADC_VAREF_CH_ID_AA_FUC3].queueRequestSourceId);

}
