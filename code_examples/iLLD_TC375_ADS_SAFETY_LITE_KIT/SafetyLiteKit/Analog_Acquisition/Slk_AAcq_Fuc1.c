/*
 * Function to run the EDSADC plausibility check: SM:EDSADC:PLAUSIBILITY
 * */
void edsadcPlausibilityCheck(void)
{
    /* Ignore first three measurements as measurement might return 0 at the beginning (self-calibration) */
    if(g_analogAcquisitionStatus.initCnt < 3)
    {
        g_analogAcquisitionStatus.initCnt++;
    }
    else
    {
        /* Comparison of mission and monitor channels values */
        /* Comparison of mission and monitor channels values */
        plausibilityCheck(g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT1_CH_ID],
                g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT2_CH_ID],
                MAX_DIFF, UPPER_LIMIT, LOWER_LIMIT, SOFT_SMU_ALM_ADC);
    }
}

/*
 *  This function gets the result of the most recent conversion
 *  */
void runEdsadcMeasurement(SlkEdsadcCfgType *edsadcCfg)
{
    /* Get the ESDADC raw value */
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT1_CH_ID] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_INPUT1_CH_ID]);
    g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT2_CH_ID] = IfxEdsadc_Edsadc_getMainResult(
            &edsadcCfg->edsadcChannel[EDSADC_INPUT2_CH_ID]);

    /* Check if only the negative channel is used */
    if((edsadcCfg->edsadcChannelConfig[EDSADC_INPUT1_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_INPUT1_CH_ID].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT1_CH_ID] *= -1;
    }
    if((edsadcCfg->edsadcChannelConfig[EDSADC_INPUT2_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_INPUT2_CH_ID].negInput != NULL_PTR))
    {
        /* If yes multiply conversion result by -1 */
        g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT2_CH_ID] *= -1;
    }

    /* Calculate the corresponding voltage */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT1_CH_ID] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT1_CH_ID]);
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT2_CH_ID] = EDSADC_RESULT_TO_V(
            g_analogAcquisitionStatus.edsadcConversionResult[EDSADC_INPUT2_CH_ID]);

    /* Deduct calibration offset */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT1_CH_ID] -= g_analogAcquisitionStatus.edsadcInput1Calibration;
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_INPUT2_CH_ID] -= g_analogAcquisitionStatus.edsadcInput2Calibration;

}

/*
 * Execute Analog Acquisition Functional Use Case 1
 * */
void runAAcqFuc1(void)
{
    runEdsadcMeasurement(&g_slkEdsadcCfg);         /* Run EDSADC measurement and get results */

    /* Run plausibility check by comparing EDSADC Input1 as the mission signal and EDSADC Input2 as the monitoring signal */
    edsadcPlausibilityCheck();
}

/*
 * As there might be a slight difference between both Input, this function executes
 * CALIBRATION_SAMPLE_AMOUNT measurements at the beginning to determine the offset.
 * */
void edsadcStartupCalibration(SlkEdsadcCfgType *edsadcCfg)
{
    /* Accumulate CALIBRATION_SAMPLE_AMOUNT ADC samples */
    float32 accumulationResult[2] = {0, 0};
    sint8 multiplerInput1 = 1, multiplerInput2 = 1;
    /* Check if only the negative channel is used */
    if((edsadcCfg->edsadcChannelConfig[EDSADC_INPUT1_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_INPUT1_CH_ID].negInput != NULL_PTR))
    {
        multiplerInput1 *= -1;
    }
    /* Check if only the negative channel is used */
    if((edsadcCfg->edsadcChannelConfig[EDSADC_INPUT2_CH_ID].posInput == NULL_PTR)  &&
       (edsadcCfg->edsadcChannelConfig[EDSADC_INPUT2_CH_ID].negInput != NULL_PTR))
    {
        multiplerInput2 *= -1;
    }

    for(uint8 sampleCnt = 0; sampleCnt < CALIBRATION_SAMPLE_AMOUNT; sampleCnt++)
    {
        /* Get the ESDADC raw value i.e Negative channel value multiple by -1 */
        accumulationResult[0] += EDSADC_RESULT_TO_V(multiplerInput1 *
                IfxEdsadc_Edsadc_getMainResult(&edsadcCfg->edsadcChannel[EDSADC_INPUT1_CH_ID]));
        accumulationResult[1] += EDSADC_RESULT_TO_V(multiplerInput2 *
                IfxEdsadc_Edsadc_getMainResult(&edsadcCfg->edsadcChannel[EDSADC_INPUT2_CH_ID]));
    }

    /* Get the calibration offset */
    float32 calibrationOffset[2] = {0};
    getCalibrationOffset(accumulationResult, CALIBRATION_SAMPLE_AMOUNT, calibrationOffset);

    /* Save calibration values to global structure */
    g_analogAcquisitionStatus.edsadcInput1Calibration = calibrationOffset[0];
    g_analogAcquisitionStatus.edsadcInput2Calibration = calibrationOffset[1];
}

/*
 * initial Analog Acquisition Functional Use Case 1
 * */
void initAAcqFuc1(void)
{
    /* Reset status */
    resetAnalogAcqStatus();

    g_slkEdsadcCfg.amountOfChannels     = EDSADC_CHANNELS_NUM_AA_FUC1;
    g_slkEdsadcCfg.edsadcChannel        = g_edsadcChannel;
    g_slkEdsadcCfg.edsadcChannelConfig  = g_edsadcChannelConfig;

    initCONVCTRL();                                     /* Initialize and verify the CONVTRL module */
    initEDSADCModule(&g_slkEdsadcCfg);              /* Initialize the EDSADC module */
    initEDSADCChannels(&g_slkEdsadcCfg);            /* Initialize the channels */

    /* Run VAREF plausibility check once per driving cycle, better would be cyclically after a predefined time or
     * number of conversions. */
    g_analogAcquisitionStatus.edsadcVoltageResults[EDSADC_VAREF_CH_ID] = edsadcVarefPlausibilityCheck(
            &g_slkEdsadcCfg, EDSADC_VAREF_CH_ID, VAREF_EXPECTED_RESULT, VAREF_TOLERANCE);

    /* Run offset calibration as both input might have not exactly the same resistance */
    edsadcStartupCalibration(&g_slkEdsadcCfg);
}
