void initAnalogAcquisition(void)
{
    static uint8 lastAnalogAcquisitionMode = noAnalogAcquisitionMode;  /* last analog Acquisition mode              */
    if (lastAnalogAcquisitionMode != g_SlkStatus.analogAcqFucSelect)
    {
    /* Initialize / Reinitialize Analog Acquisition functional use case examples */
        switch(g_SlkStatus.analogAcqFucSelect)
        {
            case initAAcqFuc0Mode:
                initAAcqFuc0();
                g_slkAnalogAcquisitionRunFlag = runAAcqFuc0Mode;
                break;
            case initAAcqFuc1Mode:
                initAAcqFuc1();
                g_slkAnalogAcquisitionRunFlag = runAAcqFuc1Mode;
                break;
            case initAAcqFuc2Mode:
                initAAcqFuc2();
                g_slkAnalogAcquisitionRunFlag = runAAcqFuc2Mode;
                break;
            case initAAcqFuc3Mode:
                initAAcqFuc3();
                g_slkAnalogAcquisitionRunFlag = runAAcqFuc3Mode;
                break;
            case initAAcqFuc4BrokenWRMode:
                initAAcqFuc4BrokenWR();
                g_slkAnalogAcquisitionRunFlag = runAAcqFuc4BrokenWRMode;
                break;
            default:
                break;
        }
        lastAnalogAcquisitionMode = g_SlkStatus.analogAcqFucSelect;
    }
}
/*
 * this function will initialize the digital acquisition and actuation use case
 * */
void initDigitalAcquistionActuation(void)
{
    static uint8 lastDigitalAcqActMode = noDigitalAcquisition;
    if (lastDigitalAcqActMode != g_SlkStatus.digitalAcqActFucSelect)
    {
        /* Initialize / Reinitialize Digital Acquisition or Actuation functional use case examples */
        switch (g_SlkStatus.digitalAcqActFucSelect)
        {
            case noDigitalAcquisition :
                 resetDAconfiguration();
                 break;
            case initializeDAcqFuc0 :
                initDAcqFuc0();
                break;
            case initializeDAcqFuc1 :
                initDAcqFuc1();
                break;
            case initializeDAcqFuc2 :
                initDAcqFuc2();
                break;
            case initializeDAcqFuc3 :
                initDAcqFuc3();
                break;
            case initializeDAcqFuc4 :
                initDAcqFuc4();
                break;
            case initializeDActFuc0 :
                initDActFuc0();
                break;
            case initializeDActFuc1 :
                initDActFuc1();
                break;
            case initializeDActFuc2 :
                initDActFuc2();
                break;
            case initializeDActFuc3 :
                initDActFuc3();
                break;
            default:
                break;
        }
        lastDigitalAcqActMode = g_SlkStatus.digitalAcqActFucSelect;
    }
}

/*
 * This function is called every millisecond from all CPUs to performs all of their own necessary checks
 * */
void runSlkStmIsr(IfxCpu_ResourceCpu cpuIndex, App_Cpu *lcl_AppCpu)
{
    /* Run SM:STM:MONITOR */
    runSTMMonitoring(cpuIndex);

    /* ------------------------------Task Running on CPU0 -----------------------------------*/
    if(CPU0_RUNNING_TASK == cpuIndex)
    {
        /* Action to do each 50ms
         * PFLASH error injection
         * */
        if((lcl_AppCpu->TickCount_1ms % 50) == 0)
        {
            /* if DBR error, then check Word line integrity */
            if(TRUE == doubleBitErrorOccur)
            {
                /* SM:NVM.PFLASH:WL_FAIL_DETECT */
                runWordlineFailDetectPFLASH();
                doubleBitErrorOccur = FALSE;
            }
            /* Inject Pflash error */
            if(TRUE == g_triggerNvmPflashBitsError.triggerSBE)
            {
                /* inject single bit error */
                injectECCerrorPFLASH(SINGLE_BIT_ERR_INJ);
                g_triggerNvmPflashBitsError.triggerSBE = FALSE;
            }
            else if(TRUE == g_triggerNvmPflashBitsError.triggerDBE)
            {
                /* inject double bit error */
                injectECCerrorPFLASH(DOUBLE_BIT_ERR_INJ);
                g_triggerNvmPflashBitsError.triggerDBE = FALSE;
            }

            else if(TRUE == g_triggerNvmPflashBitsError.triggerMBE)
            {
                /* inject multiple bit error */
                injectECCerrorPFLASH(MULTIPLE_BIT_ERR_INJ);
                g_triggerNvmPflashBitsError.triggerMBE = FALSE;
            }

        }

        /* sending frame on dedicated time */
        if ((lcl_AppCpu->TickCount_1ms % g_cyclTimeQSPI) == 0)
        {
            /* get the QSPI cycle time from OneEye */
            updateQspiCycleTime();

            /* SM:QSPI:SAFE_COMMUNICATION */
            masterTransmitDataSafeCommQSPI();
        }

        /* Action to do each 150ms
           * DMA part
           */
        if((lcl_AppCpu->TickCount_1ms % 150) == 0)
        {
            /* Initialize DMA module and channel for a data move.
             * Error injection can be started via OneEye menu. */
            initAndRunDmaTransaction();

            /* Error injection DMA ECC */
            if (TRUE == g_trigerSriError.EccErrorDma)
            {
                runDmaSriEccErrorInjection();
            }
        }

        /* Action to do each 200ms
        * SRI Part
        */
        if((lcl_AppCpu->TickCount_1ms % 200) == 0)
        {
            if(TRUE == errorOccurSRI)
            {
              /* SM:SRI:ERROR_HANDLING */
                storeSriDiagnosticInfo();
            }

            /* Error injection */
            if(TRUE == g_trigerSriError.SriError)
            {
                /* run SM:SRI:ERROR_HANDLING */
                runSriErrorInjection();
            }
        }

        /* Action to do each 300ms */
        if ((lcl_AppCpu->TickCount_1ms % 300) == 0)
        {
            transferDataClockPlausibility();
        }
    }

    /* ------------------------------Task Running on CPU1 -----------------------------------*/
    if(CPU1_RUNNING_TASK == cpuIndex)
    {
        /* Action to do each 50 ms */
        if((lcl_AppCpu->TickCount_1ms % 50) == 0)
        {
            /* run SM:AMU.LMU_DAM:DATA_INTEGRITY, needed only once but called here for error injection */
            runDataIntegrityLMUDAM();

            /* run SM:CPU:DATA_INTEGRITY, needed only once */
            runDataIntegrityCpuPspr2();
        }
        /* Action to do each 100 ms */
        if((lcl_AppCpu->TickCount_1ms % 100) == 0)
        {
            timeoutSlaveQspiSafeCommunication();
        }

        /* Action to do each 150 ms */
        if((lcl_AppCpu->TickCount_1ms % 150) == 0)
        {
            /*cyclic check of digital acquisition and digital actuation */
            /* digital Acquisition */
            if (g_SlkStatus.digitalAcqActFucSelect == initializeDAcqFuc0)
            {
                checkRedundancyGtmTim();
            }
            else if (g_SlkStatus.digitalAcqActFucSelect == initializeDAcqFuc1)
            {
                checkRedundancyGtmTimHwDiversity();
            }
            else if (g_SlkStatus.digitalAcqActFucSelect == initializeDAcqFuc2)
            {
                checkRedundancyTimCcu6();
            }
            else if (g_SlkStatus.digitalAcqActFucSelect == initializeDAcqFuc3)
            {
                checkPlausibilityCcu6Gpt12Fuc3();
            }
            else if (g_SlkStatus.digitalAcqActFucSelect == initializeDAcqFuc4)
            {
                checkPlausibilityCcu6Gpt12Fuc4();
            }
            /*digital actuation */
            else if (g_SlkStatus.digitalAcqActFucSelect == initializeDActFuc2)
            {
                plausibilityCheckDactFuc2();
            }
            else {}
        }

        /* Action to do each 200 ms */
        if((lcl_AppCpu->TickCount_1ms % 200) == 0)
        {
            /*cyclic check of Analog Acquisition */
            if (g_slkAnalogAcquisitionRunFlag == runAAcqFuc0Mode)
            {
                runAAcqFuc0();
            }
            else if (g_slkAnalogAcquisitionRunFlag == runAAcqFuc1Mode)
            {
                runAAcqFuc1();
            }
            else if (g_slkAnalogAcquisitionRunFlag == runAAcqFuc2Mode)
            {
                runAAcqFuc2();
            }
            else if (g_slkAnalogAcquisitionRunFlag == runAAcqFuc3Mode)
            {
                runAAcqFuc3();
            }
            else if (g_slkAnalogAcquisitionRunFlag == runAAcqFuc4BrokenWRMode)
            {
                runAAcqFuc4BrokenWR();
            }
            else {}
        }

        /* Action to do each 350 ms */
        if ((lcl_AppCpu->TickCount_1ms % 350) == 0)
        {
            /* run SM:PORT:LOOPBACK */
            runPortLoopbackCheck();

            /* run SM:PORT:REDUNDANCY */
            runPortRedundancyCheck();
        }

        /* The LED 1 is blinking each half-second to inform that the application is running */
        if ((lcl_AppCpu->TickCount_1ms % 500) == 0)
        {
            IfxPort_togglePin(LED1_BOARD_ALIVE_STATUS);
            if (FALSE == g_boardAliveLed)
            {
                g_boardAliveLed = TRUE;
            }
            else
            {
                g_boardAliveLed = FALSE;
            }
        }
    }

    /* ------------------------------Task Running on CPU2 -----------------------------------*/
    if(CPU2_RUNNING_TASK == cpuIndex)
    {
        /* Action to do each 1ms */
        watchdogTicks++;
        if(resetWatchdogTicks == TRUE)
        {
            watchdogTicks = 0;
            resetWatchdogTicks = FALSE;
        }

        /* Action to do each 10ms */
        if((lcl_AppCpu->TickCount_1ms % FTTI) == 0)
        {
            /* Service CPU watchdog to filled SM:CPU:SOFTERR_MONITOR & SM:SYS:SW_SUPERVISION */
            IfxScuWdt_serviceCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword()); /* Set CPU0WD timer to ~0,8 ms */
            resetWatchdogTicks = TRUE;

            /* inject error for STM */
            if(g_SlkStatus.slkRunTask.triggerStmAlarm)
            {
                injectErrorStmMonitor();
                g_SlkStatus.slkRunTask.triggerStmAlarm = FALSE;
            }

            /* Run SBST, SM:CPU:SBST each DTI */
            runSBST();

            /* Run Trap Handling to filled also SM:CPU:SOFTERR_MONITOR */
            runTrapProvocation();

            /* run SM:CPU:SFR_TEST each DTI and Run again one CPU SM if selected in the GUI */
            runCpuSafeComputation();
        }
    }
}

/*
 * This function initialize all LEDs
 * */
void initLEDs()
{
    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED1_BOARD_ALIVE_STATUS, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED1_BOARD_ALIVE_STATUS);

    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED2_ALM_DETECTED, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED2_ALM_DETECTED);
}

/*
 * This function calls all of checks required to initialize the safety lite kit
 * */
void initSafetyLiteKit()
{
    /* Initialize register monitor test */
    slkRunRegMonitorTest();

    /* clear all SMU alarms */
    slkClearAllSmuAlarms();

    /* Initialize the SMU */
    initSMUModule();

    /* Initialize LEDs for board and SMU status */
    initLEDs();

    /* init port for SM:PORT:LOOPBACK */
    initPortLoopback();

    /* init port for SM:PORT:REDUNDANCY */
    initPortRedundancy();

    /* SM:QSPI:SAFE_COMMUNICATION */
    initQspiSlaveSafeCommunication();
    initQspiMasterSafeCommunication();

    /* SM:DTS:DTS_CFG */
    initDieTemperatureSensors();

    /* SMC:GTM:GTM_CONFIG_FOR_ATOM i.e. Initialize atom for isr monitor SM*/
    initGTMATOM();

    /* SM:FCE:CRC_CFG */
    initFCECRC();

    /* Initialize SM:NVM.PFLASH:UPDATE_CHECK */
    initUpdateDataPFLASH();

    /* SM:AMU.LMU_DAM:DATA_INTEGRITY */
    initDataLMUDAM();

    /* SM:CPU:DATA_INTEGRITY */
    initDataCpuPspr2();

    initQspi3ClockPlausibility();

    g_SlkStatus.digitalAcqActFucSelect = noDigitalAcquisition;
    g_SlkStatus.analogAcqFucSelect = noAnalogAcquisitionMode;
    g_slkAnalogAcquisitionRunFlag = noAAcqFuc0Mode;

    g_SlkStatus.slkInitDone = TRUE;

    /* Following SMs are only run one time */

    /* run SM:NVM.PFLASH:INTEGRITY_CHECK, needed only once */
    runIntegrityCheckPFLASH();

    /* run SM:AMU.LMU_DAM:DATA_INTEGRITY, needed only once */
    runDataIntegrityLMUDAM();

    /* run SM:CPU:DATA_INTEGRITY, needed only once */
    runDataIntegrityCpuPspr2();
}

/*
 * This function calls all of checks required to initialize the CPU non-lockstep checks
 * */
void initCpuSafety()
{
    /* Need to initialize the error injection selection as per default */
    g_SlkStatus.provokeTrapsSelect = noTrapProvoke;
    g_CpuStatus.selectCpuTest = noneSelected;

    /* SM:CPU:AP_CHECK */
    slkCpuAccessProtection();

    /* SM:CPU:BUS:MPU_INITCHECK */
    slkCpuBusMpuInitCheck();

    /* SM:CPU:CODE_MPU_CHECK */
    slkCpuCodeMpuCheck();

    /* SM:CPU:DATA_MPU_CHECK */
    slkCpuDataMpuCheck();

    /* SM:CPU:SFR_TEST */
    slkCpuSfrTest();
}
