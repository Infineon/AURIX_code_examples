/* This function initializes the STM for generating an interrupt every milliseconds used for basic periodically tasks scheduling. */
void initStm(App_Stm *lcl_AppStm, IfxCpu_ResourceCpu cpuIndex)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Initialize configuration-structure with defaults. */
    IfxStm_initCompareConfig(&lcl_AppStm->stmConfig);

    /* Change the comparator configuration */
    switch (cpuIndex)
    {
        case IfxCpu_ResourceCpu_0:
            lcl_AppStm->stmSfr = &MODULE_STM0;
            lcl_AppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu0;
            lcl_AppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#if (IFXCPU_NUM_MODULES > 1)
        case IfxCpu_ResourceCpu_1:
            lcl_AppStm->stmSfr = &MODULE_STM1;
            lcl_AppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu1;
            lcl_AppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 2)
        case IfxCpu_ResourceCpu_2:
            lcl_AppStm->stmSfr = &MODULE_STM2;
            lcl_AppStm->stmConfig.typeOfService = IfxSrc_Tos_cpu2;
            lcl_AppStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif

        default:
            while (1) {};
            break;
    }

    lcl_AppStm->stmConfig.comparatorInterrupt   = IfxStm_ComparatorInterrupt_ir0; /* Select the interrupt output */
    lcl_AppStm->stmConfig.ticks                 = IFX_CFG_STM_TICKS_PER_MS*10;    /* Start after 10ms */

    /* Suspend by debugger enabled */
    IfxStm_enableOcdsSuspend(lcl_AppStm->stmSfr);

    /* Now Initialized the comparator */
    IfxStm_initCompare(lcl_AppStm->stmSfr, &lcl_AppStm->stmConfig);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}
