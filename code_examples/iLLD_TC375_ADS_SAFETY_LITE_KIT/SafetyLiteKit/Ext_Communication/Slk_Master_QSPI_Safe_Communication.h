#include "Ifx_Types.h"
#include "IfxQspi_SpiMaster.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define BUFFER_SIZE     4                            /* Buffers size                                                 */
#define FRAME_SIZE      BUFFER_SIZE + 3              /* Buffers size                                                 */
#define NORMAL_CYCLE_TIME        100                 /* 100ms                                                        */
#define EARLY_CYCLE_TIME         90                  /* 90ms                                                         */
#define LATE_CYCLE_TIME          110                 /* 110ms                                                        */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
extern uint8 g_cyclTimeQSPI;

/*********************************************************************************************************************/
/*-------------------------------------------------Data Structures---------------------------------------------------*/
/*********************************************************************************************************************/
typedef enum
{
    normalFrame,
    earlyFrame,
    lateFrame
} QspiSafeTimingError;

 typedef union
{
    struct {
        uint32 data_id;
        uint32 spi4MasterTxBuffer[BUFFER_SIZE];       /* QSPI Master Transmit buffer   */
        uint32 frame_counter;
        uint32 crc;
    }dataM;
    uint32 data[FRAME_SIZE];
} qspiMasterTransmit;

typedef struct
{
    qspiMasterTransmit        qspiMasterTx;           /* Buffers instance              */
    IfxQspi_SpiMaster         spiSafeComMaster;            /* QSPI Master handle            */
    IfxQspi_SpiMaster_Channel spiSafeComMasterChannel;     /* QSPI Master Channel handle    */
    QspiSafeTimingError qpsiSafeTimingErrorInjection;
} qspiMasterSafeComm;
