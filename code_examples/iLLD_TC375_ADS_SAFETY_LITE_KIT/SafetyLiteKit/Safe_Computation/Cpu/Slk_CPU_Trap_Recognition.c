/* The following code is based on the example CPU_Trap_Recognition_1 */
/*
 * The following function provokes synchronous hardware if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvokeSyncHw()
{
    /* Get the pointer to Read Data and Bit Flip Register of specific MBIST module */
    Ifx_MTU_MC *mc = (Ifx_MTU_MC*) (IFXMTU_MC_ADDRESS_BASE + 0x100 * MBIST_REGISTER);

#if AVOID_PROVOCATION
    IfxMtu_enableModule();                                                 /* Enable MTU clock               */
    IfxScuWdt_clearSafetyEndinit(IfxScuWdt_getSafetyWatchdogPassword());   /* Clear SafetyEndInit            */
    IfxMtu_enableMbistShell(MBIST_REGISTER);                               /* Enable MBIST Memory Controller */
#endif

    /* Modify the register value. If the above steps are not done, it is causing a Data Access Synchronous Error */
    mc->RDBFL[0].U++;

#if AVOID_PROVOCATION
    IfxScuWdt_setSafetyEndinit(IfxScuWdt_getSafetyWatchdogPassword());     /* Set SafetyEndInit              */
#endif
}

/*
 * The following function provokes Asynchronous hardware if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvokeAsyncHw()
{
    /* The following code is based on the example SMU_IR_Alarm_1 */
#if AVOID_PROVOCATION
    /* Enable the SMU register configuration by setting the CFGLCK field of the SMU_KEYS register to 0xBC    */
    IfxSmu_unlockConfigRegisters();
    /* Clear SafetyEndInit protection for modification of the configuration registers                        */
    IfxScuWdt_clearSafetyEndinit(IfxScuWdt_getSafetyWatchdogPasswordInline());
#endif

/* Modify the register value. If the above steps are not done, it is causing a Data Access Asynchronous Error*/
MODULE_SMU.AGC.B.IGCS0 = 1;

#if AVOID_PROVOCATION
    /* Set SafetyEndInit protection after modification of the configuration registers                        */
    IfxScuWdt_setSafetyEndinit(IfxScuWdt_getSafetyWatchdogPasswordInline());
    /* After configuration, set a temporary lock of the SMU configuration                                    */
    IfxSmu_lockConfigRegisters();
#endif
}

/*
 * The following function provokes synchronous software if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvokeSyncSw()
{
    Ifx_CPU_PSW psw;        /* Variable of the type PSW (Program Status Word, General Purpose Register)       */

    /* Get the content of the Program Status Word register with __mfcr instruction (Move From Core Register)  */
    psw.U   = __mfcr(CPU_PSW);
    psw.B.USB = 0x40;       /* Set the overflow bit in the PSV register                                       */

    #if AVOID_PROVOCATION
       psw.B.USB = 0x0;    /* Reset the overflow bit in the PSV register                                     */
    #endif

    __mtcr(CPU_PSW, psw.U); /* Write the modified register value with the __mtcr instruction                  */

    /* TRAPV instruction (trap on overflow) call, assembly instruction by using the __asm instruction.
    * If the overflow bit is set, an Arithmetic Overflow Trap will be triggered.
    */
    __asm("trapv");
}

/*
 * The following function provokes protection trap if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvokeProtectionTrap()
{
    uint32 addressToAccess = 0x00000000; /* Variable of the address to be accessed */

    #if AVOID_PROVOCATION
            addressToAccess = 0x70000000; /* change the address to an accessible one */
    #endif

    uint32* x = (uint32*)addressToAccess;
    ++*x;                       /* Try to write to a not accessible address */
}

/*
 * The following function provokes NMI trap if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvokeNmiTrap()
{
    SCU_TRAPCLR.B.TRAP2 = 1U;        /* Set the trap clear to simulate the trap trigger */
    IfxScuWdt_clearCpuEndinit(0x3C); /* if not cleared, the NMI trap will be not visible
                                        because it is slower than Data Access Asynchronous error trap*/

    SCU_TRAPDIS0.B.CPU0TRAP2T = 0U;  /* Last register in the trap generation which enable NMI software traps */

    #if AVOID_PROVOCATION
        SCU_TRAPDIS0.B.CPU0TRAP2T = 1U; /* Disable NMI software trap generation */
    #endif

    SCU_TRAPSET.B.TRAP2 = 1U;
    IfxScuWdt_setCpuEndinit(0x3C);
}

/*
 * Depending on the values of the g_provokeXYTrap (X = Synchronous / Asynchronous; Y = Hardware / Software) global
 * variables, the following function provokes five different trap types if the AVOID_PROVOCATION is set to FALSE.
 * */
void runTrapProvocation(void)
{
    static uint8 lastSlkSelectTrap = noTrapProvoke; /* last Trap selected */
    if (lastSlkSelectTrap != g_SlkStatus.provokeTrapsSelect)
    {
        /* Provoke a trap if selected in OneEye */
        switch (g_SlkStatus.provokeTrapsSelect)
        {
            case noTrapProvoke :
                __nop();
                break;
            case provokeSynchronousHardwareTrap :
                runTrapProvokeSyncHw();
                break;
            case provokeAsynchronousHardwareTrap :
                runTrapProvokeAsyncHw();
                break;
            case provokeSynchronousSoftwareTrap :
                runTrapProvokeSyncSw();
                break;
            case provokeProtectionTrap :
                runTrapProvokeProtectionTrap();
                break;
            case provokeNmiTrap :
                runTrapProvokeNmiTrap();
                break;
            default:
                break;
        }
        lastSlkSelectTrap = g_SlkStatus.provokeTrapsSelect;
    }
}
