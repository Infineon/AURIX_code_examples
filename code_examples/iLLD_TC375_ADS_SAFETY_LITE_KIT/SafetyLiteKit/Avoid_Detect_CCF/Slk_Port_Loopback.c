/*
 * Function to configure the port pins for port loopback SM
 * */
void initPortLoopback(void)
{
    g_SlkStatus.portLoopbackAlarmOnOff = FALSE;

    /* Set pin of PORT_LOOPBACK_MISSION as output to generate mission signal and the output is low */
    IfxPort_setPinModeOutput(PORT_LOOPBACK_MISSION, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Set the pin as output high */
    IfxPort_setPinHigh(PORT_LOOPBACK_MISSION);

    /* Set PORT_LOOPBACK_INTPUT_PIN as input to receive mission signal as loopback (monitor) with pullDown state */
    IfxPort_setPinModeInput(PORT_LOOPBACK_MONITOR, IfxPort_InputMode_pullDown);
}

/*
 * this function compare the mission and monitor signals
 * SM:PORT:LOOPBACK
*/
void runPortLoopbackCheck(void)
{
    boolean missionSignalOut, monitorSignalIn;

    /* get mission signal pin state */
    missionSignalOut = IfxPort_getPinState(PORT_LOOPBACK_MISSION);

    /* get monitor signal pin state */
     monitorSignalIn  = IfxPort_getPinState(PORT_LOOPBACK_MONITOR);


    /* compare loop back mission and monitor signals */
    if( missionSignalOut != monitorSignalIn)
    {
        /* check if the mission and monitor pins are connected as describe in app note */
        if (TRUE == g_SlkStatus.portLoopbackAlarmOnOff)
        {
            /* trigger SMU software alarm */
            softwareCoreAlarmTriggerSMU(SOFT_SMU_ALM_PORT_SMs);
        }
    }
}
