/* Function to configure LVGL
 * Additional information can be found on the LVGL web page */
void tft_lvgl_configure(void)
{
    tft_init();
    lv_init();
    /* Calculates the amount of Bytes per pixel for chosen RGB rendering */
    #define BYTES_PER_PIXEL (LV_COLOR_FORMAT_GET_SIZE(LV_COLOR_FORMAT_RGB565)) /*will be 2 for RGB565 */
    /* Creates display and sets flush */
    lv_display_t * disp = lv_display_create(TFT_XSIZE, TFT_YSIZE);
    lv_display_set_flush_cb(disp, my_flush_cb);
    /* Display buffer */
    static uint8_t buf1[TFT_XSIZE * 32 * BYTES_PER_PIXEL];
    lv_display_set_buffers(disp, buf1, NULL, sizeof(buf1), LV_DISPLAY_RENDER_MODE_PARTIAL);

    /* Touch setup */
    touch_init();
    lv_indev_t * indev = lv_indev_create();
    /* Basic initialization */
    lv_indev_set_type(indev, LV_INDEV_TYPE_POINTER);
    lv_indev_set_read_cb(indev, my_touchpad_read_cb);

}

/* Callback to copy a buffer's content to a specific area of the display
 * Additional information can be found on the LVGL web page */
static void my_flush_cb(lv_display_t * disp, const lv_area_t * area, uint8_t * px_map)
{
    /* The most simple case (also the slowest) to send all rendered pixels to the screen one-by-one.*/
    uint32 numberOfPixel;
    /* Reinterpretation of raw data to color, to make it compatible with existing TFT drivers*/
    lv_color_t * color_p = (lv_color_t *)px_map;
        if ((area->x2 >= area->x1) && area->y2 >= area->y1)
        {
          numberOfPixel = ((area->x2+1) - area->x1) * ((area->y2+1) - area->y1);
          tft_display_setxy((uint32)area->x1, (uint32)area->y1, (uint32)area->x2, (uint32)area->y2);
            tft_flush_row_buff(numberOfPixel, color_p);

        /* Inform the graphics library that you are ready with the flushing */
        lv_disp_flush_ready(disp);
        }
}

/* Callback to read coordinates of touch event
*  Additional information can be found on the LVGL web page */
static void my_touchpad_read_cb(lv_indev_t * indev, lv_indev_data_t * data)
{
    /* Save the pressed coordinates and the state */
    if ((touch_driver.touchmode & MASK_TOUCH_DOWN) != 0 || (touch_driver.touchmode & MASK_TOUCH_MOVE) != 0)
    {
        touch_driver.touchmode &= ~MASK_TOUCH_DOWN;
        touch_driver.touchmode &= ~MASK_TOUCH_MOVE;
        data->point.x = touch_driver.xdisp;
        data->point.y = touch_driver.ydisp;
        data->state = LV_INDEV_STATE_PRESSED;
    }
    else
    {
       data->state = LV_INDEV_STATE_RELEASED;
    }
}
