void enableVoltageSupplyRails(tlf35584 *tlfDevice)
{
    tlf35584SpiFrameType spiFrame;

    spiFrame.U = transferDataTLF35584(SpiCommand_read, DevctrlRegAddr, (uint8)DUMMY_DATA);

    /* Refresh local value of TLF register */
    tlfDevice->DEVCTRL.U = spiFrame.B.data;

    /* Enable all voltage supply rails */
    tlfDevice->DEVCTRL.B.COMEN = CommunicationLdoVoltage_enabled;
    tlfDevice->DEVCTRL.B.VREFEN = VoltageReferenceQVR_enabled;

    /* Update device register values */
    transferDataTLF35584(SpiCommand_write, DevctrlRegAddr, tlfDevice->DEVCTRL.U);
    transferDataTLF35584(SpiCommand_write, DevctrlnRegAddr, ~tlfDevice->DEVCTRL.U);
}

void setStateTransitionTLF35584(tlf35584 *tlfDevice, statereqType requestedStateTransition)
{
    tlf35584SpiFrameType spiFrame;

    spiFrame.U = transferDataTLF35584(SpiCommand_read, DevctrlRegAddr, (uint8)DUMMY_DATA);

    /* Refresh local value of TLF register */
    tlfDevice->DEVCTRL.U = spiFrame.B.data;
    /* Set the requested state transition of TLF device locally */
    tlfDevice->DEVCTRL.B.STATEREQ = requestedStateTransition;

    /* Update device register values */
    transferDataTLF35584(SpiCommand_write, DevctrlRegAddr, tlfDevice->DEVCTRL.U);
    transferDataTLF35584(SpiCommand_write, DevctrlnRegAddr, ~tlfDevice->DEVCTRL.U);
}

/* This function can enable the wake up timer and select the threshold (DEVCFG register)*/
void SLEEPConfig(tlf35584 *tlfDevice)
{

    tlf35584SpiFrameType spiFrame1, spiFrame2, spiFrame3;

    spiFrame1.U = transferDataTLF35584(SpiCommand_read, DEVCFG0RegAddr, (uint8)DUMMY_DATA);
    spiFrame2.U = transferDataTLF35584(SpiCommand_read, DEVCFG1RegAddr, (uint8)DUMMY_DATA);
    spiFrame3.U = transferDataTLF35584(SpiCommand_read, DEVCFG2RegAddr, (uint8)DUMMY_DATA);

    tlfDevice->DEVCFG0.U = spiFrame1.B.data;
    tlfDevice->DEVCFG2.U = spiFrame3.B.data;

    tlfDevice->DEVCFG0.B.WKTIMCYC = Disable;
    tlfDevice->DEVCFG0.B.WKTIMEN = Disable; /*Wake timer disable*/
    tlfDevice->DEVCFG0.B.TREDL = 0x9; /*Time before going to sleep*/
    tlfDevice->DEVCFG2.B.CTHR = 0x3; /*current threshold to go to sleep (100mA)*/

    transferDataTLF35584(SpiCommand_write, DEVCFG0RegAddr, tlfDevice->DEVCFG0.U);
    transferDataTLF35584(SpiCommand_write, DEVCFG2RegAddr, tlfDevice->DEVCFG2.U);
}

/* STBIEN should always be enable*/
void EnableSTANDBYQST(tlf35584 *tlfDevice)
{
    tlf35584SpiFrameType spiFrame;
    spiFrame.U = transferDataTLF35584(SpiCommand_read, SYSPCFG0RegAddr, (uint8)DUMMY_DATA);

    /* Refresh local value of TLF register */
    tlfDevice->SYSPCFG0.U = spiFrame.B.data;

    if(tlfDevice->SYSPCFG0.B.STBIEN == Disable)
    {
        tlfDevice->SYSPCFG0.B.STBIEN = Enable;
    }
    transferDataTLF35584(SpiCommand_write, SYSPCFG0RegAddr, tlfDevice->SYSPCFG0.U);
}

