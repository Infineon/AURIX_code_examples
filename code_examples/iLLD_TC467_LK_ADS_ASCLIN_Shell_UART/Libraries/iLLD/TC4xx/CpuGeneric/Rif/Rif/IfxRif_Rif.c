boolean IfxRif_Rif_initModule(IfxRif_Rif *rif, IfxRif_Rif_Config *config)
{
    uint8   idx;
    boolean status = TRUE;
//    uint32   timeoutCounter = IFXRIF_TIMEOUT_COUNT;

    /* Store the sfr info into the handle */
    rif->rifSFR = config->rifSFR;
    Ifx_RIF *rifSFR = rif->rifSFR;

    /* Store the number of lanes used info into the handle */
    rif->numLanes = config->numLanes;

    /* Check if  RIF clock enabled */
    if (IfxRif_isModuleEnabled(rif->rifSFR) != TRUE)
    {
        IfxRif_enableModule(rif->rifSFR);
    }

    Ifx_RIF_RSM0 rsm0;
    rsm0.U         = rifSFR->RSM0.U;
    /* Select the input as CSI2 interface */
    rsm0.B.DATSRC  = (uint8)0x1u;
    rsm0.B.LCKSTP  = (uint8)config->lockstepEnable;
    rifSFR->RSM0.U = rsm0.U;

    /* COnfigure the number of ramps/chirps and samples/ramp */
    rifSFR->RSM1.B.RAMPS   = config->numRamps - 1u;
    rifSFR->RSM2.B.SAMPLES = config->numSamples - 1u;

    /* Configuration in CSI-2 mode */
    IfxRif_Rif_Csi2Config            csi2Conf = config->csi2;
    rifSFR->ESI.B.DL = (uint8)csi2Conf.length;

    IfxRif_Rif_FormatAndRouteConfig *fruConf = csi2Conf.fruConfig;
    Ifx_RIF_FRM                      frm;
    frm.U = rifSFR->FRM.U;

    if ((config->bypassUnit != NULL_PTR) && (config->bypassUnit->bypassModeEnabled == TRUE) && (config->spuPathEnabled == FALSE))
    {
        frm.B.ALTREN           = fruConf->alternateRampEnable;
        rifSFR->RSM3.B.ALTSMPL = fruConf->numAlternateSamples - 1u;
    }

    /* For noninterleaved data, RIF writes to the SPU is not supported */
    if ((fruConf->nonInterleaveDataEnable == TRUE) && (config->spuPathEnabled == TRUE))
    {
        status = FALSE;
        IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, status);
    }

    if (fruConf->nonInterleaveDataEnable == TRUE)
    {
        frm.B.NINTEN = 0x1u;
        frm.B.CSIREN = fruConf->numRadarVirtualChannel;

        if (fruConf->numRadarVirtualChannel == 1u)
        {
            frm.B.RVC1 = (uint8)fruConf->antenna0Mapping;
        }
        else if (fruConf->numRadarVirtualChannel == 2u)
        {
            frm.B.RVC1 = (uint8)fruConf->antenna0Mapping;
            frm.B.RVC2 = (uint8)fruConf->antenna1Mapping;
        }
        else if (fruConf->numRadarVirtualChannel == 3u)
        {
            frm.B.RVC1 = (uint8)fruConf->antenna0Mapping;
            frm.B.RVC2 = (uint8)fruConf->antenna1Mapping;
            frm.B.RVC3 = (uint8)fruConf->antenna2Mapping;
        }
        else if (fruConf->numRadarVirtualChannel == 4u)
        {
            frm.B.RVC1 = (uint8)fruConf->antenna0Mapping;
            frm.B.RVC2 = (uint8)fruConf->antenna1Mapping;
            frm.B.RVC3 = (uint8)fruConf->antenna2Mapping;
            frm.B.RVC4 = (uint8)fruConf->antenna3Mapping;
        }
        else
        {
            status = FALSE;
            IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, status);
        }
    }
    else /*Interleave data*/
    {
        /* only one virtual channel is allowed to be configured for radar data */
        frm.B.NINTEN = 0x0u;
        frm.B.CSIREN = 0x1u;
        frm.B.RVC1   = (uint8)fruConf->antenna0Mapping;
    }

    rifSFR->FRM.U = frm.U;

    if ((config->bypassUnit != NULL_PTR) && (config->bypassUnit->bypassModeEnabled == TRUE))
    {
        for (idx = 0; idx < IFXRIF_NUM_VIRTUAL_CHANNEL; idx++)
        {
            rifSFR->VCFG[idx].CSIBYPAD.U = csi2Conf.csi2BypassAddrVirtualCh[idx];
            rifSFR->VCFG[idx].CSIMODTH.U = csi2Conf.csi2moduloAddrThreshold[idx];
        }
    }

    /*Enable the virtual channels */
    rifSFR->CSIVCEN.B.VCEN = (uint16)csi2Conf.vcEnable.U;

    /* Configure the Frame and lane management */
    if (config->flm != NULL_PTR)
    {
        Ifx_RIF_FLM                   flm;
        IfxRif_Rif_FifoAndLaneConfig *flmconf = config->flm;

        flm.U      = rifSFR->FLM.U;
        flm.B.MODE = (uint8)flmconf->mode;
        flm.B.FSWP = (uint8)flmconf->fullSwap;

        if (flmconf->complexDataEnable == TRUE)
        {
            flm.B.IQEN    = 0x1u;
            /* If CSI-2 is in complex mode, it always processes complex data in multiplex mode, ignoring FLM.MULCMPX. */
            flm.B.MULCMPX = 0x1u;
            flm.B.EXPIQ   = (uint8)flmconf->selectComponent;
        }
        else
        {
            flm.B.IQEN = 0x0u;
        }

        if (flmconf->crcConfig != NULL_PTR)
        {
            IfxRif_Rif_CrcConfig *crcConf = flmconf->crcConfig;

            flm.B.CRCEN    = (uint8)crcConf->crcEnable;
            flm.B.CRCALT   = (uint8)crcConf->selection;
            flm.B.CRCERIN  = (uint8)crcConf->errorInjectionEnabled;
            flm.B.EXPCRCWO = (uint8)crcConf->wordOrder;
            flm.B.CRCBS    = (uint8)crcConf->byteSwap;
        }

        rifSFR->FLM.U = flm.U;
    }

    /* Data Formatting Unit configuration */
    if (config->dfu != NULL_PTR)
    {
        Ifx_RIF_DFU                      dfu;
        IfxRif_Rif_DataFormatUnitconfig *dfuConf = config->dfu;
        dfu.U         = rifSFR->DFU.U;

        dfu.B.DF      = (uint8)dfuConf->format;
        dfu.B.DA      = (uint8)dfuConf->alignment;
        dfu.B.MSB     = (uint8)dfuConf->direction;
        rifSFR->DFU.U = dfu.U;
    }

    Ifx_RIF_BMU bmu;
    bmu.U = rifSFR->BMU.U;

    if (config->spuPathEnabled == TRUE)
    {
        bmu.B.SPUEN = 0x1u;
    }

    if (config->bypassUnit != NULL_PTR)
    {
        IfxRif_Rif_BypassUnitConfig *bu = config->bypassUnit;
        bmu.B.BYPSEN  = bu->bypassModeEnabled;
        bmu.B.FRMT    = (uint8)bu->format;
        bmu.B.BTRSEL  = (uint8)bu->operation;
        bmu.B.BTRIGEN = (uint8)bu->triggerEnable;

        Ifx_RIF_BUCFG bucfg;
        bucfg.U         = rifSFR->BUCFG.U;

        bucfg.B.MODE    = (uint8)bu->mode;
        bucfg.B.RSEEN   = (uint8)bu->rampStartEndEnable;
        bucfg.B.RS      = (uint16)bu->rampStart - 1u;
        bucfg.B.RE      = (uint16)bu->rampEnd - 1u;
        bucfg.B.SELBYP  = (bu->selectiveBypassEnable == TRUE) ? (uint8)0x1u : (uint8)0x0u;
        bucfg.B.ARO     = (uint8)bu->auxRampOffset;

        rifSFR->BUCFG.U = bucfg.U;
    }

    rifSFR->BMU.U = bmu.U;

    IfxRif_Rif_InterruptConfig *interrupt = &config->interrupt;

    Ifx_RIF_INTCON              intcon;
    intcon.U = rifSFR->INTCON.U;

    intcon.U = (uint32)(((uint32)interrupt->error.frameWatchdogEnable << IFX_RIF_INTCON_FWE_OFF) | ((uint32)interrupt->error.crcFlag0Enable << IFX_RIF_INTCON_CRCE0_OFF) | \
                        ((uint32)interrupt->error.crcFlag1Enable << IFX_RIF_INTCON_CRCE1_OFF) | ((uint32)interrupt->error.crcFlag2Enable << IFX_RIF_INTCON_CRCE2_OFF) |    \
                        ((uint32)interrupt->error.crcFlag3Enable << IFX_RIF_INTCON_CRCE3_OFF) | ((uint32)interrupt->error.ramp1Enable << IFX_RIF_INTCON_R1EE_OFF));
    intcon.U |= (uint32)(((uint32)interrupt->interrupt0.ramp1StartEnable << IFX_RIF_INTCON_R1SE_OFF) | ((uint32)interrupt->interrupt0.rampEndEnable << IFX_RIF_INTCON_REE_OFF));
    intcon.U |= (uint32)(((uint32)interrupt->interrupt1.fifoOverflowErrEnable) | ((uint32)interrupt->interrupt1.bufferOverflowErrEnable << IFX_RIF_INTCON_BFOE_OFF) |                \
                         ((uint32)interrupt->interrupt1.bypassDoneEnable << IFX_RIF_INTCON_BYPE_OFF) | ((uint32)interrupt->interrupt1.frameSyncEnable << IFX_RIF_INTCON_FSNCE_OFF) | \
                         ((uint32)interrupt->interrupt1.lineSyncEnable << IFX_RIF_INTCON_LSNCE_OFF));

    rifSFR->INTCON.U = intcon.U & 0xffffu;

    Ifx_RIF_INTEN inten;
    inten.U = rifSFR->INTEN.U;

    inten.U = (uint32)(((uint32)interrupt->error.idiEnable << IFX_RIF_INTEN_IDIEE_OFF) |   \
                       ((uint32)interrupt->error.idiParity << IFX_RIF_INTEN_IDIPEEN_OFF) | \
                       ((uint32)interrupt->error.sriErrorEnable << IFX_RIF_INTEN_SRIEE_OFF));

    inten.U |= (uint32)(((uint32)interrupt->interrupt0.lvdsAddrThresholdEnable) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_0 << IFX_RIF_INTEN_VCMTE0_OFF) |                                  \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_1 << IFX_RIF_INTEN_VCMTE1_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_2 << IFX_RIF_INTEN_VCMTE2_OFF) |     \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_3 << IFX_RIF_INTEN_VCMTE3_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_4 << IFX_RIF_INTEN_VCMTE4_OFF) |     \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_5 << IFX_RIF_INTEN_VCMTE5_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_6 << IFX_RIF_INTEN_VCMTE6_OFF) |     \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_7 << IFX_RIF_INTEN_VCMTE7_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_8 << IFX_RIF_INTEN_VCMTE8_OFF) |     \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_9 << IFX_RIF_INTEN_VCMTE9_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_10 << IFX_RIF_INTEN_VCMTE10_OFF) |   \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_11 << IFX_RIF_INTEN_VCMTE11_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_12 << IFX_RIF_INTEN_VCMTE12_OFF) | \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_13 << IFX_RIF_INTEN_VCMTE13_OFF) | ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_14 << IFX_RIF_INTEN_VCMTE14_OFF) | \
                        ((uint32)interrupt->interrupt0.addrThresholdEnable_vc_15 << IFX_RIF_INTEN_VCMTE15_OFF) | ((uint32)interrupt->interrupt0.rampStartEnd << IFX_RIF_INTEN_RSECE_OFF));
    inten.U |= (uint32)(((uint32)interrupt->interrupt1.ldoShortHighEnable << IFX_RIF_INTEN_LDOSHEN_OFF) | ((uint32)interrupt->interrupt1.ldoShortLowEnable << IFX_RIF_INTEN_LDOSLEN_OFF) | \
                        ((uint32)interrupt->interrupt1.escModeTrig << IFX_RIF_INTEN_ESCMTIEN_OFF));

    rifSFR->INTEN.U = inten.U;

    if (interrupt->error.isrPriority > 0u)
    {
        volatile Ifx_SRC_SRCR *src = IfxRif_getSrcPointer(rif->rifSFR, IfxRif_Interrupt_error);
        IfxSrc_init(src, interrupt->error.isrProvider, interrupt->error.isrPriority, interrupt->error.vmId);
        IfxSrc_enable(src);
    }

    if (interrupt->interrupt0.isrPriority > 0u)
    {
        volatile Ifx_SRC_SRCR *src = IfxRif_getSrcPointer(rif->rifSFR, IfxRif_Interrupt_int0);
        IfxSrc_init(src, interrupt->interrupt0.isrProvider, interrupt->interrupt0.isrPriority, interrupt->interrupt0.vmId);
        IfxSrc_enable(src);
    }

    if (interrupt->interrupt1.isrPriority > 0u)
    {
        volatile Ifx_SRC_SRCR *src = IfxRif_getSrcPointer(rif->rifSFR, IfxRif_Interrupt_int1);
        IfxSrc_init(src, interrupt->interrupt1.isrProvider, interrupt->interrupt1.isrPriority, interrupt->interrupt1.vmId);
        IfxSrc_enable(src);
    }

    return status;
}


