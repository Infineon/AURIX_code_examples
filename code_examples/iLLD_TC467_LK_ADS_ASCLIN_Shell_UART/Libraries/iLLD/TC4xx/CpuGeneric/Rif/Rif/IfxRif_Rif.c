void IfxRif_Rif_startDataStream(IfxRif_Rif *rif)
{
    Ifx_RIF *rifSFR = rif->rifSFR;

    switch (rif->numLanes)
    {
    case IfxRif_NumLanes_1:
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_0);
        break;
    case IfxRif_NumLanes_2:
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_1);
        break;
    case IfxRif_NumLanes_3:
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_1);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_2);
        break;
    case IfxRif_NumLanes_4:
    default:
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_1);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_2);
        IfxRif_enableDataStream(rifSFR, IfxRif_DataLane_3);
        break;
    }
}


void IfxRif_Rif_stopDataStream(IfxRif_Rif *rif)
{
    Ifx_RIF *rifSFR = rif->rifSFR;

    switch (rif->numLanes)
    {
    case IfxRif_NumLanes_1:
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_0);
        break;
    case IfxRif_NumLanes_2:
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_1);
        break;
    case IfxRif_NumLanes_3:
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_1);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_2);
        break;
    case IfxRif_NumLanes_4:
    default:
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_0);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_1);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_2);
        IfxRif_disableDataStream(rifSFR, IfxRif_DataLane_3);
        break;
    }
}


boolean IfxRif_Rif_getErrorStatus(IfxRif_Rif *rif)
{
    boolean  status = FALSE;
    Ifx_RIF *rifSFR = rif->rifSFR;

    if ((rifSFR->INTCON.U & 0xFFFF0000u) != 0x0u)
    {
        status                      = TRUE;
        rif->status.source1Status.U = rifSFR->INTCON.U & 0xFFFF0000u;
    }

    if (rifSFR->INTFLG.U != 0x0u)
    {
        status                      = TRUE;
        rif->status.source2Status.U = rifSFR->INTFLG.U;
    }

    return status;
}

