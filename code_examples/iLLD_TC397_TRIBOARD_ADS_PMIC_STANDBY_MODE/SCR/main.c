/* Use dedicated register files dependent on the used compiler */
#include "Scr_Io_sdcc.h"
#include "Scr_Scu_sdcc.h"
#include "Scr_Rtc_sdcc.h"
#include "Scr_Irq_sdcc.h"

void main(void)
{
    /* SCU (Standby Controller) module configurations*/
    SCR_SCU_PAGE = 1;
    SCR_PMCON1 = 0x30;         /* OCDS, RTC and SSC enabled */

    SCR_SCU_PAGE = 0;
    SCR_IRCON0 = 0x0;          /* Reset interrupts          */
    SCR_RTC_CON = (0 << 1);

    /* Interrupt configuration*/
    SCR_SCU_PAGE = 2;
    SCR_MODPISEL2 = 0x40;   /* selection of P33.11 as external interrupt 6 */
    SCR_SCU_PAGE = 1;
    SCR_EXICON0 = 0xFF;
    SCR_EXICON1 = 0xDF;     /* enable external interrupt 6 on rising edge */
    SCR_EXICON2 = 0xFF;
    SCR_EXICON3 = 0xFF;
    SCR_IEN0 = 0x80;        /* enable all pending interrupt request  */
    SCR_IEN1 = 0x08;        /* enable node XINTR9 (EXM)  */
    SCR_SCU_PAGE = 0;


    /* IO (Port) module configuration                                                     */
    SCR_IO_PAGE = 2;
    SCR_P00_PDISC = 0x00;      /* Enable P00 all pins  */
    SCR_P01_PDISC = 0x00;      /* Enable P01 all pins  */
    SCR_IO_PAGE = 0;
    SCR_P00_OUT = 0x00;        /* Initialize P00.4 (P33.4) out                            */
    SCR_P01_OUT = 0x04;        /* Initialize P01.2 (P33.10) and P01.3 (P33.11) out        */
    SCR_IO_PAGE = 1;

    SCR_P00_IOCR4 = 0x80;      /* set P00.4 / P33.4 as output (LED on TriBoard indicate the state of WAK)           */
    SCR_P01_IOCR2 = 0x80;      /* set P01.2 / P33.10 as output (WAK)                      */
    SCR_P01_IOCR3 = 0x00;      /* set P01.3 / P33.11 as input with no pull (Button)       */
    SCR_IO_PAGE = 0;

    while(1){}
}

/* interrupt function which toggle the sate of the PIN 33. 10 */
void ex6_interrupt(void) __interrupt(XINTR9)
{
    unsigned char ioPage, scuPage;

    scuPage = SCR_SCU_PAGE;
    SCR_SCU_PAGE = 0;
    ioPage = SCR_IO_PAGE;
    SCR_IO_PAGE = 0;
    SCR_IRCON0 = (1 << 6);

    SCR_P01_OMTR = 0x04;
    SCR_P00_OMTR = 0x10;

    SCR_IO_PAGE = ioPage;
    SCR_SCU_PAGE = scuPage;
}
