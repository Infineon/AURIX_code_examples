#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "BUS_MPU_Test.h"

extern IfxCpu_syncEvent g_cpuSyncEvent;
extern uint32 g_testIdentifier;

void core1_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG1 IS DISABLED HERE!!
     * Enable the watchdog and service it periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

#if TEST_CORE_CONFIG == TEST_CORE_1
    Test_LED_init();                                               /* Initialize the LEDs                            */
#endif


    while(1)
    {
        /*---------Test Configuration ---------*/
#if TEST_CORE_CONFIG == TEST_CORE_1
        switch (g_testIdentifier)
        {
            case TEST_1_DLMU:
                Test1_DLMU_configuration();
                break;

            case TEST_1_SPR:
                Test1_SPR_configuration();
                break;

            case TEST_1_PFLASH:
                Test1_PFlash_configuration();
                break;

            case TEST_2_DLMU:
                Test2_DLMU_configuration();
                break;

            case TEST_2_SPR:
                Test2_SPR_configuration();
                break;

            case TEST_2_PFLASH:
                Test2_PFlash_configuration();
                break;

            case TEST_3_DLMU_NON_OVERLAP:
            case TEST_3_DLMU_OVERLAP:
                Test3_DLMU_configuration();
                break;

            case TEST_3_SPR_NON_OVERLAP:
                Test3_SPR_configuration();
                break;

            case TEST_3_SPR_OVERLAP:
                Test3_SPR_configuration();
                break;

            case TEST_3_PFLASH:
                Test3_PFlash_configuration();
                break;

            default:
                break;
        }

#endif

        /*---------Synch Configuration ---------*/
        IfxCpu_emitEvent(&g_cpuSyncEvent);
        IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

        /*---------Test Execute ---------------*/
#if TEST_CORE_EXECUTE == TEST_CORE_1

        switch (g_testIdentifier)
        {
            case TEST_1_DLMU:
                Test1_DLMU_execute();
                break;

            case TEST_1_SPR:
                Test1_SPR_execute();
                break;

            case TEST_1_PFLASH:
                Test1_PFlash_execute();
                break;

            case TEST_2_DLMU:
                Test2_DLMU_execute();
                break;

            case TEST_2_SPR:
                Test2_SPR_execute();
                break;

            case TEST_2_PFLASH:
                Test2_PFlash_execute();
                break;

            case TEST_3_DLMU_NON_OVERLAP:
                Test3_DLMU_NonOverlap_execute();
                break;

            case TEST_3_DLMU_OVERLAP:
                Test3_DLMU_Overlap_execute();
                break;

            case TEST_3_SPR_NON_OVERLAP:
                Test3_SPR_NonOverlap_execute();
                break;

            case TEST_3_SPR_OVERLAP:
                Test3_SPR_Overlap_execute();
                break;

            case TEST_3_PFLASH:
                Test3_PFlash_execute();
                break;
            default:
                break;
        }

#endif
        /*---------Synch Execute ---------*/
        IfxCpu_emitEvent(&g_cpuSyncEvent);
        IfxCpu_waitEvent(&g_cpuSyncEvent, 1);
    }
}
