/* This function enables the clock of the EVADC for the fast compare ADC operation
 * */
void initFCAdc()
{
    uint16  endinit_pw;
    endinit_pw     = IfxScuWdt_getCpuWatchdogPassword(); /* Get the password for the current CPU */

    /* Clear the Endinit Function to access the protected Registers */
    IfxScuWdt_clearCpuEndinit(endinit_pw);
    EVADC_CLC.U   = 0x00000000;  /* load clock control register */
    IfxScuWdt_setCpuEndinit(endinit_pw);

}

/* This function configures the FCADC channel in the software mode with
 * the reference and boundary flag passed by the user. The boundary flag is then
 * used by the TIM channels to detect the speed pulse and the AK data
 * */
void initFCADCchannel(uint8 cluster, uint16 reference, uint8 boundaryFlag)  /* Initialize the Fast Compare ADC */
{

   MODULE_EVADC.FC[cluster].FCCTRL.U =  (0 << 0 /*STCF*/)       |        /* use the typical sample time */
                                        (1 << 5 /*RPE*/)        |        /* pre-charge enable */
                                        (0 << 6 /*AIPF*/)       |        /* no pre-charge on analog input */
                                        (0 << 8 /*CHEVMODE*/)   |        /* channel event if result is high */
                                        (3 << 10 /*DIVA*/)      |        /* default analog clock */
                                        (1 << 15 /*CPWC*/)      |        /* write control bit */
                                        (0 << 16 /*XTSEL*/)     |        /* trigger selection  GTM_adcz_trig0 */
                                        (0 << 21 /*XTMODE*/)    |        /* trigger on rising edge */
                                        (0 << 23 /*XTPOL*/)     |        /* leave polarity as normal */
                                        (0 << 24 /*GTMODE*/)    |        /* Alternate gate function */
                                        (0 << 26 /*FCCHNR*/)    |        /* Channel 0 is the only one available input */
                                        (1 << 31 /*XTWC*/);              /* Write Control */

    MODULE_EVADC.FC[cluster].FCM.U    = (1 << 0  /*RUNCOMP*/)|           /* Always Run */
                                        (0 << 2  /*RUNRAMP*/)   |        /* No ramp */
                                        (0 << 4  /*FCRDIR*/)    |        /* Don't care. not ramping */
                                        (1 << 5  /*ANON*/)      |        /* Analog Converter Control ON */
                                        (0 << 6  /*ACSD*/)      |        /* No delay for clock synchronization */
                                        (0 << 8  /*FCTRIV*/)    |        /* Interval is 16 clock cycles */
                                        (0 << 16 /*SRG*/)       |        /* No service request..will use channel event */
                                        (0 << 18 /*AUE*/)       |        /* automatic update alternate value */
                                        (0 << 20 /*SSE*/)       |        /* No synchronization */
                                        (1 << 21 /*FCMWC*/)     |        /* Write control */
                                        (reference << 22 /*FCREF*/);     /* Compare Value */

    MODULE_EVADC.FC[cluster].FCBFL.U  = (0 << 4 /*BFA*/)        |        /* Boundary Flag Activation Select */
                                        (0 << 8 /*BFI*/)        |        /* Boundary Flag Inversion */
                                        (1 << 16 /*BFM*/)       |        /* Boundary Flag Mode Control */
                                        (0 << 17 /*BFV*/)       |        /* Boundary Flag Value */
                                        (boundaryFlag << 24 /*BFLNP*/);  /* CBFLOUT */

    MODULE_EVADC.FC[cluster].FCHYST.U = (10 << 2 /*DELTAMINUS*/)|        /* Lower Delta */
                                        (10 << 18 /*DELTAPLUS*/);        /* Higher Delta */

}

/* This function enables the FCADC channels with the required threshold to
 * isolate the speed pulse and the AK data signal from the WSS output, the
 * boundary flag of these channels represents the speed pulse and the AK data
 * signal
 * */
void startFCAdc()
{
    initFCAdc();
    initFCADCchannel(FCADC0, DATA_THRESHOLD, CBFLOUT2);
    initFCADCchannel(FCADC3, SPEED_PULSE_THRESHOLD, CBFLOUT0);
}
