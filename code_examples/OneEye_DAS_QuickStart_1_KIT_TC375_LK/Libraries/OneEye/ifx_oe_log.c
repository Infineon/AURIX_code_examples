#include "ifx_oe_log.h"

#include <stdio.h>
#include <string.h>

#define IFX_OE_LOG_MESSAGE_SIZE           (255)
#define IFX_OE_LOG_FORMATTED_MESSAGE_SIZE (20)

typedef struct
{
    Ifx_Oe_Log_Verbose level;
    Ifx_Oe_Log_InfoLevel infoLevel;
    boolean initialized;
    Ifx_Oe_StdIf_DPipe* outputs[IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT];
}Ifx_Oe_Log;

Ifx_Oe_Log Ifx_Oe_Log_g_log =
{
    .level       = Ifx_Oe_Log_Verbose_warning,
    .infoLevel   = Ifx_Oe_Log_InfoLevel_0,
    .initialized = FALSE
};

void Ifx_Oe_Log_init(Ifx_Oe_StdIf_DPipe* output)
{
    uint8 i;
    Ifx_Oe_Log_g_log.level     = Ifx_Oe_Log_Verbose_warning;
    Ifx_Oe_Log_g_log.infoLevel = Ifx_Oe_Log_InfoLevel_0;

    for (i = 0; i < IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT; i++)
    {
        Ifx_Oe_Log_g_log.outputs[i] = NULL_PTR;
    }

    Ifx_Oe_Log_g_log.initialized = TRUE;

    Ifx_Oe_Log_addOutput(output);
}


void Ifx_Oe_Log_deInit()
{
    uint8 i;

    for (i = 0; i < IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT; i++)
    {
        Ifx_Oe_Log_g_log.outputs[i] = NULL_PTR;
    }
}


boolean Ifx_Oe_Log_addOutput(Ifx_Oe_StdIf_DPipe* output)
{
    if (output != NULL_PTR)
    {
        uint8 i;

        for (i = 0; i < IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT; i++)
        {
            if (Ifx_Oe_Log_g_log.outputs[i] == NULL_PTR)
            {
                Ifx_Oe_Log_g_log.outputs[i] = output;
                return TRUE;
            }
        }
    }
    else
    {
        return TRUE;
    }

    return FALSE;
}


void Ifx_Oe_Log_setVerboseLevel(Ifx_Oe_Log_Verbose level)
{
    Ifx_Oe_Log_g_log.level = level;
}


void Ifx_Oe_Log_setInfoLevel(Ifx_Oe_Log_InfoLevel level)
{
    Ifx_Oe_Log_g_log.infoLevel = level;
}


Ifx_Oe_Log_InfoLevel Ifx_Oe_Log_getInfoLevel(void)
{
    return Ifx_Oe_Log_g_log.infoLevel;
}


void Ifx_Oe_Log_sendText(const pchar text)
{
    if (Ifx_Oe_Log_g_log.initialized)
    {
        uint8 i;

        for (i = 0; i < IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT; i++)
        {
            if (Ifx_Oe_Log_g_log.outputs[i] != NULL_PTR)
            {
                Ifx_Oe_StdIf_DPipe_writeText(Ifx_Oe_Log_g_log.outputs[i], Ifx_Oe_Time_infinite, text);
            }
        }
    }
}


void Ifx_Oe_Log_fatal(pchar format, ...)
{
    va_list args;
    va_start(args, format);
    Ifx_Oe_Log_fatalVA(format, args);
    va_end(args);
}


void Ifx_Oe_Log_fatalVA(pchar format, va_list args)
{
    if (Ifx_Oe_Log_g_log.level >= Ifx_Oe_Log_Verbose_fatal)
    {
        char message1[IFX_OE_LOG_MESSAGE_SIZE + 1];
        char message2[IFX_OE_LOG_FORMATTED_MESSAGE_SIZE + IFX_OE_LOG_MESSAGE_SIZE + 1];
        vsnprintf((char*)message1, sizeof(message1), format, args);
        snprintf(message2, sizeof(message2), ">>> FATAL   : %s" IFX_OE_ENDL, message1);
        Ifx_Oe_Log_sendText(message2);
    }
}


