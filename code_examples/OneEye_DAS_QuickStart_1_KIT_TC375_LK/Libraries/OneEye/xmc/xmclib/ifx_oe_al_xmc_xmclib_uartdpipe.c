/* FIXME Remove this UART_TX_FIFO_SIZE macro and use the configuration value from register */
#define IFX_OE_UARTDPIPE_UART_BUFFER_SIZE 64

void    Ifx_Oe_UartDPipe_initiateTransmission(Ifx_Oe_UartDPipe* uart);

boolean Ifx_Oe_UartDPipe_write(Ifx_Oe_UartDPipe* uart, const void* data, Ifx_Oe_SizeT* count, Ifx_Oe_TickTime timeout)
{
    Ifx_Oe_SizeT left;
    boolean      result = TRUE;

    if (*count != 0)
    {
        left = Ifx_Oe_Fifo_write(uart->tx, data, *count, timeout);
        Ifx_Oe_UartDPipe_initiateTransmission(uart);

        *count -= left;
        result  = left == 0;
    }

    return result;
}


boolean Ifx_Oe_UartDPipe_read(Ifx_Oe_UartDPipe* uart, void* data, Ifx_Oe_SizeT* count, Ifx_Oe_TickTime timeout)
{
    Ifx_Oe_SizeT left = Ifx_Oe_Fifo_read(uart->rx, data, *count, timeout);

    *count -= left;

    return left == 0;
}


sint32 Ifx_Oe_UartDPipe_getReadCount(Ifx_Oe_UartDPipe* uart)
{
    return Ifx_Oe_Fifo_readCount(uart->rx);
}


Ifx_Oe_StdIf_DPipe_ReadEvent Ifx_Oe_UartDPipe_getReadEvent(Ifx_Oe_UartDPipe* uart)
{
    return &uart->rx->eventWriter;
}


