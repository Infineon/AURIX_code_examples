boolean Ifx_Oe_UartDPipe_initDPipe(Ifx_Oe_StdIf_DPipe* stdif, Ifx_Oe_UartDPipe* uart)
{
    Ifx_Oe_StdIf_DPipe_initStdIf(stdif);

    /* Set the API link */
    stdif->driver         = uart;
    stdif->write          = (Ifx_Oe_StdIf_DPipe_Write) & Ifx_Oe_UartDPipe_write;
    stdif->read           = (Ifx_Oe_StdIf_DPipe_Read) & Ifx_Oe_UartDPipe_read;
    stdif->getReadCount   = (Ifx_Oe_StdIf_DPipe_GetReadCount) & Ifx_Oe_UartDPipe_getReadCount;
    stdif->getReadEvent   = (Ifx_Oe_StdIf_DPipe_GetReadEvent) & Ifx_Oe_UartDPipe_getReadEvent;
    stdif->getWriteCount  = (Ifx_Oe_StdIf_DPipe_GetWriteCount) & Ifx_Oe_UartDPipe_getWriteCount;
    stdif->getWriteEvent  = (Ifx_Oe_StdIf_DPipe_GetWriteEvent) & Ifx_Oe_UartDPipe_getWriteEvent;
    stdif->canReadCount   = (Ifx_Oe_StdIf_DPipe_CanReadCount) & Ifx_Oe_UartDPipe_canReadCount;
    stdif->canWriteCount  = (Ifx_Oe_StdIf_DPipe_CanWriteCount) & Ifx_Oe_UartDPipe_canWriteCount;
    stdif->flushTx        = (Ifx_Oe_StdIf_DPipe_FlushTx) & Ifx_Oe_UartDPipe_flushTx;
    stdif->clearTx        = (Ifx_Oe_StdIf_DPipe_ClearTx) & Ifx_Oe_UartDPipe_clearTx;
    stdif->clearRx        = (Ifx_Oe_StdIf_DPipe_ClearRx) & Ifx_Oe_UartDPipe_clearRx;
    stdif->onReceive      = (Ifx_Oe_StdIf_DPipe_OnReceive) & Ifx_Oe_UartDPipe_onReceive;
    stdif->onTransmit     = (Ifx_Oe_StdIf_DPipe_OnTransmit) & Ifx_Oe_UartDPipe_onTransmit;
    stdif->onError        = (Ifx_Oe_StdIf_DPipe_OnError) & Ifx_Oe_UartDPipe_onError;
    stdif->getSendCount   = (Ifx_Oe_StdIf_DPipe_GetSendCount) & Ifx_Oe_UartDPipe_getSendCount;
    stdif->getTxTimeStamp = (Ifx_Oe_StdIf_DPipe_GetTxTimeStamp) & Ifx_Oe_UartDPipe_getTxTimeStamp;
    stdif->resetSendCount = (Ifx_Oe_StdIf_DPipe_ResetSendCount) & Ifx_Oe_UartDPipe_resetSendCount;
    stdif->txDisabled     = FALSE;
    return TRUE;
}


