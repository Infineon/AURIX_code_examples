#define CPU1_RUNNING_TASK                     1       /* CPU which run the task                                      */
#define ISR_STM_PRIORITY                      1       /* interrupt priority                                          */
#define IFX_CFG_STM_TICKS_PER_MS              100000  /* brief Number of STM ticks per millisecond                   */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
