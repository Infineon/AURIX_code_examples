/*
 * This interrupt is raised by the STM1.
 * isrProvider CPU1 and isrPriority ISR_PRIORITY_OS_TICK
 */
void isrStm1()
{
    static uint32 tickCount_1ms = 0;
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM1, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the 1ms value */
    tickCount_1ms++;

    /*
    * IR:ISR_MONITOR
    * Plausibility check for periodic interrupts is applied
    * */
    /* Check at low value (245ms), if the ATOM interrupt is already occurred */
    if (tickCount_1ms == (uint32)(ISR_CYCLE_TIME_LOW_LIMIT))
    {
        if (atomInterruptOccurred == TRUE)
        {
            /* unintended interrupt occurred i.e. LED1 On (alarm) */
            IfxPort_setPinLow(LED1);
            atomInterruptOccurred = FALSE;
        }
        else
        {
            /* LED1 Off i.e. working as expected */
            IfxPort_setPinHigh(LED1);
        }
    }

    /* Check at high value (255ms), if the ATOM interrupt is already occurred */
    if (tickCount_1ms == (uint32) (ISR_CYCLE_TIME_HIGH_LIMIT))
    {
        if (atomInterruptOccurred == TRUE)
        {
            /* LED Off i.e. working as expected */
            IfxPort_setPinHigh(LED2);
            atomInterruptOccurred = FALSE;
        }
        else
        {
            /* Missing Interrupt i.e. LED2 On (alarm) */
            IfxPort_setPinLow(LED2);
        }
    }

    /* reset the tickCount_1ms */
    if (tickCount_1ms == (uint32) (ISR_CYCLE_TIME_HIGH_LIMIT))
    {
        tickCount_1ms = (uint32) (ISR_CYCLE_TIME_HIGH_LIMIT - INTERRUPT_CYCLE_TIME);
    }
}

/*
 * This function initializes the STM for generating an interrupt every milliseconds used for basic periodically
 * */
void initSTM()
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Initialize configuration-structure with defaults. */
    IfxStm_initCompareConfig(&stmConfig);
    stmConfig.typeOfService = IfxCpu_Irq_getTos(CPU1_RUNNING_TASK);
    stmConfig.triggerPriority = ISR_STM_PRIORITY;
    stmConfig.comparatorInterrupt   = IfxStm_ComparatorInterrupt_ir0; /* Select the interrupt output */
    stmConfig.ticks                 = IFX_CFG_STM_TICKS_PER_MS;      /* Start after 1 ms */

    /* Suspend by debugger enabled */
    IfxStm_enableOcdsSuspend(&MODULE_STM1);

    /* Now Initialized the comparator */
    IfxStm_initCompare(&MODULE_STM1, &stmConfig);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}
