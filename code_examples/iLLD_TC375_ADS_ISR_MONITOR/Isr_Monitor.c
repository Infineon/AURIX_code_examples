/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define CPU0_RUNNING_TASK                  0  /* CPU which run the task                                              */
#define ISR_PRIORITY_ATOM_ISR_MON          2  /* interrupt priority                                                  */
#define INJECT_UNINTENDED_INTERRUPT_ERROR  1  /* inject error i.e. unintended interrupt error                        */
#define INJECT_MISSING_INTERRUPT_ERROR     0  /* inject error i.e. missing interrupt error                           */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxGtm_Atom_Timer  g_timerDriver;            /* ATOM driver                                                          */
boolean atomInterruptOccurred = FALSE;       /* flag for interrupt occurrence                                        */

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void initGTM(void);
void initGTMATOM(void);
void initLEDandButton(void);
IFX_INTERRUPT(isrGtmAtom, CPU0_RUNNING_TASK, ISR_PRIORITY_ATOM_ISR_MON);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/*
 * Interrupt Service Routine of the ATOM
 * */
void isrGtmAtom(void)
{
    /* flag that interrupt occurred */
    atomInterruptOccurred = TRUE;

    /* Reset the timer event */
    IfxGtm_Atom_Timer_acknowledgeTimerIrq(&g_timerDriver);
}

/*
 * Error injection for missing/unintended interrupt
 * */
void errorInjection(void)
{
    /* fault injection i.e by stopping atom timer */
    if (FALSE == IfxPort_getPinState(BUTTON1))
    {
#if (INJECT_UNINTENDED_INTERRUPT_ERROR == 1) && (INJECT_MISSING_INTERRUPT_ERROR == 0)
        /* unintended interrupt error injection */
        MODULE_GTM.CTRL.B.RF_PROT = 0;
        IfxGtm_Atom_Ch_raiseInterruptZero(g_timerDriver.atom, g_timerDriver.timerChannel);
        MODULE_GTM.CTRL.B.RF_PROT = 1;
#elif (INJECT_UNINTENDED_INTERRUPT_ERROR == 0) && (INJECT_MISSING_INTERRUPT_ERROR == 1)
        /*  inject missing interrupt error i.e stop atom timer */
        IfxGtm_Atom_Timer_stop(&g_timerDriver);
#else
        /* no error injection */
#endif
    }
}

/*
 * Initialize the GTM module
 * */
void initGTM(void)
{
    /* enable GTM module */
    IfxGtm_enable(&MODULE_GTM);

    /* set the global clock to 50MHz */
    IfxGtm_Cmu_setGclkFrequency(&MODULE_GTM, 50000000.0f);

    /* set CMU0 frequency to 50MHz */
    IfxGtm_Cmu_setClkFrequency(&MODULE_GTM, IfxGtm_Cmu_Clk_0, 50000000.0f);

    /* enable CMU0 clock */
    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK0);

    /* enable FX clock*/
    IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_FXCLK);
}

/*
 * This function initializes the GTM ATOM
 * */
void initGTMATOM(void)
{
    initGTM();

    IfxGtm_Atom_Timer_Config timerConfig;                                       /* Timer configuration structure    */
    IfxGtm_Atom_Timer_initConfig(&timerConfig, &MODULE_GTM);                    /* Initialize default parameters    */

    timerConfig.atom = IfxGtm_Atom_0;                                           /* Select the ATOM_0                */
    timerConfig.timerChannel = IfxGtm_Atom_Ch_0;                                /* Select channel 0                 */
    timerConfig.clock = IfxGtm_Cmu_Clk_0;                                       /* Select the CMU clock 0           */
    timerConfig.base.frequency = ATOM_FREQ;                                     /* Set timer frequency              */
    timerConfig.base.isrPriority = ISR_PRIORITY_ATOM_ISR_MON;                   /* Set interrupt priority           */
    timerConfig.base.isrProvider = IfxCpu_Irq_getTos(CPU0_RUNNING_TASK);        /* Set interrupt provider           */

    IfxGtm_Atom_Timer_init(&g_timerDriver, &timerConfig);                       /* Initialize the ATOM              */

    IfxGtm_Atom_Timer_run(&g_timerDriver);                                      /* Start the ATOM                   */
}

/*
 * This function initializes the port pin which drives the LEDs and button
 * */
void initLEDandButton(void)
{
    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED1, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    IfxPort_setPinModeOutput(LED2, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Initialization of the button as input */
    IfxPort_setPinModeInput(BUTTON1, IfxPort_Mode_inputPullDown);

    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED1);
    IfxPort_setPinHigh(LED2);
}

/*
 * Initialize modules to check ISR monitor
 * */
void initIsrMonitorModule(void)
{
    /* initialize LEDs and button */
    initLEDandButton();

    /* Configure the STM module to periodic check */
     initSTM();

    /* initialize GTM ATOM to generate periodic interrupt*/
    initGTMATOM();
}
