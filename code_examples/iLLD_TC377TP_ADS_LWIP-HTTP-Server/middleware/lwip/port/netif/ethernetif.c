/* Define those to better describe your network interface. */
#define IFNAME0 'e'
#define IFNAME1 'n'

extern void netif_status_callback(struct netif *const netif);
extern void netif_link_callback(struct netif *const netif);
extern uint8 netif_select_egress_queue(struct netif *const netif, struct pbuf *p);

/**
 * This function should do the actual transmission of the packet. The packet is
 * contained in the pbuf that is passed to the function. This pbuf
 * might be chained.
 *
 * @param netif the lwip network interface structure for this ethernetif
 * @param p the MAC packet to send (e.g. IP packet including MAC addresses and type)
 * @return ERR_OK if the packet could be sent
 *         an err_t value if the packet couldn't be sent
 *
 * @note Returning ERR_MEM here if a DMA queue of your MAC is full can lead to
 *       strange results. You might consider waiting for space in the DMA queue
 *       to become available since the stack doesn't retry to send a packet
 *       dropped because of memory failure (except for the TCP timers).
 */

static err_t low_level_output(struct netif *const netif, struct pbuf *p)
{
  ETH_MAC_t *const mac = (ETH_MAC_t *const)netif->state;

  struct pbuf *q;
  uint32 framelen = 0U;
  uint8 *buffer;

  uint8 queue = netif_select_egress_queue(netif, p);

  if (p->tot_len > ETH_MAC_GetTxDmaBufferSize(mac, queue))
  {
    return ERR_MEM;
  }

  while (ETH_MAC_IsTxDescriptorOwnedByDma(mac, queue) == TRUE)
  {
    ETH_MAC_ResumeTxDma(mac, queue);
  }

  buffer = ETH_MAC_GetTxDmaBuffer(mac, queue);

#if ETH_PAD_SIZE
  pbuf_header(p, -ETH_PAD_SIZE);    /* Drop the padding word */
#endif

  for(q = p; q != NULL; q = q->next)
  {
    /* Send the data from the pbuf to the interface, one pbuf at a
       time. The size of the data in each pbuf is kept in the ->len
       variable. */
    MEMCPY(buffer, q->payload, q->len);
    framelen += (uint32)q->len;
    buffer += q->len;
  }

#if ETH_PAD_SIZE
  pbuf_header(p, ETH_PAD_SIZE);    /* Reclaim the padding word */
#endif

  ETH_MAC_SetTxDmaBufferSize(mac, queue, framelen);
  ETH_MAC_ReturnTxDmaDescriptor(mac, queue);
  ETH_MAC_ResumeTxDma(mac, queue);

  LINK_STATS_INC(link.xmit);

  return ERR_OK;
}

/**
 * Should allocate a pbuf and transfer the bytes of the incoming
 * packet from the interface into the pbuf.
 *
 * @param netif the lwip network interface structure for this ethernetif
 * @return a pbuf filled with the received packet (including MAC header)
 *         NULL on memory error
 */
static struct pbuf *low_level_input(struct netif *const netif, uint8 queue)
{
  ETH_MAC_t *const mac = (ETH_MAC_t *const)netif->state;

  struct pbuf *p = NULL;
  struct pbuf *q;
  uint32 len;
  uint8 *buffer;

  len = ETH_MAC_GetRxDmaFrameSize(mac, queue);

  if ((len > 0) && (len <= ETH_MAC_GetRxDmaBufferSize(mac, queue)))
  {

#if ETH_PAD_SIZE
    len += ETH_PAD_SIZE;    /* allow room for Ethernet padding */
#endif

    /* We allocate a pbuf chain of pbufs from the pool. */
    p = pbuf_alloc(PBUF_RAW, (u16_t)len, PBUF_POOL);

    if (p != NULL)
    {

#if ETH_PAD_SIZE
      pbuf_header(p, -ETH_PAD_SIZE);  /* drop the padding word */
#endif

      buffer = ETH_MAC_GetRxDmaBuffer(mac, queue);

      len = 0U;
      /* We iterate over the pbuf chain until we have read the entire
       * packet into the pbuf. */
      for (q = p; q != NULL; q = q->next)
      {
        /* Read enough bytes to fill this pbuf in the chain. The
         * available data in the pbuf is given by the q->len
         * variable.
         * This does not necessarily have to be a memcpy, you can also preallocate
         * pbufs for a DMA-enabled MAC and after receiving truncate it to the
         * actually received size. In this case, ensure the tot_len member of the
         * pbuf is the sum of the chained pbuf len members.
         */
        MEMCPY(q->payload, buffer, q->len);
        len += q->len;
        buffer += q->len;
      }

#if ETH_PAD_SIZE
      pbuf_header(p, ETH_PAD_SIZE);    /* Reclaim the padding word */
#endif
      LINK_STATS_INC(link.recv);
    }
    else
    {
      LINK_STATS_INC(link.memerr);
      LINK_STATS_INC(link.drop);      
    }
  }
  else
  {
    LINK_STATS_INC(link.memerr);
    LINK_STATS_INC(link.drop);
  }

  ETH_MAC_ReturnRxDmaDescriptor(mac, queue);
  ETH_MAC_ResumeRxDma(mac, queue);

  return p;
}

/**
 * This function should be called when a packet is ready to be read
 * from the interface. It uses the function low_level_input() that
 * should handle the actual reception of bytes from the network
 * interface. Then the type of the received packet is determined and
 * the appropriate input function is called.
 *
 * @param netif the lwip network interface structure for this ethernetif
 */
void ethernetif_input(struct netif *const netif, uint8 queue)
{
  struct pbuf *p = NULL;

  /* move received packet into a new pbuf */
  p = low_level_input(netif, queue);

  /* if no packet could be read, silently ignore this */
  if (p != NULL)
  {
    /* pass all packets to ethernet_input, which decides what packets it supports */
    if (netif->input(p, netif) != ERR_OK)
    {
      LWIP_DEBUGF(NETIF_DEBUG, ("ethernetif_input: IP input error\n"));
      pbuf_free(p);
      p = NULL;
    }
  }
}

/**
 * Should be called at the beginning of the program to set up the
 * network interface. It calls the function low_level_init() to do the
 * actual setup of the hardware.
 *
 * This function should be passed as a parameter to netif_add().
 *
 * @param netif the lwip network interface structure for this ethernetif
 * @return ERR_OK if the loopif is initialized
 *         ERR_MEM if private data couldn't be allocated
 *         any other err_t on error
 */
err_t ethernetif_init(struct netif *const netif)
{
  LWIP_ASSERT("netif != NULL", (netif != NULL));

  ETH_MAC_t *const eth_mac = netif->state;

  netif->name[0] = IFNAME0;
  netif->name[1] = IFNAME1;

#if LWIP_NETIF_HOSTNAME
  if (netif->hostname == NULL)
  {
    /* Initialize interface hostname */
    netif->hostname = "lwip";
  }
#endif /* LWIP_NETIF_HOSTNAME */

#if LWIP_IPV6
  netif->output_ip6 = ethip6_output;
#endif /* LWIP_IPV6 */

  netif->output = etharp_output;
  netif->linkoutput = low_level_output;

  netif->hwaddr_len = ETH_HWADDR_LEN;
  SMEMCPY(netif->hwaddr, eth_mac->mac_addr, ETH_HWADDR_LEN);

  /* maximum transfer unit */
  netif->mtu = 1500U;

  /* device capabilities */
  netif->flags = NETIF_FLAG_BROADCAST | NETIF_FLAG_ETHARP;

#if LWIP_IPV6 && LWIP_IPV6_MLD
  /*
   * For hardware/netifs that implement MAC filtering.
   * All-nodes link-local is handled by default, so we must let the hardware know
   * to allow multicast packets in.
   * Should set mld_mac_filter previously. */
  if (netif->mld_mac_filter != NULL) {
    ip6_addr_t ip6_allnodes_ll;
    ip6_addr_set_allnodes_linklocal(&ip6_allnodes_ll);
    netif->mld_mac_filter(netif, &ip6_allnodes_ll, NETIF_ADD_MAC_FILTER);
  }
#endif /* LWIP_IPV6 && LWIP_IPV6_MLD */

  /*  Registers the default network interface.*/
  netif_set_default(netif);

  /* If callback enabled */
#if LWIP_NETIF_STATUS_CALLBACK == 1
  /* Initialize interface status change callback */
  netif_set_status_callback(netif, netif_status_callback);
#endif

#if LWIP_NETIF_LINK_CALLBACK == 1
  netif_set_link_callback(netif, netif_link_callback);
#endif  

  return ERR_OK;
}


