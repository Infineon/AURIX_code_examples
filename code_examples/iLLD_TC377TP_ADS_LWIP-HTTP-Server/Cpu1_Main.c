void core1_main (void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG1 IS DISABLED HERE!!
     * Enable the watchdog and service it periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    /* Init serial Interface */
    SERIALIO_Init(115200);
    printf("\n\n\n\n\n");
    printf("\n*******************************\n");
    printf("* Lite Gateway Example Project  *\n");
    printf("* Date: %10s           *\n", __DATE__);
    printf("*******************************\n");

    LED_Init(&LED_1);
    LED_On(&LED_1);
    LED_Init(&LED_RGB_R);
    LED_Init(&LED_RGB_G);
    LED_Init(&LED_RGB_B);

    /* init DTS */
    IfxDts_Dts_Config dtsConfig;
    IfxDts_Dts_initModuleConfig(&dtsConfig);

    IfxDts_Dts_initModule(&dtsConfig);

    for (volatile uint32 i = 0; i < 0x5FFFFFF; i++)
        ;

    /* Initialize the TCP/IP stack */
    net_initialize();

    /* Configure the ethernet port */
    ETH_0.mac_addr = &mac_addr;
    ETH_0.rx_queue[0] = &rx_queue_0;
    ETH_0.rx_dma_channel[0] = &rx_dma_channel_0;
    ETH_0.tx_queue[0] = &tx_queue_0;
    ETH_0.tx_dma_channel[0] = &tx_dma_channel_0;

    ETH_0.rx_queue[1] = &rx_queue_1;
    ETH_0.rx_dma_channel[1] = &rx_dma_channel_1;
    ETH_0.tx_queue[1] = &tx_queue_1;
    ETH_0.tx_dma_channel[1] = &tx_dma_channel_1;

    ETH_0.filter_table = (ETH_MAC_ADDR_FILTER_t*) &mac_addr_filter_table;
    ETH_0.filter_table_size = sizeof(mac_addr_filter_table) / sizeof(ETH_MAC_ADDR_FILTER_t);
    ETH_0.routing.ptp_packets_queue = 1;

    /* Initialize the ETH port */
    ETH_MAC_Init(&ETH_0);

    if (netif_add_noaddr(&netif_0, &ETH_0, ethernetif_init, ethernet_input) == NULL)
    {
    /* error */
        while (1);
    }

    BOARD_ETH_InitInputPins();
    ETH_MAC_Start(&ETH_0, ETH_MAC_LINK_SPEED_1000M, ETH_MAC_LINK_MODE_FULL_DUPLEX);
    BOARD_ETH_InitOutputPins();

    /* Enable PTP capabilities */
    ETH_MAC_InitPTP(&ETH_0, eth_ts_config, &eth_ts_time0);

#if LWIP_DHCP == 1
    /* Start DHCP query */
    dhcp_start(&netif_0);
  #else
    ip_addr_t ipaddr;
    ip_addr_t netmask;
    ip_addr_t gw;

    IP4_ADDR(&ipaddr, IP_ADDR0, IP_ADDR1, IP_ADDR2, IP_ADDR3);
    IP4_ADDR(&netmask, NETMASK_ADDR0, NETMASK_ADDR1, NETMASK_ADDR2, NETMASK_ADDR3);
    IP4_ADDR(&gw, GW_ADDR0, GW_ADDR1, GW_ADDR2, GW_ADDR3);

    netif_set_addr(&netif_0, &ipaddr, &netmask, &gw);
#endif

    netif_set_link_up(&netif_0);
    netif_set_up(&netif_0);
    /* Initialize HTTP server */
    httpd_init();
    cgi_init();
    ssi_init();

    /* Start LED blink timer */
    sys_timeout(1000, toggle_led, NULL);

    while (1)
    {
        /* Go through the application owned descriptors */
        while (!ETH_MAC_IsRxDescriptorOwnedByDma(&ETH_0, 0))
        {
            ethernetif_input(&netif_0, 0);
        }
        /* Read the PTP Queue */
        while (!ETH_MAC_IsRxDescriptorOwnedByDma(&ETH_0, 1))
        {
            ETH_MAC_ReturnRxDmaDescriptor(&ETH_0, 1);
            ETH_MAC_ResumeRxDma(&ETH_0, 1);
        }

        sys_check_timeouts();

    }
}
