int IntToStr (char *string_p, int number_i)
{
    int l_divide_i;
    int l_modResult_i;
    int l_length_i;
    int l_isNegative_i;
    int l_copyOfNumber_i;

    if (string_p == NULL)
    {
        return 0;
    }

    if (number_i == 0)
    {
        string_p[0] = '0';
        string_p[1] = '\0';
        return 1;
    }

    l_divide_i = 0;
    l_length_i = 0;
    l_isNegative_i = 0;
    l_copyOfNumber_i = number_i;

    if (number_i < 0)
    {
        l_isNegative_i = 1;
        number_i = -number_i;
        l_length_i++;
    }

    while (l_copyOfNumber_i != 0)
    {
        l_length_i++;
        l_copyOfNumber_i /= 10;
    }

    for (l_divide_i = 0; l_divide_i < l_length_i; l_divide_i++)
    {
        l_modResult_i = number_i % 10;
        number_i = number_i / 10;
        string_p[l_length_i - (l_divide_i + 1)] = l_modResult_i + '0';
    }

    if (l_isNegative_i == 1)
    {
        string_p[0] = '-';
    }
    string_p[l_length_i] = '\0';

    return (l_length_i);
}

