#include "Ifx_Types.h"
#include "IfxPort_PinMap.h"
#include "Power_Good_Signals.h"

Board_PG_Status_t Board_PG_Status;

void Init_PowerGood_Signals (void){
IfxPort_setPinMode(IfxPort_P00_10.port, IfxPort_P00_10.pinIndex, IfxPort_Mode_inputNoPullDevice);
IfxPort_setPinMode(IfxPort_P00_11.port, IfxPort_P00_11.pinIndex, IfxPort_Mode_inputNoPullDevice);
IfxPort_setPinMode(IfxPort_P00_12.port, IfxPort_P00_12.pinIndex, IfxPort_Mode_inputNoPullDevice);
IfxPort_setPinMode(IfxPort_P00_7.port, IfxPort_P00_7.pinIndex, IfxPort_Mode_inputNoPullDevice);
}


void Get_PowerGood_Signal(void){

    Board_PG_Status.powergood_5_0V = IfxPort_getPinState(&MODULE_P00, 7);
    Board_PG_Status.powergood_1_8V = IfxPort_getPinState(&MODULE_P00, 10);
    Board_PG_Status.powergood_1_0V = IfxPort_getPinState(&MODULE_P00, 11);
    Board_PG_Status.powergood_0_9V = IfxPort_getPinState(&MODULE_P00, 12);

}

uint8 Is_PowerGood_Signals_OK(void){

if ((Board_PG_Status.powergood_5_0V == POWER_GOOD_HIGH) &&
    (Board_PG_Status.powergood_1_8V == POWER_GOOD_HIGH) &&
    (Board_PG_Status.powergood_1_0V == POWER_GOOD_HIGH) &&
    (Board_PG_Status.powergood_0_9V == POWER_GOOD_HIGH))
    {
        return (POWER_GOOD_HIGH);
    }
    else
    {
      return (POWER_GOOD_LOW);  /* code */
    }



}


