#include "Ifx_Types.h"
#include "Ifx_Cfg.h"
#include "IfxCpu.h"
#include "IfxWtu.h"
#include "board.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define UART_BAUDRATE           115200                                  /* UART baud rate in bit/s                   */


void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword());
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());
    
    SERIALIO_Init(UART_BAUDRATE);

    printf("\n\r");
    printf("************************************\n\r");
    printf("*       TC4D LITE KIT BOARD        *\n\r");
    printf("*          Serial Printf           *\n\r");
    printf("* Date: %10s                *\n\r", __DATE__);
    printf("************************************\n\r");

    while (1)
    {
    }
}
