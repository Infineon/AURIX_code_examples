/* Function to configure and initialize the DMA transaction (transfers, moves, addresses...) */
void init_dma(void)
{
    /* Create module configuration */
    IfxDma_Dma_Config dmaConfig;
    IfxDma_Dma_initModuleConfig(&dmaConfig, &MODULE_DMA);

    /* Initialize module */
    IfxDma_Dma dma;
    IfxDma_Dma_initModule(&dma, &dmaConfig);

    /* Initial configuration for all channels */
    IfxDma_Dma_ChannelConfig dmaChnsCfg;
    IfxDma_Dma_initChannelConfig(&dmaChnsCfg, &dma);

    /* Following configuration is used by all channels */
    dmaChnsCfg.transferCount = N_TRANSFER;
    dmaChnsCfg.requestMode = IfxDma_ChannelRequestMode_completeTransactionPerRequest; /* Complete a full transaction on request */
    dmaChnsCfg.moveSize = IfxDma_ChannelMoveSize_32bit;
    dmaChnsCfg.operationMode = IfxDma_ChannelOperationMode_continuous; /* DMA channel stays enabled after one request */
    dmaChnsCfg.hardwareRequestEnabled = TRUE; /* DMA as Interrupt Service Provider */

    /* Source and destination addresses */
    dmaChnsCfg.destinationAddressCircularRange = IfxDma_ChannelIncrementCircular_none; /* Src address remains the same after transfer */
    dmaChnsCfg.sourceCircularBufferEnabled = TRUE;
    dmaChnsCfg.sourceAddressCircularRange = IfxDma_ChannelIncrementCircular_none; /* Dst address remains the same after transfer */
    dmaChnsCfg.destinationCircularBufferEnabled = TRUE;

    /* Interrupt generated after transactions */
    dmaChnsCfg.channelInterruptEnabled = TRUE;
    dmaChnsCfg.channelInterruptControl = IfxDma_ChannelInterruptControl_thresholdLimitMatch; /* DMA triggers an interrupt once the full transaction is done */
    dmaChnsCfg.channelInterruptTypeOfService = IfxSrc_Tos_cpu0; /* CPU0 is the Interrupt Service Provider of this interrupt */
    dmaChnsCfg.channelInterruptPriority = ISR_PRIORITY_DMA;

    /* Channel specific configurations */
    dmaChnsCfg.channelId = (IfxDma_ChannelId) DMA_CHANNEL;  /* Select the Channel 1                         */
    dmaChnsCfg.sourceAddress = SRC_ADDR;                    /* Address of VADC Group 0 Result Register 0    */
    dmaChnsCfg.destinationAddress = DEST_ADDR;              /* Start address of LMURAM                      */

    /* Initialize the DMA channel */
    IfxDma_Dma_initChannel(&g_chn, &dmaChnsCfg);
}

/* Function to configure and initialize the VADC module and the group(s) that will be used */
void init_vadc(void)
{
    /* Create configuration */
    IfxVadc_Adc_Config adcConfig;
    IfxVadc_Adc_initModuleConfig(&adcConfig, &MODULE_VADC);

    /* Initialize module */
    IfxVadc_Adc_initModule(&g_vadc, &adcConfig);

    /* Create and initialize group configuration with default values */
    IfxVadc_Adc_GroupConfig adcGroupConfig;
    IfxVadc_Adc_initGroupConfig(&adcGroupConfig, &g_vadc);

    /* Setting user configuration using group 0 */
    adcGroupConfig.groupId = IfxVadc_GroupId_0;
    adcGroupConfig.master = adcGroupConfig.groupId;

    adcGroupConfig.arbiter.requestSlotScanEnabled = TRUE; /* Enable scan source     */
    adcGroupConfig.scanRequest.autoscanEnabled = FALSE;   /* Disable auto scan      */

    adcGroupConfig.scanRequest.triggerConfig.gatingMode = IfxVadc_GatingMode_always; /* Enable all gates in "always" mode (no edge detection) */

    IfxVadc_Adc_initGroup(&g_adcGroup, &adcGroupConfig);  /* Initialize the group   */

    /* Create channel configuration */
    IfxVadc_Adc_ChannelConfig adcChannelConfig;
    IfxVadc_Adc_Channel adcChannel;

    IfxVadc_Adc_initChannelConfig(&adcChannelConfig, &g_adcGroup);

    adcChannelConfig.channelId = (IfxVadc_ChannelId) (ADC_CHANNEL);
    adcChannelConfig.resultRegister = (IfxVadc_ChannelResult) (ADC_CHANNEL); /* Use dedicated result register */

    adcChannelConfig.resultPriority = ISR_PRIORITY_ADC;
    adcChannelConfig.resultServProvider = IfxSrc_Tos_dma;

    /* Initialize the channel */
    IfxVadc_Adc_initChannel(&adcChannel, &adcChannelConfig);

    /* Add the channel to scan */
    uint32 channels = (1 << adcChannelConfig.channelId);
    uint32 mask = channels;

    IfxVadc_Adc_setScan(&g_adcGroup, channels, mask);
}

/* Function to start an analog/digital conversion */
void run_vadc(void)
{
    /* Start conversion */
    IfxVadc_Adc_startScan(&g_adcGroup);
}

IFX_INTERRUPT(ISR_feedback, 0, ISR_PRIORITY_DMA);
void ISR_feedback(void)
{
    send_data(COMPLETED_TRANSFER_TEXT, TEXT_LENGTH); /* Acknowledgment via UART - Triggered by DMA transaction done */
}

IFX_INTERRUPT(ASCLIN_ISR_Tx, 0, ISR_PRIORITY_ASCLIN_TX);
void ASCLIN_ISR_Tx(void)
{
    IfxAsclin_Asc_isrTransmit(&g_asc);
}

IFX_INTERRUPT(ASCLIN_ISR_Rx, 0, ISR_PRIORITY_ASCLIN_RX);
void ASCLIN_ISR_Rx(void)
{
    IfxAsclin_Asc_isrReceive(&g_asc);
}

IFX_INTERRUPT(ASCLIN_ISR_Er, 0, ISR_PRIORITY_ASCLIN_ER);
void ASCLIN_ISR_Er(void)
{
    IfxAsclin_Asc_isrError(&g_asc);
}

/* Function to initialize ASCLIN module */
void init_uart(void)
{
    IfxAsclin_Asc_Config ascConf;

    IfxAsclin_Asc_initModuleConfig(&ascConf, &MODULE_ASCLIN0);

    /* Set the desired baud rate */
    ascConf.baudrate.prescaler = ASC_PRESCALER;
    ascConf.baudrate.baudrate = ASC_BAUDRATE;
    ascConf.baudrate.oversampling = IfxAsclin_OversamplingFactor_4;

    /* ISR priorities and interrupt target */
    ascConf.interrupt.txPriority = ISR_PRIORITY_ASCLIN_TX;
    ascConf.interrupt.rxPriority = ISR_PRIORITY_ASCLIN_RX;
    ascConf.interrupt.erPriority = ISR_PRIORITY_ASCLIN_ER;
    ascConf.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* FIFO configuration */
    ascConf.txBuffer = g_ascTxBuffer;
    ascConf.txBufferSize = ASC_TX_BUFFER_SIZE;
    ascConf.rxBuffer = g_ascRxBuffer;
    ascConf.rxBufferSize = ASC_RX_BUFFER_SIZE;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
            NULL, IfxPort_InputMode_pullUp,                         /* CTS port pin not used    */
            &IfxAsclin0_RXA_P14_1_IN, IfxPort_InputMode_pullUp,     /* RX port pin              */
            NULL, IfxPort_OutputMode_pushPull,                      /* RTS port pin not used    */
            &IfxAsclin0_TX_P14_0_OUT, IfxPort_OutputMode_pushPull,  /* TX port pin              */
            IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConf.pins = &pins;

    /* Initialize module */
    IfxAsclin_Asc_initModule(&g_asc, &ascConf);
}

/* Function to transmit data over ASC */
void send_data(char *data, Ifx_SizeT length)
{
   /* Transmit data */
   IfxAsclin_Asc_write(&g_asc, data, &length, TIME_INFINITE);
}

/* Function to receive data over ASC */
void receive_data(char *data, Ifx_SizeT length)
{
   /* Receive data */
   IfxAsclin_Asc_read(&g_asc, data, &length, TIME_INFINITE);
}
