/**
 ******************************************************************************
 * The  routine void IfxMtu_Init(void)
 * This routine enables the MTU module
 ******************************************************************************
 */
void IfxMtu_Init(){
    /* Enable MTU to clear RAMs */
    IfxScuWdt_clearCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());
        IfxMtu_enableModule();
    IfxScuWdt_setCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());

    IfxMtu_DlmuInit();
}

/**
 ******************************************************************************
 * The  routine void IfxMtu_EmemInit(void)
 * This routine initializes the 1MB EMEM
 ******************************************************************************
 */
void IfxMtu_RmemInit(){
    IfxMtu_clearSram(IfxMtu_MbistSel_ememUpper0);
    IfxMtu_clearSram(IfxMtu_MbistSel_ememUpper1);
}

/**
 ******************************************************************************
 * The  routine void IfxMtu_DlmuInit(void)
 * This routine initializes the DLMUs of CPU0 & CPU1
 ******************************************************************************
 */
void IfxMtu_DlmuInit(){
    IfxMtu_clearSram(IfxMtu_MbistSel_cpu0Dlmu);
    IfxMtu_clearSram(IfxMtu_MbistSel_cpu1Dlmu);
    IfxMtu_clearSram(IfxMtu_MbistSel_cpu2Dlmu);
}

/**
 ******************************************************************************
 * The  routine void IfxMtu_SpuInit(void)
 * This routine initializes the SPU specific RAMs
 ******************************************************************************
 */
void IfxMtu_SpuInit(){
    /* Clear SPU0 SRAMs*/
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Buffer);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Config);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Fft0);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Fft1);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Fft2);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu0Fft3);

    /* Clear SPU1 SRAMs*/
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Buffer);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Config);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Fft0);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Fft1);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Fft2);
    IfxMtu_clearSram(IfxMtu_MbistSel_spu1Fft3);
}
