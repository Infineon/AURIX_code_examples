#include <IfxPort_init.h>

IFX_INTERRUPT(EruISR, 0, ISR_PRIORITY_ERU);  /* SPI Master ISR for error */

void IfxPort_Init(void)
{
    /* Init Pins */
    IfxScuWdt_clearCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());
    {
        /* MMIC_RST P33.4 -> Configure Reset Pin from MCU -> MMIC*/
        IfxPort_setPinModeOutput(BGT60_RST_PIN, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
        IfxPort_setPinPadDriver(BGT60_RST_PIN, IfxPort_PadDriver_ttl3v3Speed1);
        IfxPort_setPinHigh(BGT60_RST_PIN); /* released reset state*/

        /* LDO_ENABLE P33.6 -> Enable MMIC LDO*/
        IfxPort_setPinModeOutput(BGT60_LDO_PIN, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
        IfxPort_setPinPadDriver(BGT60_LDO_PIN, IfxPort_PadDriver_ttl3v3Speed1);
        IfxPort_setPinHigh(BGT60_LDO_PIN); /* enable LDO supplying MMIC*/

        /* MMIC_IRQ P10.7 --> Data available IRQ line*/
        IfxPort_setPinModeInput(BGT60_IRQ_PIN, IfxPort_InputMode_noPullDevice);

    }
    IfxScuWdt_setCpuEndinit(IfxScuWdt_getCpuWatchdogPassword());


    /* BGT_IRQ_PIN as interrupt line from MMIC to MCU for Radar data availability */
    /* Initialize request pin */
    IfxScuEru_initReqPin(&IfxScu_REQ0C_P10_7_IN, IfxPort_InputMode_pullDown);
    /* Initialize external request input*/
    IfxScuEru_selectExternalInput(IfxScuEru_InputChannel_0, IfxScuEru_ExternalInputSelection_2);
    /* React only on rising edge detections*/
    IfxScuEru_enableRisingEdgeDetection(IfxScuEru_InputChannel_0);
    /* Connect trigger to output unit*/
    IfxScuEru_connectTrigger(IfxScuEru_InputChannel_0, IfxScuEru_InputNodePointer_0);
    /* Enable trigger pulse generation*/
    IfxScuEru_enableTriggerPulse(IfxScuEru_InputChannel_0);
    /* Configure event gating*/
    IfxScuEru_setInterruptGatingPattern(IfxScuEru_InputChannel_0, IfxScuEru_InterruptGatingPattern_patternMatch);


    /* Install interrupt service routine for handling Radar data availability event*/
    volatile Ifx_SRC_SRCR *src;
    src = &MODULE_SRC.SCU.SCUERU[0];
    IfxSrc_init(src, ISR_PROVIDER_DMA, ISR_DMA_CH_MMIC_SPI_TX_START); /* Serve with DMA Channel 1*/
    IfxSrc_enable(src);

    /* Wait for 200us to settling the power on MMIC */
    IfxStm_waitTicks(&MODULE_STM0, 10000);
}


void EruISR(void)
{
    __debug();
}
