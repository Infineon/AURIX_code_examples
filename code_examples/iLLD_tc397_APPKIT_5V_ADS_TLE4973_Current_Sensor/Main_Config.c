/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
uint8 g_Index;
uint32 g_MeasureTime;
MEASRange g_NominalSense;
commandPType g_operativeMode;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* Main program initialization function. */
void initialisation(void)
{
    /* Configuration Value */
    g_MeasureTime = 1000;                           /* measurement time (ms) (minimum: 2us) */
    g_NominalSense = S3;                            /* measurement range (cf MEASRange value) */
    OPMode l_outPutM = SingleEnded_biDir;           /* output mode */

    /* Module initialization */
    init_UART();
    init_UART2();

    /* Configuration function */
    configOutPut(l_outPutM, g_NominalSense);
    configMode();
}

/* Main program loop function. */
void mainLoop(void)
{
    Waitms(g_MeasureTime);                                          /* Measurement period */
    if(g_operativeMode == multiMeasure)
    {
        multiMeasureCurrent();
    }
    else
    {
       int diag = getDiag(temperatureIC, g_Index);                 /* Temperature request */
       printf("Temperature of the sensor: %i (C%c) ", diag, 0xB0);
       measureCurrent(g_Index);
    }
}

/* This function is used to select the configuration */
void configMode(void)
{
    printf("Choose an option for the current measurement process: \n\r\n\r- PRESS the key A to use sensor 1 \n\r- PRESS the key B to use sensor 2 "
            "\n\r- PRESS the key C to use sensor 3 \n\r- PRESS any other keys to use all the sensors\n\r\n\r");
    uint8 rec;
    receiveUART0(1, &rec);                                          /* wait for user request */
    if(rec == 0x61)                                                 /* hexadecimal code for key A */
    {
        g_operativeMode = singleMeasure;
        g_Index = 0x0;
    }
    else if(rec == 0x62)                                            /* hexadecimal code for key B */
    {
        g_operativeMode = singleMeasure;
        g_Index = 0x1;
    }
    else if(rec == 0x63)                                            /* hexadecimal code for key C */
    {
        g_operativeMode = singleMeasure;
        g_Index = 0x2;
    }
    else
    {
        g_operativeMode = multiMeasure;
        g_Index = 0x0;
    }
}

/* Wait function in microsecond */
void Waitms(uint32 time)
{
    waitTime(IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, time));
}
