#include "Multicore.h"
#include "Ifx_Types.h"
#include "IfxPort.h"
#include "Bsp.h"
#include "pin.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define LED_PORT    &MODULE_P03         /* LED_PORT                                                                 */
#define WAIT_TIME   1000                /* Number of milliseconds to wait between each duty cycle change            */
#define ACCESS_GRP  0                   /* Access protection group to be configured and assigned to LED pin         */
#define LED         LED_1               /* LED_PORT, LED_PIN                                                        */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
volatile uint16 g_turnLEDon = TRUE;     /* Variable for the LED, CPU0 and CPU1 are toggling the LED depending on its
                                         * state                                                                    */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Function to initialize the LED port pin and the time constants */
void initLEDAndTime(void)
{
    /* Configure access protection to enable write access to CPU0, CPU1 & Cerberus */
    Ifx_ACCEN_ACCEN_WRA port_accen_wra = {0};
    port_accen_wra.U  = (1 << IfxApProt_TagId_cpu0d) | (1 << IfxApProt_TagId_cpu0ds);
    port_accen_wra.U |= (1 << IfxApProt_TagId_cpu1d) | (1 << IfxApProt_TagId_cpu1ds);
    port_accen_wra.U |= (1 << IfxApProt_TagId_cbs);
    IfxApApu_configWriteRegA((Ifx_ACCEN_ACCEN_WRA *)LED_PORT.ACCGRP[ACCESS_GRP].WRA, port_accen_wra.U);
    IfxPort_setApuGroupSelection(LED.port, LED.pinIndex, ACCESS_GRP);

    /* Turn off the LED (LED is low-level active) */
    IfxPort_setPinHigh(LED.port, LED.pinIndex);
    /* Set the port pin mode to output push-pull */
    IfxPort_setPinMode(LED.port, LED.pinIndex, IfxPort_Mode_outputPushPullGeneral);

    /* Turn off the LED (LED is low-level active) */
    IfxPort_setPinHigh(LED.port, LED.pinIndex);
}

/* Function to turn on the LED accordingly to the state of g_turnLEDon */
void turnLEDon(void)
{
    if(g_turnLEDon == TRUE)
    {
        IfxPort_setPinLow(LED.port, LED.pinIndex);     /* Turn on the LED (LED is low-level active)    */
        waitTime(IfxStm_getTicksFromMilliseconds(250));
    }
}

/* Function to turn off the LED accordingly to the state of g_turnLEDon */
void turnLEDoff(void)
{
    if(g_turnLEDon == FALSE)
    {
        IfxPort_setPinHigh(LED.port, LED.pinIndex);    /* Turn off the LED (LED is low-level active)   */
        waitTime(IfxStm_getTicksFromMilliseconds(250));
    }
}

/* Function to toggle the state of g_turnLEDon */
void controlLEDflag(void)
{
    g_turnLEDon = !g_turnLEDon;                             /* Toggle the state of the global variable      */
    waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));   /* Wait for approximately 1 second              */
}
