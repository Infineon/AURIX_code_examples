#include "IfxRadar_cfg.h"
#include "IfxEmem_Init.h"
#include "IfxSpu0_Init.h"
#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"

IfxCpu_syncEvent g_cpuSyncEvent= 0;

void core0_main(void)
{
    Radar * rdr = &g_rdr;

    IfxCpu_enableInterrupts();
    /*
     * !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdog in the demo if it is required and also service the watchdog periodically
     * */
    IfxScuWdt_disableCpuWatchdog (IfxScuWdt_getCpuWatchdogPassword ());
    IfxScuWdt_disableSafetyWatchdog (IfxScuWdt_getSafetyWatchdogPassword ());

    /* Cpu sync event wait*/
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    /* Initialize MTU. MTU is used later to initialize EMEM and the SPU CFGRAM*/
    Mtu_init();

    /* Unlock and initialize the EMEM (radar SRAM) */
    Emem_init();

    /* Clear EMEM using MTU */
    Mtu_EmemInit();

    /* Copy simulated ADC data to EMEM */
    {
        uint16 ramp = 0;
        uint16 sample = 0;
        uint16 rx = 0;
        for(ramp=0; ramp<RADAR_NUM_RAMPS; ramp++)
        {
            for(rx=0; rx<RADAR_NUM_RX; rx++)
            {
                for(sample=0; sample<RADAR_NUM_SAMPLES; sample++)
                {
                    rdr->emem->rangeMap.adc_raw[ramp][rx][sample] = adc_data[ramp*RADAR_NUM_SAMPLES + sample][rx];
                }
            }
        }
    }

    /* Initialize the SPU */
    Spu0_init();

    /*Initialize the SPU CFGRAM*/
    Spu0_cfg_init();

    /* Reload configuration #1 to SPU */
    IfxSpu_reloadConfig(rdr->spu0, 1);

    /* Wait until config reload has finished. The SPU will start processing automatically. */
    IfxSpu_waitReload(rdr->spu0);

    while(rdr->spuOk == 0); /*wait for SPU to finish processing*/

    while (1)
    {
    }

}
