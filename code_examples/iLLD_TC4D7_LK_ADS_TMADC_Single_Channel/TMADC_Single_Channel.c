/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define IFX_INTPRIO_TMADC_SR0    1                           /* Interrupt priority for TMADC                         */
#define LIMIT_HIGH      0xAAA                                /* Higher limit to be compared with the measure         */
#define LIMIT_LOW       0x555                                /* Lower limit to be compared with the measure          */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxAdc_Tmadc g_tmadc;                                        /* TMADC module handle                                  */
IfxAdc_Tmadc_Ch g_tmadcChannel;                              /* TMADC channel handle                                 */
IfxAdc_Tmadc_Config g_tmadcConfig;                           /* TMADC configuration structure                        */
IfxAdc_Tmadc_ChConfig g_tmadcChannel0Config;                 /* TMADC channel configuration structure                */
IfxAdc_Tmadc_InterruptConfig g_tmadcIntConfig0;              /* TMADC interrupt configuration                        */
IfxAdc_Tmadc_ServRequestConfig g_tmadcServRequestConfig;     /* TMADC interrupt service request configuration        */
IfxAdc_Tmadc_ChannelServReqConfig g_tmacChannel0ServRequest; /* TMADC channel service request                        */
volatile uint16  g_result;                                   /* Variable to store the result of the measurement      */

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* Macro to define the Interrupt Service Routine */
IFX_INTERRUPT(resultISR, 0, IFX_INTPRIO_TMADC_SR0);

/* The initialization of the port pins to control the LEDs is done by this function */
void initLEDs(void)
{
    /* Initialize the Port Pins */
    IfxPort_setPinMode(LED_1.port, LED_1.pinIndex, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED_2.port, LED_2.pinIndex, IfxPort_Mode_outputPushPullGeneral);

    /* Initially, turn off the LEDs */
    IfxPort_setPinHigh(LED_1.port, LED_1.pinIndex);
    IfxPort_setPinHigh(LED_2.port, LED_2.pinIndex);
}

/* Function to initialize and run the TMADC */
void initTMADC(void)
{
    /* Initialize the TMADC module */
    initTMADCModule();

    /* Initialize the channel */
    initTMADCChannel();

    /* Run the TMADC module */
    IfxAdc_Tmadc_runModule(&g_tmadc);
}

/* Function to initialize the TMADC module */
void initTMADCModule(void)
{
    /* Enable ADC module */
    IfxAdc_enableModule(&MODULE_ADC);

    /* Initialize module to default */
    IfxAdc_Tmadc_initModuleConfig(&g_tmadcConfig, &MODULE_ADC);

    /* Change TMADC configuration */
    g_tmadcConfig.id                = IfxAdc_TmadcModule_0;             /* Module ID                                 */
    g_tmadcConfig.calEnable         = 1;                                /* Calibration enabled                       */

    /* Set TMADC Interrupt configuration */
    g_tmadcIntConfig0.node          = IfxAdc_TmadcServReq_0;            /* Select service request node               */
    g_tmadcIntConfig0.eventOp       = IfxAdc_TmadcEventOp_andLogic;     /* Select service request event operation    */
    g_tmadcIntConfig0.eventSel      = IfxAdc_TmadcEventSel_result;      /* Select TMADC event                        */
    g_tmadcIntConfig0.typeOfService = IfxSrc_Tos_cpu0;                  /* Set interrupt provider                    */
    g_tmadcIntConfig0.vmId          = IfxSrc_VmId_0;                    /* Set identifier of Virtual machine interrupt
                                                                         * service provider                          */

    g_tmadcIntConfig0.priority      = IFX_INTPRIO_TMADC_SR0;            /* Set Channel event priority */

    g_tmadcServRequestConfig.intConfig[0] = &g_tmadcIntConfig0;  /* Register interrupt configuration                 */
    g_tmadcServRequestConfig.numServReqNodes = 1;                /* Configure number of interrupt service requests   */

    g_tmadcConfig.srvReqCfg = &g_tmadcServRequestConfig;         /* Assign the  service request configuration        */

    /* Initialize TMADC module with updated configuration */
    IfxAdc_Tmadc_initModule(&g_tmadc, &g_tmadcConfig);
}

/* Function to initialize the TMADC channel */
void initTMADCChannel(void)
{
    /* Initialize the configuration with default values */
    IfxAdc_Tmadc_initChannelConfig(&g_tmadcChannel0Config, &MODULE_ADC);

    /* Change TMADC channel configuration */
    /* Select the channel ID and the respective result register */
    g_tmadcChannel0Config.id             = IfxAdc_TmadcChannel_0;          /* Channel ID                             */
    g_tmadcChannel0Config.moduleId       = IfxAdc_TmadcModule_0;           /* Select TMADC module                    */
    g_tmadcChannel0Config.samplingTimeNS = 100;                            /* Set sampling time period               */
    g_tmadcChannel0Config.mode           = IfxAdc_TmadcOpMode_continuous;  /* Select operating mode - continuous     */
    g_tmadcChannel0Config.core           = IfxAdc_TmadcSarCore_0;          /* Select TMADC SAR core                  */

    g_tmadcChannel0Config.resultCfg.resultReg   = IfxAdc_TmadcResultReg_0; /* Configure result register              */
    g_tmadcChannel0Config.resultCfg.waitForRead = 1;                       /* Enable wait-for-read mode              */

    /* Set channel service request */
    g_tmacChannel0ServRequest.errorNode     = IfxAdc_TmadcServReq_none;    /* Configure Service request error node   */
    g_tmacChannel0ServRequest.boundaryNode  = IfxAdc_TmadcServReq_none;    /* Configure Service request boundary node*/
    g_tmacChannel0ServRequest.resultNode    = IfxAdc_TmadcServReq_0;       /* Configure Service request result node  */

    /* Assign configured channel service request to configuration */
    g_tmadcChannel0Config.channelSrvReq = &g_tmacChannel0ServRequest;

    /* Initialize the channel */
    IfxAdc_Tmadc_initChannel(&g_tmadcChannel, &g_tmadcChannel0Config);
}

/* Interrupt Service Routine of the TMADC */
void resultISR(void)
{
    /* Read result value */
    g_result = IfxAdc_Tmadc_readChannelResult(&g_tmadcChannel);
}

/* This function retrieves the conversion value and indicates it using LEDs */
void indicateConversionValue(void)
{
    if(g_result < LIMIT_LOW)       /* LED1 lights up if the conversion value is smaller than 0x555                   */
    {
        IfxPort_setPinLow(LED_1.port, LED_1.pinIndex);
        IfxPort_setPinHigh(LED_2.port, LED_2.pinIndex);
    }
    else if(g_result > LIMIT_HIGH) /* LED2 lights up if the conversion value is greater than 0xAAA                   */
    {
        IfxPort_setPinHigh(LED_1.port, LED_1.pinIndex);
        IfxPort_setPinLow(LED_2.port, LED_2.pinIndex);
    }
    else                           /* LED1 and LED2 light up if the conversion value is in between the limits        */
    {
        IfxPort_setPinLow(LED_1.port, LED_1.pinIndex);
        IfxPort_setPinLow(LED_2.port, LED_2.pinIndex);
    }
}
