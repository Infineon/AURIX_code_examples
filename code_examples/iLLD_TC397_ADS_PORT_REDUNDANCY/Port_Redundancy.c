/*
 * This function initializes the port pin which drives the LED
 * */
void initLEDs(void)
{
    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED_D107_CHECH_PASS, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    IfxPort_setPinModeOutput(LED_D108_CHECK_FAIL, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED_D107_CHECH_PASS);
    IfxPort_setPinHigh(LED_D108_CHECK_FAIL);
}

/*
 * Function to configure the port pins for port redundancy safety mechanism (ESM)
 */
void initPortRedundancy(void)
{
    /* Set pin of PORT_REDUNDANCY_MISSION as input to receive mission signal with pullDown state */
    IfxPort_setPinModeInput(PORT_REDUNDANCY_MISSION.port, PORT_REDUNDANCY_MISSION.pinIndex, IfxPort_InputMode_pullDown);

    /* Set pin of PORT_REDUNDANCY_MONITOR which is redundant to mission signal but called monitor with pullDown state*/
    IfxPort_setPinModeInput(PORT_REDUNDANCY_MONITOR.port, PORT_REDUNDANCY_MONITOR.pinIndex, IfxPort_InputMode_pullDown);
}

/*
 * this function compare the mission and monitor signals
 * SM:PORT:REDUNDANCY
*/
void validatePortRedundancy(void)
{
    uint8 missionSignalIn = 0x55;
    uint8 monitorSignalIn = 0xAA;

    /* get mission signal pin state */
    missionSignalIn = (uint8)IfxPort_getPinState(PORT_REDUNDANCY_MISSION.port, PORT_REDUNDANCY_MISSION.pinIndex);

    /* get monitor signal pin state */
    monitorSignalIn = (uint8)IfxPort_getPinState(PORT_REDUNDANCY_MONITOR.port, PORT_REDUNDANCY_MONITOR.pinIndex);

    /* compare the mission and monitor signals */
    if( missionSignalIn == monitorSignalIn)
    {
        /* check passed */
        IfxPort_setPinLow(LED_D107_CHECH_PASS);
        IfxPort_setPinHigh(LED_D108_CHECK_FAIL);
    }
    else
    {
        /* check failed i.e. mismatch*/
        IfxPort_setPinHigh(LED_D107_CHECH_PASS);
        IfxPort_setPinLow(LED_D108_CHECK_FAIL);
    }
}
