void IfxSpu_setupFilter(IfxSpu_Slot *slot, const IfxSpu_FilterConfig *config)
{
    Ifx_SPU_FILTCTRL filtCtrl;
    Ifx_SPU_FILTCB   filtCb;
    uint8            recursiveCoeff;

    /* Read register */
    filtCtrl.U = slot->FILTCTRL.U;
    filtCb.U   = slot->FILTCB.U;

    if (config != NULL_PTR)
    {
        recursiveCoeff = config->numRecurCoeff;
        {
            filtCtrl.B.EN = config->enableFilter;

            if (slot->ID.CONF2.B.FORMAT == IfxSpu_RifInputDataFormat_real)
            {
                if (recursiveCoeff > 16U)
                {
                    recursiveCoeff = 16U;
                }

                filtCtrl.B.NCOEFR  = recursiveCoeff;
                filtCtrl.B.NCOEFNR = 16U - recursiveCoeff;
            }
            else
            {
                if (recursiveCoeff > 8U)
                {
                    recursiveCoeff = 8U;
                }

                filtCtrl.B.NCOEFR  = recursiveCoeff;
                filtCtrl.B.NCOEFNR = 8U - recursiveCoeff;
            }

            filtCtrl.B.CONJ = config->conjugateControl;
        }

        {
            /* Address is relative to the Configuration Memory Base Address
             * and forms a 128-bit aligned
             */
            filtCb.B.CBASE = config->coeffBaseAddr >> 4u;
        }
    }
    else
    {
        filtCtrl.U = 0u;
        filtCb.U   = 0u;
    }

    /* Write Register */
    slot->FILTCTRL.U = filtCtrl.U;
    slot->FILTCB.U   = filtCb.U;
}


