void IfxSpu_setupMath1(IfxSpu_Slot *slot, IfxSpu_PassId passId, const IfxSpu_Math1Config *config)
{
    Ifx_SPU_BE_LDR_CONF  ldrConf;
    Ifx_SPU_BE_LDR_CONF2 ldrConf2;
    Ifx_SPU_BE_ANTOFST   antOfst01;
    Ifx_SPU_BE_ANTOFST   antOfst23;
    Ifx_SPU_BE_ANTOFST   antOfst45;
    Ifx_SPU_BE_ANTOFST   antOfst67;

    /* Register Read */
    ldrConf.U   = slot->BE[passId].LDR.CONF.U;
    ldrConf2.U  = slot->BE[passId].LDR.CONF2.U;

    antOfst01.U = slot->BE[passId].ANTOFST[0].U;
    antOfst23.U = slot->BE[passId].ANTOFST[1].U;
    antOfst45.U = slot->BE[passId].ANTOFST[2].U;
    antOfst67.U = slot->BE[passId].ANTOFST[3].U;

    if (config != NULL_PTR)
    {
        {
            ldrConf.B.DRPF  = config->numDropFirstSamples;
            ldrConf.B.DRPL  = config->numDropLastSamples;
            ldrConf.B.EXPNT = config->loaderExponent;
            ldrConf.B.ZI    = config->enableZeroInsertion;
        }

        ldrConf2.B.PADF          = config->numPadFrontSamples;
        ldrConf2.B.PHSHFT        = config->phaseShift;

        ldrConf2.B.WINEN         = config->windowCfg.function;
        /* Address is relative to the Configuration Memory Base Address and forms a 32-bit word address
         * aligned to a 128-bit word when combined with the 2-bit WBLBS field */
        ldrConf2.B.WBASE         = config->windowCfg.baseAddress >> 4u;
        ldrConf.B.FORMAT         = config->windowCfg.dataFormat;

        antOfst01.B.ADROFST0_ANT = config->windowCfg.antennaOffsets[0] >> 4u;
        antOfst01.B.ADROFST1_ANT = config->windowCfg.antennaOffsets[1] >> 4u;
        antOfst23.B.ADROFST0_ANT = config->windowCfg.antennaOffsets[2] >> 4u;
        antOfst23.B.ADROFST1_ANT = config->windowCfg.antennaOffsets[3] >> 4u;
        antOfst45.B.ADROFST0_ANT = config->windowCfg.antennaOffsets[4] >> 4u;
        antOfst45.B.ADROFST1_ANT = config->windowCfg.antennaOffsets[5] >> 4u;
        antOfst67.B.ADROFST0_ANT = config->windowCfg.antennaOffsets[6] >> 4u;
        antOfst67.B.ADROFST1_ANT = config->windowCfg.antennaOffsets[7] >> 4u;
    }
    else
    {
        /* Reset relevant fields */
        ldrConf.B.DRPF   = 0u;
        ldrConf.B.DRPL   = 0u;
        ldrConf.B.EXPNT  = 0u;
        ldrConf.B.ZI     = 0u;
        ldrConf.B.FORMAT = 0u;
        ldrConf2.U       = 0u;
        antOfst01.U      = 0u;
        antOfst23.U      = 0u;
        antOfst45.U      = 0u;
        antOfst67.U      = 0u;
    }

    /* Register Write */
    slot->BE[passId].LDR.CONF2.U  = ldrConf2.U;
    slot->BE[passId].LDR.CONF.U   = ldrConf.U;
    slot->BE[passId].ANTOFST[0].U = antOfst01.U;
    slot->BE[passId].ANTOFST[1].U = antOfst23.U;
    slot->BE[passId].ANTOFST[2].U = antOfst45.U;
    slot->BE[passId].ANTOFST[3].U = antOfst67.U;
}


