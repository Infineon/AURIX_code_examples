void IfxSpu_setupNci(IfxSpu_Slot *slot, IfxSpu_PassId passId, const IfxSpu_NciConfig *config)
{
    Ifx_SPU_BE_ND_BASE  ndBase;
    Ifx_SPU_BE_ND_CTRL  ndCtrl;
    Ifx_SPU_BE_ND_CBASE ndCbase;
    uint8               index = 0u;

    ndBase.U = slot->BE[passId].ND.BASE.U;

    if (config != NULL_PTR)
    {
        /* Output Configuration */
        ndBase.B.EN   = config->enableOutput;                                                 /* Enable output       */
        ndBase.B.BASE = config->outputBaseAddr >> 5u;                                         /* Output base address */

        /* Control configuration */
        ndCtrl.B.MODE    = config->mode;                                                      /* Integration Mode    */
        ndCtrl.B.FORMAT  = config->outputFormat;                                              /* Output Format       */
        ndCtrl.B.TXCHAN  = (config->txChannelCount == 0) ? 0 : (config->txChannelCount - 1u); /* Tx channels included in integration  */
        ndCtrl.B.RXCHAN  = config->rxChannelMask;                                             /* Rx channels included in integration  */
        ndCtrl.B.CIREAL  = config->ciCalculation;                                             /* Complex or Real arithmetic calculation  */
        ndCtrl.B.SUMONLY = config->onlySummation;                                             /* Only do sum, no multiplication  */

        /* Address is relative to configuration memory and is 128 bit aligned
         * Field allows 256kb of configuration memory to be addressed
         */
        ndCbase.B.RXCBASE = config->rxCoeffBase >> 4u;                                        /* Coefficient Base Address for RX relative to configuration memory */
        ndCbase.B.TXCBASE = config->txCoeffBase >> 4u;                                        /* Coefficient Base Address for TX relative to configuration memory */

        for (index = 0u; index < IFXSPU_TX_OFFSET_COUNT; index++)
        {
            slot->BE[passId].ND.OFFSET[index].U = config->txOffset[index];  /* Offset into the integration buffer */
        }
    }
    else
    {
        ndBase.U  = 0u;
        ndCtrl.U  = 0u;
        ndCbase.U = 0u;
    }

    /* Write Register back */
    slot->BE[passId].ND.BASE.U  = ndBase.U;
    slot->BE[passId].ND.CTRL.U  = ndCtrl.U;
    slot->BE[passId].ND.CBASE.U = ndCbase.U;
}


