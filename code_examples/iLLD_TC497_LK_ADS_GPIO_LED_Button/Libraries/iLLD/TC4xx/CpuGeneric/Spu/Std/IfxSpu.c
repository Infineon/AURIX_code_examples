uint32 IfxSpu_calculateRegisterCrc(IfxSpu_Slot *slot)
{
    IfxSpu_Slot slotTmp;
    uint32      k;
    uint32      count = 1 + (IFXSPU_OFFSETOF(IfxSpu_Slot, CTRL) / sizeof(uint32));

    uint32     *dst   = (void *)&slotTmp;
    uint32     *src   = (void *)slot;

    for (k = 0; k < count; k++)
    {
        dst[k] = src[k];
    }

    /* Unused registers, REGCRC and volatile registers are assumed to be zero */
    slotTmp.REGCRC.U = 0;

    /* Unused bitfields as zero */
    slotTmp.CTRL.B.TRIG = 0;

    return IfxSpu_crc32calculate((void *)&slotTmp, count);
}


uint16 IfxSpu_getNumInputSamples(IfxSpu_Slot *slot)
{
    uint16             result;
    Ifx_SPU_ID_CONF    idConf;
    Ifx_SPU_ID_RM_ABLR rmAblr;

    idConf.U = slot->ID.CONF.U;
    rmAblr.U = slot->ID.RM.ABLR.U;

    if (IfxSpu_getDataSource(slot) != IfxSpu_DataSource_radarMemory)
    {
        result = idConf.B.SMPLCNT + 1;
    }
    else
    {
        result = rmAblr.B.BLR + 1;
    }

    return result;
}


