void IfxSpu_initModule(Ifx_SPU *spu, const IfxSpu_ModuleConfig *config)
{
    IfxSpu_AttInterruptConfig *att0IntCfgPtr = config->att0IntCfg;
    IfxSpu_AttInterruptConfig *att1IntCfgPtr = config->att1IntCfg;
    IfxSpu_ErrInterruptConfig *errIntCfgPtr  = config->errIntCfg;

    /* Enable Module */
    {
        IfxSpu_enableModule(spu);

        while (spu->CLC.B.DISS != 0)
        {}
    }

    /* ATT0 Interrupt configuration */
    if (att0IntCfgPtr != NULL_PTR)
    {
        /* Interrupt configuration */
        if (att0IntCfgPtr->priority > 0)
        {
            volatile Ifx_SRC_SRCR *src = IfxSpu_getSrcPointer(spu, IfxSpu_ServiceRequest_att0);
            IfxSrc_init(src, att0IntCfgPtr->typeOfService, att0IntCfgPtr->priority, att0IntCfgPtr->vmId);
            IfxSrc_enable(src);
        }
    }

    /* ATT1 Interrupt configuration */
    if (att1IntCfgPtr != NULL_PTR)
    {
        /* Interrupt configuration */
        if (att1IntCfgPtr->priority > 0)
        {
            volatile Ifx_SRC_SRCR *src = IfxSpu_getSrcPointer(spu, IfxSpu_ServiceRequest_att1);
            IfxSrc_init(src, att1IntCfgPtr->typeOfService, att1IntCfgPtr->priority, att1IntCfgPtr->vmId);
            IfxSrc_enable(src);
        }
    }

    /* ERR Interrupt configuration */
    if (errIntCfgPtr != NULL_PTR)
    {
        /* ERR mask */
        {
            Ifx_SPU_STATUS_ERRMASK errMask;
            errMask.U      = spu->STATUS.ERRMASK.U;

            errMask.B.MASK = ((errIntCfgPtr->radarMemoryReadError) |         /* BIT 0 */
                              (errIntCfgPtr->inputDmaWriteError << 1u) |     /* BIT 1 */
                              (errIntCfgPtr->radarMemoryWriteError << 2u) |  /* BIT 2 */
                              (errIntCfgPtr->fifoOverrun << 3u) |            /* BIT 3 */
                              (errIntCfgPtr->pactrError << 4u) |             /* BIT 4 */
                              (errIntCfgPtr->inputDataOverRunError << 5u) |  /* BIT 5 */
                              (errIntCfgPtr->addrCalOverflow << 6u) |        /* BIT 6 */
                              (errIntCfgPtr->arithmeticOverflow << 7u) |     /* BIT 7 */
                              (errIntCfgPtr->idmTriggerLimitError << 8u));   /* BIT 8 */

            spu->STATUS.ERRMASK.U = errMask.U;
        }

        /* Interrupt configuration */
        volatile Ifx_SRC_SRCR *src = IfxSpu_getSrcPointer(spu, IfxSpu_ServiceRequest_err);
        IfxSrc_init(src, errIntCfgPtr->typeOfService, errIntCfgPtr->priority, errIntCfgPtr->vmId);
        IfxSrc_enable(src);
    }
}


