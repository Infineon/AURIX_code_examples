/* Macro to define the Interrupt Service Routine. */
IFX_INTERRUPT(CDSP_ResultHandling, 0, CDSP_RES_SRV_REQ_PRIO_PH_U);

/* Interrupt service to process the result from CDSP */
void CDSP_ResultHandling(void)
{
    int index;
    /* Store the CDSP result values for the application */
    (void)IfxAdc_Cdsp_readFftFilterResult(&g_CdspFilters.coreSignalA, g_CdspFilters.fftRawSignalA);
    for(index= 0; index < FFT_NUM_SAMPLES; index++)
    {
        g_CdspFilters.fftSignalA[index]= 3.3f * (g_CdspFilters.fftRawSignalA[index]/512.0f) / 4096.0f;
    }
}

/* This function configures CDSP core 0 */
void CDSP_initChannel_FC6(void)
{
    IfxAdc_Cdsp_DspCoreConfig coreConfig;                                    /* DSP Core Configuration               */

    /* Trigger Configuration */
    IfxAdc_Cdsp_ResultSrvReq resSrvReq =
    {
        NULL_PTR,           /* Result Callback. Callback function will be invoked for each result event. (ToS = CPU) */
        CDSP_RES_SRV_REQ_PRIO_PH_U,       /* Interrupt priority.If ToS is DMA, priority should be DMA channel number */
        IfxSrc_Tos_cpu0,                                                                /* type of interrupt service */
        IfxSrc_VmId_0,                                                                     /* Virtual Machine Number */
        IfxAdc_CdspSrvReq0_gateLow                                   /* Enable service request for Result event(SR0) */
    };

    IfxAdc_Cdsp_SoftwareTrigger swTriggerConfig =
    {
        IfxAdc_CdspTriggerSel_40,                                                     /* Selection value for trigger */
        IfxAdc_CdspSoftwareTriggerMode_activateOnRisingEdge,                           /* Edge selection for trigger */
        0                                                                                     /* Trigger delay count */
    };

    /* Configuring CDSP triggers  */
    IfxAdc_Cdsp_TriggerConfig trigConfig =
    {
        NULL_PTR,                                                                               /* No serviceReqTrig */
        NULL_PTR,                                                                                /* No timestampTrig */
        NULL_PTR,                                                                                 /* No boundaryTrig */
        &swTriggerConfig,                                    /* Gate to CDSP to acquire the input signals from TMADC */
    };

    /* CDSP Memory Configuration */
    IfxAdc_Cdsp_MemoryConfig memoryCfg =
    {
        (void*)&cdspFc6IccmImage,   /* FC6 is the filter chain that is CDSP SW image to be loaded by the host driver */
        (void*)&g_Fc6_FFT_Window,                                                               /* Hann window image */
        (uint16)cdspFc6IccmImageSize,
        (uint16)sizeof(UserConfig_FftType)
    };

    /* DSP core configuration structure to default values */
    IfxAdc_Cdsp_initDspCoreConfig(&coreConfig, &MODULE_ADC);

    coreConfig.coreId         = IfxAdc_CdspCore_0;

    coreConfig.inputSel       = IfxAdc_CdspInput_tmadc0Res1;                                    /* TMADC0.RES1 input */

    coreConfig.bndCfg         = NULL_PTR;                                                  /* Boundary Configuration */
    coreConfig.triggerCfg     = &trigConfig;                                                /* Trigger configuration */
    coreConfig.resSrvReqCfg   = &resSrvReq;                                        /* Result Service request trigger */
    coreConfig.eventSrvReqCfg = NULL_PTR;                             /* Wake-up Error Event service request trigger */
    coreConfig.alarmSrvReqCfg = NULL_PTR;                                           /* Alarm Service request trigger */

    coreConfig.resultCfg.readAsUnsigned = FALSE;
    coreConfig.resultCfg.disableFifo    = TRUE;
    coreConfig.resultCfg.dataReadWidth  = IfxAdc_CdspDataReadWidth_32Bit;
    coreConfig.resultCfg.fifoSrvLevel   = IfxAdc_CdspFifoSrLevel_1;

    coreConfig.memCfg = &memoryCfg;                                         /* Link memory configuration to DSP core */

    coreConfig.runCdspCore= FALSE;                         /* Don't run the DSP because the input address is not set */

    coreConfig.filterchainSel= IfxAdc_Cdsp_FilterChainSel_fft;

    IfxAdc_Cdsp_initDspCore(&g_CdspFilters.coreSignalA, &coreConfig);                         /* Initialize DSP core */

    {
        UserConfig_FftType *filterCfgPtr;
        /* Following to be done because iLLD wants to make it bit difficult for the user ;) */
        filterCfgPtr= (UserConfig_FftType*)IFXADC_CDSP_GETDCCM_ADDRESS(coreConfig.coreId);
        filterCfgPtr->InputAddress=
                (0x906CU + 4U * (coreConfig.inputSel - IfxAdc_CdspInput_tmadc0Res0));  /*(0x906C +  (4*(2*x+v)))H    */

    }
}

void CDSP_init(void)
{
    IfxAdc_Cdsp_Config moduleConfig;                                                    /* CDSP Module Configuration */

    /* Enable ADC if not enabled already */
    if(MODULE_ADC.CLC.B.DISS == 1U)
    {
        IfxAdc_enableModule(&MODULE_ADC);
    }

    /* Module configuration structure to default values */
    IfxAdc_Cdsp_initModuleConfig(&moduleConfig, &MODULE_ADC);

    /* Module Initialization */
    IfxAdc_Cdsp_initModule(&g_CdspFilters.module, &moduleConfig);

    CDSP_initChannel_FC6();

    IfxAdc_Cdsp_enableMultipleCores(&g_CdspFilters.module, (1U<<IfxAdc_CdspCore_0));

    /* Wait for CDSP core to sleep post initialization */
    while(!(IfxAdc_getCdspCoreSleepStatus(&MODULE_ADC.CDSP, IfxAdc_CdspCore_0)));
}


void CDSP_initIntegratorGatePwm(void)
{
    if(!IfxEgtm_isEnabled(&MODULE_EGTM))
    {
        float32 frequency;
        /* Enable EGTM, it is necessary if the EGTM is not initialized earlier */
        IfxEgtm_enable(&MODULE_EGTM);
        frequency = IfxEgtm_Cmu_getModuleFrequency(&MODULE_EGTM);
        /* Set the global clock frequency to the max */
        IfxEgtm_Cmu_setGclkFrequency(&MODULE_EGTM, frequency);
        /* Enable the FXCLK clock */
        IfxEgtm_Cmu_enableClocks(&MODULE_EGTM, IFXEGTM_CMU_CLKEN_FXCLK | IFXEGTM_CMU_CLKEN_CLK0);
    }

    {
        IfxEgtm_Tom_Pwm_Config pwmConfig; /* Configuration structure */
        float32 frequency;
        frequency = IfxEgtm_Cmu_getModuleFrequency(&MODULE_EGTM);

        /* Initialize with default values */
        IfxEgtm_Tom_Pwm_initConfig(&pwmConfig, &MODULE_EGTM);

        /* Update Configuration for the application need */
        pwmConfig.cluster      = (IfxEgtm_Cluster)CDSP_INT_GATE_SIGNAL.tom;
        pwmConfig.tomChannel   = CDSP_INT_GATE_SIGNAL.channel;
        pwmConfig.clock        = IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk2;                                /*Clock source /256 */
        pwmConfig.period       = (uint16)(frequency/256.0f/CDSP_INT_GATE_SIGNAL_FREQUENCY);
        pwmConfig.dutyCycle    = (uint16)(CDSP_INT_GATE_SIGNAL_DUTY * pwmConfig.period / 100.0f); /*Duty cycle ticks */
        pwmConfig.signalLevel  = Ifx_ActiveState_high;                                 /* Signal level during duty on*/
        pwmConfig.synchronousUpdateEnabled   = TRUE;                                      /* Update at end of period */
        pwmConfig.immediateStartEnabled      = TRUE;                                     /* Start PWM at end of init */


        pwmConfig.pin.outputPin  = &CDSP_INT_GATE_SIGNAL;
        pwmConfig.pin.outputMode = IfxPort_OutputMode_pushPull;
        pwmConfig.pin.padDriver  = IfxPort_PadDriver_cmosAutomotiveSpeed1;

        IfxEgtm_Tom_Pwm_init(&g_cdspGate, &pwmConfig);

        /* CDSP is gated by EGTM TOM1 CH0 Connect to ADC trigger inputs */
        IfxEgtm_Trigger_trigToAdc(pwmConfig.cluster,
                                  IfxEgtm_TrigSource_tom,
                                  (IfxEgtm_TrigChannel)pwmConfig.tomChannel,
                                  IfxEgtm_Cfg_AdcTriggerSignal_4);
    }
}
