#include "Ifx_Types.h"
#include "Ifx_Cfg.h"
#include "IfxCpu.h"
#include "IfxWtu.h"
#include "Bsp.h"
#include "IfxPort.h"
#include "IfxPort_PinMap.h"
#include "IfxStm.h"
#include "TMADC_Setup.h"
#include "CDSP_Filtering.h"
#include "PWM_SignalGenerator.h"

#define LED_TC      (IfxPort_P03_9)                                         /* LED: Port, Pin definition             */
#define WAIT_TIME   500                                                     /* Wait time constant in milliseconds    */

void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword());
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());
    
    IfxPort_setPinHigh(LED_TC.port, LED_TC.pinIndex);
    IfxPort_setPinMode(LED_TC.port, LED_TC.pinIndex, IfxPort_Mode_outputPushPullGeneral);

    CDSP_init();                                                                  /* Initialize the CDSP             */
    TMADC_init();                                                                 /* Initialize the TMADC            */
    TMADC_initTrigPwm();                                                 /* Initialize the EGTM PWM to trigger TMADC */
    CDSP_initIntegratorGatePwm();                                            /* Initialize the EGTM PWM to gate CDSP */

    PWM_SignalGenerator_init();                                               /* Initialize the Signal Generator PWM */

    void OneEye_initOsci(void);
    OneEye_initOsci();

    while (1)
    {
        IfxPort_togglePin(LED_TC.port, LED_TC.pinIndex);
        waitTime(IfxStm_getTicksFromMilliseconds(WAIT_TIME));                       /* Wait 500 milliseconds         */
        PWM_SignalGenerator_update();
    }
}
