/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void PWM_SignalGenerator_init(void);
void PWM_SignalGenerator_update(void);

#endif /* PWM_SIGNALGENERATOR_H_ */
