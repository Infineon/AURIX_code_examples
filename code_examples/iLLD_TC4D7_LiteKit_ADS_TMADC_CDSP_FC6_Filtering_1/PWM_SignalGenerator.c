/* Macro to define the Interrupt Service Routine. */
IFX_INTERRUPT(periodEventFunction, 0, PWM_EGTM_TOM_ISR_PRIORITY);

/* Interrupt callback function, used for interrupt */
void periodEventFunction(void)
{
    static float32 angleBase= 0.0f;                                                           /* Sinusoidal Position */

    /* Base Signal generator  */
    g_PwmSignalGenerator.dutycycle= g_Signal.offsetFactor +
                                    g_Signal.amplitudeFactorBase * sinf(angleBase);

    angleBase += g_Signal.freqBase * 2.0f * IFX_PI / PWM_FREQUENCY;
    if(angleBase >= (2.0f * IFX_PI)) angleBase= 0.0f;
    g_Signal.angleSignal= angleBase;

#if (PWM_NUM_OF_SINE_SIGNALS > 1)
    {
        static float32 angleA= 0.0f;
        /* Signal A generator added on top of base signal */
        g_PwmSignalGenerator.dutycycle+= g_Signal.amplitudeFactorA * sinf(angleA);
        angleA += g_Signal.freqSignalA * 2.0f * IFX_PI / PWM_FREQUENCY;
        if(angleA >= (2.0f * IFX_PI)) angleA= 0.0f;
    }
#endif
#if (PWM_NUM_OF_SINE_SIGNALS > 2)
    {
        static float32 angleB= 0.0f;
        /* Signal B generator added on top of base signal and signal A */
        g_PwmSignalGenerator.dutycycle+= g_Signal.amplitudeFactorB * sinf(angleB);
        angleB += g_Signal.freqSignalB * 2.0f * IFX_PI / PWM_FREQUENCY;
        if(angleB >= (2.0f * IFX_PI)) angleB= 0.0f;
    }
#endif

    IfxEgtm_Tom_Pwm_setDuty(&g_PwmSignalGenerator.pwm, g_PwmSignalGenerator.dutycycle);
}

/* Function to initialize the PWM Signal Generator */
void PWM_SignalGenerator_init(void)
{
    if(!IfxEgtm_isEnabled(&MODULE_EGTM))
    {
        float32 frequency;
        /* Enable EGTM, it is necessary if the EGTM is not initialized earlier */
        IfxEgtm_enable(&MODULE_EGTM);
        frequency = IfxEgtm_Cmu_getModuleFrequency(&MODULE_EGTM);
        /* Set the global clock frequency to the max */
        IfxEgtm_Cmu_setGclkFrequency(&MODULE_EGTM, frequency);
        /* Enable the FXCLK clock */
        IfxEgtm_Cmu_enableClocks(&MODULE_EGTM, IFXEGTM_CMU_CLKEN_FXCLK | IFXEGTM_CMU_CLKEN_CLK0);
    }

    {
        IfxEgtm_Tom_Pwm_Config pwmConfig; /* Configuration structure */

        g_PwmSignalGenerator.timerChannelFreq = IfxEgtm_Cmu_getModuleFrequency(&MODULE_EGTM);

        /* Initialize with default values */
        IfxEgtm_Tom_Pwm_initConfig(&pwmConfig, &MODULE_EGTM);

        /* Update Configuration for the application need */
        pwmConfig.cluster      = (IfxEgtm_Cluster)PWM_SIGNAL_A.tom;
        pwmConfig.tomChannel   = PWM_SIGNAL_A.channel;
        pwmConfig.clock        = IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk0;         /* FxCLK0 = GTM Freq is connected initially*/
        pwmConfig.period       = (uint16)((g_PwmSignalGenerator.timerChannelFreq)/PWM_FREQUENCY);
        pwmConfig.dutyCycle    = pwmConfig.period / 2;                                        /* Duty cycle in ticks */
        pwmConfig.signalLevel  = Ifx_ActiveState_high;                                   /* Signal level during duty */
        pwmConfig.synchronousUpdateEnabled   = TRUE;                   /* Update parameters synched to end of period */
        pwmConfig.immediateStartEnabled      = TRUE;                                     /* Start PWM at end of init */

        pwmConfig.interrupt.ccu0Enabled      = TRUE;                               /* Enable interrupt at period end */
        pwmConfig.interrupt.ccu1Enabled      = FALSE;                                    /* No interrupt at duty end */
        pwmConfig.interrupt.mode= IfxEgtm_IrqMode_pulseNotify;       /* Notification to interrupt mechanism as pulse */
        pwmConfig.interrupt.isrPriority= PWM_EGTM_TOM_ISR_PRIORITY;
        pwmConfig.interrupt.isrProvider= IfxSrc_Tos_cpu0;
        pwmConfig.interrupt.vmId = IfxSrc_VmId_0;

        pwmConfig.pin.outputPin  = &PWM_SIGNAL_A;
        pwmConfig.pin.outputMode = IfxPort_OutputMode_pushPull;
        pwmConfig.pin.padDriver  = IfxPort_PadDriver_cmosAutomotiveSpeed1;

        IfxEgtm_Tom_Pwm_init(&g_PwmSignalGenerator.pwm, &pwmConfig);

        /* Assign values to global data structure to be used further during run */
        g_PwmSignalGenerator.timerChannelFreq= IfxEgtm_Tom_Ch_getClockFrequency(g_PwmSignalGenerator.pwm.egtm,
                                                                                g_PwmSignalGenerator.pwm.tom,
                                                                                g_PwmSignalGenerator.pwm.tomChannel);
        g_PwmSignalGenerator.frequency= PWM_FREQUENCY;
        g_PwmSignalGenerator.dutycycle= 50.0f;
    }
}

/* Function to update the PWM Signal Generator mode and related parameters*/
void PWM_SignalGenerator_update(void)
{
    static SignalMode_t currentMode= SignalMode_sinusoidal;
    static float32 pwmFrequency= PWM_FREQUENCY;
    boolean updateFreq= FALSE;

    if(currentMode != g_Signal.signalMode)
    {
        /* Proceed only if the signal mode has changed */
        currentMode= g_Signal.signalMode;
        updateFreq= TRUE;
        if (currentMode == SignalMode_sinusoidal)
        {
            pwmFrequency= g_PwmSignalGenerator.frequency;
            g_Signal.angleSignal= 0.0f;
            IfxEgtm_Tom_Ch_setNotification(g_PwmSignalGenerator.pwm.tom, g_PwmSignalGenerator.pwm.tomChannel,
                                           IfxEgtm_IrqMode_pulseNotify, TRUE, FALSE);
        }
        else
        {
            IfxEgtm_Tom_Ch_setNotification(g_PwmSignalGenerator.pwm.tom, g_PwmSignalGenerator.pwm.tomChannel,
                                           IfxEgtm_IrqMode_pulseNotify, FALSE, FALSE);
            pwmFrequency= g_Signal.freqBase;
            g_PwmSignalGenerator.dutycycle= 50.0f; /* For the pulse train, fix the duty to 50% */
        }
    }
    else
    {
        /* Signal mode has not changed */
        if((currentMode == SignalMode_pulsetrain) && (pwmFrequency != g_Signal.freqBase))
        {
            /* Process if the mode is pulse train and the Pulse train freq is changed */
            pwmFrequency= g_Signal.freqBase;
            updateFreq= TRUE;
        }
    }

    if(updateFreq)
    {
        uint32 periodTicks, checkTicks;
        IfxEgtm_Tom_Ch_ClkSrc clkSrc;

        periodTicks= IfxEgtm_Cmu_getFxClkFrequency(&MODULE_EGTM, IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk0, TRUE)/pwmFrequency;
        clkSrc=  IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk0;

        checkTicks= periodTicks;

        /* Evaluate the appropriate TOM channel clock source to take advantage of higher possible resolution*/
        /* FxCLK0 = CMU Module Freq
         * FxCLK1 = CMU Module Freq / 16
         * FxCLK2 = CMU Module Freq / 256
         * FxCLK3 = CMU Module Freq / 4096
         * FxCLK4 = CMU Module Freq / 65536
         */
        if (checkTicks > 0xFFFF)
        {
            clkSrc=  IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk1;
            checkTicks= periodTicks / 16;
        }
        if (checkTicks > 0xFFFF)
        {
            clkSrc=  IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk2;
            checkTicks= periodTicks / 256;
        }
        if (checkTicks > 0xFFFF)
        {
            clkSrc=  IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk3;
            checkTicks= periodTicks / 4096;
        }
        if (checkTicks > 0xFFFF)
        {
            clkSrc=  IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk4;
        }

        /* Set TOM channel clock source */
        IfxEgtm_Tom_Ch_setClockSource(g_PwmSignalGenerator.pwm.tom, g_PwmSignalGenerator.pwm.tomChannel, clkSrc);
        g_PwmSignalGenerator.clkSource = clkSrc;
        g_PwmSignalGenerator.timerChannelFreq= IfxEgtm_Tom_Ch_getClockFrequency(g_PwmSignalGenerator.pwm.egtm, g_PwmSignalGenerator.pwm.tom, g_PwmSignalGenerator.pwm.tomChannel);

        /* Calculate period and duty to update to the timers */
        periodTicks= g_PwmSignalGenerator.timerChannelFreq / pwmFrequency;
        IfxEgtm_Tom_Ch_setCompareZeroShadow(g_PwmSignalGenerator.pwm.tom, g_PwmSignalGenerator.pwm.tomChannel, (uint16)periodTicks);
        IfxEgtm_Tom_Pwm_setDuty(&g_PwmSignalGenerator.pwm, g_PwmSignalGenerator.dutycycle);
    }
}

/* Function for debugging purpose, not called for the example usage */
void PWM_checkEgtmConfig(void)
{
    checkData.egtmFreq= IfxEgtm_Cmu_getModuleFrequency(&MODULE_EGTM);
    checkData.cmuClkFreq= IfxEgtm_Cmu_getClkFrequency(&MODULE_EGTM, IfxEgtm_Cmu_Clk_0, TRUE);
    checkData.cmuFxclkFreq= IfxEgtm_Cmu_getFxClkFrequency(&MODULE_EGTM, IfxEgtm_Cmu_Fxclk_0, TRUE);
}
