void OneEye_initOsci(void)
{
    Ifx_Osci_Config osciCfg;            /* Oscilloscope configuration structure */
    Ifx_Osci_initConfig(&osciCfg);  /* Oscilloscope configuration structure initialization */
    {
        /* When a signal is created, automatically assign the signal to a channel if there is a free channel */
        osciCfg.autoAddChannels = TRUE;
        /* Only used for FFT, but it doesn't hurt to configure it */
        osciCfg.samplePeriod = 50e-6;
        /* Mode of the osci after initialization (can be changed at runtime) */
        osciCfg.triggerMode = Ifx_Osci_TriggerMode_automatic;
    }
    /* Call the oscilloscope initialization function */
    Ifx_Osci_init(&g_dasOsci, &osciCfg);

    /* Add the oscilloscope signals */
    Ifx_Osci_addSignal(&g_dasOsci, "TMADC_SignalA", Ifx_Osci_DataType_Float32, &g_ResultSignalA, 0);

    /* Call the oscilloscope start function */
    Ifx_Osci_start(&g_dasOsci);
}
