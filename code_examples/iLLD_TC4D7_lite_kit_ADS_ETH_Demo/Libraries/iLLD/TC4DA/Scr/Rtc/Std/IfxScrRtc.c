void IfxScrRtc_initModule(IfxScrRtc_Config *rtcConfig)
{
	Ifx_SCR_RTC_CON rtc_con;
    SCR_SYSCON0.U = SCR_SYSCON0.U & (uint8)(0xFE);
    /* Stop the RTC */
    SCR_RTC_CON.U = (SCR_RTC_CON.U & ~(1u << IFX_SCR_RTC_CON_RTCC_OFF));

    /* Loading the 32-bit initial count value */
    /* Enable bit protection write access */
    SCR_PASSWD.U   = ((SCR_PASSWD.U & (unsigned char)0x07) | (0x13 << IFX_SCR_PASSWD_PASS_OFF));

    SCR_RTC_CNT0.U = (uint8)rtcConfig->initialCountValue;
    SCR_RTC_CNT1.U = (uint8)((rtcConfig->initialCountValue) >> 8);
    SCR_RTC_CNT2.U = (uint8)((rtcConfig->initialCountValue) >> 16);
    SCR_RTC_CNT3.U = (uint8)((rtcConfig->initialCountValue) >> 24);

    /* Disable bit protection write access */
    SCR_PASSWD.U = ((SCR_PASSWD.U & (unsigned char)0x07) | (0x15 << IFX_SCR_PASSWD_PASS_OFF));

    /* Configure RTC clock, prescalar and interrupt */
    rtc_con.U     = (SCR_RTC_CON.U & ~((IFX_SCR_RTC_CON_RTCCLKSEL_MSK << IFX_SCR_RTC_CON_RTCCLKSEL_OFF) | (1u << IFX_SCR_RTC_CON_RTPBYP_OFF) | (1u << IFX_SCR_RTC_CON_ECRTC_OFF)));
    rtc_con.U    |= (rtcConfig->clockSource << IFX_SCR_RTC_CON_RTCCLKSEL_OFF) | (1u << IFX_SCR_RTC_CON_RTCCLKSEL_P_OFF) | \
    		        (rtcConfig->enableInterruptOnCompare << IFX_SCR_RTC_CON_ECRTC_OFF) | \
    		        (rtcConfig->bypassPrescaler << IFX_SCR_RTC_CON_RTPBYP_OFF);
    SCR_RTC_CON.U = rtc_con.U;

    /* Load RTC Compare Value*/
    SCR_RTC_CR0.U = (uint8)(rtcConfig->compareValue & (uint32)0xFFU);
    SCR_RTC_CR1.U = (uint8)((rtcConfig->compareValue & ((uint32)0xFFU << 8)) >> 8);
    SCR_RTC_CR2.U = (uint8)((rtcConfig->compareValue & ((uint32)0xFF << 16)) >> 16);
    SCR_RTC_CR3.U = (uint8)((rtcConfig->compareValue & ((uint32)0xFF << 24)) >> 24);
}
