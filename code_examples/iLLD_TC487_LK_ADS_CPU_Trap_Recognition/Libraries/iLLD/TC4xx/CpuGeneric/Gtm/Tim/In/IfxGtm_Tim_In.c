boolean IfxGtm_Tim_In_init(IfxGtm_Tim_In *driver, const IfxGtm_Tim_In_Config *config)
{
    boolean             	   result = TRUE;
    Ifx_GTM_CLS_TIM_CH 		   *channel;
    IfxGtm_Tim_Ch       	   channelIndex;
    IfxGtm_Tim                 clsIndex;
    IfxGtm_Tim_In_Src          inputSRC;
    IfxGtm_Tim_In_SrcForLutIn2 input2ForLUT;
    IfxGtm_Tim_In_SrcForTimIn  srcForTimIn;
    uint8                      lookupTableValue;

    /* Determine the channel index and cluster index based on the configuration */
    if (config->inputPin != NULL_PTR)
    {
        channelIndex = config->inputPin->channel;
        clsIndex     = config->inputPin->tim;
    }
    else
    {
        channelIndex = config->channelIndex;
        clsIndex     = config->cluster;
    }

    input2ForLUT             = config->input2ForLUT;
    inputSRC                 = config->inputSrc;
    srcForTimIn              = config->srcForTimIn;
    lookupTableValue         = config->lookupTableValue;
	
    /* Gets a pointer to the TIM channel */
    channel                  = IfxGtm_Tim_getChannel(&config->gtm->CLS[clsIndex].TIM, channelIndex);
    driver->clsIndex         = clsIndex;
    driver->channelIndex     = channelIndex;
    driver->channel          = channel;
    driver->periodTick       = 0;
    driver->pulseLengthTick  = 0;
    driver->dataCoherent     = FALSE;
    driver->newData          = FALSE;
    driver->dataLost         = FALSE;
    driver->overflowCnt      = FALSE;
    driver->edgeCounterUpper = 0;
    driver->softwareInputSet = FALSE;
    driver->tim              = &config->gtm->CLS[clsIndex].TIM;

    /* Sets TIM channel mode */
    channel->CTRL.B.TIM_MODE = (uint8)config->mode;

    /* Configures the clock source */
    IfxGtm_Tim_Ch_setClockSource(channel, config->capture.clock);

    driver->captureClockFrequency = IfxGtm_Tim_Ch_getCaptureClockFrequency(config->gtm, channel);

    if ((config->mode == IfxGtm_Tim_Mode_inputEvent) || (config->mode == IfxGtm_Tim_Mode_gatedPeriodic))
    {
        channel->CTRL.B.DSL = (config->capture.activeEdge != IfxGtm_Tim_In_ActiveEdge_both) ? (uint8)config->capture.activeEdge : (uint8)0u;
        channel->CTRL.B.ISL = (config->capture.activeEdge == IfxGtm_Tim_In_ActiveEdge_both) ? (uint8)1u : (uint8)0u;

        if (config->mode == IfxGtm_Tim_Mode_gatedPeriodic)
        {
            IfxGtm_Tim_Ch_setShadowCounter(channel, config->capture.gateCount);
        }
    }
    else
    {
        IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, (config->mode == IfxGtm_Tim_Mode_pwmMeasurement));
        IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, (config->capture.mode == Ifx_Pwm_Mode_leftAligned) || (config->capture.mode == Ifx_Pwm_Mode_rightAligned));
        result             &= ((config->capture.mode == Ifx_Pwm_Mode_leftAligned) || (config->capture.mode == Ifx_Pwm_Mode_rightAligned)) ? TRUE : FALSE;

        channel->CTRL.B.DSL = config->capture.mode == Ifx_Pwm_Mode_leftAligned ? (uint8)1u : (uint8)0u;
    }

    channel->CTRL.B.CNTS_SEL = (uint8)IfxGtm_Tim_CntsSel_cntReg;
    /* Use CNTS as input for GPR0 */
    channel->CTRL.B.GPR0_SEL = (uint8)IfxGtm_Tim_GprSel_cnts;
    /* Use CNT as input for GPR1 */
    channel->CTRL.B.GPR1_SEL = (uint8)IfxGtm_Tim_GprSel_cnts;

    /* Interrupt configuration */
    if (config->isrPriority == TRUE)
    {
        volatile Ifx_SRC_SRCR *src;
        IfxGtm_Tim_Ch_setNotificationMode(channel, config->irqMode);
        IfxGtm_Tim_Ch_setChannelNotification(channel, config->capture.irqOnNewVal,
            config->capture.irqOnCntOverflow, config->capture.irqOnEcntOverflow, config->capture.irqOnDatalost);

        src = IfxGtm_Tim_Ch_getSrcPointer(config->gtm, clsIndex, channelIndex);
        /* Initializes the service request control register */
        IfxSrc_init(src, config->isrProvider, config->isrPriority, config->vmId);
        /* Enables a specific interrupt service request */
        IfxSrc_enable(src);
    }

    /* Timeout configuration */
    if (config->timeout.timeout > (float32)0.0f)
    {
        float32 clockFrequency;
        sint32  timeout;
        channel->TDUV.B.TCS = (uint8)config->timeout.clock;
        clockFrequency      = IfxGtm_Tim_Ch_getTimeoutClockFrequency(config->gtm, channel);

        timeout             = ((sint32)(float32)(config->timeout.timeout * clockFrequency) - 1);

        if (timeout < 0)
        {
            timeout = 0;
        }
        else if (timeout >= (0xFFFFFF))
        {
            timeout = 0xFFFFFF;
            result  = FALSE;
        }
        else
        {}

        channel->CTRL.B.TOCTRL = config->capture.mode == Ifx_Pwm_Mode_leftAligned ? (uint8)IfxGtm_Tim_Timeout_risingEdge : (uint8)IfxGtm_Tim_Timeout_fallingEdge;
        channel->TDUV.U       |= (0xFFFFFFu & (uint32)timeout); /* 24 bit timeout value */

        /* Sets the timeout notification */
        IfxGtm_Tim_Ch_setTimeoutNotification(channel, config->timeout.irqOnTimeout);
    }

    switch (inputSRC)
    {
    case IfxGtm_Tim_In_Src_channelCurrentOrPrevious:
        driver->tim->IN_SRC.U = (((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_MODE0_OFF) + ((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_VAL0_OFF)) << ((uint8)channelIndex * (IFX_GTM_CLS_TIM_IN_SRC_VAL0_LEN + IFX_GTM_CLS_TIM_IN_SRC_MODE0_LEN));

        if (srcForTimIn == IfxGtm_Tim_In_SrcForTimIn_currentChannel)
        {
            channel->CTRL.B.CICTRL = (uint8)IfxGtm_Tim_In_SrcForTimIn_currentChannel;
        }
        else
        {
            channel->CTRL.B.CICTRL = (uint8)IfxGtm_Tim_In_SrcForTimIn_previousChannel;
        }

        break;

    case IfxGtm_Tim_In_Src_auxiliary:
        driver->tim->IN_SRC.U = (((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_MODE0_OFF) + ((uint32)2u << IFX_GTM_CLS_TIM_IN_SRC_VAL0_OFF)) << ((uint8)channelIndex * (IFX_GTM_CLS_TIM_IN_SRC_VAL0_LEN + IFX_GTM_CLS_TIM_IN_SRC_MODE0_LEN));
        break;

    case IfxGtm_Tim_In_Src_software:
        driver->tim->IN_SRC.U = (((uint32)2u << IFX_GTM_CLS_TIM_IN_SRC_MODE0_OFF) + ((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_VAL0_OFF)) << ((uint8)channelIndex * (IFX_GTM_CLS_TIM_IN_SRC_VAL0_LEN + IFX_GTM_CLS_TIM_IN_SRC_MODE0_LEN));

        if (srcForTimIn == IfxGtm_Tim_In_SrcForTimIn_currentChannel)
        {
            channel->CTRL.B.CICTRL = (uint8)IfxGtm_Tim_In_SrcForTimIn_currentChannel;
        }
        else
        {
            channel->CTRL.B.CICTRL = (uint8)IfxGtm_Tim_In_SrcForTimIn_previousChannel;
        }

        /* Flag to set the software input if provided */
        driver->softwareInputSet = TRUE;
        break;

    default:
        /* If the user doesn't pass any input or passes invalid input, current channel will be configured by default */
        driver->tim->IN_SRC.U  = (((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_MODE0_OFF) + ((uint32)1u << IFX_GTM_CLS_TIM_IN_SRC_VAL0_OFF)) << ((uint8)channelIndex * (IFX_GTM_CLS_TIM_IN_SRC_VAL0_LEN + IFX_GTM_CLS_TIM_IN_SRC_MODE0_LEN));
        channel->CTRL.B.CICTRL = (uint8)IfxGtm_Tim_In_SrcForTimIn_currentChannel;
        break;
    }

    /* LUT Bypassed */
    if (config->useLUT == FALSE)
    {
    	/* Clearing the LUT bits to make sure is not configured */
        driver->tim->CH[channelIndex].ECTRL.B.USE_LUT = (uint32)0u;
    }
    else
    {
        /* For LUT_IN2 */
        switch (input2ForLUT)
        {
        case IfxGtm_Tim_In_SrcForLutIn2_external_capture:
        	/* Setting ECTRL 23rd bit=0 22nd bit = 1 */
            driver->tim->CH[channelIndex].ECTRL.B.USE_LUT = (uint32)1u;
            break;

        case IfxGtm_Tim_In_SrcForLutIn2_outputSignal_previousChannel :
        	/* Setting ECTRL 23rd bit=1 22nd bit = 0 */
            driver->tim->CH[channelIndex].ECTRL.B.USE_LUT = (uint32)2u;
            break;
        case IfxGtm_Tim_In_SrcForLutIn2_outputSignal_tssmMode:
        	/* Setting ECTRL 23rd bit=1 22nd bit = 1 */
            driver->tim->CH[channelIndex].ECTRL.B.USE_LUT = (uint32)3u;
            break;
        default:
            /* Default case is External capture out of this line if the user doesn't pass any thing or passes invalid input */
        	/* Setting ECTRL 23rd bit=0 22nd bit = 1 */
            driver->tim->CH[channelIndex].ECTRL.B.USE_LUT = (uint32)1u;
            break;
        }
    }

    if (config->inputPin != NULL_PTR)
    {
        IfxGtm_PinMap_setTimTin(config->inputPin, config->inputPinMode);
    }

    /* Contents of the bit field CLSi_TIM_CHx_TDUC.TO_CNT2 determines the lookup table */
    driver->tim->CH[channelIndex].TDUC.U = (uint32)lookupTableValue << IFX_GTM_CLS_TIM_CH_TDUC_TO_CNT2_OFF;

    /* Filter configuration */
    if ((config->filter.fallingEdgeMode != IfxGtm_Tim_In_ConfigFilterMode_none)
        || (config->filter.risingEdgeMode != IfxGtm_Tim_In_ConfigFilterMode_none))
    {
        float32 clockFrequency;
        sint32  fallingfilterTime;
        sint32  risingfilterTime;

        /* Enable Filter */
        channel->CTRL.B.FLT_EN      = 1;
        channel->CTRL.B.FLT_CNT_FRQ = (uint8)config->filter.clock;

        clockFrequency              = IfxGtm_Tim_Ch_getFilterClockFrequency(config->gtm, channel);

        fallingfilterTime           = ((sint32)(float32)(config->filter.fallingEdgeFilterTime * clockFrequency) - 1);

        if (fallingfilterTime < 0)
        {
            fallingfilterTime = 0;
        }
        else if (fallingfilterTime > (sint32)IFX_GTM_CLS_TIM_CH_FLT_FE_FLT_FE_MSK)
        {
            fallingfilterTime = (sint32)IFX_GTM_CLS_TIM_CH_FLT_FE_FLT_FE_MSK;
            result            = FALSE;
        }
        else
        {}

        channel->FLT_FE.B.FLT_FE = (uint32)fallingfilterTime;

        risingfilterTime         = ((sint32)(float32)(config->filter.risingEdgeFilterTime * clockFrequency) - 1);

        if (risingfilterTime < 0)
        {
            risingfilterTime = 0;
        }
        else if (risingfilterTime > (sint32)IFX_GTM_CLS_TIM_CH_FLT_FE_FLT_FE_MSK)
        {
            risingfilterTime = (sint32)IFX_GTM_CLS_TIM_CH_FLT_FE_FLT_FE_MSK;
            result           = FALSE;
        }
        else
        {}

        channel->FLT_RE.B.FLT_RE = (uint32)risingfilterTime;

        if (config->filter.fallingEdgeMode != IfxGtm_Tim_In_ConfigFilterMode_none)
        {
            if (config->filter.fallingEdgeMode == IfxGtm_Tim_In_ConfigFilterMode_immediateEdgePropagation)
            {
                channel->CTRL.B.FLT_MODE_FE = (uint8)IfxGtm_Tim_FilterMode_immediateEdgePropagation;
            }
            else
            {
                channel->CTRL.B.FLT_MODE_FE = (uint8)IfxGtm_Tim_FilterMode_individualDeglitchTime;
                channel->CTRL.B.FLT_CTR_FE  = (config->filter.fallingEdgeMode == IfxGtm_Tim_In_ConfigFilterMode_individualDeglitchTimeUpDown) ? (uint8)IfxGtm_Tim_FilterCounter_upDown : (uint8)IfxGtm_Tim_FilterCounter_hold;
            }
        }
        else
        {
            channel->CTRL.B.FLT_MODE_FE = (uint8)IfxGtm_Tim_FilterMode_immediateEdgePropagation;
            channel->FLT_FE.B.FLT_FE    = 0;
        }

        if (config->filter.risingEdgeMode != IfxGtm_Tim_In_ConfigFilterMode_none)
        {
            if (config->filter.risingEdgeMode == IfxGtm_Tim_In_ConfigFilterMode_immediateEdgePropagation)
            {
                channel->CTRL.B.FLT_MODE_RE = (uint8)IfxGtm_Tim_FilterMode_immediateEdgePropagation;
            }
            else
            {
                channel->CTRL.B.FLT_MODE_RE = (uint8)IfxGtm_Tim_FilterMode_individualDeglitchTime;
                channel->CTRL.B.FLT_CTR_RE  = (config->filter.risingEdgeMode == IfxGtm_Tim_In_ConfigFilterMode_individualDeglitchTimeUpDown) ? (uint8)IfxGtm_Tim_FilterCounter_upDown : (uint8)IfxGtm_Tim_FilterCounter_hold;
            }
        }
        else
        {
            channel->CTRL.B.FLT_MODE_RE = (uint8)IfxGtm_Tim_FilterMode_immediateEdgePropagation;
            channel->FLT_RE.B.FLT_RE    = 0;
        }

        IfxGtm_Tim_Ch_setFilterNotification(channel, config->filter.irqOnGlitch);
    }

    /* Enables TIM channel */
    channel->CTRL.B.TIM_EN = 1;

    return result;
}


void IfxGtm_Tim_In_initConfig(IfxGtm_Tim_In_Config *config, Ifx_GTM *gtm)
{
    memset(config, 0, sizeof(IfxGtm_Tim_In_Config));

    /* Initializes GTM instance used */
    config->gtm                          = gtm;
    /* Default TIM instance used */
    config->cluster                      = IfxGtm_Tim_0;
    config->channelIndex                 = IfxGtm_Tim_Ch_0;
    /* Default interrupt configuration */
    config->irqMode                      = IfxGtm_IrqMode_pulseNotify;
    config->isrProvider                  = IfxSrc_Tos_cpu0;
    config->isrPriority                  = 0;
    /* Default capture configuration */
    config->capture.irqOnNewVal          = FALSE;
    config->capture.irqOnCntOverflow     = FALSE;
    config->capture.irqOnEcntOverflow    = FALSE;
    config->capture.irqOnDatalost        = FALSE;
    config->capture.clock                = IfxGtm_Cmu_Clk_0;
    config->capture.mode                 = Ifx_Pwm_Mode_leftAligned;
    config->capture.activeEdge           = IfxGtm_Tim_In_ActiveEdge_both;
    config->capture.gateCount            = 0;
    /* Default timeout configuration */
    config->timeout.irqOnTimeout         = FALSE;
    config->timeout.clock                = IfxGtm_Cmu_Clk_0;
    config->timeout.timeout              = (float32)0.0f;
    config->input2ForLUT                 = IfxGtm_Tim_In_SrcForLutIn2_external_capture;
    config->inputSrc                     = IfxGtm_Tim_In_Src_channelCurrentOrPrevious;
    config->srcForTimIn                  = IfxGtm_Tim_In_SrcForTimIn_currentChannel;
    config->useLUT                       = FALSE;
    config->inputPin                     = NULL_PTR;
    config->inputPinMode                 = IfxPort_InputMode_noPullDevice;
    config->lookupTableValue             = 0;
    /* Default filter configuration */
    config->filter.risingEdgeMode        = IfxGtm_Tim_In_ConfigFilterMode_none;
    config->filter.fallingEdgeMode       = IfxGtm_Tim_In_ConfigFilterMode_none;
    config->filter.risingEdgeFilterTime  = 0.0f;
    config->filter.fallingEdgeFilterTime = 0.0f;
    config->filter.clock                 = IfxGtm_Cmu_Tim_Filter_Clk_0;
    /* Default TIM channel Mode used */
    config->mode                         = IfxGtm_Tim_Mode_pwmMeasurement;
}


void IfxGtm_Tim_In_onIsr(IfxGtm_Tim_In *driver)
{
	/* Updates the period and duty cycle */
    IfxGtm_Tim_In_update(driver);
}


void IfxGtm_Tim_In_update(IfxGtm_Tim_In *driver)
{
	/* Checks if data was lost and clear the event flag */
    driver->dataLost = IfxGtm_Tim_Ch_isDataLostEvent(driver->channel);

    if (driver->dataLost == TRUE)
    {
        IfxGtm_Tim_Ch_clearDataLostEvent(driver->channel);
    }

    /* Check for glitch events and clear the event flag */
    driver->glitch = IfxGtm_Tim_Ch_isGlitchEvent(driver->channel);

    if (driver->glitch == TRUE)
    {
    	/* Clears the glitch event */
        IfxGtm_Tim_Ch_clearGlitchEvent(driver->channel);
    }

    /* Checks for new data availability and process it */
    driver->newData = IfxGtm_Tim_Ch_isNewValueEvent(driver->channel);

    if (driver->newData == TRUE)
    {
        Ifx_GTM_CLS_TIM_CH_GPR0 gpr0;
        Ifx_GTM_CLS_TIM_CH_GPR1 gpr1;

        gpr0.U                  = driver->channel->GPR0.U;
        gpr1.U                  = driver->channel->GPR1.U;
        driver->periodTick      = gpr1.B.GPR1;
        driver->pulseLengthTick = gpr0.B.GPR0;
		
        /* Checks data coherence by comparing edge counters */
        driver->dataCoherent    = (gpr0.B.ECNT == gpr1.B.ECNT) ? TRUE : FALSE;

        /* Reads the edge counter */
        driver->edgeCount = driver->channel->ECNT.B.ECNT;

        /* Checks for event counter overflow and clear the event flag */
        if (IfxGtm_Tim_Ch_isEcntOverflowEvent(driver->channel) == TRUE)
        {
            driver->edgeCounterUpper++;
            IfxGtm_Tim_Ch_clearEcntOverflowEvent(driver->channel);
        }

        driver->overflowCnt = IfxGtm_Tim_Ch_isCntOverflowEvent(driver->channel);

        /* Checks for counter overflow and clear the event flag */
        if (driver->overflowCnt == TRUE)
        {
            driver->newData = FALSE;
            IfxGtm_Tim_Ch_clearCntOverflowEvent(driver->channel);
        }

        /* Clears the new value event flag */
        IfxGtm_Tim_Ch_clearNewValueEvent(driver->channel);
    }
}

