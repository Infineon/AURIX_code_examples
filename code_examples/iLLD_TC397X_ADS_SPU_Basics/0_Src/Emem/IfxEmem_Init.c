#include <0_Src/Emem/IfxEmem_Init.h>

void Emem_init(void)
{
    Radar* rdr = &g_rdr;
    rdr->emem = (SPU_Emem_Map_t*)IFXEMEM_START_ADDR_CPU;

    /* Activate EMEM clock */
    if (IfxEmem_isModuleEnabled() != TRUE)
    {
        IfxEmem_enableModule(&MODULE_EMEM);
    }
    IfxStm_waitTicks(&MODULE_STM0, 10);

    /* Unlock EMEM */
    if (IfxEmem_LockedState_locked == IfxEmem_getLockedState())
    {
        /* apply unlock sequence */
        IfxEmem_setUnlockStandbyLockFlag(0x1);
        IfxEmem_setUnlockStandbyLockFlag(0x3);
        IfxEmem_setUnlockStandbyLockFlag(0x7);
        /* wait one cycle for unlock */
        IfxStm_waitTicks(&MODULE_STM0, 10);
    }

    /* Set memory tiles 0-15 (256kB each, -> 4MB) to application mode  (==common memory mode) */
    for(uint32 tile_number=0; tile_number<16; tile_number++)
    {
        IfxEmem_setTileConfigMode(IfxEmem_TileConfigMode_unusedMode, tile_number);
        IfxEmem_setTileConfigMode(IfxEmem_TileConfigMode_commonMemoryMode, tile_number);
    }

    /* Clear EMEM using MTU */
    Mtu_EmemInit();
}
