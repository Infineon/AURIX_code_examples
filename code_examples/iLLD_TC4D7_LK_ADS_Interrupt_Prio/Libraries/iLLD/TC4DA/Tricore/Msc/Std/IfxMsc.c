void IfxMsc_clearDataFrameInterruptFlag(Ifx_MSC *msc)
{
    /* Data Frame Interrupt Clear */
    msc->ISC.B.CDEDI = 1;
}


void IfxMsc_clearReset(Ifx_MSC *msc)
{
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_config);
#endif

    if (msc->RST.STAT.B.KRST == 1)
    {
        msc->RST.CTRLB.B.STATCLR = 1; /* Clear Kernel reset status bit */
    }

#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_run);
#endif
}


void IfxMsc_disableModule(Ifx_MSC *msc)
{
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_config);
#endif
    /* Disable module  */
    msc->CLC.B.DISR = 1;
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_run);
#endif
}


uint32 IfxMsc_downstreamAbraBaudCalculator(uint32 baud)
{
    uint32 fsys = IfxClock_getSpbFrequency();    /* Baudrate depends only on fSPB for ABRA block*/
    uint32 ndd  = 0;

    /* DSTE.NDD = fsys / 2*BR */

    ndd = fsys / (2 * baud);

    return ndd - 1;
}


uint64 IfxMsc_downstreamFractionalBaudCalculator(Ifx_MSC *msc, uint32 baud)
{
    uint64 step = 0;
    uint32 fsys = 0;

    if (msc->FDR.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    step = (uint64)((uint64)(2 * baud * 1024)) / fsys;

    return step;
}


uint32 IfxMsc_downstreamNormalBaudCalculator(Ifx_MSC *msc, uint32 baud)
{
    uint32 step = 0;
    uint32 fsys = 0;

    if (msc->FDR.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    /* FDR.STEP = 1024 - fsys / (2*BR) */

    step = 1024 - (fsys / (2 * baud));

    return step;
}


void IfxMsc_enableModule(Ifx_MSC *msc)
{
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_config);
#endif
    /* Disable module disable bit */
    msc->CLC.U = 0;
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_run);
#endif
}


boolean IfxMsc_getActiveDataFrameStatus(Ifx_MSC *msc)
{
    return msc->DSS.B.DFA;
}


Ifx_MSC *IfxMsc_getAddress(IfxMsc_Index msc)
{
    Ifx_MSC *module;

    if (msc < IFXMSC_NUM_MODULES)
    {
        module = (Ifx_MSC *)IfxMsc_cfg_indexMap[msc].module;
    }
    else
    {
        module = NULL_PTR;
    }

    return module;
}


boolean IfxMsc_getDataFrameInterruptFlag(Ifx_MSC *msc)
{
    boolean flag = 0;

    flag = msc->ISR.B.DEDI;

    return flag;
}


IfxMsc_Index IfxMsc_getIndex(Ifx_MSC *msc)
{
    uint32       index;
    IfxMsc_Index result;

    result = IfxMsc_Index_none;

    for (index = 0; index < IFXMSC_NUM_MODULES; index++)
    {
        if (IfxMsc_cfg_indexMap[index].module == msc)
        {
            result = (IfxMsc_Index)IfxMsc_cfg_indexMap[index].index;
            break;
        }
    }

    return result;
}


void IfxMsc_manualLoadCxModeCommand(Ifx_MSC *msc, IfxMsc_CxModeCommand *command64)
{
    msc->DCE.U = command64->dce;
    msc->DC.U  = command64->dc;
}


void IfxMsc_resetModule(Ifx_MSC *msc)
{
#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_config);
#endif

    /* Reset kernel */
    msc->RST.CTRLA.B.KRST = 1;
    msc->RST.CTRLB.B.KRST = 1;          /* Only if both Kernel reset bits are set a reset is executed */

    while (msc->RST.STAT.B.KRST == 0)   /* Wait until reset is executed */
    {}

    msc->RST.CTRLB.B.STATCLR = 1;       /* Clear Kernel reset status bit */

    while (msc->RST.STAT.B.KRST == 1)   /* Wait until KRST is cleared, only after this reset sequence is completed */
    {}

#if (IFX_PROT_ENABLED == 1U)
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTE, IfxApProt_State_run);
#endif
}


uint64 IfxMsc_upstreamFractionalBaudCalculator(Ifx_MSC *msc, uint32 baud)
{
    uint32 fsys = 0;
    uint64 step = 0;
    uint32 df   = 1;

    if (msc->FDR.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    /* FDR.STEP = DF*BR*1024 / fsys */

    if (msc->USR.B.URR != 0)
    {
        df = 1 << (msc->USR.B.URR + 1);
    }
    else
    {
        df = 0;
    }

    step = (uint64)((uint64)((df * baud) * 1024)) / fsys;

    return step;
}


uint32 IfxMsc_upstreamNormalBaudCalculator(Ifx_MSC *msc, uint32 baud)
{
    uint32 fsys = 0;
    uint32 step = 0;
    uint32 df   = 1;

    if (msc->FDR.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    /* FDR.STEP = 1024 - fsys / DF*BR */

    if (msc->USR.B.URR != 0)
    {
        df = 1 << (msc->USR.B.URR + 1);
    }
    else
    {
        df = 0;
    }

    step = 1024 - (fsys / (df * baud));

    return step;
}


float32 IfxMsc_getDownstreamAbraBaudrate(Ifx_MSC *msc)
{
    uint32  f_MSC    = IfxClock_getMscFrequency();

    uint8   nda      = msc->ABC.B.NDA;
    uint8   low      = msc->ABC.B.LOW;
    uint8   high     = msc->ABC.B.HIGH;

    float32 baudrate = (float32)(f_MSC / ((nda + 1) * (low + high + 2)));

    return baudrate;
}


float32 IfxMsc_getDownstreamAbraDisabledBaudrate(Ifx_MSC *msc)
{
    float32     baudrate = 0;
    uint32      fsys     = 0;

    Ifx_MSC_FDR fdr;
    fdr.U = msc->FDR.U;

    uint16      step = (uint16)(fdr.B.STEP);

    if (fdr.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    /* normal divider mode( DM = 1) */
    if (fdr.B.DM == 1)
    {
        baudrate = (float32)(fsys / 2) * (1 / (1024 - step));
    }
    else
    {
        /*fractional divider mode*/
        baudrate = (float32)(fsys / 2) * (step / 1024);
    }

    return baudrate;
}


float32 IfxMsc_getDownstreamBaudrate(Ifx_MSC *msc)
{
    float32 downstreamBaudrate = 0;

    if (msc->ABC.B.ABB == 1)
    {
        /* ABRA enabled*/
        downstreamBaudrate = IfxMsc_getDownstreamAbraBaudrate(msc);
    }
    else
    {
        /* ABRA disabled*/
        downstreamBaudrate = IfxMsc_getDownstreamAbraDisabledBaudrate(msc);
    }

    return downstreamBaudrate;
}


void IfxMsc_setManchesterCoding(Ifx_MSC *msc, IfxMsc_ManchesterConfig *manConfig)
{
    msc->SCC.B.MCT = manConfig->enable;
}


void IfxMsc_setSlowChannel(Ifx_MSC *msc, const IfxMsc_SlowChannelConfig *slowChannelConfig)
{
    msc->SCC.B.SC = 1; /*Enable Slow Channel*/

    /*Configure Manchester Code*/
    msc->SCC.B.MCT = slowChannelConfig->manchesterConfig.enable;

    /*Set Command Frame length for both slaves*/
    msc->SCC.B.CFLX = slowChannelConfig->totalCommandLengthX;
    msc->SCC.B.CFLY = slowChannelConfig->totalCommandLengthY;

    /*Set slow channel length for both slaves*/
    msc->SCC.B.SCLX = slowChannelConfig->slowChannelFrameLengthX;
    msc->SCC.B.SCLY = slowChannelConfig->slowChannelFrameLengthY;

    /*Configure parity for both slaves*/
    msc->SCC.B.PCX = slowChannelConfig->parityX;
    msc->SCC.B.PCY = slowChannelConfig->parityY;

    /*Configure stop bits*/
    msc->SCC.B.CFSTOPX = slowChannelConfig->stopBitX;
    msc->SCC.B.CFSTOPY = slowChannelConfig->stopBitY;
}


void IfxMsc_setEnableXDeassertion(Ifx_MSC *msc, IfxMsc_Target enX, IfxMsc_EnableClockAdjustment adjustment)
{
    /*Set falling edge adjustment for corresponding enable output: en0, en1, en2 or en3*/
    switch (enX)
    {
    case IfxMsc_Target_en0:
        msc->OECR.B.E0F = adjustment;
        break;
    case IfxMsc_Target_en1:
        msc->OECR.B.E1F = adjustment;
        break;
    case IfxMsc_Target_en2:
        msc->OECR.B.E2F = adjustment;
        break;
    case IfxMsc_Target_en3:
        msc->OECR.B.E3F = adjustment;
        break;
    }
}


void IfxMsc_setEnableXAssertion(Ifx_MSC *msc, IfxMsc_Target enX, IfxMsc_EnableClockAdjustment adjustment)
{
    /*Set rising edge adjustment for corresponding enable output: en0, en1, en2 or en3*/
    switch (enX)
    {
    case IfxMsc_Target_en0:
        msc->OECR.B.E0R = adjustment;
        break;
    case IfxMsc_Target_en1:
        msc->OECR.B.E1R = adjustment;
        break;
    case IfxMsc_Target_en2:
        msc->OECR.B.E2R = adjustment;
        break;
    case IfxMsc_Target_en3:
        msc->OECR.B.E3R = adjustment;
        break;
    }
}


void IfxMsc_initApConfig(IfxMsc_ApConfig *config)
{
    IfxApProt_initConfig(&config->proteConfig);
    IfxApProt_initConfig(&config->protseConfig);
    IfxApApu_initConfig(&config->apuConfig);
}


void IfxMsc_initAp(Ifx_MSC *msc, IfxMsc_ApConfig *config)
{
    /* Initialize the PROTs */
    IfxApProt_init((Ifx_PROT_PROT *)&msc->PROTE, &config->proteConfig);
    IfxApProt_init((Ifx_PROT_PROT *)&msc->PROTSE, &config->protseConfig);

    /* Change the state to CONFIG, Configure APU and set PROT state back to RUN */
    /* Initialize the APU */
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTSE, IfxApProt_State_config);
    IfxApApu_init((Ifx_ACCEN_ACCEN *)&msc->ACCEN, &config->apuConfig);
    IfxApProt_setState((Ifx_PROT_PROT *)&msc->PROTSE, IfxApProt_State_run);
}


void IfxMsc_configureAccessToMscs(IfxApApu_ApuConfig *apConfig)
{
    IfxApApu_init((Ifx_ACCEN_ACCEN *)&MODULE_MSC0.ACCEN, apConfig);
}


float32 IfxMsc_getUpstreamBaudrate(Ifx_MSC *msc)
{
    float32 baudrate = 0;
    uint32  df       = 1;
    uint32  fsys     = 0;
    uint16  step     = (uint16)(msc->FDR.B.STEP);

    if (msc->FDR.B.ACS == IfxMsc_Clock_fSPB)
    {
        fsys = IfxClock_getSpbFrequency();
    }
    else
    {
        fsys = IfxClock_getSriFrequency();
    }

    if (msc->USR.B.URR != 0)
    {
        /*Divide Factor*/
        df = 1 << (msc->USR.B.URR + 1);

        /* normal divider mode( DM = 1) */
        if (msc->FDR.B.DM == 1)
        {
            baudrate = (float32)(fsys / df) * (1 / (1024 - step));
        }
        else
        {
            /*fractional divider mode*/
            baudrate = (float32)(fsys / df) * (step / 1024);
        }
    }
    /* Reception disabled */
    else
    {
        baudrate = 0.0f;
    }

    return baudrate;
}
