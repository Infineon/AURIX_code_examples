/**
 * Example of use:
 *
 *  Ifx_Oe_Log_setInfoLevel(Ifx_Oe_Log_InfoLevel_2);
 *  Ifx_Oe_Log_setVerboseLevel(Ifx_Oe_Log_Verbose_verbose);
 *
 *  Ifx_Oe_Log_fatal("My fatal");
 *  Ifx_Oe_Log_error("My error");
 *  Ifx_Oe_Log_warning("My warning");
 *  Ifx_Oe_Log_information(Ifx_Oe_Log_InfoLevel_0, "My information 0");
 *  Ifx_Oe_Log_information(Ifx_Oe_Log_InfoLevel_1, "My information 1");
 *  Ifx_Oe_Log_information(Ifx_Oe_Log_InfoLevel_2, "My information 2");
 *  Ifx_Oe_Log_debug(0x456, "My debug");
 *  Ifx_Oe_Log_verbose(0x456, "My verbose");
 *
 */

#ifndef IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT
/** Maximum number of outputs */
#define IFX_CFG_OE_LOG_MAX_OUTPUT_COUNT (2)
#endif

typedef enum
{
    /**
     * Nothing is logged
     */
    Ifx_Oe_Log_Verbose_none = 0,

    /**
     * The most critical level, Fatal events demand immediate attention.
     *
     * For example, an application failing during startup will log a Fatal event.
     *
     * If you have a pager, it goes off when one of these occurs.
     */
    Ifx_Oe_Log_Verbose_fatal = 1,

    /**
     * When functionality is unavailable or expectations broken, an Error event is used.
     *
     * For example, receiving an exception when trying to commit a database transaction is an event at this level.
     */
    Ifx_Oe_Log_Verbose_error = 2,

    /**
     * When service is degraded, endangered, or may be behaving outside of its expected parameters, Warning level events are used.
     *
     * A warning event should only be emitted when the condition is either transient or can be investigated and fixed -
     * use restraint to avoid polluting the log with spurious warnings.
     *
     * For example, slow response times from a critical database would be logged as warnings.
     */
    Ifx_Oe_Log_Verbose_warning = 3,

    /**
     * Information events describe things happening in the system that correspond to its responsibilities and functions.
     * Generally these are the observable actions the system can perform.
     *
     * For example, processing a payment or updating a user's details will be logged at this level.
     */
    Ifx_Oe_Log_Verbose_information = 4,

    /**
     * Debug is used for internal system events that are not necessarily observable from the outside,
     * but useful when determining how something happened.
     *
     * For example, the details of requests and responses made to and from external integration points would often be logged at this level.
     */
    Ifx_Oe_Log_Verbose_debug = 5,

    /**
     * Verbose is the noisiest level, generally used only as a last resort when debugging a difficult problem,
     * and rarely (if ever) enabled for a production app.
     *
     * For example, local variables within an algorithm implementation might be logged at this level.
     */
    Ifx_Oe_Log_Verbose_verbose = 6,
}Ifx_Oe_Log_Verbose;

/* Filter level for info messages
 *
 */
typedef enum
{
    Ifx_Oe_Log_InfoLevel_0,     /**< \brief Minimal information. Ex: Action processed */
    Ifx_Oe_Log_InfoLevel_1,     /**< \brief Detailed information. Ex: object added */
    Ifx_Oe_Log_InfoLevel_2,     /**< \brief Very detailed information. Ex: object value */
}Ifx_Oe_Log_InfoLevel;

