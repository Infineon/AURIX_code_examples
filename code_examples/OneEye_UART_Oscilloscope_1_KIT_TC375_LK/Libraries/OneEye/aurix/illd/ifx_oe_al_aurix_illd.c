#include "ifx_oe_al_aurix_illd.h"
#if IFX_CFG_OE_AL_UC == IFX_CFG_OE_AL_UC_AURIX_ILLD
#include  "ifx_oe_time.h"
#include  "Stm/Std/IfxStm.h"
#include "Scu/Std/IfxScuRcu.h"

sint32 Ifx_Oe_getTimerFrequency(void)
{
#if IFX_CFG_OE_AL_UC_VARIANT == IFX_CFG_OE_AL_UC_VARIANT_AURIX_ILLD_TC4
    return IfxStm_getFrequency();
#else
    return IfxStm_getFrequency(IFX_CFG_OE_DEFAULT_TIMER);
#endif
}


Ifx_Oe_TickTime Ifx_Oe_Time_now(void)
{
    Ifx_Oe_TickTime stmNow;
    boolean         interruptState;

    interruptState = Ifx_Oe_disableInterrupts();
    stmNow         = (Ifx_Oe_TickTime)IfxStm_get(IFX_CFG_OE_DEFAULT_TIMER) & Ifx_Oe_Time_infinite;
    Ifx_Oe_restoreInterrupts(interruptState);

    return stmNow;
}


void Ifx_Oe_softwareReset(void)
{
    IfxScuRcu_performReset(IfxScuRcu_ResetType_system, 0);
}


#endif
