void Ifx_Oe_StdIf_DPipe_print(Ifx_Oe_StdIf_DPipe* stdIf, pchar format, ...)
{
    if (!stdIf->txDisabled)
    {
        char         message[IFX_CFG_OE_STDIF_DPIPE_MAX_PRINT_SIZE + 1];
        Ifx_Oe_SizeT count;
        va_list      args;
        va_start(args, format);
        vsprintf((char*)message, format, args);
        va_end(args);
        count = (Ifx_Oe_SizeT)strlen(message);
        IFX_OE_ASSERT(count < IFX_CFG_OE_STDIF_DPIPE_MAX_PRINT_SIZE);
        //return
        Ifx_Oe_StdIf_DPipe_write(stdIf, (void*)message, &count, Ifx_Oe_Time_infinite);
    }
    else
    {
        //return TRUE;
    }
}


void Ifx_Oe_StdIf_DPipe_printVA(Ifx_Oe_StdIf_DPipe* stdIf, Ifx_Oe_TickTime timeout, pchar format, va_list args)
{
    if (!stdIf->txDisabled)
    {
        char         message[IFX_CFG_OE_STDIF_DPIPE_MAX_PRINT_SIZE + 1];
        Ifx_Oe_SizeT count;
        vsprintf((char*)message, format, args);
        count = (Ifx_Oe_SizeT)strlen(message);
        IFX_OE_ASSERT(count < IFX_CFG_OE_STDIF_DPIPE_MAX_PRINT_SIZE);
        Ifx_Oe_StdIf_DPipe_write(stdIf, (void*)message, &count, timeout);
    }
}


void Ifx_Oe_StdIf_DPipe_writeText(Ifx_Oe_StdIf_DPipe* stdIf, Ifx_Oe_TickTime timeout, pchar text)
{
    if (!stdIf->txDisabled)
    {
        Ifx_Oe_SizeT count;
        count = (Ifx_Oe_SizeT)strlen(text);
        Ifx_Oe_StdIf_DPipe_write(stdIf, (void*)text, &count, timeout);
    }
}


