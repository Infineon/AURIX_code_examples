#ifndef IFX_OE_AL_CY_PDL_SBCDMADPIPE_H
#define IFX_OE_AL_CY_PDL_SBCDMADPIPE_H
/*********************************************************************************************************************/
/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "ifx_oe_al.h"

#if defined(IFX_CFG_OE_AL_UTILS) && ((IFX_CFG_OE_AL_UTILS_VARIANT == IFX_CFG_OE_AL_UTILS_VARIANT_DEFAULT) || (IFX_CFG_OE_AL_UTILS_VARIANT == IFX_CFG_OE_AL_UTILS_VARIANT_CY_SCB_DMA)) && (IFX_CFG_OE_AL_UC == IFX_CFG_OE_AL_UC_CY_PDL)

#include "ifx_oe_fifo.h"
#include "ifx_oe_stdif_dpipe.h"

#include "cybsp.h"

#define IFX_OE_UART_TX_BUFFER_SIZE (1u)      /* For the moment limit to 1 byte in buffer */
#define IFX_OE_UART_RX_BUFFER_SIZE (1u)

typedef struct
{
    CySCB_Type* handle;
    cy_stc_dma_descriptor_t* txDma_Descriptor;
    DW_Type* txDma_HW;
    uint32_t txDma_CHANNEL;
    DW_Type* rxDma_HW;
    uint32_t rxDma_CHANNEL;

    Ifx_Oe_Fifo* tx;                           /**< \brief Transmit FIFO buffer */
    Ifx_Oe_Fifo* rx;                           /**< \brief Receive FIFO buffer */
    volatile boolean txInProgress;              /**< \brief Ongoing transfer. Will be set by IfxAsclin_Asc_initiateTransmission, and cleared by IfxAsclin_Asc_isrTransmit */
    boolean rxOverflow;
    Ifx_Oe_StdIf_DPipe pipe;                        /* UART standard pipe interface                 */
    uint8_t dataTxBuffer[IFX_OE_UART_TX_BUFFER_SIZE];
    uint8_t dataRxBuffer[IFX_OE_UART_RX_BUFFER_SIZE];
}Ifx_Oe_UartDPipe;

boolean Ifx_Oe_UartDPipe_initDPipe(Ifx_Oe_StdIf_DPipe* stdif, Ifx_Oe_UartDPipe* uart);

#endif /* #if defined(IFX_CFG_OE_AL_UTILS) && ((IFX_CFG_OE_AL_UTILS_VARIANT == IFX_CFG_OE_AL_UTILS_VARIANT_DEFAULT) || (IFX_CFG_OE_AL_UTILS_VARIANT == IFX_CFG_OE_AL_UTILS_VARIANT_CY_SCB_DMA)) && (IFX_CFG_OE_AL_UC == IFX_CFG_OE_AL_UC_CY_PDL) */

#endif /* IFX_OE_AL_CY_PDL_SBCDMADPIPE_H */
