/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "IfxScu_reg.h"
#include "IfxPort.h"
#include "IfxScuWdt.h"
#include "Bsp.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define LED             &MODULE_P00, 5                              /* Port 00 pin 5 for LED1                       */
#define EMS_INPUT       &MODULE_P33, 8                              /* Port 33 pin 8 for SCU Emergency Stop input   */
#define POLARITY        0x1                                         /* 0x1: Input is active low                     */
#define SYNC_MODE       0x0                                         /* Synchronous mode is selected                 */
#define PORT_A          0x0                                         /* Port A is used for emergency stop input      */
#define EN_EMSF         0x1                                         /* Enable emergency stop flag                   */
#define WAIT_TIME       500                                         /* Number of milliseconds to wait               */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
Ifx_TickTime g_ticksFor500ms;   /* Variable to store the number of ticks to wait for 1 second delay                 */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* This function initializes the SCU emergency stop */
void initScuEmergency(void)
{
    /* Initialize the time variable */
    g_ticksFor500ms = IfxStm_getTicksFromMilliseconds(BSP_DEFAULT_TIMER, WAIT_TIME);

    /* Clear Safety ENDINIT protection to allow register modification */
    IfxScuWdt_clearSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());

    SCU_EMSR.B.POL  = POLARITY;                                     /* Selection of input polarity                  */
    SCU_EMSR.B.MODE = SYNC_MODE;                                    /* Synchronous mode is selected                 */
    SCU_EMSR.B.PSEL = PORT_A;                                       /* Port A is used for emergency stop input      */
    SCU_EMSR.B.ENON = EN_EMSF;                                      /* Enable emergency stop flag                   */

    /* Set Safety ENDINIT protection to block register modification */
    IfxScuWdt_setSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());

    IfxPort_setPinMode(EMS_INPUT, IfxPort_Mode_inputPullDown);      /* Set pin mode for the emergency input         */

    IfxPort_setPinMode(LED, IfxPort_Mode_outputPushPullGeneral);    /* Set pin mode for the LED                     */
    IfxPort_setESR(LED);                                            /* Enable emergency stop for the LED            */
}

/* This function toggles the port pin of the LED every 500ms */
void toggleLED(void)
{
    IfxPort_togglePin(LED);                                         /* Toggle LED port pin                          */
    waitTime(g_ticksFor500ms);                                      /* Wait 500ms                                   */
}
