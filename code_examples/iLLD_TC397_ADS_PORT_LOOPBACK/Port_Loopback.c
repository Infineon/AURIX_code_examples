/*
 * This function initializes the port pin which drives the LED
 */
void initLEDs(void)
{
    /* Initialization of the LED used in this example */
    IfxPort_setPinModeOutput(LED_D107_CHECH_PASS, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    IfxPort_setPinModeOutput(LED_D108_CHECK_FAIL, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Switch OFF the LED (low-level active) */
    IfxPort_setPinHigh(LED_D107_CHECH_PASS);
    IfxPort_setPinHigh(LED_D108_CHECK_FAIL);
}

/*
 * Function to configure the port pins for port loopback Safety Mechanism (SM)
 */
void initPortLoopback(void)
{
    /* Set pin of PORT_LOOPBACK_MISSION as output to generate mission signal and the output is low */
    IfxPort_setPinModeOutput(PORT_LOOPBACK_MISSION.port, PORT_LOOPBACK_MISSION.pinIndex, IfxPort_OutputMode_pushPull,
            IfxPort_OutputIdx_general);

    /* Set the pin as output high */
    IfxPort_setPinHigh(PORT_LOOPBACK_MISSION.port, PORT_LOOPBACK_MISSION.pinIndex);

    /* Set PORT_LOOPBACK_INTPUT_PIN as input to receive mission signal as loopback (monitor) with pullDown state */
    IfxPort_setPinModeInput(PORT_LOOPBACK_MONITOR.port, PORT_LOOPBACK_MONITOR.pinIndex, IfxPort_InputMode_pullDown);
}

/*
 * This function compare the mission and monitor signals
 * Safety Mechanism:PORT:LOOPBACK
 */
void validatePortLoopback(void)
{
    boolean missionSignalOut, monitorSignalIn;

    /* Get mission signal pin state */
    missionSignalOut = IfxPort_getPinState(PORT_LOOPBACK_MISSION.port, PORT_LOOPBACK_MISSION.pinIndex);

    /* Get monitor signal pin state */
    monitorSignalIn  = IfxPort_getPinState(PORT_LOOPBACK_MONITOR.port, PORT_LOOPBACK_MONITOR.pinIndex);

    /* Compare loop back mission and monitor signals */
    if( missionSignalOut == monitorSignalIn)
    {
        /* Check passed */
        IfxPort_setPinLow(LED_D107_CHECH_PASS);
        IfxPort_setPinHigh(LED_D108_CHECK_FAIL);
    }
    else
    {
        /* Check failed i.e. mismatch */
        IfxPort_setPinHigh(LED_D107_CHECH_PASS);
        IfxPort_setPinLow(LED_D108_CHECK_FAIL);
    }
}
