#define PORT_LOOPBACK_MISSION   IfxPort_P00_4        /* Common Cause Failure (CCF) is avoided to select port pin     */
#define PORT_LOOPBACK_MONITOR   IfxPort_P33_5        /* Common Cause Failure (CCF) is avoided to select port pin     */

/* LEDs on Application Kit */
#define LED_D107_CHECH_PASS     &MODULE_P13, 0       /* LED D107: Port, Pin definition                               */
#define LED_D108_CHECK_FAIL     &MODULE_P13, 1       /* LED D108: Port, Pin definition                               */

