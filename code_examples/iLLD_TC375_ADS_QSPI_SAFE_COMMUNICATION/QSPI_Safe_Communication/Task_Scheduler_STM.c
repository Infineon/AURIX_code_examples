/*
 * This interrupt is raised by the STM0. The initialization is done by initSTM().
 * isrProvider CPU0 and isrPriority ISR_PRIORITY_OS_TICK
 */
void userIsrStm0()
{
    static uint32 tickCount_1ms = 0;
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM0, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    tickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();
    /* sending frame on dedicated time */
    if ((tickCount_1ms % g_cyclTimeQSPI) == 0)
    {
        /* Execute the sequence of safety mechanism in the interrupt function
         * SM:QSPI:SAFE_COMMUNICATION */
        masterTransmitDataSafeCommQSPI();

        /* update the latest QSPI cycle time */
         updateQspiCycleTime();
    }
}

/*
 * This interrupt is raised by the STM0. The initialization is done by initSTM().
 * isrProvider CPU0 and isrPriority ISR_PRIORITY_OS_TICK
 */
void userIsrStm1()
{
    static uint32 tickCount_1ms = 0;
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM1, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    tickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* sending frame on dedicated time */
    if ((tickCount_1ms % 100) == 0)
    {
        /* periodic timeout QSPI cycle time check i.e. 100ms = EXPECTED_CYCLE_TIME  */
        timeoutSlaveQspiSafeCommunication();
    }
}

/*
 * This function initializes the STM for generating an interrupt every milliseconds used for basic periodically
 * tasks scheduling. */
void initSTM(App_Stm *appStm, IfxCpu_ResourceCpu cpuIndex)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Initialize configuration-structure with defaults. */
    IfxStm_initCompareConfig(&appStm->stmConfig);

    /* Change the comparator configuration */
    switch (cpuIndex)
    {
        case IfxCpu_ResourceCpu_0:
            appStm->stmSfr = &MODULE_STM0;
            appStm->stmConfig.typeOfService = IfxSrc_Tos_cpu0;
            appStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#if (IFXCPU_NUM_MODULES > 1)
        case IfxCpu_ResourceCpu_1:
            appStm->stmSfr = &MODULE_STM1;
            appStm->stmConfig.typeOfService = IfxSrc_Tos_cpu1;
            appStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif
#if (IFXCPU_NUM_MODULES > 2)
        case IfxCpu_ResourceCpu_2:
            appStm->stmSfr = &MODULE_STM2;
            appStm->stmConfig.typeOfService = IfxSrc_Tos_cpu2;
            appStm->stmConfig.triggerPriority = ISR_PRIORITY_OS_TICK;
            break;
#endif

        default:
            break;
    }

    appStm->stmConfig.comparatorInterrupt   = IfxStm_ComparatorInterrupt_ir0; /* Select the interrupt output */
    appStm->stmConfig.ticks                 = IFX_CFG_STM_TICKS_PER_MS * 10; /* Start after 10 ms */

    /* Suspend by debugger enabled */
    IfxStm_enableOcdsSuspend(appStm->stmSfr);

    /* Now Initialized the comparator */
    IfxStm_initCompare(appStm->stmSfr, &appStm->stmConfig);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}
