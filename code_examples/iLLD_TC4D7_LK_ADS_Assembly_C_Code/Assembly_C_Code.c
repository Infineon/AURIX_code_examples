/* Define LEDs port pins */
#define LED1         LED_1          /* LED: Port, Pin definition            */
#define LED2         LED_2          /* LED: Port, Pin definition            */

#define LED_ON      1U
#define LED_OFF     0U

#define DELAY_1S    1000                    /* Time to wait for delay in ms */

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
extern void set_LED1_State_Assembly(uint32 state);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Set state of LED2 using assembly code */
void set_LED2_State_Assembly(uint32 state)
{
    __asm__ volatile(
            "jne %%d4, 0, switch_on  \n\t"        /* Check the passed value, stored in D4*/
            "mov %%d0, 0x20          \n\t"        /* If 0 "LED_OFF", switch off LED1: by writing 0x20 */
            "sh %%d0, %%d1, 5         \n\t"       /* Shift of 5 bits*/
            "j common               \n\t"

            "switch_on:               \n\t"       /* Else 1 "LED_ON", switch on LED1 by writing 0x200000 */
            "mov %%d1, 0x20           \n\t"
            "sh %%d0, %%d1, 21         \n\t"      /* Shift of 21 bits*/

            "common:                  \n\t"
            "movh.a  %%a0, 0xF004      \n\t"     /* Load the address of P03_OMR 0xF003AC3Cu*/
            "lea %%a0, [%%a0] - 0x53C4  \n\t"
            "st.w [%%a0], %%d0          \n\t"    /* Store the D0 value to the target register */
            :
            :"d" (state)
        );
}

/* Toggle LED1, LED2 using assembly code functions */
void run_Assembly_Code(void)
{
    /* Configure LED1, LED2 port pins */
    IfxPort_setPinMode(LED1.port, LED1.pinIndex, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED2.port, LED2.pinIndex, IfxPort_Mode_outputPushPullGeneral);

    /* Turn off LEDs (LEDs are low-level active)   */
    IfxPort_setPinHigh(LED1.port, LED1.pinIndex);
    IfxPort_setPinHigh(LED2.port, LED2.pinIndex);

    /* Switch ON LED1 using assembly code from Assembly_Code.src file */
    set_LED1_State_Assembly(LED_ON);

    /* Add a one second delay */
    waitTime(IfxStm_getTicksFromMilliseconds(DELAY_1S));                       /* Wait 1000 milliseconds            */

    /* Switch ON LED2 using the above assembly code function */
    set_LED2_State_Assembly(LED_ON);

    /* Add a one second delay */
    waitTime(IfxStm_getTicksFromMilliseconds(DELAY_1S));                       /* Wait 1000 milliseconds            */

    /* Switch OFF LED1 using assembly code from Assembly_Code.src file */
    set_LED1_State_Assembly(LED_OFF);

    /* Add a one second delay */
    waitTime(IfxStm_getTicksFromMilliseconds(DELAY_1S));                       /* Wait 1000 milliseconds            */

    /* Switch OFF LED2 using the above assembly code function */
    set_LED2_State_Assembly(LED_OFF);
}
