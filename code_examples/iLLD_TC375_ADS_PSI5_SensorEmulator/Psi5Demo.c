/******************************************************************************/
/*----------------------------------Includes----------------------------------*/
/******************************************************************************/
#include <stdio.h>
#include "Psi5Demo.h"
#include "_Utilities/Ifx_Assert.h"

/******************************************************************************/
/*-----------------------------------Macros-----------------------------------*/
/******************************************************************************/
#define LED2                        &MODULE_P00, 6u  /* LED2 Port, Pin definition  */
#define ISR_PRIORITY_PSI5_RX        5u               /* Define the PSI5 interrupt priority */
#define NUM_TESTED_CHANNELS         1u               /* Number of Psi5 Channels */
#define NUM_SLOT                    6u               /* Number of Slots        */

/* this value is derived from the first 10 bits
 * of transfered data from cpu0_main.c*/
#define PSI5_RX_DATA                0x3FEu

/******************************************************************************/
/*-----------------------------Data Structures--------------------------------*/
/******************************************************************************/
typedef struct
{
        IfxPsi5_Psi5         psi5;                             /*Psi5 Module handle */
        IfxPsi5_Psi5_Channel psi5Channel[NUM_TESTED_CHANNELS]; /*Psi5 Channel handle */
} App_Psi5;
/******************************************************************************/
/*------------------------------Global variables------------------------------*/
/******************************************************************************/
App_Psi5 g_Psi5;                                          /* Psi5 global data */
/******************************************************************************/
/*-------------------------Function Implementations---------------------------*/
/******************************************************************************/

IFX_INTERRUPT(PSI5_Ch0_receiveIntHandler, 0, ISR_PRIORITY_PSI5_RX);

void PSI5_Ch0_receiveIntHandler(void)
{
    uint32 dataReceived =0;
    boolean retstats = FALSE;
    IfxPsi5_Psi5_Frame frame;

    retstats = IfxPsi5_Psi5_readChannelFrame(&g_Psi5.psi5Channel[0], &frame);

    if(retstats)
    {
        dataReceived = frame.frame.readData;

        if (PSI5_RX_DATA == dataReceived)
        {
            IfxPort_setPinLow(LED2);
        }
    }
}

/* Demo init API
 * This function is called from main during initialization phase
 */
void IfxPsi5Demo_init(void)
{
    volatile  Ifx_SRC_SRCR *src = &MODULE_SRC.PSI5.PSI5[0].SR[0];

    /* create module config  */
    IfxPsi5_Psi5_Config psi5Config;
    IfxPsi5_Psi5_initModuleConfig(&psi5Config, &MODULE_PSI5);

    /* initialize module*/
    IfxPsi5_Psi5_initModule(&g_Psi5.psi5, &psi5Config);

    /* create channel config */
    IfxPsi5_Psi5_ChannelConfig psi5ChannelConfig;

    IfxPsi5_Psi5_initChannelConfig(&psi5ChannelConfig, &g_Psi5.psi5);

    psi5ChannelConfig.pinsConfig            = NULL_PTR;

    psi5ChannelConfig.watchdogTimerLimit[0] = 0x32;     /* initial delay before slot 0 starts*/
    psi5ChannelConfig.watchdogTimerLimit[1] = 0x90;
    psi5ChannelConfig.watchdogTimerLimit[2] = 0x10;
    psi5ChannelConfig.watchdogTimerLimit[3] = 0x10;
    psi5ChannelConfig.watchdogTimerLimit[4] = 0x10;
    psi5ChannelConfig.watchdogTimerLimit[5] = 0x10;
    psi5ChannelConfig.watchdogTimerLimit[6] = 0x10;

    psi5ChannelConfig.channelTrigger.channelTriggerCounter = 0x00;
    psi5ChannelConfig.channelTrigger.channelTriggerValue   = 0x1F4;

    for (uint8 slot = 0; slot < NUM_SLOT; ++slot)
    {
        psi5ChannelConfig.receiveControl.payloadLength[slot] = 0xA;

        if (slot == 2)
        {
            psi5ChannelConfig.receiveControl.frameExpectation[slot] = IfxPsi5_FrameExpectation_expected;
        }
        else
        {
            psi5ChannelConfig.receiveControl.frameExpectation[slot] = IfxPsi5_FrameExpectation_notExpected;
        }
    }

    /* initialize channels */
    

    for (uint8 chn = 0; chn < NUM_TESTED_CHANNELS; ++chn)
    {
        psi5ChannelConfig.channelId = (IfxPsi5_ChannelId)chn;

        /* Configure pins */
        IfxPsi5_Psi5_PinsConfig tempPin;

        if (chn == 0)
        {
            tempPin.out                  = &IfxPsi5_TX0_P02_2_OUT;
            tempPin.in                   = &IfxPsi5_RX0B_P02_3_IN;
            tempPin.outMode              = IfxPort_OutputMode_pushPull;
            tempPin.inMode               = IfxPort_InputMode_pullDown;
            tempPin.pinDriver            = IfxPort_PadDriver_cmosAutomotiveSpeed3; /*pad driver mode*/
            psi5ChannelConfig.pinsConfig = &tempPin;
        }

        else
        {
            IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, 0);
        }

        IfxPsi5_Psi5_initChannel(&g_Psi5.psi5Channel[chn], &psi5ChannelConfig);
    }

    IfxSrc_init(src, IfxSrc_Tos_cpu0, ISR_PRIORITY_PSI5_RX);
    IfxSrc_enable(src);

    /* enable RIR interrupt*/
    IfxPsi5_enableInterrupt(g_Psi5.psi5.psi5, g_Psi5.psi5Channel->channelId, IfxPsi5_InterruptSource_rdi, IfxPsi5_InterruptRequest_enabled);

    /*set interrupt node pointer*/
    IfxPsi5_setInterruptNodePointer(g_Psi5.psi5.psi5, g_Psi5.psi5Channel->channelId, IfxPsi5_InterruptNodePointer_rdi, IfxPsi5_TriggerOutput_0);

}
