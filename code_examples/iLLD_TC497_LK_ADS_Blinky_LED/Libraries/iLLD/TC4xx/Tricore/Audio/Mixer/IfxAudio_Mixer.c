void IfxAudio_Mixer_configureSrcFifo(IfxAudio_Audio *audio, IfxAudio_MixerSrcFifoIndex mixerSrcFifoIndex, IfxAudio_Mixer_SrcFifoConfig *config)
{
    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].SRC_CTL.B.FS_RATIO           = config->samplingFrequencyScaleRatio;

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].SRC_CTL.B.CH0_SEL            = config->dataSelect;

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].SRC_GAIN_CTL.B.CODE          = config->gainControlCode; /* Default use 115 */

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].SRC_FIFO_CTL.B.TRIGGER_LEVEL = config->triggerLevel;

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].SRC_DMA.B.DONE_SEL           = config->dmaChannelIndex;

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].INTR_SRC.U                   = 0; //TBC

    audio->audioSFR->MIXER.SRC[mixerSrcFifoIndex].INTR_SRC_MASK.U              = config->interruptMask;
}


void IfxAudio_Mixer_configureDstFifo(IfxAudio_Audio *audio, IfxAudio_Mixer_DstFifoConfig *config)
{
    audio->audioSFR->MIXER.DST.DST_GAIN_CTL.B.CODE          = config->gainControlCode;

    audio->audioSFR->MIXER.DST.DST_FIFO_CTL.B.TRIGGER_LEVEL = config->triggerLevel;

    audio->audioSFR->MIXER.DST.DST_DMA.B.DONE_SEL           = config->dmaChannelIndex;

    audio->audioSFR->MIXER.DST.INTR_DST.U                   = 0; //TBC

    audio->audioSFR->MIXER.DST.INTR_DST_MASK.U              = config->interruptMask;

    audio->audioSFR->MIXER.DST.DST_CTL.B.WORD_ALIGN         = config->wordAlign;

    audio->audioSFR->MIXER.DST.DST_CTL.B.WORD_SIGN_EXTEND   = config->wordSignExtend;
}

