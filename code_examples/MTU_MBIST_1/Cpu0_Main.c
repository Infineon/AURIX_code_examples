 /*\title Memory Built-In Test via MTU
 * \abstract The MBIST is used to assess the state of the memory
 * \description In this training the Memory Test Unit (MTU) is used to initialize and clear the content of an SRAM
 *              memory including its ECC code. Also, the non destructive inversion test is performed by Memory
 *              Built-in-Self-Test (MBIST) to verify the content of the same SRAM memory. The SRAM of the DMA
 *              (DMARAM) is selected to be tested in this example. In order to test the faulty scenario, this training
 *              provides the possibility to inject a single bit error in the memory. The LEDs on the board are used to
 *              signal the correct or faulty behavior of the MBIST.
 *
 * \name MTU_MBIST_1
 * \version V1.0.0
 * \board APPLICATION KIT TC2X7 V1.1, KIT_AURIX_TC297_TFT_BC-Step, TC29xTA/TX_BC-step
 * \keywords AURIX, MBIST, MTU_MBIST_1, MTU, Memory, Test
 * \documents https://www.infineon.com/aurix-expert-training/Infineon-AURIX_MTU_MBIST_1-TR-v01_00_00-EN.pdf
 * \documents https://www.infineon.com/aurix-expert-training/iLLD_UM_TC29B.chm
 * \lastUpdated 2020-01-17
 *********************************************************************************************************************/
#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "MTU_MBIST.h"

IfxCpu_syncEvent g_cpuSyncEvent = 0;

int core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);
    
    /* Perform the MBIST test */
    test_MTU_MBIST(IfxMtu_MbistSel_dma);

    while(1)
    {
    }
    return (1);
}
