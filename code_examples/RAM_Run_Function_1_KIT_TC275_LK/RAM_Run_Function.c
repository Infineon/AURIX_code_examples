/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define LED1            &MODULE_P00,5           /* LED1 Port 00, Pin 5                      */
#define LED2            &MODULE_P00,6           /* LED2 Port 00, Pin 6                      */

#define TOGGLE_TIME_MS  (1000U)                 /* Toggling time in milliseconds for LEDs   */

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void wait_ms(uint32 ms);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Initialize the Port peripheral */
void initPeripherals(void)
{
    /* Set the port pin 13.0 (to which the LED D107 is connected) to output push-pull mode */
    IfxPort_setPinModeOutput(LED1, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    /* Set the port pin 13.1 (to which the LED D108 is connected) to output push-pull mode */
    IfxPort_setPinModeOutput(LED2, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Turn off the LEDs (LEDs are low-level active) */
    IfxPort_setPinHigh(LED1);
    IfxPort_setPinHigh(LED2);
}

/* Toggle LED1: code is executed from LMURAM memory */
#pragma section code not_cached_lmuram
void toggleLedSram(void)
{
    /* Switch On LED1 */
    IfxPort_setPinLow(LED1);

    /* Wait one second */
    wait_ms(TOGGLE_TIME_MS);

    /* Switch Off LED1 */
    IfxPort_setPinHigh(LED1);

    /* Wait one second */
    wait_ms(TOGGLE_TIME_MS);
}
#pragma section code restore

/* Toggle LED2: code is executed from Flash memory */
void toggleLedFlash(void)
{
    /* Switch On LED2 */
    IfxPort_setPinLow(LED2);

    /* Wait one second */
    wait_ms(TOGGLE_TIME_MS);

    /* Switch Off LED2 */
    IfxPort_setPinHigh(LED2);

    /* Wait one second */
    wait_ms(TOGGLE_TIME_MS);
}

/* Wait until a timeout (in milliseconds) */
void wait_ms(uint32 ms)
{
    sint32 fSys = IfxStm_getFrequency(&MODULE_STM0);
    Ifx_TickTime wait_ms = (fSys / (1000 / ms));

    wait(wait_ms);
}


