void IfxRdma_Rdma_setTriggerConfiguration(IfxRdma_Rdma_Channel *channel, IfxRdma_Rdma_TriggerConfig *config)
{
    Ifx_RDMA_CH_CTRL_CHCSR chcsr;
    chcsr.U                           = channel->channelSFR->CTRL.CHCSR.U;

    chcsr.B.IMODE                     = (uint8)config->incrementalTriggerMode;
    chcsr.B.PTHRESH                   = (uint8)config->preTriggerThreshold;
    chcsr.B.TRIGACK                   = (uint8)config->spuRequest;
    channel->channelSFR->CTRL.CHCSR.U = chcsr.U;

    Ifx_RDMA_CH_CTRL_TRIGCTRL trigctrl;
    trigctrl.U                           = channel->channelSFR->CTRL.TRIGCTRL.U;

    trigctrl.B.BATCHSZ                   = config->outputTriggerBatchSize;
    trigctrl.B.BUFSZ                     = config->outputTriggerBuffSize;
    channel->channelSFR->CTRL.TRIGCTRL.U = trigctrl.U;
}


