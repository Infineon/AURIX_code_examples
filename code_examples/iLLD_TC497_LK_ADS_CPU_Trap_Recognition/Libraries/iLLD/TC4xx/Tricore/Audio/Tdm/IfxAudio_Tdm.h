/** \brief Configuration Structure of the Tdm Tx & Rx Fifo
 */
typedef struct
{
    uint8          triggerLevel;          /**< \brief FIFO trigger level. Valid range: 0 - 0x7F */
    IfxAudio_DmaCh dmaChannelIndex;       /**< \brief Select the SDMA Channel */
} IfxAudio_Tdm_FifoConfig;

/** \brief Configuration Structure of the Tdm Rx block
 */
typedef struct
{
    IfxAudio_Tdm_ControlMode           slaveOrMasterMode;            /**< \brief Master or slave mode */
    IfxAudio_Tdm_ControlFormat         format;                       /**< \brief Delayed Format */
    IfxAudio_Tdm_ControlSize           wordSize;                     /**< \brief PCM word size. Size of the valid data in the 32 bits of FIFO width. */
    boolean                            wordSignExtendType;           /**< \brief Select Word Sign Extension Type. FALSE: Zero extension, TRUE: Sign extension */
    uint16                             clockDiv;                     /**< \brief Interface clock divider. Range[0x7-0xBFF], provide odd value for 50-50% duty cycle clock */
    IfxAudio_Tdm_IfControlMckDiv       mckClockDiv;                  /**< \brief MCK clock divider. */
    boolean                            clockSource;                  /**< \brief Select the interface clock source. FALSE: Selects Audio PLL clock, that is 98.304 MHz. TRUE: Selects TDM_RXMCKI */
    boolean                            clockPolarity;                /**< \brief Select the polarity of the clock in both master and slave mode. FALSE: Clock signal is used "as is". TRUE: Clock signal is inverted */
    boolean                            channelSyncPolarity;          /**< \brief Select the channel synchronization polarity in both master and slave mode. FALSE: Channel synchronization signal is used "as is". TRUE: Channel synchronization signal is inverted */
    boolean                            pcmDataSamplingPoint;         /**< \brief Select the PCM Data sampling point. FALSE: Sample PCM bit value on rising edge, TRUE: Sample PCM bit value on falling edge */
    boolean                            channelSyncPulseFormat;       /**< \brief Select the channel synchronization pulse format. FALSE: BIT_PERIOD - Duration of a single bit period. TRUE: CH_PERIOD - Duration of the first channel */
    uint8                              numChannels;                  /**< \brief Number of channels per frame. Valid range [0x1: 2 channels - 0x1F: 32 channels]. Additionally refer to TDMi_TX_IF_CTL.CH_NR for limitations */
    uint8                              channelSize;                  /**< \brief Size of the channel. Valid range [0x3: 4-bit size - 0x1F: 32-bit size]. Additionally refer to TDMi_TX_IF_CTL.CH_SIZE for limitations */
    IfxAudio_Tdm_RxIfControlExtraDelay lateCaptureCycles;            /**< \brief Select delayed capture as required */
    boolean                            tdmOrI2sMode;                 /**< \brief Select the mode. FALSE: TDM. TRUE: I2S */
    uint32                             enableChannels;               /**< \brief Each bit represents a channel. 0: Corresponding channel is disabled, 1: Corresponding channel is enabled. Note: Only bits from 0 to TX_IF_CTL.CH_NR may be set to 1 which means only channels that are present in the frame can be enabled. */
    IfxAudio_Tdm_TxRouteControl        rxPinsRouteControl;           /**< \brief Select the route control mode */
} IfxAudio_Tdm_RxConfig;

/** \brief Configuration Structure for TSN Trigger Generator
 */
typedef struct
{
    uint16                  divider;                  /**< \brief Phantom TDM Clock Divider. Refer to TSN_PH_TDM.CLOCK_DIVPH in UM register description for details */
    IfxAudio_PtpMatchSource matchTriggerSource;       /**< \brief Select the TSN Phantom TDM match source trigger. */
    boolean                 matchType;                /**< \brief Phantom match wait. FALSE: Timer starts by writing 1 to TSN_PH_TDM.PH_START. TRUE: Timer starts by writing 1 to TSN_PH_TDM.PH_START and the timer also waits for a high value on the selected ptpTsMatch[PH_MATCH_SRC] signal */
} IfxAudio_Tdm_TsnPhantomTdmConfig;

/** \brief Configuration Structure for TSN Trigger Generator
 */
typedef struct
{
    uint16                         divider;             /**< \brief Sample rate divider ratio. Valid range: 0 - Sample rate divided by 1 to 0xFFFF - Sample rate divided by 65536 */
    IfxAudio_Tdm_TsnCaptureTrigger triggerSource;       /**< \brief Select the TSN Capture Trigger Source. */
} IfxAudio_Tdm_TsnTriggerGenConfig;

/** \brief Configuration Structure of the Tdm Tx block
 */
typedef struct
{
    IfxAudio_Tdm_ControlMode     slaveOrMasterMode;            /**< \brief Master or slave mode */
    IfxAudio_Tdm_ControlFormat   format;                       /**< \brief Delayed Format */
    IfxAudio_Tdm_ControlSize     wordSize;                     /**< \brief PCM word size. Size of the valid data in the 32 bits of FIFO width. */
    uint16                       clockDiv;                     /**< \brief Interface clock divider. Range[0x7-0xBFF], provide odd value for 50-50% duty cycle clock */
    IfxAudio_Tdm_IfControlMckDiv mckClockDiv;                  /**< \brief MCK clock divider. */
    boolean                      clockSource;                  /**< \brief Select the interface clock source. FALSE: Selects Audio PLL clock, that is 98.304 MHz. TRUE: Selects TDM_TXMCKI */
    boolean                      clockPolarity;                /**< \brief Select the polarity of the clock in both master and slave mode. FALSE: Clock signal is used "as is". TRUE: Clock signal is inverted */
    boolean                      channelSyncPolarity;          /**< \brief Select the channel synchronization polarity in both master and slave mode. FALSE: Channel synchronization signal is used "as is". TRUE: Channel synchronization signal is inverted */
    boolean                      channelSyncPulseFormat;       /**< \brief Select the channel synchronization pulse format. FALSE: BIT_PERIOD - Duration of a single bit period. TRUE: CH_PERIOD - Duration of the first channel */
    uint8                        numChannels;                  /**< \brief Number of channels per frame. Valid range [0x1: 2 channels - 0x1F: 32 channels]. Additionally refer to TDMi_TX_IF_CTL.CH_NR for limitations */
    uint8                        channelSize;                  /**< \brief Size of the channel. Valid range [0x3: 4-bit size - 0x1F: 32-bit size]. Additionally refer to TDMi_TX_IF_CTL.CH_SIZE for limitations */
    boolean                      tdmOrI2sMode;                 /**< \brief Select the mode. FALSE: TDM. TRUE: I2S */
    uint32                       enableChannels;               /**< \brief Each bit represents a channel. 0: Corresponding channel is disabled, 1: Corresponding channel is enabled. Note: Only bits from 0 to TX_IF_CTL.CH_NR may be set to 1 which means only channels that are present in the frame can be enabled. */
    IfxAudio_Tdm_TxRouteControl  txPinsRouteControl;           /**< \brief Select the route control mode */
} IfxAudio_Tdm_TxConfig;

