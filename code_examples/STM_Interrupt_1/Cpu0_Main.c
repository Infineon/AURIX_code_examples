 /*\name Interrupt generation via STM trigger
 * \brief The STM is configured to trigger an interrupt every 100 ms. The interrupt will toggle an LED.
 * \description The LED is driven by pin 0 of the port 13.
 *              The state of the pin will change based on the interrupt, which is triggered by STM.
 *              The timer is counting and when it reaches the configured value configured in the compare register,
 *              it will trigger an interrupt and reset its current value.
 *
 * \identifier STM_Interrupt_1
 * \version 1.0.0
 * \board APPLICATION KIT TC2X7 V1.1, KIT_AURIX_TC297_TFT_BC-Step, TC29xTA/TX_BC-step
 * \keywords Interrupt, LED, STM, blink
 * \documents STM_Interrupt_1.pdf
 * \date 2019-08-14
 *********************************************************************************************************************/

#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "STM_Interrupt.h"

IfxCpu_syncEvent cpuSyncEvent= 0;

int core0_main (void)
{
    IfxCpu_enableInterrupts();
    /*
     * !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdog in the demo if it is required and also service the watchdog periodically
     * */
    IfxScuWdt_disableCpuWatchdog (IfxScuWdt_getCpuWatchdogPassword ());
    IfxScuWdt_disableSafetyWatchdog (IfxScuWdt_getSafetyWatchdogPassword ());

    /* Cpu sync event wait*/
    IfxCpu_emitEvent(&cpuSyncEvent);
    IfxCpu_waitEvent(&cpuSyncEvent, 1);

    initPeripherals();

    while (1)
    {
    }

    return (1);
}

