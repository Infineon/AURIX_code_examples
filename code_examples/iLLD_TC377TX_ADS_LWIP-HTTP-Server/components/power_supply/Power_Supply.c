#include "power_supply.h"


Power_Supply_t Board_Power_Supply;

/*
 *  Init's the power supply status measurment's
 */
sint32 Init_Power_Supply_Measurments (void)
{
sint32 Init_Status = 0;

/*
 * we init the TLF and set to normal state
 */
IfxTLF3XX8X_init();
IfxTLF3XX8X_goto_normal_state();

#ifdef POWER_ANA_MEASURE
    Init_Status += Init_Analog_Measurment ();
    Board_Power_Supply.Board_Analog_Measure = &Board_Analog_Measure;
#endif

#ifdef POWER_GOOD_SIGNALS
    Init_PowerGood_Signals ();
    Board_Power_Supply.Board_PG_Status = &Board_PG_Status;
#endif




    return(Init_Status);
}

/*
 *  measures updates the power supply status
 */
void Power_Supply_Measurments (void)
{

#ifdef POWER_ANA_MEASURE
    Analog_Measurment ();
#endif

#ifdef POWER_GOOD_SIGNALS
    Get_PowerGood_Signal();
#endif



}


