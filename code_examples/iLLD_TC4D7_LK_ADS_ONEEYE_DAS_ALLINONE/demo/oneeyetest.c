#include "oneeyetest.h"
#include "ifx_oe_time.h"
#include "ifx_oe_stdif_dpipe.h"
#include "oneeyedas.h"

/* For test purpose only */
void OneEyeTest_process(void)
{
    static Ifx_Oe_TickTime deadline = 0;

    if (Ifx_Oe_Time_isDeadLine(deadline))
    {
        Ifx_Oe_TickTime now = Ifx_Oe_Time_now();

        /* Print the time */
        uint32 low  = now & 0xFFFFFFFF;
        uint32 high = (now >> 32) & 0xFFFFFFFF;
        deadline = Ifx_Oe_Time_getDeadLine(Ifx_Oe_Time_getTickForMilliseconds(1000));

        Ifx_Oe_StdIf_DPipe_print(
            &g_dPipe,
            "Time %d ms, %08x%08x" IFX_OE_ENDL,
            Ifx_Oe_Time_getMillisecondForTicks(now),
            high,
            low);
    }

#if  defined(TEST_ECHO_CHAR_VALUE)
    /* Test char number */
    char         data;
    Ifx_Oe_SizeT count = 1;
    Ifx_Oe_StdIf_DPipe_read(&g_dPipe, &data, &count, Ifx_Oe_Time_0s);

    if (count)
    {
        Ifx_Oe_StdIf_DPipe_print(&g_dPipe, "%d" IFX_OE_ENDL, data);
    }

#elif  defined(TEST_ECHO)

    /* Test ECHO */
    char         data[128];
    Ifx_Oe_SizeT count = sizeof(data);
    Ifx_Oe_StdIf_DPipe_read(&g_dPipe, data, &count, Ifx_Oe_Time_0s);
    Ifx_Oe_StdIf_DPipe_write(&g_dPipe, data, &count, Ifx_Oe_Time_0s);
#endif
}
