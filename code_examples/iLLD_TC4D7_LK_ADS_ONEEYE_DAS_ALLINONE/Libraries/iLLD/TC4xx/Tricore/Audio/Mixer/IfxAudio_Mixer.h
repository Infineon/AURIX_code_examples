/** \addtogroup IfxLld_Audio_Mixer_Enum
 * \{ */
/** \brief Specifies selection of the channel's 24-bit PCM data from the source FIFO data[31:0]. Refer MIXER_SRCi_CTL.CH0_SEL
 */
typedef enum
{
    IfxAudio_Mixer_PcmDataSelect_dataLow  = 0,  /**< \brief 0.5 (down sampling by 2x) */
    IfxAudio_Mixer_PcmDataSelect_dataHigh = 1,  /**< \brief 1 (Same as input. No re sampling.) */
    IfxAudio_Mixer_PcmDataSelect_const_0  = 2   /**< \brief 2 (up sampling by 2x) */
} IfxAudio_Mixer_PcmDataSelect;

/** \brief Sample Frequency Scale Ratio. Refer MIXER_SRCi_CTL.FS_RATIO
 */
typedef enum
{
    IfxAudio_Mixer_SampleFrequencyScaleRatio_0p5              = 0, /**< \brief 0.5 (down sampling by 2x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_1                = 1, /**< \brief 1 (Same as input. No re sampling.) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_2                = 2, /**< \brief 2 (up sampling by 2x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_3                = 3, /**< \brief 3 (up sampling by 3x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_4                = 4, /**< \brief 4 (up sampling by 4x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_6                = 5, /**< \brief 6 (up sampling by 6x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_8                = 6, /**< \brief 8 (up sampling by 8x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_12               = 7, /**< \brief 12 (up sampling by 12x) */
    IfxAudio_Mixer_SampleFrequencyScaleRatio_44p1KHz_to_48KHz = 8  /**< \brief 44.1KHz to 48KHz (up sampling by 160/147) */
} IfxAudio_Mixer_SampleFrequencyScaleRatio;

/** \} */

