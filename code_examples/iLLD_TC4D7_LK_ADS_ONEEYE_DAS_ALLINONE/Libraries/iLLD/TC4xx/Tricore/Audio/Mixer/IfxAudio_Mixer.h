/** \addtogroup IfxLld_Audio_Mixer_Structures
 * \{ */
/** \brief Configuration Structure of the Mixer Destination Fifo
 */
typedef struct
{
    uint8          gainControlCode;       /**< \brief Mixer Destination Fifo gain control. Default is 115 for no change. Range is 0 (digital mute) to 127. Refer to MIXER_DST_GAIN_CTL.CODE */
    uint8          triggerLevel;          /**< \brief Mixer Destination fifo interrupt trigger level. When the source FIFO has less entries than the number of this field, a transmitter trigger event is generated. Range is 0 to 127. */
    IfxAudio_DmaCh dmaChannelIndex;       /**< \brief Select the Dma channel */
    uint32         interruptMask;         /**< \brief Mixer Destination Fifo interrupt mask */
    boolean        wordAlign;             /**< \brief Mixer destination word alignment, FALSE: Left Align, TRUE: Right Align . */
    boolean        wordSignExtend;        /**< \brief Mixer destination word sign extension, FALSE: Zero extension, TRUE: Sign extension. */
} IfxAudio_Mixer_DstFifoConfig;

/** \brief Configuration Structure of the Mixer Source Fifo
 */
typedef struct
{
    IfxAudio_Mixer_SampleFrequencyScaleRatio samplingFrequencyScaleRatio;       /**< \brief Select the sampling frequency scale ratio at the Source Fifo */
    IfxAudio_Mixer_PcmDataSelect             dataSelect;                        /**< \brief Select the 24 bit PCM data from the Source Fifo data */
    uint8                                    gainControlCode;                   /**< \brief Mixer source fifo gain control. Default is 115 for no change. Range is 0 (digital mute) to 127. Refer to MIXER_SRCi_GAIN_CTL.CODE */
    uint8                                    triggerLevel;                      /**< \brief Mixer source fifo interrupt trigger level. When the source FIFO has less entries than the number of this field, a transmitter trigger event is generated. Range is 0 to 127. */
    IfxAudio_DmaCh                           dmaChannelIndex;                   /**< \brief Select the Dma channel */
    uint32                                   interruptMask;                     /**< \brief Mixer Source Fifo interrupt mask */
} IfxAudio_Mixer_SrcFifoConfig;

/** \} */

