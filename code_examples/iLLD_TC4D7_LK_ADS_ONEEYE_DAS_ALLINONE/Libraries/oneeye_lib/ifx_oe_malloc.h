#ifndef IFX_OE_MALLOC_H
#define IFX_OE_MALLOC_H
//---------------------------------------------------------------------------

#include "ifx_oe_al.h"
#include "stddef.h"

typedef struct _Ifx_Oe_Malloc_Blk  Ifx_Oe_Malloc_Blk;
typedef struct _Ifx_Oe_Malloc_Pool Ifx_Oe_Malloc_Pool;

//#define  IFX_CFG_OE_MALLOC_DEBUG
#define MEMORY_POOL_STATS

/**
 * Note: sizeof( Ifx_Oe_Malloc_Blk ) must be multiple of IFX_OE_ALIGN_64
 * Note: Note sizeof(_Ifx_Oe_Malloc_Blk) must be a multiple of 64
 */
struct _Ifx_Oe_Malloc_Blk
{
    Ifx_Oe_Malloc_Blk* next;   /* next block on list */
    Ifx_Oe_Malloc_Blk* prev;   /* previous block or 0 if block is free */
    Ifx_Oe_Malloc_Pool* memoryPool;
    void* Reserved;
};

struct _Ifx_Oe_Malloc_Pool
{
    /* _sbrk */
    char* _lc_bh;
    char* _lc_eh;
    char* _end;

    /* alloc */
    Ifx_Oe_Malloc_Blk head;           /* head of memory block list */
    Ifx_Oe_Malloc_Blk* last;           /* last memory block */
    Ifx_Oe_Malloc_Blk* start;          /* starting point for block search */
    boolean mutex;
#ifdef MEMORY_POOL_STATS

    size_t memUsed;
    size_t memFree;
    size_t memMaxUsed;
#endif
};

//---------------------------------------------------------------------------
/** Create / initialize the memory pool
 * @param address Address of the memory block to be used as memory pool
 * @param Block size in bytes
 * @return   If succeed return a pointer on the Created memory pool, else return NULL if the memory block is too small
 */
Ifx_Oe_Malloc_Pool* Ifx_Oe_Malloc_create(void* address, size_t size);

void*               Ifx_Oe_Malloc_malloc(Ifx_Oe_Malloc_Pool* memoryPool, size_t size);

void                Ifx_Oe_Malloc_free(void* MemoryBlock);

void*               Ifx_Oe_Malloc_calloc(Ifx_Oe_Malloc_Pool* memoryPool, size_t nel, size_t size);

/** Realloc within the same memory block */
void* Ifx_Oe_Malloc_realloc(void* MemoryBlock, size_t newsize);

/** Realloc within a given memory block */
void*  Ifx_Oe_Malloc_reallocMove(Ifx_Oe_Malloc_Pool* memoryPool, void* MemoryBlock, size_t newsize);

void   Ifx_Oe_Malloc_destroy(Ifx_Oe_Malloc_Pool* memoryPool);

size_t Ifx_Oe_Malloc_size(Ifx_Oe_Malloc_Pool* memoryPool);

//---------------------------------------------------------------------------
#endif
