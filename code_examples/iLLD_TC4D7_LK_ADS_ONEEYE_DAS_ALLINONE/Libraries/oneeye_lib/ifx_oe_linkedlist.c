#include "ifx_oe_linkedlist.h"
#include  <stdlib.h>

static void Ifx_Oe_LinkedList_allocateItems(Ifx_Oe_LinkedList* list);

void        Ifx_Oe_LinkedList_init(Ifx_Oe_LinkedList* list, uint32 preAllocate)
{
    list->items.front      = NULL_PTR;
    list->items.back       = NULL_PTR;
    list->nextFreeItemBack = NULL_PTR;
    list->preAllocate      = preAllocate;
    Ifx_Oe_LinkedList_allocateItems(list);
}


void Ifx_Oe_LinkedList_deinit(Ifx_Oe_LinkedList* list)
{
    Ifx_Oe_LinkedListItem* item;

    item = list->items.front;

    while (item)
    {
        Ifx_Oe_LinkedListItem* next = item->next;
        free(item);
        item = next;
    }

    item = list->nextFreeItemBack;

    while (item)
    {
        Ifx_Oe_LinkedListItem* prev = item->prev;
        free(item);
        item = prev;
    }

    list->items.front      = NULL_PTR;
    list->items.back       = NULL_PTR;
    list->nextFreeItemBack = NULL_PTR;
}


static void Ifx_Oe_LinkedList_allocateItems(Ifx_Oe_LinkedList* list)
{
    Ifx_Oe_LinkedListItem* item = NULL_PTR;
    uint32                 i; /* FIXME for improved memory usage, allocate a single block for all items */

    for (i = 0; i < list->preAllocate; i++)
    {
        item = malloc(sizeof(Ifx_Oe_LinkedListItem));
        IFX_OE_ASSERT(item != NULL_PTR);
        /* Append item to the free queue */
        item->prev             = list->nextFreeItemBack;
        item->next             = NULL_PTR;
        item->list             = list;
        item->inItemsList      = FALSE;
        list->nextFreeItemBack = item;
    }

    if (item)
    {
        item->next = NULL_PTR;
    }
}


Ifx_Oe_LinkedListItem* Ifx_Oe_LinkedList_getFreeItem(Ifx_Oe_LinkedList* list)
{
    Ifx_Oe_LinkedListItem* item = NULL_PTR;

    if (list->nextFreeItemBack == NULL_PTR)
    {
        Ifx_Oe_LinkedList_allocateItems(list);
    }

    /* Remove item from the free queue */
    item = list->nextFreeItemBack;

    if (item)
    {
        list->nextFreeItemBack = item->prev;
        item->prev             = NULL_PTR;
    }

    return item;
}


void Ifx_Oe_LinkedList_releaseItem(Ifx_Oe_LinkedListItem* item, Ifx_Oe_LinkedList* list)
{
    list = list == NULL_PTR ? item->list : list;
    IFX_OE_ASSERT(list != NULL_PTR);
    Ifx_Oe_LinkedList_pop(item);

    item->prev             = list->nextFreeItemBack;
    list->nextFreeItemBack = item;
}


Ifx_Oe_LinkedListItem* Ifx_Oe_LinkedList_popFront(Ifx_Oe_LinkedList* list)
{
    Ifx_Oe_LinkedListItem* item = list->items.front;

    if (item != NULL_PTR)
    {
        list->items.front = item->next;

        if (list->items.front != NULL_PTR)
        {
            list->items.front->prev = NULL_PTR;
        }
        else
        {
            list->items.back = NULL_PTR;
        }

        item->next        = NULL_PTR;
        item->list        = NULL_PTR;
        item->inItemsList = FALSE;
    }

    return item;
}


Ifx_Oe_LinkedListItem* Ifx_Oe_LinkedList_popBack(Ifx_Oe_LinkedList* list)
{
    Ifx_Oe_LinkedListItem* item = list->items.back;

    if (item != NULL_PTR)
    {
        list->items.back = item->prev;

        if (list->items.back != NULL_PTR)
        {
            list->items.back->next = NULL_PTR;
        }
        else
        {
            list->items.front = NULL_PTR;
        }

        item->prev        = NULL_PTR;
        item->list        = NULL_PTR;
        item->inItemsList = FALSE;
    }

    return item;
}


Ifx_Oe_LinkedListItem* Ifx_Oe_LinkedList_pop(Ifx_Oe_LinkedListItem* item)
{
    Ifx_Oe_LinkedList* list = item->list;

    if (item->inItemsList == FALSE)
    {   /* Item not in queue */
        return item;
    }

    if (item->next)
    {
        item->next->prev = item->prev;
    }
    else
    {
        list->items.back = item->prev;
    }

    if (item->prev)
    {
        item->prev->next = item->next;
    }
    else
    {
        list->items.front = item->next;
    }

    item->next        = NULL_PTR;
    item->prev        = NULL_PTR;
    item->list        = NULL_PTR;
    item->inItemsList = FALSE;

    return item;
}


void Ifx_Oe_LinkedList_pushFront(Ifx_Oe_LinkedList* list, Ifx_Oe_LinkedListItem* item)
{
    Ifx_Oe_LinkedList_pop(item);

    item->next = list->items.front;

    if (item->next != NULL_PTR)
    {
        item->next->prev = item;
    }

    item->prev        = NULL_PTR;
    item->list        = list;
    item->inItemsList = TRUE;

    list->items.front = item;

    if (list->items.back == NULL_PTR)
    {
        list->items.back = item;
    }
}


void Ifx_Oe_LinkedList_pushBack(Ifx_Oe_LinkedList* list, Ifx_Oe_LinkedListItem* item)
{
    Ifx_Oe_LinkedList_pop(item);

    item->prev = list->items.back;

    if (item->prev != NULL_PTR)
    {
        item->prev->next = item;
    }

    item->next        = NULL_PTR;
    item->list        = list;
    item->inItemsList = TRUE;

    list->items.back  = item;

    if (list->items.front == NULL_PTR)
    {
        list->items.front = item;
    }
}


void Ifx_Oe_LinkedList_insertAfter(Ifx_Oe_LinkedListItem* after, Ifx_Oe_LinkedListItem* item)
{
    Ifx_Oe_LinkedList* list = after->list;

    if (after == NULL_PTR)
    {
        Ifx_Oe_LinkedList_pushFront(list, item);
    }
    else
    {
        Ifx_Oe_LinkedList_pop(item);

        item->prev        = after;
        item->next        = after->next;
        after->next       = item;
        item->list        = list;
        item->inItemsList = TRUE;

        if (item->next != NULL_PTR)
        {
            item->next->prev = item;
        }
        else if (list->items.back == after)
        {
            list->items.back = item;
        }
        else
        {
            /* Consistency error */
            IFX_OE_ASSERT(FALSE);
        }
    }
}


void Ifx_Oe_LinkedList_insertBefore(Ifx_Oe_LinkedListItem* before, Ifx_Oe_LinkedListItem* item)
{
    Ifx_Oe_LinkedList* list = before->list;

    if (before == NULL_PTR)
    {
        Ifx_Oe_LinkedList_pushBack(list, item);
    }
    else
    {
        Ifx_Oe_LinkedList_pop(item);

        item->next        = before;
        item->prev        = before->prev;
        before->prev      = item;
        item->list        = list;
        item->inItemsList = TRUE;

        if (item->prev != NULL_PTR)
        {
            item->prev->next = item;
        }
        else if (list->items.front == before)
        {
            list->items.front = item;
        }
        else
        {
            /* Consistency error */
            IFX_OE_ASSERT(FALSE);
        }
    }
}


void Ifx_Oe_LinkedList_appendDataPointer(Ifx_Oe_LinkedList* list, void* data)
{
    Ifx_Oe_LinkedListItem* item = Ifx_Oe_LinkedList_getFreeItem(list);
    IFX_OE_ASSERT(item != NULL_PTR);
    item->data = data;
    Ifx_Oe_LinkedList_pushBack(list, item);
}


void Ifx_Oe_LinkedList_prependDataPointer(Ifx_Oe_LinkedList* list, void* data)
{
    Ifx_Oe_LinkedListItem* item = Ifx_Oe_LinkedList_getFreeItem(list);
    IFX_OE_ASSERT(item != NULL_PTR);
    item->data = data;
    Ifx_Oe_LinkedList_pushFront(list, item);
}
