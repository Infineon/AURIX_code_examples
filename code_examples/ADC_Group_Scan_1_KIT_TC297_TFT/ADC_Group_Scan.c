#include "ADC_Group_Scan.h"
#include "IfxVadc_Adc.h"
#include "IfxAsclin_Asc.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define N_CHANNELS                  5           /* Number of ADC channels that will be used                         */
#define ASCII_SHIFT                 48          /* Shift in the ASCII table: '0', '1', '2'[...], start at value 48  */

#define ISR_PRIORITY_ASCLIN_TX      1           /* Priority for interrupt ISR Transmit                              */
#define ISR_PRIORITY_ASCLIN_RX      2           /* Priority for interrupt ISR Receive                               */
#define ISR_PRIORITY_ASCLIN_ER      3           /* Priority for interrupt ISR Errors                                */
#define ASC_TX_BUFFER_SIZE          64          /* TX buffer size for UART communication                            */
#define ASC_RX_BUFFER_SIZE          64          /* RX buffer size for UART communication                            */
#define ASC_PRESCALER               1           /* Division ratio of the predivider for UART communication          */
#define ASC_BAUDRATE                115200      /* Baud rate of the UART communication                              */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxAsclin_Asc g_asc;                            /* Handle for the ASC communication module registers                */
IfxVadc_Adc g_vadc;                             /* Handle for VADC registers                                        */
IfxVadc_Adc_Group g_adcGroup;                   /* Handle for the VADC group registers                              */
IfxVadc_Adc_Channel g_adcChannel[N_CHANNELS];   /* Handle for the VADC channel registers                            */

/* The transfer buffers allocate memory for the data itself and for FIFO runtime variables.
 * 8 more bytes have to be added to ensure a proper circular buffer handling independent from
 * the address to which the buffers have been located.
 */
uint8 g_ascTxBuffer[ASC_TX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];     /* Array for handling the transmission buffer   */
uint8 g_ascRxBuffer[ASC_RX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];     /* Array for handling the reception buffer      */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Configuration and initialization of the VADC module and the group(s) that will be used */
void init_vadc(void)
{
    /* Create and initialize the module configuration */
    IfxVadc_Adc_Config adcConf;                             /* Define a configuration structure for the VADC module */
    IfxVadc_Adc_initModuleConfig(&adcConf, &MODULE_VADC);   /* Fill it with default values                          */

    IfxVadc_Adc_initModule(&g_vadc, &adcConf);              /* Apply the configuration to the module                */

    /* Create and initialize the group configuration */
    IfxVadc_Adc_GroupConfig adcGroupConf;                   /* Define a configuration structure for the VADC group  */
    IfxVadc_Adc_initGroupConfig(&adcGroupConf, &g_vadc);    /* Fill it with default values                          */

    /* Configuration of the group */
    adcGroupConf.groupId = IfxVadc_GroupId_0;               /* Select the group                                     */
    adcGroupConf.master = adcGroupConf.groupId;             /* Select the master group                              */

    adcGroupConf.arbiter.requestSlotScanEnabled = TRUE;     /* Enable scan source                                   */
    adcGroupConf.scanRequest.autoscanEnabled = TRUE;        /* Enable auto scan mode                                */

    /* Enable all gates in "always" mode (no edge detection) */
    adcGroupConf.scanRequest.triggerConfig.gatingMode = IfxVadc_GatingMode_always;

    IfxVadc_Adc_initGroup(&g_adcGroup, &adcGroupConf);      /* Apply the configuration to the group                 */

    /* Create and initialize the channels configuration */
    uint32 chnIx;

    /* Create channel configuration */
    IfxVadc_Adc_ChannelConfig adcChannelConf[N_CHANNELS]; /* Define a configuration structure for the VADC channels */

    for(chnIx = 0; chnIx < N_CHANNELS; ++chnIx)
    {
        IfxVadc_Adc_initChannelConfig(&adcChannelConf[chnIx], &g_adcGroup);     /* Fill it with default values      */

        adcChannelConf[chnIx].channelId = (IfxVadc_ChannelId)(chnIx);           /* Select the channel ID            */
        adcChannelConf[chnIx].resultRegister = (IfxVadc_ChannelResult)(chnIx);  /* Use dedicated result register    */

        /* Initialize the channel */
        IfxVadc_Adc_initChannel(&g_adcChannel[chnIx], &adcChannelConf[chnIx]);

        /* Add the channel to the scan sequence */
        uint32 enableChnBit = (1 << adcChannelConf[chnIx].channelId);   /* Set the the corresponding input channel  */
        uint32 mask = enableChnBit;                                     /* of the respective group to take part in  */
        IfxVadc_Adc_setScan(&g_adcGroup, enableChnBit, mask);           /* the background scan sequence.            */
    }

    /* Start the scan */
    IfxVadc_Adc_startScan(&g_adcGroup);
}

/* Get and print VADC conversions */
void run_vadc(void)
{
    uint32 chnIx;

    /* Get the VADC conversions */
    for(chnIx = 0; chnIx < N_CHANNELS; ++chnIx)
    {
        Ifx_VADC_RES conversionResult;

        /* Wait for a new valid result */
        do
        {
            conversionResult = IfxVadc_Adc_getResult(&g_adcChannel[chnIx]);
        } while(!conversionResult.B.VF); /* B for Bitfield access, VF for Valid Flag */

        /* Print the conversion to the UART */
        send_vadc(chnIx, conversionResult.B.RESULT);
    }
}

/* Print, via UART, the VADC results after conversions */
void send_vadc(uint32 chnIx, uint32 adcVal)
{
    char str[TX_LENGTH] = {'C','h','.','X',':',' ','X','X','X','X','\n','\r'};  /* X to be replaced by correct values*/

    str[3] = (char) chnIx + ASCII_SHIFT;                                        /* Channel index                    */

    /* Turns the digital converted value into its ASCII characters, e.g. 1054 -> '1','0','5','4' */
    /* 12-bits range value: 0-4095 */
    str[6] = (adcVal / 1000) + ASCII_SHIFT;                                     /* Thousands                        */
    str[7] = ((adcVal % 1000) / 100) + ASCII_SHIFT;                             /* Hundreds                         */
    str[8] = ((adcVal % 100) / 10) + ASCII_SHIFT;                               /* Tens                             */
    str[9] = (adcVal % 10) + ASCII_SHIFT;                                       /* Units                            */

    /* Print via UART */
    send_data(str, TX_LENGTH);
}

/* Function initializing the ASCLIN module */
void init_uart(void)
{
    IfxAsclin_Asc_Config ascConf;

    IfxAsclin_Asc_initModuleConfig(&ascConf, &MODULE_ASCLIN0);

    /* Set the desired baudrate */
    ascConf.baudrate.prescaler = ASC_PRESCALER;
    ascConf.baudrate.baudrate = ASC_BAUDRATE;                                   /* Set the baud rate in bit/s       */
    ascConf.baudrate.oversampling = IfxAsclin_OversamplingFactor_16;            /* Set the oversampling factor      */

    /* Configure the sampling mode */
    ascConf.bitTiming.medianFilter = IfxAsclin_SamplesPerBit_three;             /* Set the number of samples per bit*/
    ascConf.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;    /* Set the first sample position    */

    /* ISR priorities and interrupt target */
    ascConf.interrupt.txPriority = ISR_PRIORITY_ASCLIN_TX;
    ascConf.interrupt.rxPriority = ISR_PRIORITY_ASCLIN_RX;
    ascConf.interrupt.erPriority = ISR_PRIORITY_ASCLIN_ER;
    ascConf.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* FIFO configuration */
    ascConf.txBuffer = g_ascTxBuffer;
    ascConf.txBufferSize = ASC_TX_BUFFER_SIZE;
    ascConf.rxBuffer = g_ascRxBuffer;
    ascConf.rxBufferSize = ASC_RX_BUFFER_SIZE;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
            NULL,                       IfxPort_InputMode_pullUp,               /* CTS port pin not used            */
            &IfxAsclin0_RXA_P14_1_IN,   IfxPort_InputMode_pullUp,               /* RX port pin                      */
            NULL,                       IfxPort_OutputMode_pushPull,            /* RTS port pin not used            */
            &IfxAsclin0_TX_P14_0_OUT,   IfxPort_OutputMode_pushPull,            /* TX port pin                      */
            IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConf.pins = &pins;

    /* Initialize the module */
    IfxAsclin_Asc_initModule(&g_asc, &ascConf);
}

/* Function to transmit data over UART */
void send_data(char *data, Ifx_SizeT length)
{
    /* Transmit data */
    IfxAsclin_Asc_write(&g_asc, data, &length, TIME_INFINITE);
}

/* Function to receive data over ASC */
void receive_data(char *data, Ifx_SizeT length)
{
    /* Receive data */
    IfxAsclin_Asc_read(&g_asc, data, &length, TIME_INFINITE);
}

/* Macro to define Interrupt Service Routine. */
IFX_INTERRUPT(ASCLIN_ISR_Tx, 0, ISR_PRIORITY_ASCLIN_TX);
void ASCLIN_ISR_Tx(void)
{
    IfxAsclin_Asc_isrTransmit(&g_asc);
}

IFX_INTERRUPT(ASCLIN_ISR_Rx, 0, ISR_PRIORITY_ASCLIN_RX);
void ASCLIN_ISR_Rx(void)
{
    IfxAsclin_Asc_isrReceive(&g_asc);
}

IFX_INTERRUPT(ASCLIN_ISR_Er, 0, ISR_PRIORITY_ASCLIN_ER);
void ASCLIN_ISR_Er(void)
{
    IfxAsclin_Asc_isrError(&g_asc);
}
