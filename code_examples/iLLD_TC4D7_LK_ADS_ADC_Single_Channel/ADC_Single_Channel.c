/* Macro to define the Interrupt Service Routine */

/* Function to initialize and run the TMADC */
void initTMADC(void)
{
    /* Initialize the TMADC module */
    initTMADCModule();

    /* Initialize the channel */
    initTMADCChannel();

    /* Run the TMADC module */
    IfxAdc_Tmadc_runModule(&g_tmadc);
}

/* Function to initialize the TMADC module */
void initTMADCModule(void)
{
    /* Enable ADC module */
    IfxAdc_enableModule(&MODULE_ADC);

    /* Initialize module to default */
    IfxAdc_Tmadc_initModuleConfig(&g_tmadcConfig, &MODULE_ADC);

    /* Change TMADC configuration */
    g_tmadcConfig.id                = IfxAdc_TmadcModule_0;             /* Module ID                                 */
    g_tmadcConfig.calEnable         = 1;                                /* Calibration enabled                       */

    /* Initialize TMADC module with updated configuration */
    IfxAdc_Tmadc_initModule(&g_tmadc, &g_tmadcConfig);
}

/* Function to initialize the TMADC channel */
void initTMADCChannel(void)
{
    /* Initialize the configuration with default values */
    IfxAdc_Tmadc_initChannelConfig(&g_tmadcChannel0Config, &MODULE_ADC);

    /* Change TMADC channel configuration */
    /* Select the channel ID and the respective result register */
    g_tmadcChannel0Config.id             = IfxAdc_TmadcChannel_0;          /* Channel ID                             */
    g_tmadcChannel0Config.moduleId       = IfxAdc_TmadcModule_0;           /* Select TMADC module                    */
    g_tmadcChannel0Config.samplingTimeNS = 100;                            /* Set sampling time period               */
    g_tmadcChannel0Config.mode           = IfxAdc_TmadcOpMode_continuous;  /* Select operating mode - continuous     */
    g_tmadcChannel0Config.core           = IfxAdc_TmadcSarCore_0;          /* Select TMADC SAR core                  */

    g_tmadcChannel0Config.resultCfg.resultReg   = IfxAdc_TmadcResultReg_0; /* Configure result register              */
    g_tmadcChannel0Config.resultCfg.waitForRead = 1;                       /* Enable wait-for-read mode              */

    /* Set channel service request */
    g_tmacChannel0ServRequest.errorNode     = IfxAdc_TmadcServReq_none;    /* Configure Service request error node   */
    g_tmacChannel0ServRequest.boundaryNode  = IfxAdc_TmadcServReq_none;    /* Configure Service request boundary node*/
    g_tmacChannel0ServRequest.resultNode    = IfxAdc_TmadcServReq_0;       /* Configure Service request result node  */

    /* Assign configured channel service request to configuration */
    g_tmadcChannel0Config.channelSrvReq = &g_tmacChannel0ServRequest;

    /* Initialize the channel */
    IfxAdc_Tmadc_initChannel(&g_tmadcChannel, &g_tmadcChannel0Config);
}

/* read from ADCC */
int resultADC(void)
{
    /* Read result value */
    g_result = IfxAdc_Tmadc_readChannelResult(&g_tmadcChannel);

    return g_result;
}