#ifndef PPU_KERNEL_ID_H
#define PPU_KERNEL_ID_H 1

/* List all the kernels that are part of the DSP application */
/* Only those that are to be remotely called by TriCore software are to be listed here */
typedef enum kernel_id
{
  /* Place OpenCL kernels here */
  PPU_OPENCL_KERNEL_ID_MIN    = 0,
  PPU_OPENCL_KERNEL_ID_FFT    = 0,
  PPU_OPENCL_KERNEL_ID_ROTATE = 1,
  PPU_OPENCL_KERNEL_ID_BUCK   = 2,
  PPU_OPENCL_KERNEL_ID_MAX,

  /* Place C kernels here */
  PPU_C_KERNEL_ID_MIN = PPU_OPENCL_KERNEL_ID_MAX,
  PPU_C_KERNEL_ID_SF  = PPU_C_KERNEL_ID_MIN,
  PPU_C_KERNEL_ID_MAX
} Kernel_Id_t;

#define C_WRAPPER_EXTERN(X) extern int _##X##_C_Wrapper(int *ptr[])

/* Although the following information is better placed in kernel_description.h of PPU,
 * we retain it here because TriCore software also requires some of the information below.
 */
#define FFT_KERNEL_NAME "FFT"
#define FFT_NUM_ARGS    (6U)

#define ROTATE_KERNEL_NAME "ROTATE"
#define ROTATE_NUM_ARGS    (2U)

#define BUCK_KERNEL_NAME "BUCK"
#define BUCK_NUM_ARGS    (3U)

#define SF_KERNEL_NAME "SF"
#define SF_NUM_ARGS    (2U)

#endif /* PPU_KERNEL_ID_H */
