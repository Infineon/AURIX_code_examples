/** \brief Handle the 'showtlf' command
 *
 * \par Syntax
 *  - showtlf x: show the value of TLF register x
 */
boolean Show_TLF_Register(pchar args, void *data, IfxStdIf_DPipe *io)
{
	uint32 regAddr, uiValue;

    if (Ifx_Shell_matchToken(&args, "?") != FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntax     : showtlf x"ENDL);
        IfxStdIf_DPipe_print(io, "           > Show the value of TLF register x"ENDL);
        return TRUE;
    }
	/* first we get the first argument */
    regAddr = 0;
	if (Ifx_Shell_parseUInt32(&args, &regAddr, FALSE) == FALSE)
   	{
   		IfxStdIf_DPipe_print(io, "Syntaxerror : invalid register address %s"ENDL, args);
   		return TRUE;
   	}

    uiValue = IfxTLF4D985_read_register((uint16)regAddr);

    IfxStdIf_DPipe_print(io, "Reg%d : 0x%02X %03d"ENDL, regAddr, uiValue, uiValue);

    return TRUE;
}

/** \brief Handle the 'standby' command
 *
 * \par Syntax
 *  - standby : switch the TLF4D985 to standby state or sleep state (sleep on A11 step)
 */
boolean TLF_Standby_Sleep(pchar args, void *data, IfxStdIf_DPipe *io)
{
    if (Ifx_Shell_matchToken(&args, "?") != FALSE)
    {
        IfxStdIf_DPipe_print(io, "Syntax     : standby"ENDL);
        IfxStdIf_DPipe_print(io, "           > Switch TLF4D985 to standby/sleep"ENDL);
    }
    else
    {
    	IfxTLF4D985_goto_standby_sleep_state();
    }

    return TRUE;
}

