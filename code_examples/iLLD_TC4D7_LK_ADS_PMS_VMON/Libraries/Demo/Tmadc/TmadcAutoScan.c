/** \brief Demo init API
 *
 * This function is called from main during initialization phase
 */
void TmadcAutoScan_init(void)
{
    uint32 uiCnt;
	/* TMADC Configuration */

    /* create configuration */
    IfxAdc_Tmadc_Config moduleConfig;
    IfxAdc_Tmadc_initModuleConfig(&moduleConfig, &MODULE_ADC);

    /* Enable ADC module */
    IfxAdc_enableModule(&MODULE_ADC);

    /* Set the global configuration of module to default values. */
    IfxAdc_Tmadc_initModuleConfig(&moduleConfig, &MODULE_ADC);
    /* set the      */
    moduleConfig.id               = IfxAdc_TmadcModule_0;
    moduleConfig.calEnable        = 1;

    /* initialize modules */
    for (uiCnt=0; uiCnt<IFXADC_NUM_TMADC; uiCnt++)
    {
        IfxAdc_Tmadc_initModule(&g_TmadcAutoScan.vadc[uiCnt], &moduleConfig);
        moduleConfig.id++;
    }
}


/** \brief Demo run API
 *
 * This function is called from main
 */
void TmadcAutoScan_run(void)
{
    uint32 grpIx, glbIx;

    glbIx = 0;

    for (grpIx=0; grpIx<USED_GROUPS; grpIx++)
    {
        uint32                    chnIx, usedChannels;
        /* create channel config */
        IfxAdc_Tmadc_ChConfig adcChannelConfig;

    	/* start from channel 0 */
    	chnIx = 0;
    	/* we are using 16 channels (tmadc0, tmadc6, tmadc7) */
    	usedChannels = 16;
    	/* start from channel 4 */
    	if (grpIx == 1) chnIx = 4;
        if (grpIx == 3) usedChannels = 12;
        if ((grpIx == 2) || (grpIx == 4) || (grpIx == 5)) usedChannels = 0;

        for (chnIx=chnIx; chnIx < usedChannels; chnIx++)
        {
            IfxAdc_Tmadc_initChannelConfig(&adcChannelConfig, &MODULE_ADC);

            adcChannelConfig.moduleId = (IfxAdc_TmadcModule)(grpIx);
            adcChannelConfig.id       = (IfxAdc_TmadcChannel)(chnIx);
            adcChannelConfig.mode     = IfxAdc_TmadcOpMode_continuous;
            adcChannelConfig.resultCfg.resultReg = (IfxAdc_TmadcResultReg)(chnIx);  /* use dedicated result register */
            adcChannelConfig.samplingTimeNS = 500;
            /* initialize the channel */
            IfxAdc_Tmadc_initChannel(&g_TmadcAutoScan.adcChannel[glbIx], &adcChannelConfig);
            glbIx++;
        }
        /* start the Queue */
        IfxAdc_Tmadc_runModule(&g_TmadcAutoScan.vadc[grpIx]);
    }
}

