/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define TICKS_TO_WAIT 0x100000

void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required */
    IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword());
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());

    /* Initialization of the supply rail voltage measurements */
    measurement_init();

    /* Initialization of the ASCLIN interface */
    AsclinShellInterface_init();

    /* Configuration of LED P33.0 for alive indication */
    configLED();

    /* Configuration of the system timer (STM) */
    configSystemTimer();

    while(1)
    {
        for(uint32 i = TICKS_TO_WAIT; i > 0; i--)
        {
            /* Polling the terminal application */
            AsclinShellInterface_run();
        }
        /* Toggling the LED P33.0 as alive indicator */
        toggleLED();
    }
}
