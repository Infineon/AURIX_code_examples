/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define IFX_INTPRIO_TMADC_SR0   1                           /* Interrupt priority for TMADC                          */
#define TMADC_SAMPLE_TIME_NS    4000u                       /* TMADC sampling time in ns                             */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
IfxAdc_Tmadc     g_tmadc;                                    /* TMADC module handle variable                         */
IfxAdc_Tmadc_Ch  g_adcChannel;                               /* TMADC channel handle variable                        */

volatile uint16 g_resultsTmAdc[RESULT_TABLE_SIZE];           /* TMADC Results Array                                  */
volatile uint32 g_irqCntTmAadc = 0;                          /* ISR counter for TMADC results                        */

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void TMADC0SR0ISR(void);                                     /* TMADC Interrupt handler function prototype           */

void initTMADCModule(void);                                  /* Function to initialize the TMADC module              */
void initTMADCChannel(void);                                 /* Function to initialize the channel of TMADC module   */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* Macro to define the Interrupt Service Routine */
IFX_INTERRUPT(TMADC0SR0ISR, 0, IFX_INTPRIO_TMADC_SR0);

/* Function to initialize and run the TMADC */
void initTMADC(void)
{
    initTMADCModule();                                       /* Initialize the TMADC module                          */
    initTMADCChannel();                                      /* Initialize the channel                               */

    IfxAdc_Tmadc_runModule(&g_tmadc);                        /* Run the TMADC module                                 */
}

/* Function to initialize the TMADC module */
void initTMADCModule(void)
{
    /* Enable ADC module */
    IfxAdc_enableModule(&MODULE_ADC);

    /* Create configuration container */
    IfxAdc_Tmadc_Config adcConfig;
    /* Initialize module to default */
    IfxAdc_Tmadc_initModuleConfig(&adcConfig, &MODULE_ADC);

    /* Change TMADC configuration */
    adcConfig.id               = IfxAdc_TmadcModule_0;       /* Module ID                                            */
    adcConfig.calEnable        = 1;                          /* Calibration enabled                                  */

    /* TMADC Interrupt configuration */
    IfxAdc_Tmadc_InterruptConfig intConfig0 = {IfxAdc_TmadcServReq_0,
                                               IfxAdc_TmadcEventOp_andLogic,
                                               IfxAdc_TmadcEventSel_result,
                                               IfxSrc_Tos_cpu0,
                                               IfxSrc_VmId_0,
                                               IFX_INTPRIO_TMADC_SR0};
    IfxAdc_Tmadc_ServRequestConfig serviceRequestConfig;     /* Create TMADC interrupt service request configuration */
    serviceRequestConfig.intConfig[0] = &intConfig0;         /* Register correct interrupt configuration             */
    serviceRequestConfig.numServReqNodes = 1;                /* Configure number of interrupt service requests       */
    adcConfig.srvReqCfg = &serviceRequestConfig;             /* Assign the correct service request configuration     */

    /* Initialize ADC module with updated configuration */
    IfxAdc_Tmadc_initModule(&g_tmadc, &adcConfig);
}

/* Function to initialize the TMADC channel */
void initTMADCChannel(void)
{
    /* Create channel configuration container */
    IfxAdc_Tmadc_ChConfig adcChannel0Config;

    /* Initialize the configuration with default values */
    IfxAdc_Tmadc_initChannelConfig(&adcChannel0Config, &MODULE_ADC);

    /* Change TMADC channel configuration */
    /* Select the channel ID and the respective result register */
    adcChannel0Config.id             = IfxAdc_TmadcChannel_2;          /* Channel ID                                 */
    adcChannel0Config.moduleId       = IfxAdc_TmadcModule_0;           /* TMADC module                               */
    adcChannel0Config.samplingTimeNS = TMADC_SAMPLE_TIME_NS;           /* Sampling time period                       */
    adcChannel0Config.mode           = IfxAdc_TmadcOpMode_continuous;  /* Operating mode - continuous                */
    adcChannel0Config.core           = IfxAdc_TmadcSarCore_0;          /* TMADC core 0                               */

    adcChannel0Config.resultCfg.resultReg   = IfxAdc_TmadcResultReg_0; /* Configure result register                  */
    adcChannel0Config.resultCfg.waitForRead = 1;                       /* Enable wait-for-read mode                  */

    /* Configure channel service request */
    IfxAdc_Tmadc_ChannelServReqConfig channel0ServiceRequest = {IfxAdc_TmadcServReq_none,
                                                                IfxAdc_TmadcServReq_none,
                                                                IfxAdc_TmadcServReq_0};
    /* Assign configured channel service request to configuration */
    adcChannel0Config.channelSrvReq = &channel0ServiceRequest;

    /* Initialize the channel */
    IfxAdc_Tmadc_initChannel(&g_adcChannel, &adcChannel0Config);
}

void TMADC0SR0ISR(void)
{
    /* Index boundary check for TMADC results array */
    if (RESULT_TABLE_SIZE <= g_irqCntTmAadc)
    {
        g_irqCntTmAadc = 0;
    }

    /* Store the TMADC result value in the TMADC results array */
    g_resultsTmAdc[g_irqCntTmAadc] = IfxAdc_Tmadc_readChannelResult(&g_adcChannel);

    /* Increment index */
    g_irqCntTmAadc++;
}
