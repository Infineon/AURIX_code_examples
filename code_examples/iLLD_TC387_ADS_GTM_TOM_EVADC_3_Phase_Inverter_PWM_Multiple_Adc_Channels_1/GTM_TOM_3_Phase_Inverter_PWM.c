/* Period interrupt callback function */
void IfxGtm_periodEventFunction(void *data)
{

}

/* This function initializes the TOM */
void initGtmTom3phInv(void)
{
    /* Configuration variables
     * Neither application (in most of the cases) nor the PWM driver need configuration variables post initialization.
     * It is recommended to define such variables within the function (local), alternatively such structures could
     * also be initialized (element-wise) as constant (in FLASH)
     */
    static IfxGtm_Pwm_Config config;                                /* Main PWM config structure                            */
    static IfxGtm_Pwm_ChannelConfig channelConfig[NUM_OF_CHANNELS]; /* Array containing configuration for each channel      */
    static IfxGtm_Pwm_DtmConfig dtmConfig[NUM_OF_CHANNELS];         /* Only needed if using DTM module                      */
    static IfxGtm_Pwm_InterruptConfig interruptConfig;              /* Only needed if using interrupt. Can also be an array */
    static IfxGtm_Pwm_OutputConfig output[NUM_OF_CHANNELS];         /* Only needed if connected to pin                      */

    /* 1. Config structure initialization
     * If the configuration structure is located in the RAM, the initConfig API would be handy to initialize the
     * elements to their default values
     */
    IfxGtm_Pwm_initConfig(&config, &MODULE_GTM);

    /* 2. Output configuration
     * Output configurations are optional.
     */

    /* CH0 configurations */
    output[0].pin                   = (IfxGtm_Pwm_ToutMap*)PHASE_U_HS;          /* Phase V PWM High-side             */
    output[0].complementaryPin      = (IfxGtm_Pwm_ToutMap*)PHASE_U_LS;          /* Phase V PWM Low-side              */
    output[0].polarity              = Ifx_ActiveState_high;                     /* Active low/high of pin            */
    output[0].complementaryPolarity = Ifx_ActiveState_low;                      /* Active low/high of pin            */
    output[0].outputMode            = IfxPort_OutputMode_pushPull;              /* Output mode                       */
    output[0].padDriver             = IfxPort_PadDriver_cmosAutomotiveSpeed1;   /* Pad driver                        */

    /* CH1 configurations */
    output[1].pin                   = (IfxGtm_Pwm_ToutMap*)PHASE_V_HS;          /* Phase U PWM High-side             */
    output[1].complementaryPin      = (IfxGtm_Pwm_ToutMap*)PHASE_V_LS;          /* Phase U PWM Low-side              */
    output[1].polarity              = Ifx_ActiveState_high;                     /* Active low/high of pin            */
    output[1].complementaryPolarity = Ifx_ActiveState_low;                      /* Active low/high of pin            */
    output[1].outputMode            = IfxPort_OutputMode_pushPull;              /* Output mode                       */
    output[1].padDriver             = IfxPort_PadDriver_cmosAutomotiveSpeed1;   /* Pad driver                        */

    /* CH2 configurations */
    output[2].pin                   = (IfxGtm_Pwm_ToutMap*)PHASE_W_HS;          /* Phase W PWM High-side             */
    output[2].complementaryPin      = (IfxGtm_Pwm_ToutMap*)PHASE_W_LS;          /* Phase W PWM Low-side              */
    output[2].polarity              = Ifx_ActiveState_high;                     /* Active low/high of pin            */
    output[2].complementaryPolarity = Ifx_ActiveState_low;                      /* Active low/high of pin            */
    output[2].outputMode            = IfxPort_OutputMode_pushPull;              /* Output mode                       */
    output[2].padDriver             = IfxPort_PadDriver_cmosAutomotiveSpeed1;   /* Pad driver                        */

    /* 3. Dead-time configuration */

    dtmConfig[0].deadTime.rising    = 1e-6f;                /* Rising Dead time in seconds                           */
    dtmConfig[0].deadTime.falling   = 1e-6f;                /* Falling Dead time in seconds                          */

    dtmConfig[1].deadTime.rising    = 1e-6f;                /* Rising Dead time in seconds                           */
    dtmConfig[1].deadTime.falling   = 1e-6f;                /* Falling Dead time in seconds                          */

    dtmConfig[2].deadTime.rising    = 1e-6f;                /* Rising Dead time in seconds                           */
    dtmConfig[2].deadTime.falling   = 1e-6f;                /* Falling Dead time in seconds                          */

    /* 4. [Optional] Interrupt configuration, Example configuration of interrupt for base channel                    */

    interruptConfig.mode          = IfxGtm_IrqMode_pulseNotify;  /* IRQ mode of interrupt                            */
    interruptConfig.isrProvider   = IfxSrc_Tos_cpu0;             /* Type of Service                                  */
    interruptConfig.priority      = 0;                           /* Interrupt priority                               */
    /* Connect callback function for period event interrupt */
    interruptConfig.periodEvent   = IfxGtm_periodEventFunction;
    interruptConfig.dutyEvent     = NULL_PTR;                /* Do not duty event interrupt                          */

    /*5. Channel configuration */
    /*Base channel - CH0 configuration. */
    channelConfig[0].timerCh      = IfxGtm_Pwm_SubModule_Ch_5;   /* Atom channel index to be used for sync channel   */
    channelConfig[0].phase        = 0.0f;                        /* Phase shift in radians (range: 0.0 .. 2pi;
     * only for edge aligned sync channels)             */
    channelConfig[0].duty         = PHASE_V_DUTY;                /* PWM duty in % (range: 0.0 .. 100.0)              */
    channelConfig[0].dtm          = &dtmConfig[0];               /* Dead time configuration for this channel         */
    channelConfig[0].output       = &output[0];                  /* Pin connections and polarities for this channel  */
    channelConfig[0].mscOut       = NULL_PTR;                    /* MSC configuration for this channel               */
    channelConfig[0].interrupt    = &interruptConfig;            /* Attach Interrupt configuration for this channel  */

    /* Sync channel - CH1 configuration */
    channelConfig[1].timerCh      = IfxGtm_Pwm_SubModule_Ch_6;   /* Atom channel index to be used for base channel   */
    channelConfig[1].phase        = 0.0f;                        /* Phase shift in radians (range: 0.0 .. 2pi;
     * only for edge aligned sync channels)             */
    channelConfig[1].duty         = PHASE_U_DUTY;                /* PWM duty in % (range: 0.0 .. 100.0) */
    channelConfig[1].dtm          = &dtmConfig[1];               /* Attach Dead time configuration for this channel  */
    channelConfig[1].output       = &output[1];                  /* Pin connections and polarities for this channel  */
    channelConfig[1].mscOut       = NULL_PTR;                    /* MSC configuration for this channel               */
    channelConfig[1].interrupt    = NULL_PTR;                    /* Interrupt configuration for this channel         */

    /*Sync channel CH2 configuration.*/
    channelConfig[2].timerCh      = IfxGtm_Pwm_SubModule_Ch_7;   /* Atom channel index to be used for sync channel   */
    channelConfig[2].phase        = 0.0f;                        /* Phase shift in radians (range: 0.0 .. 2pi;
     * only for edge aligned sync channels)             */
    channelConfig[2].duty         = PHASE_W_DUTY;                /* PWM duty in % (range: 0.0 .. 100.0)              */
    channelConfig[2].dtm          = &dtmConfig[2];               /* Dead time configuration for this channel         */
    channelConfig[2].output       = &output[2];                  /* Pin connections and polarities for this channel  */
    channelConfig[2].mscOut       = NULL_PTR;                    /* MSC configuration for this channel               */
    channelConfig[2].interrupt    = NULL_PTR;                    /* Interrupt configuration for this channel         */

    /* 5. Other configurations */

    config.cluster                = IfxGtm_Cluster_1;                  /* Cluster                                    */
    config.subModule              = IfxGtm_Pwm_SubModule_tom;          /* Sub module                                 */
    config.alignment              = IfxGtm_Pwm_Alignment_center;       /* Alignment                                  */
    config.syncStart              = FALSE;                              /* Start all channels after init             */
    config.numChannels            = NUM_OF_CHANNELS;                   /* Number of channels configured              */
    config.channels               = channelConfig;                     /* Attach Channel configuration               */
    config.frequency              = PWM_FREQUENCY;                     /* PWM frequency                              */
    config.clockSource.tom        = IfxGtm_Cmu_Fxclk_0;                /* Clock source for tom                       */
    config.dtmClockSource         = IfxGtm_Dtm_ClockSource_cmuClock0;  /* Clock source for dtm                       */
    config.syncUpdateEnabled      = TRUE;                              /* TRUE: Update compare registers from shadow
     * at the end of period */

    /*****************************************************************************************************************/
    /* ADC TMADC Trigger Configuration                                                                               */
    /*****************************************************************************************************************/

    /* Configuration variables */

    IfxGtm_Pwm_Config configAdcTrig;                                        /* Main PWM config structure             */
    IfxGtm_Pwm_ChannelConfig channelAdcTrigConfig[NUM_OF_ADC_TRIG_CHANNELS];/* Channel configuration structure       */
    IfxGtm_Pwm_OutputConfig outputAdcTrig[NUM_OF_ADC_TRIG_CHANNELS];        /* Output configuration structure        */

    /* 1. Config structure initialization
     * If the configuration structure is located in the RAM, the initConfig API would be handy to initialize the
     * elements to their default values
     */
    IfxGtm_Pwm_initConfig(&configAdcTrig, &MODULE_GTM);

    /* 2. Output configuration
     * Output configurations are optional.
     */

    /* CH8 configurations */
    outputAdcTrig[0].pin                   = (IfxGtm_Pwm_ToutMap*)ADC_TRIG_OUT;       /* Phase U PWM High-side       */
    outputAdcTrig[0].polarity              = Ifx_ActiveState_low;                     /* Active low/high of pin      */
    outputAdcTrig[0].outputMode            = IfxPort_OutputMode_pushPull;             /* Output mode                 */
    outputAdcTrig[0].padDriver             = IfxPort_PadDriver_cmosAutomotiveSpeed1;  /* Pad driver                  */

    /*3. Channel configuration */

    /* Base channel - CH8 configuration */
    channelAdcTrigConfig[0].timerCh      = IfxGtm_Pwm_SubModule_Ch_11;   /* Tom channel index                        */
    channelAdcTrigConfig[0].phase        = 0.0f;                        /* Phase shift in radians (range: 0.0 .. 2pi;
     * only for edge aligned sync channels)      */
    channelAdcTrigConfig[0].duty         = ADC_TRIG_DUTY;               /* PWM duty in % (range: 0.0 .. 100.0)       */
    channelAdcTrigConfig[0].dtm          = NULL_PTR;                    /* Attach Dead time configuration            */
    channelAdcTrigConfig[0].output       = &outputAdcTrig[0];           /* Attach Pin connections and polarities     */
    channelAdcTrigConfig[0].mscOut       = NULL_PTR;                    /* MSC configuration for this channel        */
    channelAdcTrigConfig[0].interrupt    = NULL_PTR;                    /* Attach Interrupt configuration            */

    /* 4. Other configurations */

    configAdcTrig.cluster                = IfxGtm_Cluster_1;            /* Cluster                                   */
    configAdcTrig.subModule              = IfxGtm_Pwm_SubModule_tom;    /* Sub module */
    configAdcTrig.alignment              = IfxGtm_Pwm_Alignment_edge;   /* Alignment */
    configAdcTrig.syncStart              = FALSE;                       /* Start all channels after init             */
    configAdcTrig.numChannels            = NUM_OF_ADC_TRIG_CHANNELS;    /* Number of channels configured             */
    configAdcTrig.channels               = channelAdcTrigConfig;        /* Attach Channel configuration              */
    configAdcTrig.frequency              = PWM_FREQUENCY;               /* PWM frequency                             */
    configAdcTrig.clockSource.tom        = IfxGtm_Cmu_Fxclk_0;          /* Clock source for atom                     */
    configAdcTrig.syncUpdateEnabled      = TRUE;                        /* TRUE: Update compare registers from shadow
     * at the end of period                      */


    /*6. Call the init function */

    if(!IfxGtm_isEnabled(&MODULE_GTM))
    {
        float32 frequency;
        /* Enable GTM, it is necessary if the GTM is not initialized earlier */
        IfxGtm_enable(&MODULE_GTM);

        frequency = IfxGtm_Cmu_getModuleFrequency(&MODULE_GTM);
        /* Set the global clock frequency to the max */
        IfxGtm_Cmu_setGclkFrequency(&MODULE_GTM, frequency);
        /* Set the CMU CLK0 */
        IfxGtm_Cmu_setClkFrequency(&MODULE_GTM, IfxGtm_Cmu_Clk_0, frequency);
        /* Enable the FXCLK clock */
        IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_FXCLK | IFXGTM_CMU_CLKEN_CLK0);
    }

    IfxGtm_Trig_toEVadc(&MODULE_GTM, IfxGtm_Trig_AdcGroup_0, IfxGtm_Trig_AdcTrig_2,
            IfxGtm_Trig_AdcTrigSource_tom1,
            IfxGtm_Trig_AdcTrigChannel_11); /* ADC G0 Triggered by GTM TOM1 CH11 */
    IfxGtm_Trig_toEVadc(&MODULE_GTM, IfxGtm_Trig_AdcGroup_1, IfxGtm_Trig_AdcTrig_2,
            IfxGtm_Trig_AdcTrigSource_tom1,
            IfxGtm_Trig_AdcTrigChannel_11); /* ADC G1 Triggered by GTM TOM1 CH11 */
    IfxGtm_Trig_toEVadc(&MODULE_GTM, IfxGtm_Trig_AdcGroup_2, IfxGtm_Trig_AdcTrig_2,
            IfxGtm_Trig_AdcTrigSource_tom1,
            IfxGtm_Trig_AdcTrigChannel_11); /* ADC G2 Triggered by GTM TOM1 CH11 */
    IfxGtm_Trig_toEVadc(&MODULE_GTM, IfxGtm_Trig_AdcGroup_3, IfxGtm_Trig_AdcTrig_2,
            IfxGtm_Trig_AdcTrigSource_tom1,
            IfxGtm_Trig_AdcTrigChannel_11); /* ADC G3 Triggered by GTM TOM1 CH11 */


    IfxGtm_Pwm_init(&g_gtmTom3phInv.pwm, &g_gtmTom3phInv.channels[0], &config);
    IfxGtm_Pwm_init(&g_gtmTomTrigToAdc.pwm, &g_gtmTomTrigToAdc.channels[0], &configAdcTrig);
    IfxGtm_Pwm_startSyncedGroups(&g_gtmTom3phInv.pwm, &g_gtmTomTrigToAdc.pwm);


    /** Store the current duty and dead-time values for runtime calls */
    g_gtmTom3phInv.dutyCycles[0]= channelConfig[0].duty;
    g_gtmTom3phInv.dutyCycles[1]= channelConfig[1].duty;
    g_gtmTom3phInv.dutyCycles[2]= channelConfig[2].duty;
    g_gtmTom3phInv.deadTimes[0]= channelConfig[0].dtm->deadTime;
    g_gtmTom3phInv.deadTimes[1]= channelConfig[1].dtm->deadTime;
    g_gtmTom3phInv.deadTimes[2]= channelConfig[2].dtm->deadTime;
    /** Store the current duty values for runtime calls */
    g_gtmTomTrigToAdc.dutyCycles[0]= channelAdcTrigConfig[0].duty;

}

/* This function update duty cycles */
void updateGtmTom3phInvDuty(float32 *requestDuty)
{
    g_gtmTom3phInv.dutyCycles[0] = requestDuty[0];
    g_gtmTom3phInv.dutyCycles[1] = requestDuty[1];
    g_gtmTom3phInv.dutyCycles[2] = requestDuty[2];

    /* Update duty of all configured channels to requested values immediately */
    IfxGtm_Pwm_updateChannelsDutyImmediate(&g_gtmTom3phInv.pwm, (float32*)g_gtmTom3phInv.dutyCycles);
}
