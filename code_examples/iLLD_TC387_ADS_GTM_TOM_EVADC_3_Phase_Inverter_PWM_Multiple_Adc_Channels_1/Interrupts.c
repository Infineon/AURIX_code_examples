/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
float32 g_dutyCycles[NUM_OF_CHANNELS] = {PHASE_U_DUTY, PHASE_V_DUTY, PHASE_W_DUTY};

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
IFX_INTERRUPT(Current_Sense_Isr, 0, INTERRUPT_PRIORITY_EVADC_CUR);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* Interrupt service unit for ADC, end of ADC conversion call back */
void Current_Sense_Isr(void)
{
    IfxPort_togglePin(ADC_ISR_TOGGLE_PIN);                    /* Toggle the ADC_ISR_TOGGLE_PIN                   */

    /* Read ADC results */
    readAdcResults();

    /* Control algorithm */

    if((g_dutyCycles[0] + PHASE_DUTY_STEP) >= 100)
    {
        g_dutyCycles[0] = 0;                                  /* Set duty to zero                       */
    }
    if((g_dutyCycles[1] + PHASE_DUTY_STEP) >= 100)
    {
        g_dutyCycles[1] = 0;                                  /* Set duty to zero                       */
    }
    if((g_dutyCycles[2] + PHASE_DUTY_STEP) >= 100)
    {
        g_dutyCycles[2] = 0;                                   /* Set duty to zero                      */
    }

    g_dutyCycles[0] += PHASE_DUTY_STEP;                        /* Increment PWM duty cycle of phase U   */
    g_dutyCycles[1] += PHASE_DUTY_STEP;                        /* Increment PWM duty cycle of phase V   */
    g_dutyCycles[2] += PHASE_DUTY_STEP;                        /* Increment PWM duty cycle of phase W   */

    /* Update PWM parameters */
    updateGtmTom3phInvDuty(g_dutyCycles);
}
