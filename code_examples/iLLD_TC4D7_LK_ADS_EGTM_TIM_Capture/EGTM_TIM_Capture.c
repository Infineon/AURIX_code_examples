/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "Ifx_Types.h"
#include "IfxEgtm_Tim_In.h"
#include "IfxEgtm_Tom_Pwm.h"
#include "EGTM_TIM_Capture.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define PWM_OUT         IfxEgtm_TOM0_0_TOUT0_P02_0_OUT   /* Port pin which is driven by the PWM                      */
#define PWM_IN          IfxEgtm_TIM0_0_P02_0_IN          /* Input port pin for the PWM signal                        */
#define PWM_PERIOD      31250                            /* PWM signal period for the TOM                            */
#define DUTY_CYCLE      PWM_PERIOD / 2                   /* PWM signal duty cycle for the TOM                        */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
float32 g_measuredPwmDutyCycle = 0.0;                    /* Global variable for duty cycle of generated PWM signal   */
float32 g_measuredPwmFreqHz = 0.0;                       /* Global variable for frequency calculation of PWM signal  */
float32 g_measuredPwmPeriod = 0.0;                       /* Global variable for period calculation of PWM signal     */
IfxEgtm_Tim_In g_driverTIM;                              /* TIM driver structure                                     */
boolean g_dataCoherent = FALSE;                          /* Boolean to know if the measured data is coherent         */
/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

/* This function initializes the TIM to capture a PWM signals */
void initEgtmTim(void)
{
    IfxEgtm_enable(&MODULE_EGTM);                                        /* Enable the EGTM                          */
    IfxEgtm_Cmu_enableClocks(&MODULE_EGTM, IFXEGTM_CMU_CLKEN_CLK0);      /* Enable the CMU clock 0                   */

    IfxEgtm_Tim_In_Config configTIM;

    IfxEgtm_Tim_In_initConfig(&configTIM, &MODULE_EGTM);                 /* Initialize default parameters            */
    configTIM.inputPin = &PWM_IN;                                        /* Select input port pin                    */
    configTIM.inputPinMode = IfxPort_InputMode_pullDown;                 /* Select input port pin mode               */
    IfxEgtm_Tim_In_init(&g_driverTIM, &configTIM);                       /* Initialize the TIM                       */
}

/* Initialization of the TOM and generation of a PWM signal */
void initEgtmTomPwm(void)
{
    IfxEgtm_Tom_Pwm_Config tomConfig;                                    /* Timer configuration structure            */
    IfxEgtm_Tom_Pwm_Driver tomDriver;                                    /* Timer driver structure                   */
    IfxEgtm_Cmu_enableClocks(&MODULE_EGTM, IFXEGTM_CMU_CLKEN_FXCLK);     /* Enable the FXU clocks                    */

    IfxEgtm_Tom_Pwm_initConfig(&tomConfig, &MODULE_EGTM);                /* Initialize default parameters            */

    tomConfig.cluster = IfxEgtm_Cluster_0;                               /* Select the TOM                           */
    tomConfig.tomChannel = PWM_OUT.channel;                              /* Select the channel                       */
    tomConfig.period = PWM_PERIOD;                                       /* Set timer period in ticks                */
    tomConfig.dutyCycle = DUTY_CYCLE;                                    /* Set the duty cycle in ticks              */
    tomConfig.pin.outputPin = &PWM_OUT;                                  /* Set the pin as output                    */
    tomConfig.synchronousUpdateEnabled = TRUE;                           /* Enable synchronous update                */
    tomConfig.clock = IfxEgtm_Tom_Ch_ClkSrc_cmuFxclk1;                   /* Select the FXU clock 1                   */

    IfxEgtm_Tom_Pwm_init(&tomDriver, &tomConfig);                        /* Initialize the module                    */
    IfxEgtm_Tom_Pwm_start(&tomDriver, TRUE);                             /* Start the generation of the PWM signal   */
}

/* This function measures the period, the frequency and the duty cycle of the PWM signal */
void measurePwm(void)
{
    IfxEgtm_Tim_In_update(&g_driverTIM);                                         /* Update the measured data         */
    g_measuredPwmPeriod = IfxEgtm_Tim_In_getPeriodSecond(&g_driverTIM);          /* Get the period of the PWM signal */
    g_measuredPwmFreqHz = 1 / g_measuredPwmPeriod;                               /* Calculate the frequency          */
    g_measuredPwmDutyCycle = IfxEgtm_Tim_In_getDutyPercent(&g_driverTIM, &g_dataCoherent); /* Get the duty cycle     */
}
